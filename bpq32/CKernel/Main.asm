	PUBLIC	CONVTOAX25
CONVTOAX25:
;
;	CONVERT CALLSIGN IN [ESI] TO AX25 FORMAT IN _AX25CALL
;	RETURN NZ IF CALLSIGN INVALID
;
	MOV	EDI,OFFSET32 _AX25CALL
	MOV	ECX,6			; MAX ALPHANUMERICS
	MOV	AL,40H
	REP STOSB			; CLEAR IN CASE SHORT CALL
	MOV	AL,01100000B		; DEFAULT SSID
	STOSB
	MOV	EDI,OFFSET32 _AX25CALL
	MOV	CL,7
	PUBLIC	CONVAX00
CONVAX00:
	LODSB
	CMP	AL,'-'
	JE SHORT CONVAX20		; SSID FOLLOWS
	CMP	AL,' '
	JBE SHORT CONVAX40		; END OF NAME
;
	CMP	AL,'#'
	JNE SHORT NOTHASH

	CMP	CL,7
	JE SHORT CONVAX10		; OK AS FIRST CHAR
	PUBLIC	NOTHASH
NOTHASH:
	CMP	AL,'/'			; ALLOW / AND DIGITS (FOR W0RLI!)
	JB SHORT CONVAX49		; INVALID
	CMP	AL,'9'
	JBE SHORT CONVAX10		; OK

	CMP	AL,'A'
	JB SHORT CONVAX49		; INVALID
	CMP	AL,'Z'
	JBE SHORT CONVAX10		; OK


	CMP	AL,'a'
	JB SHORT CONVAX10		; OK
	CMP	AL,'z'
	JA SHORT CONVAX49		; INVALID

	AND	AL,NOT 20H		; CONVERT TO UPPER CASE
	PUBLIC	CONVAX10
CONVAX10:
	SAL	AL,1			; SHIFT UP
	STOSB
	LOOP	CONVAX00
;
;	HAVE PROCESSED 7 CHARS - NAME IS INVALID
;
	JMP SHORT CONVAX49

	PUBLIC	CONVAX20
CONVAX20:
	LODSB				; FIRST CHAR IF SSID
	CMP	AL,'0'
	JB SHORT CONVAX49		; INVALID
	CMP	AL,'9'
	JA SHORT CONVAX49		; OK
;
	MOV	AH,AL			; SAVE
	LODSB
	CMP	AL,' '
	JBE SHORT CONVAX30		; END OF SSID
	CMP	AL,','
	JE SHORT CONVAX30		; END OF SSID

	INC	ESI			; OTHER CODE EXPECTS ESI TO POINT TO NEXT FIELD
	CMP	AL,'0'
	JB SHORT CONVAX49		; INVALID
	CMP	AL,'5'			; LIMIT 10 - 15
	JA SHORT CONVAX49		; OK
;
	SUB	AL,30H
	CMP	AH,'1'
	JNE SHORT CONVAX49		; IF 2 DIGITS, FIRST MUST BE 1

	ADD	AL,10
	MOV	AH,AL
	PUBLIC	CONVAX30
CONVAX30:
	SAL	AH,1			; UP 1 BIT
	OR	AH,01100000B		; DEFAULT RR BITS
	MOV	_AX25CALL+6,AH

	PUBLIC	CONVAX40
CONVAX40:
;
;	NO SSID SUPPLIED
;
	XOR	AL,AL			; SET ZERO
	RET

	PUBLIC	CONVAX49
CONVAX49:
	OR	AL,1			; SET NZ
	RET



	PUBLIC	CONVFROMAX25
CONVFROMAX25:
;
;	CONVERT AX25 FORMAT CALL IN [ESI] TO NORMAL FORMAT IN _NORMCALL
;	   RETURNS LENGTH IN CX AND NZ IF LAST ADDRESS BIT IS SET
;
	PUSH	ESI			; SAVE
	MOV	EDI,OFFSET32 _NORMCALL
	MOV	ECX,10			; MAX ALPHANUMERICS
	MOV	AL,20H
	REP STOSB			; CLEAR IN CASE SHORT CALL
	MOV	EDI,OFFSET32 _NORMCALL
	MOV	CL,6
	PUBLIC	CONVAX50
CONVAX50:
	LODSB
	CMP	AL,40H
	JE SHORT CONVAX60		; END IF CALL - DO SSID

	SHR	AL,1
	STOSB
	LOOP	CONVAX50
	PUBLIC	CONVAX60
CONVAX60:
	POP	ESI
	ADD	ESI,6			; TO SSID
	LODSB
	MOV	AH,AL			; SAVE FOR LAST BIT TEST
	SHR	AL,1
	AND	AL,0FH
	JZ SHORT CONVAX90		; NO SSID - FINISHED
;
	MOV	BYTE PTR [EDI],'-'
	INC	EDI
	CMP	AL,10
	JB SHORT CONVAX70
	SUB	AL,10
	MOV	BYTE PTR [EDI],'1'
	INC	EDI
	PUBLIC	CONVAX70
CONVAX70:
	ADD	AL,30H			; CONVERT TO DIGIT
	STOSB
	PUBLIC	CONVAX90
CONVAX90:
	MOV	ECX,EDI
	SUB	ECX,OFFSET32 _NORMCALL
	MOV	_NORMLEN,ECX		; SIGNIFICANT LENGTH

	TEST	AH,1			; LAST BIT SET?
	RET

