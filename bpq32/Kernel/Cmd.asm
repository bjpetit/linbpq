	CMP	SUPPRESS,1
	JE SHORT DEC044			; SUPPRESS LEADING SPACES

	MOV	AL,' '
DEC040:
	STOSB
DEC044:
	ADD	ESI,4
	LOOP	DEC010

	MOV	AL,ACCUM		; REMAINDER
	ADD	AL,'0'
	STOSB				; PRINT IT
;
	POP	ECX
	POP	ESI
	POP	EBX

	RET
;
;	ROUTINE TO ADD TWO 4 BYTE NUMBERS
;
;
ADD4:
	MOV	AL,[ESI]
	ADD	ACCUM,AL
	MOV	AL,[ESI+1]
	ADC	ACCUM+1,AL
	MOV	AL,[ESI+2]
	ADC	ACCUM+2,AL
	MOV	AL,[ESI+3]
	ADC	ACCUM+3,AL

	RET
;
;	ROUTINE TO SUBTRACT TWO 4 BYTE NUMBERS
;
;
SUB4:
	MOV	AL,[ESI]
	SUB	ACCUM,AL
	MOV	AL,[ESI+1]
	SBB	ACCUM+1,AL
	MOV	AL,[ESI+2]
	SBB	ACCUM+2,AL
	MOV	AL,[ESI+3]
	SBB	ACCUM+3,AL

	RET

