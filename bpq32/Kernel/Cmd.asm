STATSTIMER:

	MOV	ESI,PORTTABLE
	MOVZX	ECX,NUMBEROFPORTS

STATST10:

	MOV	AX,SENDING[ESI]

	DIV	D11

	MOV	AVSENDING[ESI],AL

	MOV	AX,SENDING[ESI]
	ADD	AX,ACTIVE[ESI]

	DIV	D11

	MOV	AVACTIVE[ESI],AL


	MOV	SENDING[ESI],0
	MOV	ACTIVE[ESI],0


	MOV	ESI,PORTPOINTER[ESI]
	LOOP	STATST10

	RET


COMMANDHANDLER:
;
	MOV	CMDSTACKP,ESP		; SAVE STACK FOR NO BUFFER RECOVERY
;
;	GET PACLEN FOR THIS CONNECTION
;
	MOV	AH,0
	MOV	AL,SESSPACLEN[EBX]
	OR	AX,AX
	JNZ SHORT PACLENOK

	MOV	AX,PACLEN		; DONT ALLOW ZERO!
PACLENOK:
	MOV	CMDPACLEN,AX

	PUSH	EDI
;
;	TRANSPORT TABLE ENTRY IN EBX, MESSAGE IN EDI
;
 	LEA	ESI,8[EDI]		; TO DATA

	MOV	EDI,OFFSET32 COMMANDBUFFER
	MOV	ECX,40
	MOV	AX,2020H
	REP STOSW

	MOV	EDI,OFFSET32 COMMANDBUFFER
	MOV	ECX,80

COMMANDDECODE00:

	LODSB
	AND	AL,7FH			; MASK PARITY

	CMP	AL,0DH
	JE SHORT COMMANDDECODE10

	CMP	AL,61H
	JB SHORT COMMANDDECODE05
	CMP	AL,7AH
	JA SHORT COMMANDDECODE05
	SUB	AL,20H

COMMANDDECODE05:

	STOSB
	LOOP	COMMANDDECODE00

COMMANDDECODE10:
;
;	USE INPUT MESSAGE BUFFER FOR REPLY
;
	POP	EDI
	MOV	REPLYBUFFER,EDI
;
;	SET UP HEADER
;
	MOV	AL,LINKPORT[EBX]	; FROM LINK TABLE
	MOV	MSGPORT[EDI],AL

	LEA	EDI,7[EDI]		; SKIP HEADER
	MOV	AL,0F0H
	STOSB				; PID

	CALL	_SETUPNODEHEADER


	PUSH	EDI			; SAVE REPLY BUFFER

REDOCOMMAND:

	MOV	ESI,OFFSET32 COMMANDBUFFER ; MESSAGE ADDR
	CALL	SCAN			; STRIP LEADING SPACES
;
	CMP	BYTE PTR [ESI],20H
	JE SHORT NULLCMD		; IGNORE NULL COMMAND

	MOV	EDI,OFFSET32 COMMANDLIST
 	MOV	ECX,NUMBEROFCOMMANDS

COMMANDLOOP:

	CMP	EDI,OFFSET32 APPL1
	JNE SHORT NOTFIRSTAPPL

	MOV	APPLMASK,1		; FOR APPLICATION ATTACH REQUESTS
	MOV	ALIASPTR,OFFSET32 CMDALIAS

NOTFIRSTAPPL:

	MOV	CMDPTR,EDI		; POINTER TO COMMAND NAME

	PUSH	ESI
	PUSH	EDI
	PUSH	ECX

	MOV	CL,CMDLEN[EDI]		; MIN LENGTH
	REP CMPSB
	JE SHORT COMMANDFOUND

	POP	ECX
	POP	EDI
	POP	ESI
TRYNEXT:

	SHL	APPLMASK,1
	ADD	ALIASPTR,16
	ADD	EDI,TYPE CMDX

	LOOP	COMMANDLOOP

	POP	EDI

INVALIDCMD:

	INC	BADCOMMANDS[EBX]
	CMP	BADCOMMANDS[EBX],6
	JE SHORT FORCEDISC		; TOO MANY ERRORS


	MOV	ESI,OFFSET32 CMDERRMSG
	MOV	ECX,CMDERRLEN
	REP MOVSB

	JMP	SENDCOMMANDREPLY

NULLCMD:

	POP	EDI

NOREPLY:

	MOV	EDI,REPLYBUFFER
	CALL	RELBUFF
	RET

BYECMD:
FORCEDISC:

	MOV	EDI,REPLYBUFFER
	CALL	RELBUFF

	MOV	STAYFLAG[EBX],0
	CALL	CLOSECURRENTSESSION

	RET


COMMANDFOUND:

	CMP	BYTE PTR [ESI],' '
	JE SHORT ENDOFCOMMAND

	CMPSB
	JE SHORT COMMANDFOUND		; LOOP TILL ALL COMPARED OR END
;
;	END OF STRING WRONG
;
	POP	ECX
	POP	EDI
	POP	ESI	

	JMP	TRYNEXT

ENDOFCOMMAND:

	MOV	BADCOMMANDS[EBX],0	; RESET ERROR COUNT
	POP	ECX
	POP	EDI
	POP	EAX			; TO CLEAR STACK
;
;	SEE IF SYSOP COMMAND, AND IF SO IF PASSWORD HAS BEEN ENTERED
;
	CMP	EDI,OFFSET32 PASSCMD
	JAE SHORT COMM_OK
;
;	NEEDS PASSWORD FOR SYSOP COMMANDS
;
	CMP	PASSWORD[EBX],0FFFFH
	JNE SHORT NEEDPASS

COMM_OK:

	MOV	VALNODESFLAG,0		; NOT VALID NODES COMMAND

	MOV	EAX,CMDPROC[EDI]	; COMMAND PROCESSOR

	POP	EDI			; COMMAND REPLY BUFFER

	PUSH	EAX
	RET				; ENTER COMMAND PROCESSOR -
					; ESI POINTS TO CHAR FOLLOWING COMMAND
;
NEEDPASS:

	POP	EDI

PASSERROR:

	MOV	ESI,OFFSET32 PASSWORDMSG
	MOV	ECX,LPASSMSG
	REP MOVSB

	JMP	SENDCOMMANDREPLY

