	PUBLIC	SDNDM
SDNDM:
	MOV	AL,SDCBYTE		; GET FRAME CONTROL BYTE
	AND	AL,0FFH-10H		; MASK P/F

	CMP	AL,UA
	JE SHORT SDND40

	CMP	AL,DM
	JE SHORT SDND30

	CMP	AL,FRMR
	JNE SHORT NOTFRMR

	JMP	SDUF80
	PUBLIC	NOTFRMR
NOTFRMR:

	JMP	L2DISCARD		; ANY OTHER - IGNORE

;
	PUBLIC	SDND30
SDND30:
;
;	DM RESPONSE - IF TO SABM, SEND BUSY MSG
;
	CMP	L2STATE[EBX],2		; CONNECTING?
	JNE SHORT SDND35			; NO - CLEAR OUT LINK

	PUSH	EBX
	CALL	CONNECTREFUSED		; SEND MESSAGE IF DOWNLINK
	POP	EBX
	PUBLIC	SDND35
SDND35:
;
;	DM OR UA RESP TO DISC RECEIVED - OTHER END HAS LOST SESSION
;
;
;	CLEAR OUT TABLE ENTRY - IF INTERNAL TNC, SHOULD SEND *** DISCONNECTED
;
	CALL	L2DISCARD
	CALL	INFORMPARTNER		; SEND DISC TO OTHER END
	CALL	CLEAROUTLINK

	RET

	PUBLIC	SDND40
SDND40:
;
;	UA RECEIVED
;
	CMP	L2STATE[EBX],2		; RESPONSE TO SABM?
	JNE SHORT SDND42			; NO
;
;	RESPONSE TO SABM - SET LINK  UP
;
	CALL	RESET2X			; LEAVE QUEUED STUFF

	MOV	L2STATE[EBX],5
	MOV	L2TIMER[EBX],0		; CANCEL TIMER
	MOV	L2RETRIES[EBX],0
	MOV	AX,T3
	MOV	L2SLOTIM[EBX],AX		; SET FRAME SENT RECENTLY
;
;	IF VERSION 1 MSG, SET FLAG
;
	TEST	MSGFLAG,VER1
	JZ SHORT SDND41			; NORMAL (VERSION 2)

	MOV	VER1FLAG[EBX],1

	PUBLIC	SDND41
SDND41:

;	TELL PARTNER CONNECTION IS ESTABLISHED

	MOV	ESI,OFFSET32 CONNECTEDMSG
	MOV	ECX,LCONNECTEDMSG

	PUSH	EBX
	MOV	EDI,BUFFER
	CALL	SENDCONNECTREPLY
	POP	EBX

	RET

	PUBLIC	SDND42
SDND42:
	CMP	L2STATE[EBX],4		; DISCONNECTING?
	JE SHORT SDND35			; CLEAR OUT LINK
;
;	UA, BUT NOT IN STATE 2 OR 4 - IGNORE
; 
	JMP	L2DISCARD



	PUBLIC	SDSF20
SDSF20:
	JMP	SDFRMR			; PROCESS FRAME REJECT CONDITION
;
;	SUPERVISORY COMMAND
;
;	ONLY VALID ONES ARE RR/RNR
;
;	CHECK COUNTS, AND IF RNR INDICATE BUFFER SHORTAGE AT OTHER END
;
	PUBLIC	SDSFRM
SDSFRM:
	CMP	SDREJF[EBX],0		; ARE ANY REJECT FLAGS SET?
	JNE SHORT SDSF20			; J IF YES
	PUBLIC	SDSF10
SDSF10:
	CALL	SDNRCHK			; CHECK RECEIVED N(R)
	CMP	SDREJF[EBX],0		; ARE ANY REJECT FLAGS SET?
	JNE SHORT SDSF20			; J IF YES
;
;	VALID RR/RNR RECEIVED
;
	AND	L2FLAGS[EBX],NOT RNRSET	; CLEAR RNR

	MOV	AL,SDCBYTE		; GET CONTROL BYTE
	AND	AL,0FH
	CMP	AL,RNR			; IS COMMAND RNR?
	JNE SHORT SDSF15			; NO

	OR	L2FLAGS[EBX],RNRSET	; SET OTHER END CANT RECEIVE
	PUBLIC	SDSF15
SDSF15:
	TEST	MSGFLAG,CMD
	JNZ SHORT SDSF17			; ALWAYS REPLY TO RR COMMAND

	TEST	SDCBYTE,PFBIT
	JNZ SHORT SDSF16			; RESPONSE WITH F SET - RESET N(S)
;
;	RESPONSE WITHOUT P/F DONT RESET N(S) (UNLESS V1)
;
	CMP	VER1FLAG[EBX],1
	JE SHORT SDSF16

	JMP	L2DISCARD

	PUBLIC	SDSF16
SDSF16:
;
;	RESPONSE WITH P/F - MUST BE REPLY TO POLL FOLLOWING TIMEOUT OR I(P)
;
	AND	L2FLAGS[EBX],NOT POLLSENT; CLEAR I(P) SET
;
;	THERE IS A PROBLEM WITH REPEATED RR(F), SAY CAUSED BY DELAY AT L1
;
;	AS FAR AS I CAN SEE, WE SHOULD ONLY RESET N(S) IF AN RR(F) FOLLOWS
;	AN RR(P) AFTER A TIMEOUT - AN RR(F) FOLLOWING AN I(P) CANT POSSIBLY
;	INDICATE A LOST FRAME. ON THE OTHER HAND, A REJ(F) MUST INDICATE
;	A LOST FRAME.
;
;
;	AL STILL CONTAINS THE COMMAND
;
	CMP	AL,REJ
	JE SHORT SDSF16A			; RESET NS
;
	CMP	L2RETRIES[EBX],0		; RETRYING?
	JE SHORT SDSF16B			; NO, SO PROBABLY REPLY TO REPEATED
					; RR(P), DELAYED IN LINK LEVEL CODE

	PUBLIC	SDSF16A
SDSF16A:

	PUSH	EAX
	MOV	AL,SDCBYTE
	ROL	AL,1			; SHIFT IT TO BOTTOM 3 BITS
	ROL	AL,1
	ROL	AL,1
	AND	AL,7
	CALL	RESETNS			; RESET N(S) AND COUNT RETRIED FRAMES 
	
	POP		EAX				; KEEP COMMAND IN AL
	
	MOV	L2RETRIES[EBX],0
	MOV	L2TIMER[EBX],0		; WILL RESTART TIMER WHEN RETRY SENT

	PUBLIC	SDSF16B
SDSF16B:

	CMP	AL,RNR
	JNE @F
;
;	Dont Clear timer on receipt of RNR(F), spec says should poll for clearing of busy,
;	and loss of subsequent RR will cause hang. Perhaps should set slightly longer time??
;	Timer may have been cleared earlier, so restart it

	MOV	AL,L2TIME[EBX]
	MOV	AH,0
	MOV	L2TIMER[EBX],AX		; SET TIMER

@@:

	JMP	L2DISCARD
;
;	RR/RNR/REJ COMMAND - SEND RESPONSE
;
	PUBLIC	SDSF17
SDSF17:
;
;	FIRST PROCESS RESEQ QUEUE
;
;	PUSH	BUFFER
;	CALL	PROCESS_RESEQ
;	POP	BUFFER
;
;	IGNORE IF AN 'F' HAS BEEN SENT RECENTLY
;
	MOV	DX,REALTIMETICKS
	SUB	DX,LAST_F_TIME[EBX]
	CMP	DX,15					; 1.5 SECS
	JB SHORT SDSF16B			; DISCARD

	CALL	RR_OR_RNR

	MOV	AL,LINKNR[EBX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE
	PUBLIC	SDCB10
SDCB10:
	MOV	L2ACKREQ[EBX],0		; CANCEL DELAYED ACK

	MOV	CX,T3
	MOV	L2SLOTIM[EBX],CX		; SET FRAME SENT RECENTLY

	PUSH	EBX
	CALL	L2SENDRESP
	POP	EBX
;
;	SAVE TIME IF 'F' SENT'
;
	TEST	SDCBYTE,10H
	JZ SHORT SDSF18

	MOV		DX,REALTIMETICKS
	MOV	LAST_F_TIME[EBX],DX

	PUBLIC	SDSF18
SDSF18:
	RET

	PUBLIC	RESETNS
RESETNS:
	MOV	AH,LINKNS[EBX]
	MOV	LINKNS[EBX],AL		; RESET N(S)

	SUB	AH,AL			; FRAMES TO RESEND
	AND	AH,7			; MODULO SEVEN

	CMP	LINKTYPE[EBX],3
	JNE SHORT RESETNSRET

	PUSH	EBX
	MOV	EBX,NEIGHBOUR[EBX]
	OR	EBX,EBX
	JZ SHORT NONEIGHBOUR		; WOT!!

	MOVZX	EAX,AH
	ADD	NBOUR_RETRIES[EBX],EAX

	PUBLIC	NONEIGHBOUR
NONEIGHBOUR:

	POP	EBX

	PUBLIC	RESETNSRET
RESETNSRET:

	RET





