	MOV	ECX,7
	REP MOVSB			; SAVE ORIGIN

	MOV	EDI,BUFFER

	LEA	ESI,MSGDEST[EDI]	
	LEA	EDI,MSGORIGIN[EDI]
	MOV	ECX,7
	REP MOVSB
;
	AND	BYTE PTR -1[EDI],00011110B	; MASK SSID
	OR	BYTE PTR -1[EDI],11100000B	; SET RESPONSE AND RESERVED

	MOV	EDI,BUFFER

	LEA	ESI,TEMPFIELD	
	LEA	EDI,MSGDEST[EDI]
	MOV	ECX,7
	REP MOVSB
;
	AND	BYTE PTR -1[EDI],00011110B	; MASK SSID - C BIT ZERO FOR RESPONSE
	OR	BYTE PTR -1[EDI],01100000B	; SET RESERVED
;
	ADD	EDI,7				; TO FIRST DIGI IF PRESENT
	TEST	BYTE PTR -1[ESI],1
	JNZ SHORT L2SWAPRET
;
;	THERE ARE DIGIS TO PROCESS - COPY TO WORK AREA, THEN COPY BACK
;
	MOV	ESI,EDI				; TO FIRST DIGI

	PUSH	ESI				; SAVE

	MOV	EDI,OFFSET32 TEMPDIGI
	MOV	ECX,7*8
	XOR	AX,AX
	REP STOSB				; CLEAR (MAX 8 DIGIS, 7 BYTES EACH)

	SUB	EDI,7				; TO LAST TEMP FIELD
	PUBLIC	L2SWAP000
L2SWAP000:
	MOV	ECX,7
	REP MOVSB

	AND	BYTE PTR -1[EDI],01111110B	; MASK REPEATED AND LAST BITS
	TEST	BYTE PTR -1[ESI],1
	JNZ SHORT L2SWAP010			; END OF LIST

	SUB	EDI,14				; TO PREVIOUS
	JMP	L2SWAP000

	PUBLIC	L2SWAP010
L2SWAP010:
	MOV	ESI,EDI
	SUB	ESI,7				; TO START OF LAST DIGI (NOW FIRST)
	POP	EDI
	PUBLIC	L2SWAP020
L2SWAP020:
	CMP	BYTE PTR [ESI],0
	JE SHORT L2SWAPRET			; NO MORE

	MOV	ECX,7
	REP MOVSB
	JMP	L2SWAP020			; LOOP
	PUBLIC	L2SWAPRET
L2SWAPRET:
	OR	BYTE PTR -1[EDI],1		; SET END OF ADDRESS BIT
	RET

	PUBLIC	L2_PROCESS
L2_PROCESS:
;
;	PROCESS LEVEL 2 PROTOCOL STUFF
;
;	SEE IF COMMAND OR RESPONSE
;
	TEST	MSGFLAG,CMD
	JNZ SHORT L2_COMMAND
;
;	RESPONSE OR VERSION 1
;
	PUBLIC	L2_RESPONSE
L2_RESPONSE:
;
;	IF RETRYING, MUST ONLY ACCEPT RESPONSES WITH F SET (UNLESS RUNNING V1) 
;
	CMP	VER1FLAG[EBX],1
	JE SHORT HDRX09			; VERSION 1

	TEST	SDCBYTE,PFBIT
	JZ SHORT HDRX10			; F NOT SET - DONT RESET TIMER
;
;	F  SET - CAN CANCEL TIMER
;
	PUBLIC	HDRX09
HDRX09:
	MOV	L2TIMER[EBX],0		; CANCEL LINK TIMER
	JMP SHORT HDRX10

	PUBLIC	L2_COMMAND
L2_COMMAND:
	PUBLIC	HDRX10
HDRX10:
	CMP	L2STATE[EBX],3
	JNE SHORT NOTFRMRSTATE
;
;	FRMR STATE - IF C(P) SEND FRMR, ELSE IGNORE
;
	MOV	AL,SDCBYTE
	AND	AL,NOT PFBIT

	CMP	AL,FRMR
	JE SHORT BOTHFRMR		; DONT EXCHANGE FOR EVER!

	TEST	MSGFLAG,CMD
	JZ SHORT FRMRDISCARD		; RESPONSE

	TEST	SDCBYTE,PFBIT
	JZ SHORT FRMRDISCARD
;
;	SEND FRMR AGAIN
;
	JMP	SDFRMR

	PUBLIC	FRMRDISCARD
FRMRDISCARD:

	JMP	L2DISCARD

	PUBLIC	BOTHFRMR
BOTHFRMR:

	JMP	SDUF80			; SEND SABM

	PUBLIC	NOTFRMRSTATE
NOTFRMRSTATE:

	CMP	L2STATE[EBX],5		; LINK OK?
	JB SHORT SDNDM			; NO - PROCESS LINK SETUP 
;
;	LINK IN STATE 5 OR ABOVE - LINK RUNNING
;
	MOV	AL,SDCBYTE		; GET CONTROL BYTE
	AND	AL,0FFH-10H		; MASK OFF P/F BIT

	TEST	AL,1
	JNZ SHORT NOTIFRM

	JMP	SDIFRM			; J IF AN I-FRAME

	PUBLIC	NOTIFRM
NOTIFRM:

	TEST	AL,2
	JZ SHORT NOTUFRM

	JMP	SDUFRM			; J IF UNNUMBERED FORMAT
	PUBLIC	NOTUFRM
NOTUFRM:
	AND	AL,0FH			; ELSE SUPERVISORY, MASK OFF N(R) AND P-BIT
	CMP	AL,RR
	JNE SHORT NOTRR

	JMP	SDSFRM			; J IF 'RECEIVE READY'
	PUBLIC	NOTRR
NOTRR:
	CMP	AL,RNR
	JNE SHORT NOTRNR

	JMP	SDSFRM			; J IF 'RECEIVE NOT READY'
	PUBLIC	NOTRNR
NOTRNR:
	CMP	AL,REJ
	JNE SHORT SDRX50
;
	INC	L2REJCOUNT[EBP]
;
	JMP	SDSFRM
;
;	UNRECOGNISABLE COMMAND
;
	PUBLIC	SDRX50
SDRX50:
