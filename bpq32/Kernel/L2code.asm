	PUBLIC	L2TIMERPROC
L2TIMERPROC:
;
;	CHECK FOR TIMER EXPIRY OR BUSY CLEARED  
;
	MOVZX	ECX,MAXLINKS
	MOV	EBX,LINKS
	PUBLIC	HTIM00
HTIM00:
	CMP	BYTE PTR [EBX],0
	JNE SHORT HTIM01

	JMP	HTIM400			; SPARE ENTRY

	PUBLIC	HTIM01
HTIM01:

	PUSH	EBX

	MOV	AL,LINKPORT[EBX]
	CALL	GETPORTTABLEENTRY

	MOV	EBP,EBX
	POP	EBX

	MOV	AX,L2TIMER[EBX]
	OR	AX,AX
	JZ SHORT HTIM100		; NOT RUNNING

	DEC	AX
	MOV	L2TIMER[EBX],AX		; DECREMENT

	JNZ SHORT HTIM101		; NOT EXPIRED

	CALL	L2TIMEOUT		; TIMEOUT HAS OCCURED

	JMP SHORT HTIM101

	PUBLIC	HTIM100
HTIM100:
;
;	TIMER NOT RUNNING - MAKE SURE STATE NOT BELOW 5 - IF
;	IT IS, SOMETHING HAS GONE WRONG, AND LINK WILL HANG FOREVER
;
	CMP	L2STATE[EBX],2
	JE SHORT HTIM101		; CONNECT - PROBABLY TO CQ

	CMP	L2STATE[EBX],5
	JAE SHORT HTIM101		; OK

	MOV	L2TIMER[EBX],2		; ARBITRARY VALUE

	PUBLIC	HTIM101
HTIM101:
;
;	TEST FOR RNR SENT, AND NOT STILL BUSY
;
	TEST	L2FLAGS[EBX],RNRSENT
	JZ SHORT NOTBSY			; RNR NOT SENT

	CALL	RR_OR_RNR		; SEE IF STILL BUSY

	CMP	AH,RNR
	JE SHORT NOTBSY			; STILL BUSY
;
;	Just sending RR will hause a hang of RR is missed, and other end does not poll on Busy
;	Try sending RR CP, so we will retry if not acked

	MOV	L2ACKREQ[EBX],0		; CLEAR ANY DELAYED ACK TIMER
	
	CMP	L2RETRIES[EBX],0
	JNE SHORT HTIM200			; IF RR(P) OUTSTANDING WILl REPORT ANYWAY
	
	JMP	HTIM40					; Send RR(P)

	PUBLIC	NOTBSY
NOTBSY:

	CMP	L2ACKREQ[EBX],0			; DELAYED ACK TIMER
	JE SHORT HTIM200			; NOT RUNNING
	
	CMP	L2RETRIES[EBX],0
	JNE SHORT HTIM200			; DONT SEND RR RESPONSE WHILEST RR(P) OUTSTANDING


 	DEC	L2ACKREQ[EBX]
	JNZ SHORT HTIM200			; STILL OK
;
	MOV	AL,0			; NO F BIT
	CALL	SEND_RR_RESP

	PUBLIC	HTIM200
HTIM200:
;
;	CHECK FOR REJ TIMEOUT
;
	CMP	REJTIMER[EBX],0
	JE SHORT NOTREJTIMER

	DEC	REJTIMER[EBX]
	JNZ SHORT NOTREJTIMER
;
;	REJ HAS TIMED OUT (THIS MUST BE A VERSION 1 SESSION)
;
;
;	CANCEL REJ STATE
;
	CMP	L2STATE[EBX],6		; REJ?
	JNE SHORT NOTREJTIMER

	MOV	L2STATE[EBX],5		; CLEAR REJ 


	PUBLIC	NOTREJTIMER
NOTREJTIMER:

	CMP	L2SLOTIM[EBX],0
	JZ SHORT HTIM300
	DEC	L2SLOTIM[EBX]
	JNZ SHORT HTIM300			; STILL OK

	JMP	HTIM40			; SEND RR/RNR(P) AS LINK VALIDATION

	PUBLIC	HTIM300
HTIM300:
	INC	KILLTIMER[EBX]
	MOV	AX,L2KILLTIME
	OR	AX,AX
	JZ SHORT HTIM400			; ZERO = RUN FOR EVER

	CMP	KILLTIMER[EBX],AX
	JB SHORT HTIM400
;
;	CIRCUIT HAS BEEN IDLE TOO LONG - SHUT IT DOWN
;
	MOV	KILLTIMER[EBX],0
	MOV	L2TIMER[EBX],1		; TO FORCE DISC
	MOV	L2STATE[EBX],4		; DISCONNECTING

	PUSH	EBX
	PUSH	ECX
	CALL	INFORMPARTNER		; TELL OTHER LEVELS
	POP	ECX
	POP	EBX
	MOV	CIRCUITPOINTER[EBX],0
	PUBLIC	HTIM400
HTIM400:
	ADD	EBX,TYPE LINKTABLE
	LOOP	HTIM00_J
	RET

	PUBLIC	HTIM00_J
HTIM00_J:
	JMP	HTIM00

	PUBLIC	SEND_RR_RESP
SEND_RR_RESP:

	PUSH	EBX
	PUSH	ECX
;
;	SEND RR RESPONSE TO ACK OUTSTANDING FRAMES
;
	PUSH	EAX			; F BIT
	CALL	RR_OR_RNR		; GET COMMAND
	MOV	AL,LINKNR[EBX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE

	POP	ECX			; F BIT
	OR	AL,CL
;
	CALL	SETUPL2MESSAGE
	JZ SHORT HTIM143			; NO BUFFERS
;
	OR	BYTE PTR MSGORIGIN+6[EDI],80H	; SET RESPONSE
	CALL	L2QUEUEFRAME

	PUBLIC	HTIM143
HTIM143:
	MOV	AX,T3
	MOV	L2SLOTIM[EBX],AX		; SET FRAME SENT RECENTLY

	CALL	ACKMSG			; SEE IF STILL WAITING FOR ACK
	POP	ECX
	POP	EBX
	RET

	PUBLIC	L2TIMEOUT
L2TIMEOUT:
;
;	TIMER EXPIRED
;
;	    IF LINK UP (STATE 5 OR ABOVE) SEND RR/RNR AS REQUIRED
;	    IF S2, REPEAT SABM
;	    IF S3, REPEAT FRMR
;	    IF S4, REPEAT DISC
;
;
	INC	L2TIMEOUTS[EBP]		; FOR STATS	

	CMP	L2STATE[EBX],1		; NO IDLE STATE
	JLE	HTIMRET

	CMP	L2STATE[EBX],4
	JA	HTIM30			; LINK UP
	JE SHORT HTIM05			; SEND DISC AGAIN

	CMP	L2STATE[EBX],2
	JE SHORT HTIM10			; SEND SABM AGAIN
;
;	FRMR
;
	INC	L2RETRIES[EBX]
	MOV	AL,PORTN2[EBP]
	CMP	L2RETRIES[EBX],AL
	JL SHORT HTIM60
;
;	RETRIED N2 TIMES - RESET LINK
;
	MOV	L2RETRIES[EBX],0
	MOV	L2STATE[EBX],2

	JMP	SENDSABM

	PUBLIC	HTIM60
HTIM60:
;
	CALL	SENDFRMR
	PUBLIC	HTIMRET
HTIMRET:
	RET

	PUBLIC	HTIM05
HTIM05:
;
;	DISCONNECTING
;
	INC	L2RETRIES[EBX]
	MOV	AL,PORTN2[EBP]
	CMP	L2RETRIES[EBX],AL
	JL SHORT HTIM08
;
;	RETRIED N2 TIMES - JUST CLEAR OUT LINK
;
	PUSH	EBX
	PUSH	ECX
	CALL	CLEAROUTLINK	
	POP	ECX
	POP	EBX
	RET

	PUBLIC	HTIM08
HTIM08:
	PUSH	EBX
	PUSH	ECX
	MOV	AL,DISC OR PFBIT
	CALL	L2SENDCOMMAND
	POP	ECX
	POP	EBX

	RET

	PUBLIC	HTIM10
HTIM10:
;
;	CONNECTING
;
	INC	L2RETRIES[EBX]
	MOV	AL,PORTN2[EBP]
	CMP	L2RETRIES[EBX],AL
	JL SHORT HTIM12
;
;	RETRIED N2 TIMES - FAIL LINK
;
	PUSH	EBX
	PUSH	ECX

	CALL	CONNECTFAILED		; TELL LEVEL 4 IT FAILED
	CALL	CLEAROUTLINK	
	POP	ECX
	POP	EBX
	RET

	PUBLIC	HTIM12
HTIM12:
	PUBLIC	SENDSABM
SENDSABM:


	PUSH	EBX
	PUSH	ECX
	MOV	AL,SABM OR PFBIT
	CALL	L2SENDCOMMAND
	POP	ECX
	POP	EBX

	RET

	PUBLIC	HTIM30
HTIM30:
;
;	STATE 5 OR ABOVE
;
;	SEND RR(P) UP TO N2 TIMES
;
	INC	L2RETRIES[EBX]
	MOV	AL,PORTN2[EBP]
	CMP	L2RETRIES[EBX],AL
	JL SHORT HTIM40
;
;	RETRIED N2 TIMES - SEND A COUPLE OF DISCS AND THEN CLOSE
;
	PUSH	EBX
	PUSH	ECX
	CALL	INFORMPARTNER		; TELL OTHER END ITS GONE

	SUB	L2RETRIES[EBX],2
	MOV	L2STATE[EBX],4		; CLOSING
	POP	ECX
	POP	EBX

	CALL	HTIM08
	RET
;
	PUBLIC	HTIM40
HTIM40:
	CMP	VER1FLAG[EBX],1
	JE SHORT HTIMVER1		; VERSION 1 TIMEOUT
;
;	SEND RR/RNR/REJ(P)
;
	PUSH	EBX
	PUSH	ECX
;
;	SEND RR COMMAND - EITHER AS LINK VALIDATION POLL OR FOLLOWING TIMEOUT
;
	MOV	L2ACKREQ[EBX],0		; CLEAR ACK NEEDED

	CALL	RR_OR_RNR

;	MOV	L2STATE[EBX],5		; CANCEL REJ - ACTUALLY GOING TO 'PENDING ACK'

	MOV	AL,LINKNR[EBX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE
	OR	AL,PFBIT
	OR	L2FLAGS[EBX],POLLSENT

	CALL	L2SENDCOMMAND
	MOV	AX,T3
	MOV	L2SLOTIM[EBX],AX		; SET FRAME SENT RECENTLY

	POP	ECX
	POP	EBX
	RET

	PUBLIC	HTIMVER1
HTIMVER1:
;
;	RESET TO RESEND I FRAMES
;
	MOV	AL,LINKOWS[EBX]
	MOV	LINKNS[EBX],AL

	PUSH	ECX
	CALL	SDETX			; PREVENT FRMR (I HOPE)
	POP	ECX

	RET

	PUBLIC	SENDFRMR
SENDFRMR:

;	RESEND FRMR
;
	PUSH	EBX
	PUSH	ECX

	MOV	AL,L2TIME[EBX]
	MOV	AX,0
	MOV	L2TIMER[EBX],AX		; SET TIMER


	CALL	GETBUFF
	JZ SHORT HTIM66			; NO BUFFER - JUST PRETEND, AND SET TIMER

	PUSH	EDI
	CALL	SETUPADDRESSES		; SET UP ADDRESS STRING

	MOV	AL,FRMR
	STOSB

	MOV	AL,SDRBYTE		; MOVE REJECT C-BYTE
	STOSB

	MOV	AL,LINKNR[EBX]		; GET OUR N(R)
	ROR	AL,1			; SHIFT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	MOV	AH,AL			; SAVE IT
	MOV	AL,LINKNS[EBX]		; GET OUR N(S)
	SHL	AL,1			; SHIFT TO BITS 1-3
	OR	AL,AH			; COMBINE WITH N(S)

	STOSB
	MOV	AL,SDREJF[EBX]		; MOVE REJECT FLAGS TO IDP
	STOSB

	MOV	ECX,EDI
	POP	EDI

	SUB	ECX,EDI
	MOV	MSGLENGTH[EDI],CX	; SET LENGTH
;
	OR	BYTE PTR MSGORIGIN+6[EDI],80H	; SET RESPONSE
	CALL	L2QUEUEFRAME

	PUBLIC	HTIM66
HTIM66:
	POP	ECX
	POP	EBX

	RET

	PUBLIC	CLEAROUTLINK
CLEAROUTLINK:

	CALL	CLEARL2QUEUES		; TO RELEASE ANY BUFFERS

	MOV	EDI,EBX
	MOV	ECX,TYPE LINKTABLE
	XOR	AL,AL
	REP STOSB			; CLEAR OUT ENTRY

	RET


	PUBLIC	RESET3
RESET3:
	RET

