	PUBLIC	L2DIGIPEAT
L2DIGIPEAT:
;
;	WE MAY HAVE DISABLED DIGIPEAT ALTOGETHER, (DIGIFLAG=0),
;	OR ALLOW ALLOW ONLY UI FRAMES TO BE DIGIED (DIGIFLAG=-1)
;
	CMP	DIGIFLAG[EBP],0
	JE SHORT DISCARDIT

	OR	MSGORIGIN+6[EDI],80H	; SET HAS BEEN REPEATED
;
;	SEE IF UI FRAME
;
	PUBLIC	FINDCTRL
FINDCTRL:

	TEST	MSGORIGIN+6[EDI],1	; LAST CALL BIT?
	JNZ SHORT CHKCTRL			; YES

	ADD	EDI,7
	LOOP	FINDCTRL

	JMP	DISCARDIT		; DUFF

	PUBLIC	CHKCTRL
CHKCTRL:

	MOV	AL,MSGORIGIN+7[EDI]		; CONTROL BYTE
	AND	AL,0FFH-10H				; MASK OFF P/F BIT
	CMP	AL,3
	JNE NOT_UI_DIGI
	
;	UI FRAME. IF DIGIMASK IS NON-ZERO, SEND TO ALL PORTS SET, OTHERWISE SEND TO DIGIPORT

	CMP	DIGIMASK[EBP],0
	JE	OKTODIGI
	
	PUSH BUFFER
	push	EBP
	CALL _DigiToMultiplePorts
	
	ADD	ESP,8
	
	JMP SHORT DISCARDIT
	
	PUBLIC	NOT_UI_DIGI
NOT_UI_DIGI:
;
;	ONLY DIGI IF DIGIFLAG IS NOT -1 (UI ONLY)
	CMP	DIGIFLAG[EBP],-1
	JE SHORT DISCARDIT			; NOT UI FRAME

	PUBLIC	OKTODIGI
OKTODIGI:

 	INC	L2DIGIED[EBP]

	MOV	AL,DIGIPORT[EBP]		; CROSSBAND DIGI ENABLED?
	OR	AL,AL
	JZ SHORT NOTMAPPED		; NO PORT MAPPING DEFINED

	MOV	EDI,BUFFER
	MOV	MSGPORT[EDI],AL		; CHANGE PORT

	CALL	GETPORTTABLEENTRY
	MOV	CURRENTPORTPTR,EBX

	PUBLIC	NOTMAPPED
NOTMAPPED:
	JMP	L2QUEUEONPORT		; SEND IT

	PUBLIC	L2DIGI10
L2DIGI10:

	MOV	ADJBUFFER,EDI
;
;	GET CMD/RESP BITS
;
	MOV	EDI,BUFFER
	MOV	AH,MSGDEST+6[EDI]
	MOV	AL,MSGORIGIN+6[EDI]	; GET COMMAND/RESPONSE BITS
	TEST	AH,80H
	JNZ SHORT L2_CMD

	TEST	AL,80H
	JNZ SHORT L2_RESP			; VALID COMBINATION

	PUBLIC	L2_VER1
L2_VER1:				; MUST BE VERSION 1
	OR	MSGFLAG,VER1
	JMP SHORT CHECKFORUS

	PUBLIC	L2_RESP
L2_RESP:
	OR	MSGFLAG,RESP
	JMP SHORT CHECKFORUS

	PUBLIC	L2_CMD
L2_CMD:
	TEST	AL,80H
	JNZ SHORT L2_VER1			; BOTH SET - ASSUME VER 1

	OR	MSGFLAG,CMD		; SET COMMAND

	PUBLIC	CHECKFORUS
CHECKFORUS:
;
;	SEE IF FOR AN ACTIVE LINK SESSION
;
	MOV	EDI,ADJBUFFER		; BASE EXCLUDING DIGIS
	MOV	AL,MSGCONTROL[EDI]
	MOV	SDCBYTE,AL		; SAVE
;
;	IF A UI, THERE IS NO SESSION
;
	MOV	EDI,BUFFER
	CALL	FINDLINK		; GET LINK FOR THIS ADDRESS PAIR (IN BX)
	JNZ SHORT NOSESSION

	JMP	L2LINKACTIVE		; THERE IS AN ACTIVE SESSION

	PUBLIC	NOSESSION
NOSESSION:
;
;	NOT FOR ACTIVE LINK - SEE IF ADDRESSED TO OUR NODE ADDR
;
;	FIRST TRY PORT ADDR/ALIAS
;
	TEST	PORTBBSFLAG[EBP],1
	JNZ SHORT PORTCALLISBBS		; PORT CALL/ALIAS ARE FOR BBS

	CMP	NODE,0
	JNE SHORT USINGNODE
;
	PUBLIC	PORTCALLISBBS
PORTCALLISBBS:
;
;	NODE IS NOT ACTIVE, SO PASS CALLS TO PORTCALL/ALIAS TO BBS
;
	MOV	APPLMASK,1

	PUBLIC	USINGNODE
