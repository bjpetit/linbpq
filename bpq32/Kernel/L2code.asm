FINDLK10:
	ADD	ESI,TYPE LINKTABLE
	LOOP	FINDLK00
;
;	ENTRY NOT FOUND - BX HAS FIRST FREE ENTRY, OR ZERO IF TABLE FULL
;
	OR	AL,1			; SET NZ
	RET
	PUBLIC	FINDLKRET
FINDLKRET:
	MOV	EBX,ESI
	XOR	AL,AL
	RET

	PUBLIC	COMPARECALLS
COMPARECALLS:
;
;	COMPARE AX25 CALLSIGNS IN ESI, EDI IGNORING EXTRA BITS IN SSID
;
	MOV	ECX,6
	REP CMPSB			; COMPARE

	JNE SHORT COMPCALLRET		; DONT MATCH

	LODSB
	MOV	AH,[EDI]

	AND	AX,0001111000011110B	; MASK NON-SSID BITS
	CMP	AL,AH
	PUBLIC	COMPCALLRET
COMPCALLRET:
	RET

	PUBLIC	GETPORTWINDOW
GETPORTWINDOW:

	PUSH	EBX

	MOV	AL,LINKPORT[EBX]
	CALL	GETPORTTABLEENTRY
	MOV	AL,PORTWINDOW[EBX]

	POP	EBX
	RET
			
	PUBLIC	GETPORTPACLEN
GETPORTPACLEN:

	PUSH	EBX

	MOV	AL,LINKPORT[EBX]
	CALL	GETPORTTABLEENTRY
	MOV	AL,PORTPACLEN[EBX]

	POP	EBX
	RET
			
	PUBLIC	RR_OR_RNR
RR_OR_RNR:

	AND	L2FLAGS[EBX],NOT RNRSENT
;
;	SET UP APPROPRIATE SUPER COMMAND
;
	CMP	LINKTYPE[EBX],3
	JE SHORT CHKBUFFS		; NODE TO NODE - ONLY BUSY IF SHORT OF BUFFERS
;
;	UP OR DOWN LINK - SEE IF SESSION IS BUSY
;
	CMP	CIRCUITPOINTER[EBX],0
	JE SHORT CHKBUFFS		; NOT CONNECTED
;
;	UPLINK WITH CIRCUIT
;
	PUSH	EBX			; SAVE LINK
	MOV	EBX,CIRCUITPOINTER[EBX]	; TO CIRCUIT ENTRY

	CALL	CHECKIFBUSY		; TARGET SESSION BUSY?
	POP	EBX

	TEST	AL,L4BUSY
	JNZ SHORT SENDRNR			; BUSY

	PUBLIC	CHKBUFFS
CHKBUFFS:

	MOV	AH,RR
	CMP	QCOUNT,20
	JBE SHORT SENDRNR			; NOT ENOUGH
;
;	SEND REJ IF IN REJ STATE
;
	CMP	L2STATE[EBX],6
	JNE SHORT HTIM142			; OK

	MOV	AH,REJ
	RET

	PUBLIC	SENDRNR
SENDRNR:
	MOV	AH,RNR			; RUNNING SHORT
	OR	L2FLAGS[EBX],RNRSENT	; REMEMBER
	PUBLIC	HTIM142
HTIM142:
	RET


	PUBLIC	COUNTLINKS
COUNTLINKS:
;
;	COUNT LINKS ON PORT IN AL
;
	MOV	ESI,LINKS
	MOVZX	ECX,MAXLINKS
	MOV	AH,0
	PUBLIC	COUNTL00
COUNTL00:
	CMP	BYTE PTR [ESI],0
	JE SHORT COUNTL10		; SPARE ENTRY

	CMP	AL,LINKPORT[ESI]
	JNE SHORT COUNTL10		; WRONG PORT

	INC	AH

	PUBLIC	COUNTL10
COUNTL10:

	ADD	ESI,TYPE LINKTABLE
	LOOP	COUNTL00
;
	RET

	PUBLIC	PUT_ON_PORT_Q
PUT_ON_PORT_Q:
;
;	TIME STAMP IT
;

	push	ecx
	push	edx
	
	push	0

	call	_time

	add	esp,4
	
	pop		edx
	pop		ecx
	MOV	BUFFLEN-8[EDI],EAX

	PUSH	EBX

	CMP	TXPORT[EBX],0
	JE SHORT NORMALPORT
;
;	NEED TO SEND TO DIFERENT PORT
;
	MOV	AL,TXPORT[EBX]
	MOV	MSGPORT[EDI],AL		; CHANGE HEADER (FOR KISS TX)

	CALL	GETPORTTABLEENTRY

	PUBLIC	NORMALPORT
NORMALPORT:

	LEA	ESI,PORTTX_Q[EBX]
	CALL	_Q_ADD			; PUT ON QUEUE

	POP	EBX
	RET

	PUBLIC	MHPROC
MHPROC:
;
;	MAINTAIN MHEARD LIST
;
	CMP	PORTMHEARD[EBX],0
	JE SHORT MHRET			; NOT ENABLED
;
;	GET CONTROL BYTE
;
	PUSH	EDI
	MOV	ECX,8			; MAX DIGIS
	PUBLIC	CTRLLOOP
CTRLLOOP:
	TEST	BYTE PTR MSGCONTROL-1[EDI],1
	JNZ SHORT CTRLFOUND

	ADD	EDI,7
	LOOP	CTRLLOOP
;
;	INVALID FRAME
;
	POP	EDI
	PUBLIC	MHRET
MHRET:
	RET

	PUBLIC	CTRLFOUND
CTRLFOUND:

	MOV	AL,MSGCONTROL[EDI]
	POP	EDI
;
	TEST	AL,1			; I FRAME
	JZ SHORT MHIFRAME

	AND	AL,NOT PFBIT		; CLEAR P/F
	CMP	AL,3			; UI
	JNE SHORT MHRET			; ONLY USE I AND UI FRAMES
;
	PUBLIC	MHIFRAME
MHIFRAME:
;
;	LOOK FOR CALL IN LIST
;
	MOV	ESI,PORTMHEARD[EBX]
	LEA	EDI,MSGORIGIN[EDI]
	MOV	ECX,20			; MAX ENTRIES

	PUBLIC	MHF000
MHF000:
	CMP	BYTE PTR [ESI],0
	JE SHORT MHF120			; EMPTY ENTRY FOUND - USE IT

	PUBLIC	MHF005
MHF005:

	PUSH	ECX
	PUSH	EDI
	PUSH	ESI

	CALL	COMPARECALLS		; COMPARE WITH LIST

	POP	ESI
	POP	EDI
	POP	ECX

	JE SHORT MHF120			; FOUND

	ADD	ESI,TYPE MHSTRUC

	LOOP	MHF000
;
;	TABLE FULL AND ENTRY NOT FOUND - MOVE DOWN ONE, AND ADD TO TOP
;
	MOV	ECX,(19*TYPE MHSTRUC)
	JMP SHORT MHF130


	PUBLIC	MHF120
MHF120:
;
;	ENTRY FOUND - MOVE EARLIER ONES DOWN, AND PUT ON FRONT
;
	MOV	EAX,20
	SUB	EAX,ECX
	JZ SHORT MHF140			; FIRST ENTRY

	MUL	MHLEN
	MOV	ECX,EAX

	PUBLIC	MHF130
MHF130:

	MOV	ESI,PORTMHEARD[EBX]
	ADD	ESI,(TYPE MHSTRUC)-1
	ADD	ESI,ECX			; TO END + LEN-1

	MOV	EDI,ESI
	SUB	ESI,TYPE MHSTRUC		; TO END
	STD
	REP MOVSB
	CLD


	PUBLIC	MHF140
MHF140:
	
	MOV	EDI,PORTMHEARD[EBX]
	MOV	ESI,BUFFER
	LEA	ESI,MSGORIGIN[ESI]
	MOV	DL,6[ESI]		; SAVE LAST BYTE OF CALL (FOR DIGI CHK) 

	MOV	ECX,7
	REP MOVSB			; COPY CALL

	SUB	EDI,7
;
;	GET TIME FROM BIOS 

	push	edx
	
	push	0
	call	_time

	add	esp,4
	
	pop		edx

	MOV	DWORD PTR MHTIME[EDI],EAX

	MOV	MHDIGI[EDI],20H		; ASSUME NOT DIGI-ED

	TEST	DL,1
	JNZ SHORT MHRET1			; LAST BIT SET, SO NO DIGIS
;
;	THERE ARE DIGIS - IF DIGI-ED BIT IS SET ON FIRST, WE ARE NOT HEARING
;	  THIS PACKET DIRECT
;
	MOV	ESI,BUFFER
	TEST	BYTE PTR MSGORIGIN+7+6[ESI],80H
	
	JZ SHORT MHRET1			; NOT DIGIED - MUST HAVE HEARD IT DIRECT

	MOV	MHDIGI[EDI],'*'

	PUBLIC	MHRET1
MHRET1:

	RET


