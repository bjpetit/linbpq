	MOV	MSGCONTROL+1[EDI],AL
	MOV	AL,LINKNR[EBX]		; GET OUR N(R)
	ROR	AL,1			; SHIFT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	MOV	AH,AL			; SAVE IT
	MOV	AL,LINKNS[EBX]		; GET OUR N(S)
	SHL	AL,1			; SHIFT TO BITS 1-3
	OR	AL,AH			; COMBINE WITH N(S)
	MOV	MSGCONTROL+2[EDI],AL	; STORE
	MOV	AL,SDREJF[EBX]		; MOVE REJECT FLAGS TO IDP
	MOV	MSGCONTROL+3[EDI],AL

	LEA	ECX,MSGCONTROL+4[EDI]	; END OF MESSAGE
	MOV	EDI,BUFFER
	SUB	ECX,EDI			; CALCULATE LENGTH
 	MOV	MSGLENGTH[EDI],CX	; SET UP BYTE COUNT

