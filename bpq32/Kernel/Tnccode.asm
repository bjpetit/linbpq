	POP	ESI

SENDUILOOP:

	CMP	BYTE PTR [ESI],0
	JE SHORT SENDUIEND		; END OF DIGIS

	MOV	ECX,7
	REP MOVSB

	JMP	SENDUILOOP

SENDUIEND:

	OR	BYTE PTR -1[EDI],1	; SET LAST CALL

	MOV	BYTE PTR [EDI],3		; UI
	INC	EDI

	POP	ESI			; MESSAGE
	MOVZX	ECX,MSGLENGTH[ESI]
	ADD	ESI,7
	SUB	ECX,7

	REP MOVSB			; COPY IN MESSAGE

	MOV	ECX,EDI
	MOV	EDI,EAX			; BUFFER
	SUB	ECX,EDI

	MOV	MSGLENGTH[EDI],CX

	PUSH	EBX

	MOV	EBX,EDX			; PORT TABLE ENTRY
	MOV	AL,PORTNUMBER[EBX]
	MOV	MSGPORT[EDI],AL

	MOV	ESI,OFFSET32 IDMSG_Q	; Q FOR SENDING LATER (AT INTERVALS)
	CALL	Q_ADD

	POP	EBX
	RET

SKIP_UI:

	POP	ESI
	RET

COUNT_QUEUED_FRAMES:
;
;	COUNT NUMBER OF FRAMES QUEUED ON A SESSION (IN ESI)
;	RETURN TOTAL IN AL, NUMBER QUEUED ABOVE LINK IN AH (FOR HOST MODE TESTS)
;
	PUSH	EBX

	TEST	L4CIRCUITTYPE[ESI],BPQHOST

	JZ SHORT CQF00

	LEA	EBX,L4TX_Q[ESI]
	CALL	COUNT_Q

	MOV	AH,AL
	POP	EBX
	RET

CQF00:
	TEST	L4CIRCUITTYPE[ESI],SESSION
	JZ SHORT CQF10			; MUST BE L2 SESSION
;
;	L4 SESSION - GET NUMBER UNACKED, AND ADD NUMBER ON TX QUEUE
;
	LEA	EBX,L4TX_Q[ESI]
	CALL	COUNT_Q
	MOV	AH,AL

	MOV	AL,TXSEQNO[ESI]
	SUB	AL,L4WS[ESI]		; LAST FRAME ACKED - GIVES NUMBER OUTSTANING
;
	ADD	AL,AH
	POP	EBX
	RET

CQF10:
;
;	L2 CIRCUIT
;
	LEA	EBX,L4TX_Q[ESI]
	CALL	COUNT_Q
	MOV	AH,AL			; NUMBER ON SESSION

	MOV	EBX,L4TARGET[ESI]		; TO LINK

	CALL	COUNT_AT_L2

	POP	EBX
	RET
 
COUNT_AT_L2:
;
;	COUNTS FRAMES QUEUED ON AN L2 SESSION (IN BX)
;
	CMP	EBX,0			; NO SESSION??
	JE	C_L2_RET
	
	PUSH	EBX

	LEA	EBX,TX_Q[EBX]

	CALL	COUNT_Q
	ADD	AH,AL			; NUMBER ON L2 TX QUEUE

	POP	EBX			; LINK
;
;	COUNT FRAMES IN TSLOTS
;
	PUSH	ECX
	MOV		AL,0
	MOV		ECX,8

TSLOOP:

	CMP	FRAMES[EBX],0		; FRAME THERE?
	JE SHORT TSLOOP2			; NO

	INC	AL

TSLOOP2:
	ADD	EBX,4
	LOOP	TSLOOP

	POP	ECX

C_L2_RET:

	ADD	AL,AH			; TOTAL IN AL, NUMBER ABOVE LINK IN AH


	RET


COUNT_Q:
;
;	SEE HOW MANY BUFFERS ATTACHED TO Q HEADER IN BX
;
 	XOR	AL,AL

COUNT_Q_LOOP:

	CMP	DWORD PTR [EBX],0
	JE SHORT COUNT_RET

	INC	AL
	MOV	EBX,[EBX]		; FOLLOW CHAIN
	JNZ SHORT COUNT_Q_LOOP		; JUST IN CASE!

COUNT_RET:

	RET



