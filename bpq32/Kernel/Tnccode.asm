	POP	ESI
	JZ SHORT SKIP_UI			; NO BUFFERS

	MOV	EAX,EDI			; BUFFER

	LEA	EDI,MSGDEST[EDI]
	MOV	ECX,7
	REP MOVSB

	OR	BYTE PTR -1[EDI],0C0H	; SET COMMAND BITS

 	PUSH	ESI			; SAVE POSSIBLE DIGIS

