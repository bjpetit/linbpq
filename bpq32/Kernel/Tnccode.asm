TNCTIMERPROC:

;
;	CALLED AT 3HZ
;
;
;	CHECK LENGTH OF HOST MODE RAW DATA QUEUES
;
	MOV	ECX,BPQHOSTSTREAMS
	MOV	EBX,OFFSET32 BPQHOSTVECTOR
;
;	VECTOR FORMAT IS:
;
;	0-3  4 BYTES - ADDRESS OF SESSION ENTRY
;	  4  1 BYTE  - STATE CHANGE FLAGS
;	  5  1 BYTE  - APPLICATION MASK
;	  6  1 BYTE  - APPLICATION FLAGS - TOP BIT ENABLES MONITORING
;	7-10 4 BYTES - RAW FRAME QUEUE
;

BPQTRACELOOP:

	TEST	HOSTAPPLFLAGS[EBX],80H
	JZ SHORT NOTRACE			; NOT ENABLED
;
	CMP	HOSTTRACEQ[EBX],0
	JE SHORT NOTRACE			; NONE QUEUED

	PUSH	EBX
	LEA	EBX,HOSTTRACEQ[EBX]		; TO HEADER
	CALL	COUNT_Q
	POP	EBX

	CMP	AL,100					; MAX LENGTH
	JB SHORT NOTRACE

	LEA	ESI,HOSTTRACEQ[EBX]
	CALL	Q_REM
	JZ SHORT NOTRACE			; WOT?

	CALL	RELBUFF

NOTRACE:

	ADD	EBX,VECTORLENGTH
	LOOP	BPQTRACELOOP

	RET				

SENDIDMSG:

	PUSH	EBX

	MOV	EBX,PORTTABLE
	MOVZX	ECX,NUMBEROFPORTS

SENDID10:

	MOV	ESI,IDMSG
	PUSH	ECX
	CALL	SENDIDMSG00
	POP	ECX
	MOV	EBX,PORTPOINTER[EBX]

	LOOP	SENDID10

	POP	EBX
	RET

SENDIDMSG00:

	PUSH	ESI			; SAVE MESSAGE ADDR
	CALL	GETBUFF
	POP	ESI
	JZ SHORT SENDIDRET

	PUSH	EDI
	MOVZX	ECX,MSGLENGTH[ESI]
	REP MOVSB

	POP	EDI

	MOV	AL,PORTNUMBER[EBX]
	MOV	MSGPORT[EDI],AL
;
;	IF PORT HAS A CALLSIGN DEFINED, SEND THAT INSTEAD
;
	CMP	PORTCALL[EBX],20H
	JBE SHORT NOPORTCALL

	PUSH	ESI
	PUSH	EDI

	LEA	ESI,PORTCALL[EBX]
	LEA	EDI,14[EDI]		; COPY TO ID MESSAGE
	MOV	ECX,7
	REP MOVSB

	OR	BYTE PTR -1[EDI],1	; SET END OF CALL BIT
 	POP	EDI
	POP	ESI

NOPORTCALL:

	MOV	ESI,OFFSET32 IDMSG_Q	; Q FOR SENDING LATER (AT INTERVALS)
	CALL	Q_ADD

SENDIDRET:
	RET


SENDBTMSG:

	MOV	ESI,OFFSET32 BTHDDR

	MOV	EDX,PORTTABLE		; FIRST HDLC LINK 
	MOVZX	ECX,NUMBEROFPORTS

SENDBT10:

	PUSH	ESI			; MESSAGE
	PUSH	ECX
	CALL	SENDBTMSG00
	POP	ECX

	MOV	ESI,EDX
	MOV	EDX,PORTPOINTER[ESI]

	POP	ESI

	LOOP	SENDBT10

	RET

SENDBTMSG00:

	PUSH	ESI			; SAVE MESSAGE
;
	MOV	ESI,EDX
	MOV	ESI,PORTUNPROTO[ESI]
	CMP	ESI,0
	JE	SKIP_UI			; IGNORE

	PUSH	ESI			; SAVE UNPROTO ADDR
	CALL	GETBUFF
	POP	ESI
	JZ	SKIP_UI			; NO BUFFERS

	MOV	EAX,EDI			; BUFFER

	LEA	EDI,MSGDEST[EDI]
	MOV	ECX,7
	REP MOVSB

	OR	BYTE PTR -1[EDI],0C0H	; SET COMMAND BITS

 	PUSH	ESI			; SAVE POSSIBLE DIGIS

;	Send from BBSCALL unless PORTBCALL defined

	MOV	ESI,OFFSET32 APPLCALLTABLE
	LEA	ESI,APPLCALL[ESI]
		
;	IF PORT HAS A BCALL DEFINED, SEND THAT INSTEAD
;
	CMP	PORTBCALL[EDX],20H
	JBE SHORT NOBCALL

	LEA	ESI,PORTBCALL[EDX]

NOBCALL:

	MOV	ECX,7
	REP MOVSB

	POP	ESI

@@:

	CMP	BYTE PTR [ESI],0
	JE SHORT @F				; END OF DIGIS

	MOV	ECX,7
	REP MOVSB

	JMP	SHORT @B

@@:

	OR	BYTE PTR -1[EDI],1	; SET LAST CALL

	MOV	BYTE PTR [EDI],3		; UI
	INC	EDI

	POP	ESI			; MESSAGE
	MOVZX	ECX,MSGLENGTH[ESI]
	ADD	ESI,7
	SUB	ECX,7

	REP MOVSB			; COPY IN MESSAGE

	MOV	ECX,EDI
	MOV	EDI,EAX			; BUFFER
	SUB	ECX,EDI

	MOV	MSGLENGTH[EDI],CX

	PUSH	EBX

	MOV	EBX,EDX			; PORT TABLE ENTRY
	MOV	AL,PORTNUMBER[EBX]
	MOV	MSGPORT[EDI],AL

	MOV	ESI,OFFSET32 IDMSG_Q	; Q FOR SENDING LATER (AT INTERVALS)
	CALL	Q_ADD

	POP	EBX
	RET


SENDUIMESSAGE:

