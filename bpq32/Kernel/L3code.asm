	MOV	AL,OBSMIN		; MINIMUM TO BROADCAST
	CMP	ROUT1_OBSCOUNT[EBX],AL
	JAE SHORT SENDNODE44		; SEND

;	TEST	DEST_STATE[EBX],80H
;	JNZ SHORT SENDNODE44		; LOCKED DESTINATION - INCLUDE IT

IGNORENODE:
;
;	IGNORE IT
;
	ADD	EBX,TYPE DEST_LIST
SENDNODE42_J:
	JMP	SENDNODE42

SENDNODE44:

	LEA	ESI,DEST_CALL[EBX]

	IF	BLACKBITS

	CALL	CHECKBLACKLIST		; RETURN Z IF NODE IS IN LIST

	JZ SHORT IGNORENODE		; DONT BROADCAST

	ENDIF

	PUSH	ECX
	MOV	ECX,13
	REP MOVSB			; CALL AND ALIAS

	MOV	ESI,ROUT1_NEIGHBOUR[EBX]
	CMP	ESI,0
	JNE SHORT SENDNODE45		; VALID POINTER
;
;	DUMMY POINTER IN BBS ENTRY - PUT IN OUR CALL
;
	MOV	ESI,OFFSET32 _MYCALL

SENDNODE45:

	MOV	AH,NEIGHBOUR_PORT[ESI]	; GET PORT OF BEST NEIGHBOUR

 	MOV	ECX,7
	REP MOVSB			; PUT IN CALL

	MOV	AL,100
	CMP	AH,CURRENTPORTNO
	JNE SHORT NOTSAMEPORT
;
;	BEST NEIGHBOUR IS ON CURRENT PORT - REDUCE QUALITY BY QUAL_ADJUST
;
	MOV	ESI,CURRENTPORT
	SUB	AL,QUAL_ADJUST[ESI]	; PERCENTAGE REDUCTION

NOTSAMEPORT:

	MUL	ROUT1_QUALITY[EBX]
	DIV	B100

	STOSB
	POP	ECX

	ADD	EBX,TYPE DEST_LIST
	LOOP	SENDNODE42_J
;
;	CURRENT MESSAGE FULL, SEND IT AND START ANOTHER
;
	MOV	CURRENTNODE,EBX

SENDNODE48:

	MOV	ECX,EDI
	POP	EDI
	SUB	ECX,EDI

	MOV	MSGLENGTH[EDI],CX

	POP	EBX

	CMP	TXPORT[EBX],0
	JNE SHORT SKIPTX1			; DONT SEND IF SHARED TX

	CALL	PUT_ON_PORT_Q

	JMP SHORT SENDNODE48A

SKIPTX1:

	CALL	RELBUFF

SENDNODE48A:
;
;	SEE IF MORE TO GO
;
	CMP	CURRENTNODE,0
	JNE SHORT STILLMORE

SENDNODEXX:
;
;	FINISHED THIS PORT - SEE IF MORE
;
	DEC	PORTSLEFT
	JNZ SHORT ANOTHERPORT
;
	MOV	NODESINPROGRESS,0	; FINISHED
	RET

ANOTHERPORT:

	MOV	EBX,CURRENTPORT
	MOV	EBX,PORTPOINTER[EBX]
	MOV	CURRENTPORT,EBX

STILLMORE:

	MOV	NODESINPROGRESS,1	; RESTART TIMER
	RET


SETUPNODESMSG:

	MOV	AL,PORTNUMBER[EBX]
	MOV	MSGPORT[EDI],AL

	LEA	EDI,MSGDEST[EDI]
	MOV	ESI,OFFSET32 NODECALL
	MOV	ECX,7
	REP MOVSB

	MOV	ESI,OFFSET32 _MYCALL
	MOV	ECX,7
	REP MOVSB
	OR	BYTE PTR -1[EDI],01100001B ; SET CMD END AND RESERVED BITS

	MOV	AL,UI
	STOSB				; CONTROL

	MOV	AL,0CFH
	STOSB				; PID

	MOV	AL,0FFH
	STOSB				; FLAG

	MOV	ECX,6
	REP MOVSB			; ALIAS FOLLOWS CALL

	RET

