L3TIMERPROC:
;
;	CHECK FOR EXCESSIVE BUFFERS QUEUED AT LINK LEVEL
;
	CMP	QCOUNT,100
	JA SHORT COUNT_OK

	MOV	ESI,LINKS
	MOVZX	ECX,MAXLINKS

CHKLINK00:

	CMP	LINKTYPE[ESI],3
	JNE SHORT CHKLINK10		; NOT INTERNODE

	MOV	EBX,ESI
;
	MOV	AH,0
	CALL	COUNT_AT_L2		; SEE HOW MANY QUEUED

	CMP	AH,50
	JB SHORT CHKLINK10		; OK
;
;	DESTROY THE SESSION
;
	MOV	EBX,ESI
	CALL	L3LINKCLOSED		; REPORT TO LEVEL 3
	CALL	CLEAROUTLINK

	JMP SHORT COUNT_OK		; CANT BE ANY MORE OVERLOADED LINKS

CHKLINK10:

	ADD	ESI,TYPE LINKTABLE
	LOOP	CHKLINK00
;
COUNT_OK:

	ADD	STATSTIME,1

	CMP	IDTIMER,0
	JE SHORT NOIDMSG			; SUPPRESSED

	DEC	IDTIMER
	JNZ SHORT NOIDMSG

	MOV	AX,IDINTERVAL
	MOV	IDTIMER,AX

	CALL	SENDIDMSG

NOIDMSG:
;
;	CHECK FOR BEACON
;
	CMP	BTTIMER,0
	JE SHORT NOBTMSG			; SUPPRESSED

	DEC	BTTIMER
	JNZ SHORT NOBTMSG

	MOV	AX,BTINTERVAL
	MOV	BTTIMER,AX

	MOV	EBX,TNCTABLE		; IN CASE SENDING MAIL BEACON
	CALL	SENDBTMSG


NOBTMSG:
;
;	CHECK FOR NODES BROADCAST
;
	CMP	L3TIMER,0
	JE SHORT L3TIMRET		; TIMER UPDATE AND BROADCAST SUPPRESSED

	DEC	L3TIMER
	JNZ SHORT L3TIMRET
;
;	UPDATE DEST LIST AND SEND 'NODES' MESSAGE
;
	MOV	AX,L3INTERVAL
	MOV	L3TIMER,AX

	CALL	UPDATEDESTLIST
	CALL	SENDNODESMSG

;
;	TIDY ROUTES
;
	MOV	ESI,NEIGHBOURS
	MOVZX	ECX,MAXNEIGHBOURS
CMDR10:
	PUSH	ECX

	TEST	NEIGHBOUR_FLAG[ESI],1
	JNZ SHORT CMDR88			; LOCKED

	CMP	NEIGHBOUR_LINK[ESI],0
	JNE SHORT CMDR88			; HAS AN ACTIVE SESSION

	CALL	COUNTNODES		; NODES USING THIS DESTINATION
;
;	IF NUMBER USING ROUTE IS ZERO, DELETE IT
;
	OR	AL,AL
	JNZ SHORT CMDR88

	PUSH	EDI
	MOV	EDI,ESI
	MOV	ECX,TYPE NEIGHBOUR_LIST
	REP STOSB

	POP	EDI

CMDR88:

	POP	ECX
	ADD	ESI,TYPE NEIGHBOUR_LIST

	LOOP	CMDR10

L3TIMRET:

	RET

L3FASTTIMER:
;
;	CALLED ONCE PER SECOND - USED ONLY TO SEND NEXT PART OF A NODES OR
;	ID MESSAGE SEQUENCE
;
	CALL	_INP3TIMER
	
	DEC	L3_10SECS
	JNZ SHORT L3TIMRET
	
	MOV	L3_10SECS,10
	
	CMP	IDMSG_Q,0		; ID/BEACON TO SEND?
	JE SHORT L3FT00			; NO

	MOV	ESI,OFFSET32 IDMSG_Q
	CALL	Q_REM

	JZ SHORT L3FT00			; ??

	MOV	AL,MSGPORT[EDI]

	CALL	GETPORTTABLEENTRY

	CMP	TXPORT[EBX],0
	JNE SHORT SKIPTX			; DONT SEND IF SHARED TX

	CALL	PUT_ON_PORT_Q

	JMP	L3TIMRET

SKIPTX:

	CALL	RELBUFF

	JMP	L3TIMRET

L3FT00:

	CMP	NODESINPROGRESS,0
	JE SHORT L3TIMRET

	CALL	SENDNEXTNODESFRAGMENT

	RET

L3BG:
;
;	TRANSFER MESSAGES FROM DEST TO LINK
;
	MOV	EBX,DESTS
	MOVZX	ECX,_MAXDESTS
L3BG60:
	CMP	DEST_CALL[EBX],0
	JE SHORT L3BG90			; SPARE

L3BG61:
	LEA	ESI,DEST_Q[EBX]		; FRAMES TO SEND?
	CMP	WORD PTR [ESI],0
	JE SHORT L3BG90			; NO

	PUSH	EBX
	PUSH	ECX
;
	MOVZX	EAX,DEST_ROUTE[EBX]
	OR	AL,AL
	JZ SHORT L3BG80

	DEC	AL
	MUL	BYTE PTR ROUTEVECLEN

	ADD	EBX,EAX			; TO ROUTE ENTRY
	MOV	EBX,ROUT1_NEIGHBOUR[EBX]	; GET NEIGHBOUR ENTRY
	OR	EBX,EBX
	JZ SHORT L3BG80			; NO NEIGHBOUR?

	MOV	EAX,EBX			; SAVE NEIGHBOUR ADDR

	MOV	EBX,NEIGHBOUR_LINK[EBX]	; GET LINK TABLE ENTRY
	OR	EBX,EBX
	JZ SHORT L3BG80			; ???? INACTIVE ????

	CMP	L2STATE[EBX],0
	JA SHORT L3BG70
;
;	LINK ENTRY IS INVALID - IT PROBABLY HAS BEEN 'ZAPPED', SO CANCEL IT
;
	MOV	EBX,EAX
	MOV	NEIGHBOUR_LINK[EBX],0

	JMP SHORT L3BG80

L3BG70:
	CMP	L2STATE[EBX],5		; LINK UP?
	JL SHORT L3BG85			; NO, SO WAIT FOR IT

	PUSH	EBX			; SAVE LINK
	MOV	EBX,EAX			; NEIGHBOUR
;
;	COUNT FRAMES TO NEIGHBOUR
;
	ADD	NBOUR_IFRAMES[EBX],1

	POP	EBX

	CALL	Q_REM			; GET THE FRAME

 	LEA	ESI,TX_Q[EBX]
	CALL	_Q_ADD			; QUEUE ON LINK

	POP	ECX
	POP	EBX

	JMP	L3BG61			; SEE IF MORE

L3BG80:
	POP	ECX
	POP	EBX			; RECOVER DEST TABLE ADDR
	PUSH	EBX
	PUSH	ECX
	CALL	ACTIVATE_DEST
L3BG85:
	POP	ECX
	POP	EBX

L3BG90:
	ADD	EBX,TYPE DEST_LIST
	LOOP	L3BG60_J
L3BG95:
	RET

L3BG60_J:

	JMP	L3BG60

ACTIVATE_DEST:

	PUSH	EBX

	MOV	AL,DEST_ROUTE[EBX]
	OR	AL,AL
	JNZ SHORT DESTISOK		; ALREADY HAVE A SELECTED ROUTE
;
	MOV	DEST_ROUTE[EBX],1	; TRY TO ACTIVATE FIRST
DESTISOK:

	MOVZX	EAX,DEST_ROUTE[EBX]
	DEC	AL
	MUL	BYTE PTR ROUTEVECLEN

	ADD	EBX,EAX			; TO ROUTE ENTRY
	MOV	EBX,ROUT1_NEIGHBOUR[EBX]	; GET NEIGHBOUR ENTRY
	OR	EBX,EBX
	JZ SHORT NOROUTETODEST

	MOV	EAX,NEIGHBOUR_LINK[EBX]	; GET LINK TABLE ENTRY
	OR	EAX,EAX
	JZ SHORT ACTIVATENEIGHBOUR	; NEED TO SET UP L2 SESSION TO NEIGHBOUR

	POP	EBX			; ? MUST BE WAITING FOR LINK TO COME UP
	RET

ACTIVATENEIGHBOUR:
;
;	SET UP LINK TABLE ENTRY
;
	CALL	L2SETUPCROSSLINK	; START SESSION TO NODE IN [EBX]
	POP	EBX
	RET

NOROUTETODEST:
;
;	CURRENT NEIGHBOUR NOT DEFINED - RESET TO USE FIRST
;
	POP	EBX
	MOV	DEST_ROUTE[EBX],0
	RET

L3CONNECTFAILED:
;
;	L2 LINK SETUP HAS FAILED - SEE IF ANOTHER NEIGHBOUR CAN BE USED
;
	MOV	ESI,NEIGHBOUR[EBX]		; TO NEIGHBOUR
	CMP	ESI,0
	JE SHORT L3CFRET				; NOTHING ???

	MOV	NEIGHBOUR_LINK[ESI],0		; CLEAR IT
	PUSH	EBX
	CALL	L3TRYNEXTDEST			; RESET ASSOCIATED DEST ENTRIES
	POP	EBX
L3CFRET:
	RET


L3TRYNEXTDEST:
;
;	FIND ANY DESINATIONS WITH [ESI] AS ACTIVE NEIGHBOUR, AND
;	SET NEXT BEST NEIGHBOUR (IF ANY) ACTIVE
;
	MOV	EBX,DESTS
	MOVZX	ECX,_MAXDESTS
;
L3CFR010:
	CMP	DEST_ROUTE[EBX],0
	JE SHORT L3CFR090		; NO ACTIVE ROUTE
;
	PUSH	EBX

	MOVZX	EAX,DEST_ROUTE[EBX]
	DEC	AL
	MUL	BYTE PTR ROUTEVECLEN
	ADD	EBX,EAX			; TO ACTIVE ROUTE ENTRY
	
	CMP	ROUT1_NEIGHBOUR[EBX],ESI	; GET NEIGHBOUR ENTRY
	JNE SHORT L3CFR080		; NOT THIS DEST
;
;	NEIGHBOUR HAS FAILED - DECREMENT OBSCOUNT
;	AND TRY TO ACTIVATE ANOTHER (IF ANY)
;
	CMP	ROUT1_OBSCOUNT[EBX],0
	JE SHORT L3CFR020		; ALREADY ZERO - WILL BE DELETED BY NEXT NODES UPDATE

	TEST	ROUT1_OBSCOUNT[EBX],80H
	JNZ SHORT L3CFR020		; LOCKED

	DEC	ROUT1_OBSCOUNT[EBX]
	JNZ SHORT L3CFR020		; STILL OK
;
;	(ROUTE HAS EXPIRED - WE SHOULD CLEAR IT, AND MOVE OTHERS (IF ANY) UP)
;
L3CFR020:
	POP	EBX
;
;	REMOVE FIRST MESSAGE FROM DEST_Q. L4 WILL RETRY - IF IT IS LEFT HERE
;	WE WILL TRY TO ACTIVATE THE DESTINATION FOR EVER
;
	PUSH	ESI
	LEA	ESI,DEST_Q[EBX]
	CALL	Q_REM
	JZ SHORT L3CFR025		; NONE??

	CALL	RELBUFF
L3CFR025:
	POP	ESI
	INC	DEST_ROUTE[EBX]		; TO NEXT 
	CMP	DEST_ROUTE[EBX],4
	JNE SHORT L3CFR040
;
	MOV	DEST_ROUTE[EBX],1	; TRY TO ACTIVATE FIRST
L3CFR040:
	JMP SHORT L3CFR090

L3CFR080:
	POP	EBX
L3CFR090:
	ADD	EBX,TYPE DEST_LIST
	LOOP	L3CFR010

	RET

CHECKNEIGHBOUR:
;
;	MESSAGE RECEIVED ON LINK WITH NO DESTINATION - SET ONE UP
;
	LEA	ESI,LINKCALL[EBX]
	CALL	_FINDNEIGHBOUR
	JZ SHORT L3CONN07		; IN LIST - OK

	CMP	EBX,0
	JE SHORT L3CONN08		; TABLE FULL??
;
;	FIRST MAKE SURE WE ARE ALLOWING NETWORK ACTIVITY ON THIS PORT
;
	MOV	AL,SAVEPORT
	CALL	GETPORTQUALITY
	CMP	AL,0
	JNE SHORT QUALOK
;
;	NOT A NETWORKING PORT
;
	RET

QUALOK:

	MOV	NEIGHBOUR_QUAL[EBX],AL	; QUALITY

	MOV	ECX,7
	MOV	EDI,EBX
	REP MOVSB			; PUT IN CALL

	MOV	AL,SAVEPORT
	MOV	NEIGHBOUR_PORT[EBX],AL

L3CONN07:
;
;	SET THIS AS ACTIVE LINK IF NONE PRESENT
;
	CMP	NEIGHBOUR_LINK[EBX],0
	JNE SHORT L3CONN08

	MOV	EAX,SAVELINK
	MOV	NEIGHBOUR_LINK[EBX],EAX
	MOV	AL,SAVEPORT
	MOV	NEIGHBOUR_PORT[EBX],AL

L3CONN08:

	MOV	EAX,EBX
	MOV	EBX,SAVELINK
	MOV	NEIGHBOUR[EBX],EAX	; SET LINK - NEIGHBOUR
	RET



CHECKL3TABLES:
;
;	CHECK THAT FAR NODE IS IN 'NODES'.
;	BX POINTS TO L4 TABLE ENTRY
;	RETURNS POINTER TO NEIGHBOUR ENTRY IN AX
;
	PUSH	EBX

	MOV	EBX,SAVELINK
	MOV	EBX,NEIGHBOUR[EBX]	; NEIGHBOUR ENTRY

	MOV	SAVENBOUR,EBX

	MOV	ESI,BUFFER
	LEA	ESI,L3SRCE[ESI]

	CALL	_FINDDESTINATION
	JZ SHORT L3CONN09		; OK
;
	CMP	EBX,0
	JNE SHORT CAN_ADD

DONT_ADD:

	MOV	EAX,0
	POP	EBX

	RET				; TABLES FULL

CAN_ADD:
;
;	ADD DESTINATION VIA NEIGHBOUR, UNLESS ON BLACK LIST
;
	IF	BLACKBITS

	PUSH	ESI
	CALL	CHECKBLACKLIST
	POP	ESI

	JZ SHORT DONT_ADD
;
	ENDIF

	MOV	ECX,7
	LEA	EDI,DEST_CALL[EBX]
	REP MOVSB			; PUT IN CALL

L3CONN09:
;
;	MAKE SURE NEIGHBOUR IS DEFINED FOR DESTINATION
;
;	IF NODE - NEIGHBOUR, THEN CAN USE NEIGHBOUR QUALITY,
;	   OTHERWISE WE DONT KNOW ROUTE, SO MUST SET QUAL TO 0
;
	MOV	ROUTEQUAL,0		; DONT KNOW ROUTING, SO SET QUALITY TO ZERO

	MOV	EAX,SAVENBOUR
	CALL	PROCROUTES		; ADD NEIGHBOUR  IF NOT PRESENT
;
	CMP	DEST_ROUTE[EBX],0	; IS THERE AN ACTIVE ROUTE?
	JNE SHORT L3CONN20		; DEST IS ACTIVE
;
;	MAKE CURRENT NEIGHBOUR ACTIVE
;
	MOV	EAX,SAVENBOUR
	INC	DEST_ROUTE[EBX]
	CMP	ROUT1_NEIGHBOUR[EBX],EAX
	JE SHORT L3CONN20		; FOUND REQUIRED ENTRY
;
	INC	DEST_ROUTE[EBX]
	CMP	ROUT2_NEIGHBOUR[EBX],EAX
	JE SHORT L3CONN20		; FOUND REQUIRED ENTRY
;
	INC	DEST_ROUTE[EBX]
	CMP	ROUT3_NEIGHBOUR[EBX],EAX
	JE SHORT L3CONN20		; FOUND REQUIRED ENTRY
;
;	CURRENT NEIGHBOUR ISNT IN DEST LIST - SET TO USE BEST
;
	MOV	DEST_ROUTE[EBX],1

L3CONN20:
;
;	REFRESH OBS COUNT
;
	PUSH	EBX			; SAVE DEST TABLE ENTRY

	MOVZX	EAX,DEST_ROUTE[EBX]
	OR	AL,AL
	JZ SHORT REFROUTY		; NONE ACTIVE???

	DEC	AL
	MUL	BYTE PTR ROUTEVECLEN

	ADD	EBX,EAX			; TO ROUTE ENTRY
	TEST	ROUT1_OBSCOUNT[EBX],80H
	JNZ SHORT REFROUTY		; LOCKED

	MOV	AL,OBSINIT
	MOV	ROUT1_OBSCOUNT[EBX],AL	; SET OBSOLESCENCE COUNT

REFROUTY:

	POP	EAX			; DEST TABLE ENTRY
	POP	EBX
	RET


PROCESSNODEMESSAGE:
;
;	PROCESS A NET/ROM 'NODES' MESSAGE
;
;	    UPDATE NEIGHBOURS LIST WITH ORIGINATING CALL, AND
;	    DESTINATION LIST WITH ANY PRACTICAL ROUTES
;
;
;	    INCOMING MESSAGE IS IN [EDI]
;
;
	MOV	AL,SAVEPORT		; PORT MESSAGE CAME IN ON
	CALL	GETPORTQUALITY
	OR	AL,AL
	JZ SHORT PROCNODE02		; PORT QUALITY=0, SO IGNORE

	MOV	AX,MSGLENGTH[EDI]	; GET MESSAGE LENGTH
	MOV	NODEMSGLEN,AX		; SAVE

	MOV	AL,MSGPORT[EDI]
	MOV	SAVEPORT,AL
	
	MOV	SAVEPTR,EDI		; SAVE POINTER
;
;	SEE IF OUR CALL - DONT WANT TO PUT IT IN LIST!
;
	LEA	ESI,MSGORIGIN[EDI]	; SET TO ADDR OF ORIGINATING STATION
	MOV	EDI,OFFSET32 _MYCALL
	PUSH	ESI
	CALL	COMPARECALLS	
	POP	ESI
	JZ SHORT PROCNODE02

	MOV	EDI,OFFSET32 APPLCALLTABLE
	LEA	EDI,APPLCALL[EDI]
	CALL	COMPARECALLS	

	JNZ SHORT PROCNODE03		; OK

PROCNODE02:

	RET

PROCNODE03:
;
	MOV	EDI,SAVEPTR
	LEA	ESI,MSGORIGIN[EDI]	; SET TO ADDR OF ORIGINATING STATION
	AND	BYTE PTR 6[ESI],01EH	; MASK OFF LAST ADDR BIT
;
;	SEE IF ORIGINATING CALL IS IN NEIGHBOUR LIST - IF NOT ADD IT
;
	CALL	_FINDNEIGHBOUR		; RETURNS ZF AND BX ALIGNED TO ENTRY
					; OR FIRST FREE ENTRY
	JZ SHORT PROCNODE10		; ENTRY FOUND
;
	CMP	EBX,0
	JE TABLESFULL_JMP		; NO ROOM
;
;	CREATE NEIGHBOUR RECORD
;
	PUSH	ESI

	LEA	EDI,NEIGHBOUR_CALL[EBX]
	MOV	ECX,7
	REP MOVSB

	AND	BYTE PTR -1[EDI],01EH	; MASK END OF ADDR BIT

	MOV	AL,SAVEPORT		; PORT MESSAGE CAME IN ON
	MOV	NEIGHBOUR_PORT[EBX],AL	; SAVE

	CALL	GETPORTQUALITY
	MOV	NEIGHBOUR_QUAL[EBX],AL	; QUALITY

	MOV	NEIGHBOUR_LINK[EBX],0	; CANT HAVE A LINK IF NEW NODE
	POP	ESI

PROCNODE10:

	MOV	SAVENBOUR,EBX		; SAVE POINTER TO NEIGHBOUR FOR DEST
							; LIST UPDATES
	
;	IF not locked, update rute quality from port quality (may have changed config and not cleared SAVENODES

	TEST	NEIGHBOUR_FLAG[EBX],1	 ; LOCKED ROUTE
	JNZ		@F
	
	MOV	AL,SAVEPORT		; PORT MESSAGE CAME IN ON

	CALL	GETPORTQUALITY
	MOV	NEIGHBOUR_QUAL[EBX],AL	; QUALITY
	
@@:	

;
;	GET TIME FROM BIOS DATA AREA OR RTC
;

	push	0
	call	_time

	add	esp,4
	

	MOV	EDX,0
	DIV	DWORD PTR D86400	; REMAINDER IS SECS IN DAY
	
	MOV	EAX,EDX
	MOV	EDX,0
	DIV	DWORD PTR D3600		; GIVES HOURS, REM = SECS
	MOV	BYTE PTR NEIGHBOUR_TIME+1[EBX],AL	; SAVE
	MOV	EAX,EDX
	MOV	EDX,0
	DIV	DWORD PTR D60		; GIVES MINS, REM = SECS
	MOV	BYTE PTR NEIGHBOUR_TIME[EBX],AL	; SAVE

;	GET QUALITY
;
	MOV	AL,NEIGHBOUR_QUAL[EBX]
	MOV	ROUTEQUAL,AL		; FOR INITIAL ROUTE TABLE UPDATE
	MOV	NODEQUAL,AL
;
;	CHECK LINK IS IN DEST LIST
;
	CALL	_FINDDESTINATION		; RETURNS ZF AND BX ALIGNED TO ENTRY
					; OR FIRST FREE ENTRY
	JZ SHORT PROCNODE20		; ENTRY FOUND
;
	CMP	EBX,0
	JNE SHORT PROCNODE11		; OK
TABLESFULL_JMP:
	RET

PROCNODE11:
;
;	CREATE DESTINATION RECORD
;
	PUSH	ESI

	MOV	EDI,EBX
	MOV	ECX,TYPE DEST_LIST
	MOV	AL,0
	REP STOSB			; CLEAR ENTRY

	LEA	EDI,DEST_CALL[EBX]
	MOV	ECX,7
	REP MOVSB

	AND	BYTE PTR -1[EDI],01EH	; MASK END OF ADDR BIT

	POP	ESI
	
	INC _NUMBEROFNODES

	JMP SHORT PROCNODE21

PROCNODE20:
;
;	ALWAYS UPDATE ALIAS IN CASE NOT PRESENT IN ORIGINAL TABLE
;
PROCNODE21:

	PUSH	ESI
	ADD	ESI,10			; SKIP CONTROL, PID AND SIG BYTES
	LEA	EDI,DEST_ALIAS[EBX]

;	Validate Alias, mainly for DISABL KPC3 Problem

	MOV	ECX,6
@@:
	lodsb
	
	CMP	AL,7AH
	JA SHORT DuffAlias

	CMP	AL,20H
	JAE SHORT AliasOK
	
DuffAlias:	
	mov al,20h
	
AliasOK:
	stosb
	loop @B
	
	POP	ESI
;
;	UPDATE QUALITY AND OBS COUNT
