_TNCTIMERPROC:

	PUBLIC _TNCTIMERPROC
;
;	CALLED AT 10 HZ
;
;	CHECK FOR TRANSPARENT MODE SEND TIMOUT
;
	MOV	EBX,TNCTABLE
	MOV	ECX,NUMBEROFSTREAMS
	OR	ECX,ECX
	JZ	TIMEXIT

	MOV	AH,0

TIM000:

	OR	AH,TRACEFLAG[EBX]	; SEE IF ANY PORTS ARE MONITORING

;	CHECK FOR PACTIMER EXPIRY AND CMDTIME
;
	MOV AL,CMDTMR[EBX]
	CMP	CMDTMR[EBX],0
	JE	TIM010				; NOT RUNNING
;
	DEC	CMDTMR[EBX]
	JNZ	TIM010				; NOT EXPIRED
;
;	CMDTMR HAS EXPIRED - IF 3 COMM CHARS RECEIVED, ENTER COMMAND MODE
;
	MOV	AL,COMCOUNT[EBX]
	OR	AL,AL
	JZ	TIM010				; NONE RECEIVED

	CMP	AL,3
	JNE	KB10_A			; 1 OR 2 - TIMER EXPIRED, SO RESET
;
;	3 ESCAPE CHARS RECEIVED WITH GUARDS - LEAVE TRAN MODE


	PUSH	EBX
	PUSH	ECX
	CALL	SETCOMM00
	POP		ECX
	POP		EBX

KB10_A:

	MOV	COMCOUNT[EBX],0		; RESET COUNT

	JMP SHORT TIM100		; DONT RISK TRANSTIMER AND CMDTIME FIRING AT ONCE

TIM010:

	CMP	TRANSTIMER[EBX],0
	JE	TIM100			; NOTHING DOING

	DEC	TRANSTIMER[EBX]
	JNZ	TIM100			; STILL OK
;
	CMP	MSGLEN[EBX],0
	JE	TIM100			; MESSAGE ALREADY SENT

	PUSHA
	
	CALL	SENDPACKET
	
	POPA

TIM100:

;	CHECK FLOW CONTROL

	TEST	VMSR[EBX],00010000B	; ALREADY OFF?
	JNZ	@F

	PUSH	ECX
	CALL	CHECKCTS
	POP		ECX
		
@@:

	ADD	EBX,TYPE TNCDATA
			
	DEC	ECX
	JNZ	TIM000			; TOO FAR FOR LOOP

	CALL DOMONITORING

TIMEXIT:

	RET


DOMONITORING:
;
;	IF ANY PORTS HAVE MONITOR ENABLED, SET MONITOR BIT ON FIRST PORT
;
	MOV	CL,0
	OR	AH,AH
	JZ	NO_MON

	MOV	CL,80H			; TOP BIT CONTROLS MONITOR

NO_MON:
;
	MOV	EBX,TNCTABLE
	MOV	DL,APPLICATION[EBX]
	OR	CL,APPLFLAGS[EBX]

	CMP	CONOK[EBX],0
	JNE	SETAPPLM

	MOV	DL,0			; STOP CONNECTS

SETAPPLM:

	MOV	AH,1
	MOV	AL,NODEPORT[EBX]
	CALL	NODE
;
;	DO ANY MONITORING
;
	MOV	EBX,TNCTABLE
	MOV	AL,NODEPORT[EBX]
	MOV	AH,11
	MOV	EDI,OFFSET MONITORDATA

	CALL	NODE

	CMP	ECX,0
	JE	TRACEEND

	mov	esi,OFFSET MONITORDATA
	mov	edi,offset MONBUFFER

	call	[_MONDECODE]

	mov	MONLEN,ECX

	MOV	ECX,NUMBEROFSTREAMS
	JCXZ	TRACEEND
;
	MOV	EBX,TNCTABLE

TRACELOOP:

	TEST	TRACEFLAG[EBX],1	; Does this port wnat monitoring?
	JZ SHORT NOTRACE

	push	ECX
	
	mov	esi,offset MONBUFFER
	mov	ecx,MONLEN
@@:
	lodsb

	call	PUTCHARINBUFFER

	loop	@B

	pop		ECX

NOTRACE:

	ADD	EBX,TYPE TNCDATA
	
	LOOP	TRACELOOP

TRACEEND:

	RET


