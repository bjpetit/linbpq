GETONECHAR:

	mov	eax,0
	MOV	ESI,GETPTR[EBX]
	LODSB				; GET CHAR

	DEC	RXCOUNT[EBX]

	MOV	EDX,TNCBUFFLEN
	ADD	EDX,EBX
	CMP	ESI,EDX			; BUFFER WRAP?
	JB	L153C			; NO

	MOV	ESI,EBX			; RESET TO START
L153C:
	CMP	RXCOUNT[EBX],128		; GETTING LOW?
	JNB	L1560			; NO

	TEST	RTSFLAG[EBX],1		; RTS UP?
	JZ	L1560			; YES
;
;	RTS HAD BEEN DROPPED TO STOP OTHER END SENDING - RAISE IT AGAIN
;
	AND	RTSFLAG[EBX],0FEH
L1560:
	MOV	GETPTR[EBX],ESI

	RET


