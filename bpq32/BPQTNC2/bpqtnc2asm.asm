_APIKEYSUPPORT:

	PUBLIC	_APIKEYSUPPORT
	
	IMUL EBX,ECX,TYPE TNCDATA

	ADD	EBX,TNCTABLE

KEYSUPPORT:

	TEST	MODEFLAG[EBX],COMMAND
	JNZ	KEYB06C			; COMMAND MODE - SKIP TRANS TEST

	TEST	MODEFLAG[EBX],TRANS
	JNZ	KEYB06T			; TRANS MODE	
;
;	CONV MODE - SEE IF CPACTIME ON
;
	CMP	CPACTIME[EBX],0
	JE	KEYB06			; NOT CPACTIME

	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[EBX],AH	; REPRIME SEND TIMER
	JMP SHORT KEYB06		; PROCESS CHAR

KEYB06T:
;
;	Transparent Mode - See if Escape Sequence Received (3 esc chars, with guard timer)
;
;
;	CHECK FOR COMMAND CHAR IF CMDTIME EXPIRED OR COMAND CHAR ALREADY RECEIVED
;
	CMP	COMCOUNT[EBX],0
	JNE	KBTRN3		; ALREADY GOT AT LEAST 1

	CMP	CMDTMR[EBX],0
	JNE	KBTRN5		; LESS THAN CMDTIME SINCE LAST CHAR

KBTRN3:

	CMP	AL,COMCHAR[EBX]
	JNE	KBTRN5			; NOT COMMAND CHAR

	INC	COMCOUNT[EBX]

KBTRN5:

	MOV	AH,CMDTIME[EBX]
	MOV	CMDTMR[EBX],AH		; REPRIME ESCAPE TIMER

CHKPACTIM:

	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[EBX],AH
	JMP SHORT KBNORM		; TRANSPARENT MODE

KEYB06:
;
;	STILL JUST CONV MODE
;
	CMP	AL,SENDPAC[EBX]
	JNE	NOTSENDPAC
;
;	SEND PACKET CHAR - SHOUD WE SEND IT?
;
	MOV	TRANSTIMER[EBX],0

	CMP	CRFLAG[EBX],1
	JE	KBCR_JMP		; SEND IT

	JMP	SENDPACKET

KBCR_JMP:
	JMP	KBCR

NOTSENDPAC:

KEYB06C:
;
;	COMMAND OR CONV MODE
;
	CMP	AL,20H
	JB	KBCTRL
KBNORM:
	CMP	MSGLEN[EBX],256
	JA	TOOLONG			; PROTECT BUFFER
;
	INC	MSGLEN[EBX]
	MOV	EDI,CURSOR[EBX]
	STOSB
	MOV	CURSOR[EBX],EDI

TOOLONG:

	TEST	ECHOFLAG[EBX],0FFH
	JZ	NOECHO

	CALL	KBECHO

NOECHO:

	MOV	AL,TPACLEN[EBX]
	CMP	BYTE PTR MSGLEN[EBX],AL
	JB	NOSEND
;
;	DONT APPLY PACLEN IN COMMAND MODE
;
	TEST	MODEFLAG[EBX],COMMAND
	JNZ	NOSEND

	JMP	SENDPACKET
NOSEND:
	RET

KBCTRL:
	CMP	LFIGNORE[EBX],0
	JE	KBCTRL00
;
;	IF LF, IGNORE IT
;
	CMP	AL,0AH
	JE	NOSEND			; IGNORE

	RET


KBCTRL00:

	CMP	AL,8
	JE	KBBSPACE

	CMP	AL,1AH			; CTRL/Z
	JE	KBNORM			; FOR MBX TERMINATOR

	TEST	MODEFLAG[EBX],CONV
	JNZ	CTRLREST
;
	CMP	AL,0DH
	JE	KBCR

	JMP SHORT CTRLREST
;
;	CONV MODE
;
CTRLREST:

	CMP	AL,COMCHAR[EBX]

	JE	KBESCAPE		; RETURN TO COMMAND MODE
;
	CMP	AL,14H			; CTRL/T
	JNE	IGNORECTRL

	XOR	TRACEFLAG[EBX],1
	RET

IGNORECTRL:

	JMP	KBNORM

KBESCAPE:
	CALL	SETCOMMANDMODE
	JMP	KBRET			; EXIT

SETCOMMANDMODE:

	CMP	MSGLEN[EBX],0
	JE	SETCOMM00

	CALL	SENDPACKET		; PROCESS WAITING DATA

SETCOMM00:

	OR	MODEFLAG[EBX],COMMAND	; BACK TO COMMAND MODE
	AND	MODEFLAG[EBX],NOT (CONV+TRANS)
	MOV	TRANSTIMER[EBX],0	; CANCEL TRANS MODE SEND TIMER
	MOV	AUTOSENDFLAG[EBX],0	; IN CASE ALREADY SET

	MOV	ESI,OFFSET CMDMSG
	MOV	ECX,4
	CALL	SENDREPLY

	CALL	KBRET

	RET

KBBSPACE:

	CMP	MSGLEN[EBX],0
	JE	KBBS00

	DEC	MSGLEN[EBX]
	DEC	CURSOR[EBX]

	TEST	ECHOFLAG[EBX],0FFH
	JZ	KBBS00

	CALL	KBECHO
KBBS00:
	RET

KBCR:
	CALL	KBNORM			; PUT CR IN BUFFER

SENDPACKET:
;
;	SEE IF COMMAND STATE
;
	TEST	MODEFLAG[EBX],COMMAND
	JZ	NOTCOMMAND

	JMP	TNCCOMMAND		; COMMAND TO TNC

NOTCOMMAND:
;
;	IF CONNECTED, SEND TO L4 (COMMAND HANDLER OR DATA),
;	   OTHERWISE SEND AS AN UNPROTO FRAME (TO ALL PORTS)
;

	TEST	VMSR[EBX],80H		; CONNECTED?
	JNZ	SENDTOL4
;
;	SEND BUFFER AS AN UNPROTO FRAME
;
	MOV	AL,0			; UNPROTO STREAM

	JMP SHORT SENDTONODE

SENDTOL4:

;TESTFORBUSY:
;
;	TEST	VMSR[EBX],00010000B	; CHECK CTS
;	JNZ	NOTBUSY

;	CALL	CHECKCTS

;	JMP	TESTFORBUSY

;NOTBUSY:


	MOV	AL,NODEPORT[EBX]

SENDTONODE:

	MOV	ECX,MSGLEN[EBX]
	LEA	ESI,TXBUFFER[EBX]	

	MOV	CURSOR[EBX],ESI		; RESET POINTER

	MOV	AH,2
	CALL	NODE			; SEND FRAME
		
	MOV	MSGLEN[EBX],0
;
	CALL	CHECKCTS		; SEE IF NOW BUSY
	RET


KBECHO:
	CALL	PUTCHARINBUFFER
;
	RET

TNCCOMMAND:
;
;	PROCESS COMMAND TO TNC CODE
;
	LEA	ESI,TXBUFFER[EBX]
	MOV	EDI,ESI
	PUSH	ESI
;
;	CONVERT TO UPPER CASE
;
	MOV	ECX,200			; IN CASE GARBAGE!!
TNCUCLOOP:
	LODSB
	CMP	AL,0DH
	JE	TNCUCEND

	CMP	AL,61H
	JB	TNCUCOK
	CMP	AL,7AH
	JA	TNCUCOK

	SUB	AL,20H
TNCUCOK:
	STOSB
	LOOP	TNCUCLOOP
;
TNCUCEND:

	MOV	CMDENDADDR,ESI		; SAVE REAL END OF COMMAND

	MOV	AL,20H
	MOV	ECX,40
	REP STOSB			; PUT A FEW SPACES ON END FOR DECODE ROUTINES

	POP	ESI
	CALL	SCAN

