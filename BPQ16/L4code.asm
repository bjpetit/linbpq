	PUBLIC	SETUPCONSOLESESSION
	EXTRN	DOCONMODECHANGE:NEAR
	EXTRN	SEND_CONNECTED:NEAR,SENDSTARDISC:NEAR,CONSOLESEND:NEAR

	ENDIF

	EXTRN	Q_ADD:NEAR,GETBUFF:NEAR,RELBUFF:NEAR,COMPARECALLS:NEAR
	EXTRN	GETPORTQUALITY:NEAR,CHECKNEIGHBOUR:NEAR
	EXTRN	CONVFROMAX25:NEAR,CONVTOAX25:NEAR,GETPORTTABLEENTRY:NEAR
	EXTRN	Q_REM:NEAR,REFRESHROUTE:NEAR,COUNT_Q:NEAR
	EXTRN	GETPORTPACLEN:NEAR,HEXOUT:NEAR

	EXTRN	COMMANDHANDLER:NEAR,FINDDESTINATION:NEAR,L3SWAPADDRESSES:NEAR
	EXTRN	FINDNEIGHBOUR:NEAR,SETUPNODEHEADER:NEAR
	EXTRN	CHECKL3TABLES:NEAR,L3CONNECTFAILED:NEAR,L3LINKCLOSED:NEAR
