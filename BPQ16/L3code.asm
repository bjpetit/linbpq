	MOV	MSGLENGTH[DI],CX

	POP	BX

	CMP	TXPORT[BX],0
	JNE	SKIPTX1			; DONT SEND IF SHARED TX

	CALL	PUT_ON_PORT_Q

	JMP SHORT SENDNODE48A

SKIPTX1:

	CALL	RELBUFF

SENDNODE48A:
;
;	SEE IF MORE TO GO
;
	CMP	CURRENTNODE,0
	JNE	STILLMORE

SENDNODEXX:
;
;	FINISHED THIS PORT - SEE IF MORE
;
	DEC	PORTSLEFT
	JNZ	ANOTHERPORT
;
	MOV	NODESINPROGRESS,0	; FINISHED
	RET

ANOTHERPORT:

	MOV	BX,CURRENTPORT
	MOV	BX,PORTPOINTER[BX]
	MOV	CURRENTPORT,BX

STILLMORE:

	MOV	NODESINPROGRESS,1	; RESTART TIMER
	RET


SETUPNODESMSG:

	MOV	AL,PORTNUMBER[BX]
	MOV	MSGPORT[DI],AL

	LEA	DI,MSGDEST[DI]
	MOV	SI,OFFSET NODECALL
	MOV	CX,7
	REP MOVSB

	MOV	SI,OFFSET MYCALL
	MOV	CX,7
	REP MOVSB
	OR	BYTE PTR -1[DI],01100001B ; SET CMD END AND RESERVED BITS

	MOV	AL,UI
	STOSB				; CONTROL

	MOV	AL,0CFH
	STOSB				; PID

	MOV	AL,0FFH
	STOSB				; FLAG

	MOV	CX,6
	REP MOVSB			; ALIAS FOLLOWS CALL

	RET

CHAINNODE:
;
;	PUT NODE IN BX ON SORTED NODES CHAIN
;
	INC	NUMBEROFNODES

	PUSH	SI
	PUSH	DI

	MOV	SI,DESTHEADER
	CMP	SI,0
	JNE	NOTEMPTY
;
;	LIST IS EMPTY - THIS MUST BE FIRST
;
	MOV	DESTHEADER,BX
	JMP SHORT CHAINRET

NOTEMPTY:

	MOV	LASTPOINTER,OFFSET DESTHEADER

SCANLOOP:
	PUSH	SI
	LEA	SI,DEST_ALIAS[SI]
	LEA	DI,DEST_ALIAS[BX]
	MOV	CX,6
	REP CMPSB
	POP	SI

	JB	CARRYON
;
;	ADD IT HERE
;
	MOV	SI,LASTPOINTER
	MOV	AX,DEST_CHAIN[SI]	; NEXT
	MOV	DEST_CHAIN[SI],BX
	MOV	DEST_CHAIN[BX],AX

	JMP SHORT CHAINRET

CARRYON:

	CMP	DEST_CHAIN[SI],0
	JE	SCANEND

	MOV	LASTPOINTER,SI
	MOV	SI,DEST_CHAIN[SI]
	JMP	SCANLOOP

SCANEND:
;
;	ADD ON END
;
	MOV	DEST_CHAIN[SI],BX
	MOV	DEST_CHAIN[BX],0	; END


CHAINRET:

	POP	DI
	POP	SI
	RET

UNCHAINNODE:
;
;	REMOVE NODE IN BX FROM SORTED NODES CHAIN
;
	DEC	NUMBEROFNODES
;
	PUSH	SI
	MOV	SI,OFFSET DESTHEADER
;
MAINLOOP:

	CMP	SI,0
	JE	UNLINKRET	; NOT THERE!!

	CMP	DEST_CHAIN[SI],BX
	JE	FOUNDOURS

	MOV	SI,DEST_CHAIN[SI]
	JMP	MAINLOOP

FOUNDOURS:

	MOV	AX,DEST_CHAIN[BX]	; NEXT
	MOV	DEST_CHAIN[SI],AX	; REMOVE OURS FROM CHAIN
UNLINKRET:
	MOV	DEST_CHAIN[BX],0

	POP	SI
	RET

CODE	ENDS
	END

