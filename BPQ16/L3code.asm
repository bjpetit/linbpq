L3TIMERPROC:
;
;	CHECK FOR EXCESSIVE BUFFERS QUEUED AT LINK LEVEL
;
	CMP	QCOUNT,30
	JA	COUNT_OK

	MOV	SI,LINKS
	MOV	CX,MAXLINKS

CHKLINK00:

	CMP	LINKTYPE[SI],3
	JNE	CHKLINK10		; NOT INTERNODE

	MOV	BX,SI
;
	MOV	AH,0
	CALL	COUNT_AT_L2		; SEE HOW MANY QUEUED

	CMP	AH,50
	JB	CHKLINK10		; OK
;
;	DESTROY THE SESSION
;
	MOV	BX,SI
	CALL	L3LINKCLOSED		; REPORT TO LEVEL 3
	CALL	CLEAROUTLINK

	JMP	COUNT_OK		; CANT BE ANY MORE OVERLOADED LINKS

CHKLINK10:

	ADD	SI,TYPE LINKTABLE
	LOOP	CHKLINK00
;
COUNT_OK:

	CMP	IDTIMER,0
	JE	NOIDMSG			; SUPPRESSED

	DEC	IDTIMER
	JNZ	NOIDMSG

	MOV	AX,IDINTERVAL
	MOV	IDTIMER,AX

	CALL	SENDIDMSG

NOIDMSG:
;
;	CHECK FOR BEACON
;
	CMP	BTTIMER,0
	JE	NOBTMSG			; SUPPRESSED

	DEC	BTTIMER
	JNZ	NOBTMSG

	MOV	AX,BTINTERVAL
	MOV	BTTIMER,AX

	MOV	BX,TNCTABLE		; IN CASE SENDING MAIL BEACON
	CALL	SENDBTMSG


NOBTMSG:
;
;	CHECK FOR NODES BROADCAST
;
	CMP	L3TIMER,0
	JE	L3TIMRET		; TIMER UPDATE AND BROADCAST SUPPRESSED

	DEC	L3TIMER
	JNZ	L3TIMRET
;
;	UPDATE DEST LIST AND SEND 'NODES' MESSAGE
;
	MOV	AX,L3INTERVAL
	MOV	L3TIMER,AX

	CALL	UPDATEDESTLIST
	CALL	SENDNODESMSG

;
;	TIDY ROUTES
;
	MOV	SI,NEIGHBOURS
	MOV	CX,MAXNEIGHBOURS
CMDR10:
	PUSH	CX

	TEST	NEIGHBOUR_FLAG[SI],1
	JNZ	CMDR88			; LOCKED

	CMP	NEIGHBOUR_LINK[SI],0
	JNE	CMDR88			; HAS AN ACTIVE SESSION

	CALL	COUNTNODES		; NODES USING THIS DESTINATION
;
;	IF NUMBER USING ROUTE IS ZERO, DELETE IT
;
	OR	AL,AL
	JNZ	CMDR88

	PUSH	DI
	MOV	DI,SI
	MOV	CX,TYPE NEIGHBOUR_LIST
	REP STOSB

	POP	DI

CMDR88:

	POP	CX
	ADD	SI,TYPE NEIGHBOUR_LIST

	LOOP	CMDR10

L3TIMRET:

	RET

L3FASTTIMER:
;
;	CALLED ONCE PER SECOND - USED ONLY TO SEND NEXT PART OF A NODES OR
;	ID MESSAGE SEQUENCE
;
	DEC	L3_10SECS
	JNZ	L3TIMRET

	MOV	L3_10SECS,10

	CMP	IDMSG_Q,0		; ID/BEACON TO SEND?
	JE	L3FT00			; NO

	MOV	SI,OFFSET IDMSG_Q
	CALL	Q_REM

	JZ	L3FT00			; ??

	MOV	AL,MSGPORT[DI]

	CALL	GETPORTTABLEENTRY

	CMP	TXPORT[BX],0
	JNE	SKIPTX			; DONT SEND IF SHARED TX

	CALL	PUT_ON_PORT_Q

	JMP	L3TIMRET

SKIPTX:

	CALL	RELBUFF

	JMP	L3TIMRET

L3FT00:

	CMP	NODESINPROGRESS,0
	JE	L3TIMRET

	CALL	SENDNEXTNODESFRAGMENT

	RET

L3BG:
;
;	TRANSFER MESSAGES FROM DEST TO LINK
;
	MOV	BX,DESTS
	MOV	CX,MAXDESTS
L3BG60:
	CMP	DEST_CALL[BX],0
	JE	L3BG90			; SPARE

L3BG61:
	LEA	SI,DEST_Q[BX]		; FRAMES TO SEND?
	CMP	WORD PTR [SI],0
	JE	L3BG90			; NO

	PUSH	BX
	PUSH	CX
;
	MOV	AL,DEST_ROUTE[BX]
	OR	AL,AL
	JZ	L3BG80

	DEC	AL
	MOV	AH,0
	ADD	AX,AX			; * 2
	ADD	AX,AX			; * 4

	ADD	BX,AX			; TO ROUTE ENTRY
	MOV	BX,ROUT1_NEIGHBOUR[BX]	; GET NEIGHBOUR ENTRY
	OR	BX,BX
	JZ	L3BG80			; NO NEIGHBOUR?

	MOV	AX,BX			; SAVE NEIGHBOUR ADDR

	MOV	BX,NEIGHBOUR_LINK[BX]	; GET LINK TABLE ENTRY
	OR	BX,BX
	JZ	L3BG80			; ???? INACTIVE ????

	CMP	L2STATE[BX],0
	JA	L3BG70
;
;	LINK ENTRY IS INVALID - IT PROBABLY HAS BEEN 'ZAPPED', SO CANCEL IT
;
	MOV	BX,AX
	MOV	NEIGHBOUR_LINK[BX],0

	JMP SHORT L3BG80

L3BG70:
	CMP	L2STATE[BX],5		; LINK UP?
	JL	L3BG85			; NO, SO WAIT FOR IT

	PUSH	BX			; SAVE LINK
	MOV	BX,AX			; NEIGHBOUR
;
;	COUNT FRAMES TO NEIGHBOUR
;
	ADD	WORD PTR NBOUR_IFRAMES[BX],1
	ADC	WORD PTR NBOUR_IFRAMES+2[BX],0

	POP	BX

	CALL	Q_REM			; GET THE FRAME

 	LEA	SI,TX_Q[BX]
	CALL	Q_ADD			; QUEUE ON LINK

	POP	CX
	POP	BX

	JMP	L3BG61			; SEE IF MORE

L3BG80:
	POP	CX
	POP	BX			; RECOVER DEST TABLE ADDR
	PUSH	BX
	PUSH	CX
	CALL	ACTIVATE_DEST
L3BG85:
	POP	CX
	POP	BX

L3BG90:
	ADD	BX,TYPE DEST_LIST
	LOOP	L3BG60
L3BG95:
	RET

ACTIVATE_DEST:

	PUSH	BX

	MOV	AL,DEST_ROUTE[BX]
	OR	AL,AL
	JNZ	DESTISOK		; ALREADY HAVE A SELECTED ROUTE
;
	MOV	DEST_ROUTE[BX],1	; TRY TO ACTIVATE FIRST
DESTISOK:
	MOV	AL,DEST_ROUTE[BX]
	DEC	AL
	MOV	AH,0
	ADD	AX,AX			; * 2
	ADD	AX,AX			; * 4

	ADD	BX,AX			; TO ROUTE ENTRY
	MOV	BX,ROUT1_NEIGHBOUR[BX]	; GET NEIGHBOUR ENTRY
	OR	BX,BX
	JZ	NOROUTETODEST

	MOV	AX,NEIGHBOUR_LINK[BX]	; GET LINK TABLE ENTRY
	OR	AX,AX
	JZ	ACTIVATENEIGHBOUR	; NEED TO SET UP L2 SESSION TO NEIGHBOUR

	POP	BX			; ? MUST BE WAITING FOR LINK TO COME UP
	RET

ACTIVATENEIGHBOUR:
;
;	SET UP LINK TABLE ENTRY
;
	CALL	L2SETUPCROSSLINK	; START SESSION TO NODE IN [BX]
	POP	BX
	RET

NOROUTETODEST:
;
;	CURRENT NEIGHBOUR NOT DEFINED - RESET TO USE FIRST
;
	POP	BX
	MOV	DEST_ROUTE[BX],0
	RET

L3CONNECTFAILED:
;
;	L2 LINK SETUP HAS FAILED - SEE IF ANOTHER NEIGHBOUR CAN BE USED
;
	MOV	SI,NEIGHBOUR[BX]		; TO NEIGHBOUR
	CMP	SI,0
	JE	L3CFRET				; NOTHING ???

	MOV	NEIGHBOUR_LINK[SI],0		; CLEAR IT
	PUSH	BX
	CALL	L3TRYNEXTDEST			; RESET ASSOCIATED DEST ENTRIES
	POP	BX
L3CFRET:
	RET


L3TRYNEXTDEST:
;
;	FIND ANY DESINATIONS WITH [SI] AS ACTIVE NEIGHBOUR, AND
;	SET NEXT BEST NEIGHBOUR (IF ANY) ACTIVE
;
	MOV	BX,DESTS
	MOV	CX,MAXDESTS
;
L3CFR010:
	CMP	DEST_ROUTE[BX],0
	JE	L3CFR090		; NO ACTIVE ROUTE
;
	PUSH	BX

	MOV	AL,DEST_ROUTE[BX]
	DEC	AL
	MOV	AH,0
	ADD	AX,AX			; * 2
	ADD	AX,AX			; * 4

	ADD	BX,AX			; TO ACTIVE ROUTE ENTRY
	CMP	ROUT1_NEIGHBOUR[BX],SI	; GET NEIGHBOUR ENTRY
	JNE	L3CFR080		; NOT THIS DEST
;
;	NEIGHBOUR HAS FAILED - DECREMENT OBSCOUNT
;	AND TRY TO ACTIVATE ANOTHER (IF ANY)
;
	CMP	ROUT1_OBSCOUNT[BX],0
	JE	L3CFR020		; ALREADY ZERO - WILL BE DELETED BY NEXT NODES UPDATE

	TEST	ROUT1_OBSCOUNT[BX],80H
	JNZ	L3CFR020		; LOCKED

	DEC	ROUT1_OBSCOUNT[BX]
	JNZ	L3CFR020		; STILL OK
;
;	(ROUTE HAS EXPIRED - WE SHOULD CLEAR IT, AND MOVE OTHERS (IF ANY) UP)
;
L3CFR020:
	POP	BX
;
;	REMOVE FIRST MESSAGE FROM DEST_Q. L4 WILL RETRY - IF IT IS LEFT HERE
;	WE WILL TRY TO ACTIVATE THE DESTINATION FOR EVER
;
	PUSH	SI
	LEA	SI,DEST_Q[BX]
	CALL	Q_REM
	JZ	L3CFR025		; NONE??

	CALL	RELBUFF
L3CFR025:
	POP	SI
	INC	DEST_ROUTE[BX]		; TO NEXT 
	CMP	DEST_ROUTE[BX],4
	JNE	L3CFR040
;
	MOV	DEST_ROUTE[BX],1	; TRY TO ACTIVATE FIRST
L3CFR040:
	JMP SHORT L3CFR090

L3CFR080:
	POP	BX
L3CFR090:
	ADD	BX,TYPE DEST_LIST
	LOOP	L3CFR010

	RET

CHECKNEIGHBOUR:
;
;	MESSAGE RECEIVED ON LINK WITH NO DESTINATION - SET ONE UP
;
	LEA	SI,LINKCALL[BX]
	CALL	FINDNEIGHBOUR
	JZ	L3CONN07		; IN LIST - OK

	CMP	BX,0
	JE	L3CONN08		; TABLE FULL??
;
;	FIRST MAKE SURE WE ARE ALLOWING NETWORK ACTIVITY ON THIS PORT
;
	MOV	AL,SAVEPORT
	CALL	GETPORTQUALITY
	CMP	AL,0
	JNE	QUALOK
;
;	NOT A NETWORKING PORT
;
	RET

QUALOK:

	MOV	NEIGHBOUR_QUAL[BX],AL	; QUALITY

	MOV	CX,7
	MOV	DI,BX
	REP MOVSB			; PUT IN CALL

	MOV	AL,SAVEPORT
	MOV	NEIGHBOUR_PORT[BX],AL

L3CONN07:
;
;	SET THIS AS ACTIVE LINK IF NONE PRESENT
;
	CMP	NEIGHBOUR_LINK[BX],0
	JNE	L3CONN08

	MOV	AX,SAVELINK
	MOV	NEIGHBOUR_LINK[BX],AX
	MOV	AL,SAVEPORT
	MOV	NEIGHBOUR_PORT[BX],AL

L3CONN08:

	MOV	AX,BX
	MOV	BX,SAVELINK
	MOV	NEIGHBOUR[BX],AX	; SET LINK - NEIGHBOUR
	RET



CHECKL3TABLES:
;
;	CHECK THAT FAR NODE IS IN 'NODES'.
;	BX POINTS TO L4 TABLE ENTRY
;	RETURNS POINTER TO NEIGHBOUR ENTRY IN AX
;
	PUSH	BX

	MOV	BX,SAVELINK
	MOV	BX,NEIGHBOUR[BX]	; NEIGHBOUR ENTRY

	MOV	SAVENBOUR,BX

	MOV	SI,BUFFER
	LEA	SI,L3SRCE[SI]

	CALL	FINDDESTINATION
	JZ	L3CONN09		; OK
;
	CMP	BX,0
	JNE	CAN_ADD

DONT_ADD:

	MOV	AX,0
	POP	BX

	RET				; TABLES FULL

CAN_ADD:
;
;	ADD DESTINATION VIA NEIGHBOUR, UNLESS ON BLACK LIST
;
	IF	BLACKBITS

	PUSH	SI
	CALL	CHECKBLACKLIST
	POP	SI

	JZ	DONT_ADD
;
	ENDIF

	MOV	CX,7
	LEA	DI,DEST_CALL[BX]
	REP MOVSB			; PUT IN CALL

	CALL	CHAINNODE		; PUT ON SORTED CHAIN

L3CONN09:
;
;	MAKE SURE NEIGHBOUR IS DEFINED FOR DESTINATION
;
;	IF NODE - NEIGHBOUR, THEN CAN USE NEIGHBOUR QUALITY,
;	   OTHERWISE WE DONT KNOW ROUTE, SO MUST SET QUAL TO 0
;
	MOV	ROUTEQUAL,0		; DONT KNOW ROUTING, SO SET QUALITY TO ZERO

	MOV	AX,SAVENBOUR
	CALL	PROCROUTES		; ADD NEIGHBOUR  IF NOT PRESENT
;
	CMP	DEST_ROUTE[BX],0	; IS THERE AN ACTIVE ROUTE?
	JNE	L3CONN20		; DEST IS ACTIVE
;
;	MAKE CURRENT NEIGHBOUR ACTIVE
;
	MOV	AX,SAVENBOUR
	INC	DEST_ROUTE[BX]
	CMP	ROUT1_NEIGHBOUR[BX],AX
	JE	L3CONN20		; FOUND REQUIRED ENTRY
;
	INC	DEST_ROUTE[BX]
	CMP	ROUT2_NEIGHBOUR[BX],AX
	JE	L3CONN20		; FOUND REQUIRED ENTRY
;
	INC	DEST_ROUTE[BX]
	CMP	ROUT3_NEIGHBOUR[BX],AX
	JE	L3CONN20		; FOUND REQUIRED ENTRY
;
;	CURRENT NEIGHBOUR ISNT IN DEST LIST - SET TO USE BEST
;
	MOV	DEST_ROUTE[BX],1

L3CONN20:
;
;	REFRESH OBS COUNT
;
	PUSH	BX			; SAVE DEST TABLE ENTRY

	MOV	AL,DEST_ROUTE[BX]
	OR	AL,AL
	JZ	REFROUTY		; NONE ACTIVE???

	DEC	AL
	MOV	AH,0
	ADD	AX,AX			; * 2
	ADD	AX,AX			; * 4

	ADD	BX,AX			; TO ROUTE ENTRY
	TEST	ROUT1_OBSCOUNT[BX],80H
	JNZ	REFROUTY		; LOCKED

	MOV	AL,OBSINIT
	MOV	ROUT1_OBSCOUNT[BX],AL	; SET OBSOLESCENCE COUNT

REFROUTY:

	POP	AX			; DEST TABLE ENTRY
	POP	BX
	RET


PROCESSNODEMESSAGE:
;
;	PROCESS A NET/ROM 'NODES' MESSAGE
;
;	    UPDATE NEIGHBOURS LIST WITH ORIGINATING CALL, AND
;	    DESTINATION LIST WITH ANY PRACTICAL ROUTES
;
;
;	    INCOMING MESSAGE IS IN [DI]
;
;
	MOV	AL,SAVEPORT		; PORT MESSAGE CAME IN ON
	CALL	GETPORTQUALITY
	OR	AL,AL
	JZ	PROCNODE02		; PORT QUALITY=0, SO IGNORE

	MOV	AX,MSGLENGTH[DI]	; GET MESSAGE LENGTH
	MOV	NODEMSGLEN,AX		; SAVE

	MOV	AL,MSGPORT[DI]
	MOV	SAVEPORT,AL
	
	MOV	SAVEPTR,DI		; SAVE POINTER
;
;	SEE IF OUR CALL - DONT WANT TO PUT IT IN LIST!
;
	LEA	SI,MSGORIGIN[DI]	; SET TO ADDR OF ORIGINATING STATION
	MOV	DI,OFFSET MYCALL
	PUSH	SI
	CALL	COMPARECALLS	
	POP	SI
	JZ	PROCNODE02

	MOV	DI,OFFSET BBSCALL
	CALL	COMPARECALLS	

	JNZ	PROCNODE03		; OK

PROCNODE02:

	RET

PROCNODE03:
;
	MOV	DI,SAVEPTR
	LEA	SI,MSGORIGIN[DI]	; SET TO ADDR OF ORIGINATING STATION
	AND	BYTE PTR 6[SI],01EH	; MASK OFF LAST ADDR BIT
;
;	SEE IF ORIGINATING CALL IS IN NEIGHBOUR LIST - IF NOT ADD IT
;
	CALL	FINDNEIGHBOUR		; RETURNS ZF AND BX ALIGNED TO ENTRY
					; OR FIRST FREE ENTRY
	JZ	PROCNODE10		; ENTRY FOUND
;
	CMP	BX,0
	JE	TABLESFULL_JMP		; NO ROOM
;
;	CREATE NEIGHBOUR RECORD
;
	PUSH	SI

	LEA	DI,NEIGHBOUR_CALL[BX]
	MOV	CX,7
	REP MOVSB

	AND	BYTE PTR -1[DI],01EH	; MASK END OF ADDR BIT

	MOV	AL,SAVEPORT		; PORT MESSAGE CAME IN ON
	MOV	NEIGHBOUR_PORT[BX],AL	; SAVE

	CALL	GETPORTQUALITY
	MOV	NEIGHBOUR_QUAL[BX],AL	; QUALITY

	MOV	NEIGHBOUR_LINK[BX],0	; CANT HAVE A LINK IF NEW NODE
	POP	SI

PROCNODE10:

	MOV	SAVENBOUR,BX		; SAVE POINTER TO NEIGHBOUR FOR DEST
					; LIST UPDATES
;
;	GET TIME FROM BIOS DATA AREA OR RTC
;
	IFDEF	KANT

	MOV	AL,1
	OUT	CLOCK_D,AL

GETTIMELOOP:

	IN	AL,CLOCK_D		; WAIT FOR NOT BUSY
	TEST	AL,2
	JZ	OKTOREAD

	MOV	AL,0
	OUT	CLOCK_D,AL

	MOV	AL,1
	OUT	CLOCK_D,AL

	JMP	GETTIMELOOP

OKTOREAD:

	IN	AL,CLOCK_MINUTE+1
	AND	AL,0FH
	MUL	B10
	MOV	CL,AL

	IN	AL,CLOCK_MINUTE
	AND	AL,0FH
	ADD	CL,AL			; MINS TO CL

	IN	AL,CLOCK_HOUR+1
	AND	AL,0FH
	MUL	B10
	MOV	CH,AL

	IN	AL,CLOCK_HOUR
	AND	AL,0FH
	ADD	CH,AL			; HOURS TO CH

	MOV	NEIGHBOUR_TIME[BX],CX	; SAVE

	MOV	AL,0
	OUT	CLOCK_D,AL


	ENDIF


	IFDEF	MSC

	MOV	AH,HOURS
	MOV	AL,MINS

	MOV	NEIGHBOUR_TIME[BX],AX	; SAVE
;
	ENDIF

	IFDEF	PCSWITCH

	MOV	AH,HH
	MOV	AL,MM

	MOV	NEIGHBOUR_TIME[BX],AX	; SAVE
;
	ENDIF

;	GET QUALITY
;
	MOV	AL,NEIGHBOUR_QUAL[BX]
	MOV	ROUTEQUAL,AL		; FOR INITIAL ROUTE TABLE UPDATE
	MOV	NODEQUAL,AL
;
;	CHECK LINK IS IN DEST LIST
;
	CALL	FINDDESTINATION		; RETURNS ZF AND BX ALIGNED TO ENTRY
					; OR FIRST FREE ENTRY
	JZ	PROCNODE20		; ENTRY FOUND
;
	CMP	BX,0
	JNE	PROCNODE11		; OK
TABLESFULL_JMP:
	RET

PROCNODE11:
;
;	CREATE DESTINATION RECORD
;
	PUSH	SI

	MOV	DI,BX
	MOV	CX,TYPE DEST_LIST
	MOV	AL,0
	REP STOSB			; CLEAR ENTRY

	LEA	DI,DEST_CALL[BX]
	MOV	CX,7
	REP MOVSB

	AND	BYTE PTR -1[DI],01EH	; MASK END OF ADDR BIT

	POP	SI

	JMP SHORT PROCNODE21

PROCNODE20:
;
;	ALWAYS UPDATE ALIAS IN CASE NOT PRESENT IN ORIGINAL TABLE
;
	CALL	UNCHAINNODE		; REMOVE FROM CHAIN

PROCNODE21:

	PUSH	SI
	ADD	SI,10			; SKIP CONTROL, PID AND SIG BYTES
	LEA	DI,DEST_ALIAS[BX]
	MOV	CX,6
	REP MOVSB			; PUT IN ALIAS

	POP	SI

	CALL	CHAINNODE		; PUT BACK ON SORTED LIST
;
;	UPDATE QUALITY AND OBS COUNT
;
	MOV	AX,SAVENBOUR		; POINTER TO NEIGHBOUR
	CALL	PROCROUTES
;
	SUB	NODEMSGLEN,23		; LEVEL 2 HEADER + NODE MNEMONIC
;
;	PROCESS KNOWN DESTINATIONS
;
	ADD	SI,16			; TO DESTINATIONS

PROCNODE30:

	CMP	NODEMSGLEN,21
	JAE	PROCNODE31		; STILL AT LEAST 1 ENTRY LEFT

	RET

PROCNODE31:

	IF	BLACKBITS

	PUSH	SI
	CALL	CHECKBLACKLIST
	POP	SI

	JZ	PROCNODE50_J
;
	ENDIF
;
;	SEE IF OUR CALL - DONT WANT TO PUT IT IN LIST!
;
