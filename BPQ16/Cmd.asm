	MOV	SPYFLAG[BX],AL

	MOV	DI,REPLYBUFFER
	CALL	RELBUFF

	CALL	SENDL4CONNECT
	INC	WORD PTR L4CONNECTSOUT
	JNZ	INC000
	INC	WORD PTR L4CONNECTSOUT+2
