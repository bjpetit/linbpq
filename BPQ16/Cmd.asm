DECODECALLSTRING:
;
;	CONVERT CALL + OPTIONAL DIGI STRING AT [SI] TO AX25, RETURN ADDRESS
;	OF CONVERTED STRING IN [SI], WITH NZ SET IF INVALID
;
;	ALSO RETURN END OF INPUT STRING IN AX

	MOV	STAYSWITCH,0
	MOV	SPYSWITCH,0

	MOV	DI,OFFSET AX25STRING
	MOV	CX,64
	MOV	AL,0
	REP STOSB			; CLEAR STRING

	MOV	DI,OFFSET CONNECTCALL
	MOV	AX,2020H
	MOV	CX,5
	REP STOSW

	MOV	DI,OFFSET CONNECTCALL
	MOV	CX,10
EXTRACTCALL:
	LODSB
	CMP	AL,20H
	JE	EXTR10			; END

	STOSB
	LOOP	EXTRACTCALL

DECODECALL99_J:

	JMP	DECODECALL99		; DUFF

EXTR10:
	PUSH	SI

	MOV	SI,OFFSET CONNECTCALL
	CALL	CONVTOAX25		; CONVERT PARAM TO AX25 FORMAT

	MOV	SI,OFFSET AX25CALL
	MOV	DI,OFFSET AX25STRING
	MOV	CX,7
	REP MOVSB

	POP	SI
	JNZ	DECODECALL99_J		; INVALID
;
;	GET NEXT FIELD = COULD BE CALLSIGN, VIA, OR S (FOR SAVE)
;
	MOV	DIGIPTR,OFFSET AX25STRING+7

DIGILOOP:

	MOV	DI,OFFSET DIGICALL
	MOV	AX,2020H
	MOV	CX,5
	REP STOSW

	MOV	DI,OFFSET DIGICALL
	MOV	CX,10

	CMP	BYTE PTR [SI],20H
	JE	NOTDIGI			; END OF DIGI STRING

EXTRACTDCALL:

	LODSB
	CMP	AL,20H
	JE	EXTRD10			; END

	CMP	AL,','
	JE	EXTRD10			; END

	STOSB
	LOOP	EXTRACTDCALL

	JMP SHORT DECODECALL99		; DUFF

EXTRD10:

;
;	CHECK FOR VIA, ETC
;
	CMP	DIGICALL,'V'
	JNE	NOTVIA

	MOV	AL,DIGICALL+1
	CMP	AL,20H
	JE	DIGILOOP		; V

	CMP	AL,'I'
	JNE	NOTVIA

	MOV	AL,DIGICALL+2
	CMP	AL,20H
	JE	DIGILOOP		; VI

	CMP	AL,'A'
	JNE	NOTVIA

	CMP	DIGICALL+3,' '
	JE	DIGILOOP		; VIA

NOTVIA:

	CMP	WORD PTR DIGICALL,' S'	; S SPACE = STAY
	JNE	NOTSTAY

	MOV	STAYSWITCH,'S'
	JMP	DIGILOOP

NOTSTAY:

	CMP	WORD PTR DIGICALL,' Z'	; Z SPACE = 'SPY'
	JNE	NOTSPY

	MOV	SPYSWITCH,'Z'
	JMP	DIGILOOP

NOTSPY:

	CMP		DIGIPTR,OFFSET AX25STRING+63	; GOT 8 DIGIS?
	JE		DECODECALL99

	PUSH	SI			; SAVE COMMAND POINTER

	MOV	SI,OFFSET DIGICALL
	CALL	CONVTOAX25		; CONVERT PARAM TO AX25 FORMAT

	MOV	SI,OFFSET AX25CALL
	MOV	DI,DIGIPTR
	MOV	CX,7
	REP MOVSB

	POP	SI
	JNZ	DECODECALL99		; INVALID


	MOV	DIGIPTR,DI		; SAVE

	JMP	DIGILOOP

NOTDIGI:

	XOR	AL,AL			; RETURN ZERO

	MOV	AX,SI			; NEXT CHAR IN INPUT STRING
	MOV	SI,OFFSET AX25STRING
