	LEA	SI,L4USER[SI]
	LEA	DI,OURCALL[BX]
	MOV	CX,7
	REP MOVSB
;
	IF	TNC2
	TEST	AL,HOST+BPQHOST
	ELSE
	TEST	AL,BPQHOST
	ENDIF
	JNZ	DONTFLIP		; LEAVE ALONE FOR HOST

	XOR	BYTE PTR -1[DI],00011110B	; FLIP SSID

DONTFLIP:

	MOV	AL,CONNECTPORT
	MOV	LINKPORT[BX],AL		; PUT IN PORT

	MOV	AL,DS:PORTT1[BP]
	MOV	L2TIME[BX],AL		; SET STANDARD TIMER VALUE
