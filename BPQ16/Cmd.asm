	MOV	L4CROSSLINK[SI],BX	; POINTER FROM OLD TO NEW
 
	LEA	SI,L4USER[SI]
	LEA	DI,L4USER[BX]
	MOV	CX,7
	REP MOVSB			; PUT IN CALL
