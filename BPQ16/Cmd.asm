	MOV	SUPPRESS,0
DEC000:
	PUSH	BX
	PUSH	SI
	PUSH	CX

	MOV	CONVFLAG,0
	MOV	CX,8
	MOV	WORD PTR ACCUM,AX
	MOV	WORD PTR ACCUM+2,DX

	MOV	SI,OFFSET POWERS	; LOW ORDER OF FIRST
DEC010:
	MOV	BL,0			; LOOP COUNT FOR SUBTRACT
DEC020:
	CALL	SUB4
	JC	DEC030			; TOO SMALL
	INC	BL			; NUMBER OF SUBTRACTS
	JMP	DEC020
;
DEC030:
	CALL	ADD4			; PUT BACK
	MOV	AL,BL
	ADD	AL,'0'			; TO CHARACTER FORM
	TEST	CONVFLAG,1
	JNZ	DEC040			; ALREADY HAVE A SIGNIFICANT CHAR
	CMP	AL,'0'			; IS IT A ZERO
	JE	DEC036			; YES, SO CHANGE TO SPACE
	OR	CONVFLAG,1		; SET NON-ZERO CHAR FOUND
	JMP SHORT DEC040		; AND PRINT IT
;
DEC036:
	CMP	SUPPRESS,1
	JE	DEC044			; SUPPRESS LEADING SPACES

	MOV	AL,' '
DEC040:
	STOSB
DEC044:
	ADD	SI,4
	LOOP	DEC010

	MOV	AL,ACCUM		; REMAINDER
	ADD	AL,'0'
	STOSB				; PRINT IT
;
	POP	CX
	POP	SI
	POP	BX

	RET
;
;	ROUTINE TO ADD TWO 4 BYTE NUMBERS
;
;
ADD4:
	MOV	AL,[SI]
	ADD	ACCUM,AL
	MOV	AL,[SI+1]
	ADC	ACCUM+1,AL
	MOV	AL,[SI+2]
	ADC	ACCUM+2,AL
	MOV	AL,[SI+3]
	ADC	ACCUM+3,AL

	RET
;
;	ROUTINE TO SUBTRACT TWO 4 BYTE NUMBERS
;
;
SUB4:
	MOV	AL,[SI]
	SUB	ACCUM,AL
	MOV	AL,[SI+1]
	SBB	ACCUM+1,AL
	MOV	AL,[SI+2]
	SBB	ACCUM+2,AL
	MOV	AL,[SI+3]
	SBB	ACCUM+3,AL

	RET

