STATSTIMER:

	ADD	WORD PTR STATSTIME,1
	ADC	WORD PTR STATSTIME+2,0		; ONCE PER MINUTE

	MOV	SI,PORTTABLE
	MOV	CH,0
	MOV	CL,NUMBEROFPORTS

STATST10:

	MOV	AX,SENDING[SI]
	MOV	DX,0			; PROTECT AGAINST OVERFLOW IN OS/2
	IFDEF	PCSWITCH
	DIV	TICKDIV
	ELSE
	DIV	D60
	ENDIF


	MOV	AVSENDING[SI],AL

	MOV	AX,SENDING[SI]
	ADD	AX,ACTIVE[SI]
	MOV	DX,0

	IFDEF	PCSWITCH
	DIV	TICKDIV
	ELSE
	DIV	D60
	ENDIF

	MOV	AVACTIVE[SI],AL


	MOV	SENDING[SI],0
	MOV	ACTIVE[SI],0


	MOV	SI,PORTPOINTER[SI]
	LOOP	STATST10

	RET


COMMANDHANDLER:
;
	MOV	CMDSTACKP,SP		; SAVE STACK FOR NO BUFFER RECOVERY
;
;	GET PACLEN FOR THIS CONNECTION
;
	MOV	AH,0
	MOV	AL,SESSPACLEN[BX]
	OR	AX,AX
	JNZ	PACLENOK

	MOV	AX,PACLEN		; DONT ALLOW ZERO!
PACLENOK:
	MOV	CMDPACLEN,AX

	PUSH	DI
;
;	TRANSPORT TABLE ENTRY IN BX, MESSAGE IN DI
;
 	LEA	SI,6[DI]		; TO DATA

	MOV	DI,OFFSET COMMANDBUFFER
	MOV	CX,40
	MOV	AX,2020H
	REP STOSW

	MOV	DI,OFFSET COMMANDBUFFER
	MOV	CX,80

COMMANDDECODE00:

	LODSB
	AND	AL,7FH			; MASK PARITY

	CMP	AL,0DH
	JE	COMMANDDECODE10

	CMP	AL,61H
	JB	COMMANDDECODE05
	CMP	AL,7AH
	JA	COMMANDDECODE05
	SUB	AL,20H

COMMANDDECODE05:

	STOSB
	LOOP	COMMANDDECODE00

COMMANDDECODE10:
;
;	USE INPUT MESSAGE BUFFER FOR REPLY
;
	POP	DI
	MOV	REPLYBUFFER,DI
;
;	SET UP HEADER
;
	MOV	AL,LINKPORT[BX]		; FROM LINK TABLE
	MOV	MSGPORT[DI],AL

	LEA	DI,5[DI]		; SKIP HEADER
	MOV	AL,0F0H
	STOSB				; PID

	CALL	SETUPNODEHEADER

	PUSH	DI			; SAVE REPLY BUFFER

REDOCOMMAND:

	MOV	SI,OFFSET COMMANDBUFFER ; MESSAGE ADDR
	CALL	SCAN			; STRIP LEADING SPACES
;
	CMP	BYTE PTR [SI],20H
	JE	NULLCMD			; IGNORE NULL COMMAND

	MOV	DI,OFFSET COMMANDLIST
	MOV	CX,NUMBEROFCOMMANDS

COMMANDLOOP:

	CMP	DI,OFFSET APPL1
	JNE	NOTAPP1

	MOV	APPLMASK,1		; FOR APPLICATION ATTACH REQUESTS
	MOV	ALIASPTR,OFFSET CMDALIAS

NOTAPP1:

 	MOV	CMDPTR,DI		; POINTER TO COMMAND NAME

	PUSH	SI
	PUSH	DI
	PUSH	CX

	MOV	CL,CMDLEN[DI]		; MIN LENGTH
	REP CMPSB
	JE	COMMANDFOUND

	POP	CX
	POP	DI
	POP	SI
TRYNEXT:

	SHL	APPLMASK,1
	ADD	ALIASPTR,16

	ADD	DI,TYPE CMDX

	LOOP	COMMANDLOOP

	POP	DI

INVALIDCMD:

	INC	BADCOMMANDS[BX]
	CMP	BADCOMMANDS[BX],6
	JE	FORCEDISC		; TOO MANY ERRORS


	MOV	SI,OFFSET CMDERRMSG
	MOV	CX,CMDERRLEN
	REP MOVSB

	JMP	SENDCOMMANDREPLY

NULLCMD:

	POP	DI

NOREPLY:

	MOV	DI,REPLYBUFFER
	CALL	RELBUFF
	RET

BYECMD:
FORCEDISC:

	MOV	DI,REPLYBUFFER
	CALL	RELBUFF

	MOV	STAYFLAG[BX],0
	CALL	CLOSECURRENTSESSION

	RET


COMMANDFOUND:

	CMP	BYTE PTR [SI],' '
	JE	ENDOFCOMMAND

	CMPSB
	JE	COMMANDFOUND		; LOOP TILL ALL COMPARED OR END
;
;	END OF STRING WRONG
;
	POP	CX
	POP	DI
	POP	SI	

	JMP	TRYNEXT

ENDOFCOMMAND:

	MOV	BADCOMMANDS[BX],0	; RESET ERROR COUNT
	POP	CX
	POP	DI
	POP	AX			; TO CLEAR STACK

	IFDEF	SYSOP
;
;	SEE IF SYSOP COMMAND, AND IF SO IF PASSWORD HAS BEEN ENTERED
;
	CMP	DI,OFFSET PASSCMD
	JAE	COMM_OK
;
;	NEEDS PASSWORD FOR SYSOP COMMANDS
;
	CMP	PASSWORD[BX],0FFFFH
	JNE	NEEDPASS

COMM_OK:

	MOV	VALNODESFLAG,0		; NOT VALID NODES COMMAND

	ENDIF

	MOV	AX,CMDPROC[DI]		; COMMAND PROCESSOR

	POP	DI			; COMMAND REPLY BUFFER


	PUSH	AX
	RET				; ENTER COMMAND PROCESSOR -
					; SI POINTS TO CHAR FOLLOWING COMMAND
;
	IFDEF	SYSOP

NEEDPASS:

	POP	DI

PASSERROR:

	MOV	SI,OFFSET PASSWORDMSG
	MOV	CX,LPASSMSG
	REP MOVSB

	JMP	SENDCOMMANDREPLY

	ENDIF



CMDI00:

	MOV	SI,INFOMSG
	MOV	CX,INFOLEN

	CALL	MOVEANDCHECK		; CHECK FOR PACLEN

	JMP	SENDCOMMANDREPLY


BADLINK:

	JMP	INVALIDCMD

LINKCMD:
;
;	PROCESS *** LINKED to CALLSIGN
;
	CMP	LINKEDFLAG,'Y'		; UNCONDITIONAL?
	JE	LINKOK

	CMP	LINKEDFLAG,'A'		; APPL ONLY?
	JNE	BADLINK			; NOT ALLOWED
;
;	SEE IF HOST SESSION OR PASSWORD GIVEN
;
	IF	TNC2
	TEST	L4CIRCUITTYPE[BX],HOST+BPQHOST
	ELSE
	TEST	L4CIRCUITTYPE[BX],BPQHOST
	ENDIF
	JNZ	LINKOK			; LET HOST DO ANYTHING

	IFDEF	SYSOP
;
	CMP	PASSWORD[BX],0FFFFH
	JE	LINKOK

	JMP	PASSERROR		; SAY NEEDS PASSWORD

	ELSE

	JMP	BADLINK

	ENDIF

LINKOK:

	MOV	CONNECTREPLYPTR,DI	; SAVE POINTER
	MOV	CONNECTSESSION,BX
	ADD	SI,3			; SKIP ' to '
	CALL	SCAN
	CMP	BYTE PTR [SI],20H
	JE	LINKC00			; NO CALLSIGN

	CALL	DECODECALLSTRING
	JZ	LINKC10			; OK

LINKC00:
	JMP	CMDC98			; SEND 'INVALID CALL'

LINKC10:

	LEA	DI,L4USER[BX]
	MOV	CX,7
	REP MOVSB

	MOV	SI,OFFSET OKMSG
	MOV	DI,CONNECTREPLYPTR
	MOV	CX,3
	REP MOVSB

	JMP	SENDCOMMANDREPLY

;
CMD?00:
;
;	DISPLAY AVAILABLE COMMANDS
;
	MOV	CX,8
	MOV	SI,OFFSET APPLS
ADDAPPLS:
	CMP	BYTE PTR [SI],'*'
	JE	NEXTAPPL

	PUSH	SI
	CALL	COPYTOSPACE
	MOV	AL,20H
	STOSB
	POP	SI

NEXTAPPL:

	ADD	SI,TYPE CMDX
	LOOP	ADDAPPLS

	MOV	SI,OFFSET CMDLIST
	MOV	CX,CMDLISTLEN

	CMP	NEEDMH,1
	JE	LEAVECX

	SUB	CX,7			; REMOVE MHEARD

LEAVECX:

	REP MOVSB

	MOV	AL,0DH
