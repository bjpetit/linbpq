	ASSUME  CS:CODE

	PUBLIC	BPQHOSTPORT,STOPNODE,SYSTEM,REFRESH,BWFLAG,CLOSEIP

	EXTRN	TERMINAL:NEAR,TERMINIT:NEAR,TERMTRACE:NEAR


	ASSUME  CS:CODE
;
TERMDATA	SEGMENT PUBLIC	'TERMDATA'

	EXTRN	LOGNAME:BYTE

TERMDATA	ENDS

PORT		DB	0

PAC4NAME	DB	'PAC4LOG.XXX',0

BADPORTMSG	DB	'Port specified is invalid',0dh,0ah,'$'

TEN		DB	10
NEWVALUE	DW	0
WORD10		DW	10
WORD16		DW	16

SYSTEM		DB	0		; SUBSYSTEM OWNING DISPLAY - TERM OR IPGATE
REFRESH		DB	0
BWFLAG		DB	0

CLOSEIP:

	RET

	PUBLIC	BUFFLEN

BUFFLEN	EQU	340

NODELINE	DB	350 DUP (0)

START:

	MOV	AX,CODE
	MOV	DS,AX

	MOV	BWFLAG,AL

	MOV	SI,5DH

	MOV	AL,ES:[SI]
	MOV	PAC4NAME+8,AL

	MOV	AL,ES:1[SI]
	MOV	PAC4NAME+9,AL

	MOV	AL,ES:2[SI]
	MOV	PAC4NAME+10,AL

	CALL	GETVALUE

	MOV	AL,BYTE PTR NEWVALUE

	CMP	AL,64
	JBE	PORTOK

DUFFPORT:

	mov	dx,offset BADPORTMSG
	mov	ah,9
	int	21h

	MOV	AH,4CH
	INT	21H
;

portok:

	MOV	BYTE PTR PORT,AL	; PARAM IS PORT NUMBER +

	PUSH	ES

	MOV	AX,CODE
	MOV	ES,AX

	CALL	INIT

	POP	ES

	MOV	AL,ES:6DH		; 2ND PARAM
	MOV	BWFLAG,AL
;
;	IF PORT IS 0, ASK SWITCH FOR A STREAM TO USE
;
	MOV	AL,PORT
	CMP	AL,0
	JNE	GOTPORT

	MOV	AH,13
	CALL	NODE

	CMP	AL,255
	JNE	GOTPORT

	MOV	DX,OFFSET NOFREEPORTS

	JMP	CONFIGERR

GOTPORT:

	MOV	BYTE PTR PORT,AL

	PUSH	ES

	MOV	BX,TERMDATA
	MOV	ES,BX

	MOV	SI,OFFSET PAC4NAME
	MOV	DI,OFFSET LOGNAME
	MOV	CX,12
	REP MOVSB

	POP	ES

	CALL	TERMINIT

	MOV	AH,1
	MOV	DX,2			; APPL MASK
	MOV	CX,80H			; APPL FLAGS

	CALL	NODE			; SET APPL MASK TO ENABLE MONITORING
;
BG:

	MOV	SYSTEM,0

	CALL	TERMINAL
;
	MOV	SYSTEM,0

	MOV	AX,CODE
	MOV	DS,AX
	MOV	ES,AX
;
;	LOOK FOR TRACE DATA
;
	MOV	AH,11
	MOV	DI,OFFSET NODELINE
	CALL	NODE

	CMP	CX,0
	JE	NOMONITOR

	MOV	DI,OFFSET NODELINE
	CALL	TERMTRACE		; DISPLAY TRACE FRAME


NOMONITOR:

	MOV	AX,1000H
	INT	15H			; FOR DESQVIEW

	STI				; FOR IBM XT BIOS BUG

	MOV	AX,1680H
	INT	2FH			; WINDOWS RELEASE TIMESLICE

	JMP	BG

STOPNODE:

EXIT:
;
;	DISCONNECT, CLEAR APPL FLAGS AND DEALLOCATE STREAM
;
	MOV	AH,6
	MOV	CX,2
	CALL	NODE			; DISCONNECT

	MOV	AH,1
	MOV	DX,0			; APPL MASK
	MOV	CX,0

	CALL	NODE			; CLEAR APPL MASK TO STOP MONITORING
;
;	RELEASE STREAM
;
;
;	AH = 13 Allocate/deallocate stream
;		If AL=0, return first free stream and allocate
;		If AL>0, CL=1, Allocate stream. If aleady allocated,
;		   return CX nonzero, else allocate, and return CX=0
;		If AL>0, CL=2, Release stream
;
	MOV	AH,13
	MOV	CL,2

	CALL	NODE

 	MOV	AH,4CH
	INT	21H
;
GETVALUE:
;
;	EXTRACT NUMBER (HEX OR DECIMAL) FROM INPUT STRING
;
	MOV	NEWVALUE,0

	MOV	AL,ES:[SI]
	CMP	AL,'$'			; HEX?
	JE	DECODEHEX

VALLOOP:

	MOV	AL,ES:[SI]
	CMP	AL,' '
	JBE	ENDVALUE
;
;	ANOTHER DIGIT - MULTIPLY BY 10
;
	MOV	AX,NEWVALUE
	MUL	WORD10
	MOV	NEWVALUE,AX

	MOV	AL,ES:[SI]
	INC	SI
	SUB	al,'0'
	JC	DUFFVALUE
	CMP	AL,10
	JNC	DUFFVALUE

	MOV	AH,0
	ADD	NEWVALUE,AX

	JC	DUFFVALUE
	JMP	VALLOOP

DECODEHEX:

	INC	SI

HEXLOOP:

	MOV	AL,ES:[SI]
	CMP	AL,' '
	JBE	ENDVALUE
;
;	ANOTHER DIGIT - MULTIPLY BY 16
;
	MOV	AX,NEWVALUE
	MUL	WORD16
	MOV	NEWVALUE,AX

	MOV	AL,ES:[SI]
	CMP	AL,'Z'
	JBE	CHAROK

	SUB	AL,20H			; TO LC

CHAROK:

	INC	SI
	SUB	al,'0'
	JC	DUFFVALUE
	CMP	AL,10
	JC	HEXOK
	SUB	AL,7
	CMP	AL,10
	JC	DUFFVALUE

	CMP	AL,16
	JNC	DUFFVALUE
HEXOK:
	MOV	AH,0
	ADD	NEWVALUE,AX
	JMP	HEXLOOP

ENDVALUE:
	CLC
	RET

DUFFVALUE:
	STC
	RET

NODE:
	MOV	AL,PORT
NODEINT:
	INT	7FH
	RET


BPQHOSTPORT:

	CALL	NODE

	IRET

