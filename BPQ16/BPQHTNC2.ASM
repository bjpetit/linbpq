	MOV	TRANSTIMER[BX],0

	CMP	CRFLAG[BX],1
	JE	KBCR_JMP		; SEND IT

	JMP	SENDPACKET

KBCR_JMP:
	JMP	KBCR

NOTSENDPAC:

KEYB06C:
;
;	COMMAND OR CONV MODE
;
	CMP	AL,20H
	JB	KBCTRL
KBNORM:
	CMP	MSGLEN[BX],256
	JA	TOOLONG			; PROTECT BUFFER
;
	INC	MSGLEN[BX]
	MOV	DI,CURSOR[BX]
	STOSB
	MOV	CURSOR[BX],DI

TOOLONG:

	TEST	ECHOFLAG[BX],0FFH
	JZ	NOECHO

	CALL	KBECHO

NOECHO:

	MOV	AL,TPACLEN[BX]
	CMP	BYTE PTR MSGLEN[BX],AL
	JB	NOSEND
;
;	DONT APPLY PACLEN IN COMMAND MODE
;
