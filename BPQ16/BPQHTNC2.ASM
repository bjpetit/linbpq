INIT:
	MOV	DATASEG,DS

	MOV	DX,OFFSET MSG
	MOV	AH,9
	INT	21H
;
;	GET NODE INTERRUPT NUMBER FROM CONFIG FILE
;
	MOV	DX,OFFSET CONFIGFILENAME
	MOV	AH,3DH
	MOV	AL,0			; READ ONLY
	INT	21H			; OPEN IT

	JC	NOCONFIGFILE

	MOV	CONFIGHANDLE,AX

	MOV	BX,AX
	MOV	DX,OFFSET CONFIGTABLE
	MOV	CX,256
	MOV	AH,3FH

	INT	21H			; READ
	CMP	AX,CX
	JNE	SHORTCONFIG

	JMP SHORT PROCESSCONFIG

NOCONFIGFILE:

	MOV	DX,OFFSET NOCONFIGMSG

DISPERR:

	MOV	AH,9
	INT	21H

	MOV	AX,4C01H
	INT	21H


SHORTCONFIG:

	MOV	DX,OFFSET BADCONFIGMSG
	JMP SHORT DISPERR


PROCESSCONFIG:

	MOV	AL,BYTE PTR C_PACLEN
	MOV	SAVEPACLEN,AL

	MOV	SI,OFFSET C_UNPROTO
	MOV	DI,OFFSET UNPROTOCALL	; UNPROTO DEST
	MOV	CX,80

UNPLOOP:
;
;	CONVERT TO UPPER CASE
;
	LODSB
	CMP	AL,'a'
	JB	UNPOK

	CMP	AL,'z'
	JA	UNPOK

	SUB	AL,20H
UNPOK:
	STOSB
	LOOP	UNPLOOP
;
;	CHECK SWITCH IS LOADED
;
	PUSH	DS

	MOV	AL,C_HOSTINTERRUPT	; INTERRUPT
	MOV	BYTE PTR INTVAL+1,AL	; PATCH NODE CALL INSTRUCTION

	MOV	AH,0
	ADD	AX,AX
	ADD	AX,AX
	MOV	SI,AX

	XOR	AX,AX
	MOV	DS,AX
;
;	DS:SI POINTS TO VECTOR
;
	CMP	WORD PTR DS:2[SI],0
	POP	DS

	JNE	SWITCHINTOK

NOSWITCH:

	MOV	DX,OFFSET NOTBPQERROR

	JMP SHORT DISPERR


SWITCHINTOK:

	MOV	AH,0
	CALL	NODE			; CHECK IF LOADED AND GET VERSIONS

	CMP	AX,'PB'			; NOT BPQ
	JE	SWITCHOK

	MOV	DX,OFFSET NOTBPQERROR
	JMP SHORT DISPERR

SWITCHOK:

	CMP	DH,MAJORVERSION
	JNE	BADVERSION

	CMP	DL,MINORVERSION
	JE	VERSIONOK

BADVERSION:

	MOV	DX,OFFSET VERSERROR
	JMP SHORT DISPERR

VERSIONOK:
;
;	READ TNCPORTS FROM CONFIG FILE
;
	MOV	BX,CONFIGHANDLE
	MOV	DX,OFFSET CONFIGTABLE
	MOV	CX,256
	MOV	AH,3FH

	INT	21H			; READ
	CMP	AX,CX
	JNE	SHORTCONFIG
;
;	SEE IF DEFAULT PARAMS
;
	CMP	WORD PTR DS:5DH,'  '	; SPACES?
	JNE	USEPARAMS
;
;	INITIALISE FROM INFO IN TNCPORTS IN CONFIG FILE
;
	MOV	BX,TNCTABLE
;
	MOV	SI,OFFSET CONFIGTABLE+128
	MOV	CX,16			; MAX

TNCLOOP:

	LODSB				; PORT NUMBER
	OR	AL,AL			; END OF LIST?
	JE	NOMORETNCS

	DEC	AL			; COMBIOS NUMBERS START FROM 1, INT 14 PORTS FROM 0
	MOV	BIOSPORT[BX],AL

	INC	NUMBEROFSTREAMS

	PUSH	SI
	CALL	INITIALISETNCPORT
	POP	SI

	LODSB
;
;	SET APPL CONTROL BITS
;
	MOV	AL,3[SI]
	MOV	APPLFLAGS[BX],AL	; FLAGS
;
;	SET APPL IF SPECIFIED
;
	MOV	AL,[SI]
	OR	AL,AL
	JZ	NOAPPL			; NOT SUPPLIED

	MOV	APPLICATION[BX],AL

NOAPPL:
	ADD	SI,6			; TO NEXT ENTRY
	ADD	BX,TYPE TNCDATA
	LOOP	TNCLOOP

NOMORETNCS:

	CMP	BX,TNCTABLE
	JNE	GOTSOME
;
;	NO PORTS DEFINED - EXIT
;
	MOV	DX,OFFSET NOPORTSMSG
	JMP	DISPERR

GOTSOME:

	JMP	INSTALLINTS


BADPORTVAL:

	MOV	AH,9
	MOV	DX,OFFSET ERRORMSG
	INT	21H

	RET

USEPARAMS:

	MOV	SI,82H			; PARAM STRING

	CALL	GETVALUE		; NUMBER OF PORTS
	JC	BADPORTVAL

	MOV	AL,BYTE PTR NEWVALUE
	OR	AL,AL
	JZ	BADPORTVAL

	CMP	AL,32
	JA	BADPORTVAL

	MOV	AL,BYTE PTR NEWVALUE
	CBW
	MOV	NUMBEROFSTREAMS,AX

	CALL	GETVALUE		; BIOS PORT
	JC	BADPORTVAL

	MOV	AL,BYTE PTR NEWVALUE
	OR	AL,AL
	JZ	BADPORTVAL

	DEC	AL
	MOV	PORT,AL
;
	CALL	GETVALUE		; NODE PORT 
	JC	BADPORTVAL

	MOV	AL,BYTE PTR NEWVALUE
	OR	AL,AL
	JZ	BADPORTVAL

	ADD	AL,BYTE PTR NUMBEROFSTREAMS
	CMP	AL,65
	JA	BADPORTVAL

	MOV	AL,BYTE PTR NEWVALUE
	MOV	STREAMBASE,AL
;
;	INITIALISE PORTS
;
	MOV	CX,NUMBEROFSTREAMS
	MOV	BX,TNCTABLE
;
;	BX = PORT ENTRY, AL = INT14 PORT NUMBER
;
INITPORT:

	MOV	AL,PORT
	MOV	BIOSPORT[BX],AL

	CALL	INITIALISETNCPORT
	ADD	BX,TYPE TNCDATA

	INC	PORT

	LOOP	INITPORT

INSTALLINTS:

	PUSH	BX
;
;	INSTALL INTERRUPTS
;
	MOV	AL,14H				
	MOV	AH,35H				
	INT	21H			; GET VECTOR	
	MOV	WORD PTR OLDINT14,BX		
	MOV	WORD PTR OLDINT14+2,ES	; SAVE	

	MOV	DX,OFFSET INT14			
	MOV	AL,14H				
	MOV	AH,25H			; SET NEW VECTOR
