;	TCP/IP DATAGRAM
;
	mov	SI,OFFSET IP_MSG
	call	NORMSTR
;
;	MAY DECODE IT LATER
;
	JMP	ADD_CR

NOTIP:

	mov	SI,OFFSET DUFF_NET_MSG
	call	NORMSTR


END_NETROM:

	jmp	add_cr

