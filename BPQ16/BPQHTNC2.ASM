TESTTIMER:
;
;	SEE IF TIME TO CALL TIMER CODE
;
	PUSH	AX

TIMLOOP:

	MOV	AH,15
	CALL	NODE

	SUB	AX,CS:LASTTIME
	CMP	AX,6			; 1/3RD SEC
	JB	LEAVETIMER

	ADD	CS:LASTTIME,6

	PUSH	ES
	PUSH	CS
	POP	ES

	PUSH	BX
	PUSH	CX
	PUSH	DX
	PUSH	DI

	CALL	TNCTIMERPROC

	POP	DI
	POP	DX
	POP	CX
	POP	BX

	POP	ES

	JMP	TIMLOOP

LEAVETIMER:

	POP	AX

	RET

TNCTIMERPROC:
;
;	CALLED AT 3HZ
;
;	CHECK FOR TRANSPARENT MODE SEND TIMOUT
;
	
	cmp	StartedFlag,0
	je	TIMRET

	MOV	BX,TNCTABLE
	MOV	CX,NUMBEROFSTREAMS
	JCXZ	TIMRET

	MOV	AH,0

TIM000:

	OR	AH,TRACEFLAG[BX]	; SEE IF ANY PORTS ARE MONITORING
	CMP	TRANSTIMER[BX],0
	JE	TIM100			; NOTHING DOING

	DEC	TRANSTIMER[BX]
	JNZ	TIM100			; STILL OK
;
	MOV	AUTOSENDFLAG[BX],1	; CANT DO IT HERE - WAIT TILL NEXT INT 14

TIM100:
;
	ADD	BX,TYPE TNCDATA		
	LOOP	TIM000
;
;	IF ANY PORTS HAVE MONITOR ENABLED, SET MONITOR BIT ON FIRST PORT
;
	MOV	CL,0
	OR	AH,AH
	JZ	NO_MON

	MOV	CL,80H			; TOP BIT CONTROLS MONITOR

NO_MON:
;
	MOV	BX,TNCTABLE
	MOV	DL,APPLICATION[BX]
	OR	CL,APPLFLAGS[BX]

	CMP	CONOK[BX],0
	JNE	SETAPPLM

	MOV	DL,0			; STOP CONNECTS

SETAPPLM:

	MOV	AH,1
	MOV	AL,NODEPORT[BX]
	CALL	NODE
;
;	DO ANY MONITORING
;
	MOV	BX,TNCTABLE
	MOV	AL,NODEPORT[BX]
	MOV	AH,11
	MOV	DI,OFFSET MONITORDATA

	CALL	NODE

	CMP	CX,0
	JE	TRACEEND

	MOV	DI,OFFSET MONITORDATA

	MOV	CX,NUMBEROFSTREAMS
	JCXZ	TRACEEND
;
TRACELOOP:

	PUSH	BX
	PUSH	CX
	PUSH	DI
	CALL	DISPLAYFRAME
	POP	DI
	POP	CX
	POP	BX

	ADD	BX,TYPE TNCDATA
	LOOP	TRACELOOP

TRACEEND:
TIMRET:

	RET


KEYSUPPORT:

	TEST	MODEFLAG[BX],COMMAND
	JNZ	KEYB06C			; COMMAND MODE - SKIP TRANS TEST

	TEST	MODEFLAG[BX],TRANS
	JNZ	KEYB06T			; TRANS MODE	
;
;	CONV MODE - SEE IF CPACTIME ON
;
	CMP	CPACTIME[BX],0
	JE	KEYB06			; NOT CPACTIME

	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[BX],AH	; REPRIME SEND TIMER
	JMP SHORT KEYB06		; PROCESS CHAR

KEYB06T:
	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[BX],AH
	JMP SHORT KBNORM		; TRANSPARENT MODE

KEYB06:
;
;	STILL JUST CONV MODE
;
	CMP	AL,SENDPAC[BX]
	JNE	NOTSENDPAC
;
;	SEND PACKET CHAR - SHOUD WE SEND IT?
