	call	DISPLAY_BYTE_1

	mov	AL,'>'
	call	PUTCHARINBUFFER

	CALL	DOL4FLAGS

	JMP SHORT END_NETROM

NOT_L4IACK:

	OR	AL,AL
	JNZ	NOTIP
