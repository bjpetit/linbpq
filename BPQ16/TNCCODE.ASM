	IFDEF	PCSWITCH
;
;	CHECK LENGTH OF HOST MODE RAW DATA QUEUES
;
	MOV	CX,BPQHOSTSTREAMS
	MOV	BX,OFFSET BPQHOSTVECTOR
;
;	VECTOR FORMAT IS:
;
;	0-1  2 BYTES - ADDRESS OF SESSION ENTRY
;	  2  1 BYTE  - STATE CHANGE FLAGS
;	  3  1 BYTE  - APPLICATION MASK
;	  4  1 BYTE  - APPLICATION FLAGS - TOP BIT ENABLES MONITORING
;	5-6  2 BYTES - RAW FRAME QUEUE
;

BPQTRACELOOP:

	TEST	BYTE PTR 4[BX],80H
	JZ	NOTRACE			; NOT ENABLED
;
	CMP	WORD PTR 5[BX],0
	JE	NOTRACE			; NONE QUEUED

	PUSH	BX
	ADD	BX,5			; TO HEADER
	CALL	COUNT_Q
	POP	BX

	CMP	AL,10			; MAX LENGTH
	JB	NOTRACE

	LEA	SI,5[BX]
	CALL	Q_REM
	JZ	NOTRACE			; WOT?

	CALL	RELBUFF

NOTRACE:

	ADD	BX,VECTORLENGTH
	LOOP	BPQTRACELOOP

	ENDIF

	RET				

