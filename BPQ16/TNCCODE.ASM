;
;	INTERRUPT HANDLERS FOR ASYNC PORT
;
	PUBLIC	GOTTNCCHAR,TNCSENDCHAR,KANTTNCBG
;
GOTTNCCHAR:
;
;	CHARACTER AVAILABLE INTERRUPT
;
	IN	AL,(SST)
	TEST	AL,40H
	JZ	NOBREAK
;
	MOV	BREAKING,1		;	BREAK DETECTED

	RET

NOBREAK:

	CMP	BREAKING,1
	JNE	NOTBREAKEND
;
;	END OF BREAK
;
	MOV	BREAKING,0
	CALL	SETCOMMANDMODE

	IN	AL,SRB			; DISCARD NULL
	RET

NOTBREAKEND:

	TEST	AL,01110000B		; ERROR BITS
	JZ	NOERROR

	MOV	AL,00110101B
	OUT	(SCM),AL		; RESET ERROR

	RET

NOERROR:

	IN	AL,(SRB)

	CALL	SENDCHARSUPPORT
	RET

TNCSENDCHAR:
;
;	CHARACTER SEND INTERRUPT - JUST TURN OFF FOR NOW
;
	MOV	AL,10B
	OUT	SIMK,AL			; MASK TX INT

	RET

KANTTNCBG:
;
;	SEND ON ASYNC IF ANYTHING AVAILABLE
;
	CMP	RXCOUNT[BX],0
	JE	KANTTNCRET		; NOWT THERE

	CMP	ASYNCMODE,'T'
	JNE	DISCARDIT		; NOT IN TERMINAL MODE

	IN	AL,SST
	TEST	AL,TXRDY

	JZ	KANTTNCRET		; CANT SEND
;
;	CHECK CTS TO SEE IF TERMINAL WANTS US TO STOP SENDING
;
	IN	AL,IPFW
	TEST	AL,80H
	JNZ	KANTTNCRET		; CTS LOW

	CALL	GETONECHAR
	OUT	STB,AL

KANTTNCRET:
;
;	LOOK FOR TRANSPARENT MODE SEND
;
	CMP	AUTOSENDFLAG[BX],1
	JNE	KNOSEND

	MOV	AUTOSENDFLAG[BX],0
	CMP	KBBUFFER[BX],0
	JE	KNOSEND			; MESSAGE ALREADY SENT

	CALL	SENDPACKET
 
KNOSEND:

;
;	IF CTS HAS BEEN DROPPED, SEE IF OK TO RAISE AGAIN
;
	TEST	VMSR[BX],10H
	JNZ	KCTSOK

	CALL	CHECKCTS

KCTSOK:

	RET


DISCARDIT:

	CALL	GETONECHAR

	JMP	KNOSEND			; CHECK FOR CTS CHANGE

KDROPCTS:

	CTRL	CTS,OFF
	RET

KRAISECTS:

	CTRL	CTS,ON
	RET

	ENDIF

