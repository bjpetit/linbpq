	AND	BYTE PTR 2[BX],0FCH	; CLEAR IT

	IFDEF	OS2

	RET

	ELSE

	POP	DS
	IRET

	ENDIF

GETBUFFERSTATUS:
;
;	AH = 7	Get buffer counts for stream.  Returns:
;
;		BX = number of frames queued for receive
;		CX = number of un-acked frames to be sent
;		DX = number of buffers left in node
;		SI = number of monitor frames queued for receive

	XOR	CX,CX			; unacked frames
;
	PUSH	BX

	ADD	BX,5			; TO TRACE QUEUE
	CALL	COUNT_Q
	XOR	AH,AH

	POP	BX

	PUSH	AX			; SAVE

	MOV	SI,[BX]			; LOCAL SESSION
	CMP	SI,0
	JNE	BPQSTATE01		; CONNECTED
;
;	NOT CONNECTED - JUST RETURN FREE Q COUNT AND CHANGE BITS
;
	MOV	BX,0			; NOTHING QUEUED
	JMP SHORT BPQSTATUSEND

BPQSTATE01:
;
;	CONNECTED
;
	LEA	BX,L4TX_Q[SI]

	CALL	COUNT_Q
	XOR	AH,AH
	MOV	BX,AX			; QUEUED RX FRAMES

BPQSTATE02:

	CMP	L4CROSSLINK[SI],0
	JE	BPQSTATUSEND		; NOT CONNECTED BEYOND SWITCH

	MOV	SI,L4CROSSLINK[SI]
	CALL	COUNT_QUEUED_FRAMES

	XOR	AH,AH
	MOV	CX,AX			; QUEUED TO SEND

BPQSTATUSEND:

	XOR	DH,DH
	MOV	DL,QCOUNT		; FREE BUFFERS

	POP	SI

	IFDEF	OS2

	RET

	ELSE

	POP	DS
	IRET

	ENDIF


GETCONNECTIONINFO:
;
;	PACLEN IN BX
;
	PUSH	BP

	MOV	AX,0			; PORT
	MOV	BP,AX			; PACLEN
	MOV	CX,AX			; MAXFRAME
	MOV	DX,AX			; L4 WINDOW

	MOV	BX,[BX]			; SESSION
	CMP	BX,0
	JE	NO_INFO			; NOT CONNECTED

	MOV	BP,WORD PTR SESSPACLEN[BX]

	MOV	SI,OFFSET SWITCHSP

	CMP	L4CROSSLINK[BX],0
	JE	CALLCOPY
;
;	CONNECTED OUT - GET TARGET SESSION
;
	MOV	SI,L4CROSSLINK[BX]
	MOV	DL,L4WINDOW[SI]

	MOV	AH,L4CIRCUITTYPE[SI]	; UPLINK SESSION TYPE

	TEST	AH,BPQHOST
	JNZ	HOST_ENTRY

	TEST	AH,L2LINK
	JNZ	L2_ENTRY
;
;	ANOTHER NODE
;
;	IF THE HOST IS THE UPLINKING STATION, WE NEED THE TARGET CALL
;
	TEST	L4CIRCUITTYPE[BX],UPLINK; GET SESSION TYPE	
	JZ	RETURNCALL
;

	MOV	SI,L4TARGET[SI]		; NODE
	LEA	SI,DEST_CALL[SI]	; NODE CALLSIGN

	JMP SHORT RETURNCALL

L2_ENTRY:

	PUSH	SI			; SAVE SESSION
	MOV	SI,L4TARGET[SI]
;
;	SI IS LINK ENTRY - EXTRACT PORT AND MAXFRAME
;
	MOV	AL,LINKPORT[SI]
	MOV	CL,LINKWINDOW[SI]
	MOV	DL,0			; NO L4 WINDOW
;
	LEA	SI,LINKCALL[SI]

	TEST	AH,UPLINK
	JZ	L2_DOWN			; DOWNLINK, SO WE HAVE THE RIGHT CALL
;
;	IF UPLINK, SHOULD USE SEESION CALL, IN CASE *** LINKED HAS BEEN USED
;
	POP	SI			; BACK TO SESSION
	JMP SHORT HOST_ENTRY
	
L2_DOWN:

	ADD	SP,2			; LOOSE SAVES SI

	JMP SHORT RETURNCALL

HOST_ENTRY:

	LEA	SI,L4USER[SI]

RETURNCALL:

	PUSH	DI			; USERS BUFFER
	PUSH	ES

	PUSH	AX
	PUSH	CX

	MOV	ES,CS:DATASEG
	CALL	CONVFROMAX25		; CONVERT CALL TO NORMAL

	POP	CX
	POP	AX

	POP	ES
	POP	DI

	MOV	SI,OFFSET NORMCALL

CALLCOPY:

	PUSH	CX
	MOV	CX,10

	IFDEF	OS2
.386
;
;	NEED TO USE 32 BIT MOVE, SO ENSURE TOP WORD OF ESI IS ZERO
;
	AND	ESI,0FFFFH
	AND	ECX,0FFFFH
	DB	67H
.286
	ENDIF

	REP MOVSB			; RETURN CALL
	POP	CX

NO_INFO:
	MOV	BX,BP
	MOV	BH,0
	POP	BP

	IFDEF	OS2

	RET

	ELSE

	POP	DS
	IRET

	ENDIF


BPQCONDIS:
;
;	CL=0 CONNECT USING APPL MASK IN DL
;	CL=1, CONNECT. CL=2 - DISCONNECT. CL=3 RETURN TO NODE
;
	PUSH	ES
	MOV	ES,CS:DATASEG

	CMP	CL,1
	JA	BPQDIS

	OR	BYTE PTR 2[BX],80H	; SET STREAM IN USE

	CMP	WORD PTR [BX],0
	JNE	BPQCONRET		; ALREADY CONNECTED??
;
;	SET UP A SESSION FOR THE CONSOLE
;
	PUSH	BX
	MOV	AL,DL
	CMP	CL,0
	JE	GOTMASK

	MOV	AL,3[BX]		; SO WE GET CORRECT CALLSIGN	
GOTMASK:
	CALL	SETUPBPQCONSOLESESSION
;
	MOV	SI,BX
	POP	BX
;
;	SI=SESSION, BX=VECTOR
;
	CMP	SI,0
	JE	BPQDIS00		; RETURN DISCONNECTED??

	MOV	[BX],SI
 	MOV	L4CIRCUITTYPE[SI],BPQHOST+UPLINK

BPQCONRET:

	OR	BYTE PTR 2[BX],1	; STATE CHANGE
	
	POP	ES

	JMP	BPQRETURN

;
BPQDIS:

	CMP	WORD PTR [BX],0
	JE	BPQCONRET		; NOT CONNECTED??

	CMP	CL,3
	JNE	DO_DISC

	MOV	DISCFLAG,'S'		; SET 'STAY' FLAG

DO_DISC:

	PUSH	BX
	MOV	BX,[BX]			; TO SESSION
	CALL	CLOSESESSIONPARTNER
	POP	BX

	MOV	DISCFLAG,0		; CLEAR STAY FLAG

BPQDIS00:

	MOV	WORD PTR [BX],0
	OR	BYTE PTR 2[BX],3	; STATE CHANGE
;
	JMP SHORT BPQCONRET

SENDBPQFRAME:
;
;	ES:(E)SI = MESSAGE, CX = LENGTH, BX = VECTOR
;
	CMP	AL,0
	JE	BPQHOSTUNPROTO

	MOV	BX,[BX]
	CMP	BX,0
	JE	SENDHOSTRET		; NO SESSION
;
;	GET A BUFFER
;
BWAITFORBUFFER:

	CMP	QCOUNT,20		; PLENTY FREE?
	JAE	BENOUGH			; YES

	PUSH	AX
	CALL	RELEASE_TIMESLICE	; WAIT FOR MORE TO BE RELEASED
	POP	AX

	JMP	BWAITFORBUFFER

BENOUGH:

	PUSH	SI			; USERS BUFFER
	CALL	GETBUFF
	POP	SI

	JZ	SENDHOSTRET		; ??


	PUSH	ES			; SAVE

	PUSH	ES
	POP	DS			; MSG SEG TO DS

	MOV	ES,CS:DATASEG

	PUSH	DI			; SAVE BUFFER
	ADD	DI,5
	MOV	AL,0F0H			; PID
	STOSB

	IFDEF	OS2
.386
	AND	EDI,0FFFFH
	AND	ECX,0FFFFH
	DB	67H			; USE ESI/EDI
.286
	ENDIF

	REP MOVSB

	MOV	CX,DI
	POP	DI
	SUB	CX,DI			; LENGTH

	MOV	DS,CS:DATASEG
	
	MOV	MSGLENGTH[DI],CX
;
;	IF CONNECTED, PASS MESSAGE TO TARGET CIRCUIT - FLOW CONTROL AND
;	DELAYED DISC ONLY WORK ON ONE SIDE
;
	LEA	SI,L4RX_Q[BX]

	MOV	L4KILLTIMER[BX],0	; RESET SESSION TIMEOUT

	CMP	L4CROSSLINK[BX],0	; CONNECTED?
	JE	SENDF50			; NO, SO QUEUE NORMALLY

 	MOV	BX,L4CROSSLINK[BX]	; TO SESSION PARTNER
	CMP	L4STATE[BX],5

	JNE	SENDF50			; NOT UP YET - AGAIN QUEUE NORAMLLY

	LEA	SI,L4TX_Q[BX]

SENDF50:
	CALL	Q_ADD			; PUT ON QUEUE
;
	POP	ES

SENDHOSTRET:

	JMP	BPQRETURN

BPQHOSTUNPROTO:

	CMP	AH,2
	JNE	UNPROTORET		; ONLY USED FOR SEND DATA
;
;	SEND FRAME TO ALL RADIO PORTS
;
;	COPY DATA TO A BUFFER IN OUR SEGMENTS - SIMPLFIES THINGS LATER
;
	CMP	QCOUNT,20		; PLENTY FREE?
	JB	UNPROTORET		; DONT BOTHER IF VERY LOW

	PUSH	SI			; USERS BUFFER
	CALL	GETBUFF
	POP	SI

	JZ	UNPROTORET		; ??

	PUSH	ES			; SAVE USERS ES

	PUSH	ES
	POP	DS			; MSG SEG TO DS

	MOV	ES,CS:DATASEG

	PUSH	DI			; SAVE BUFFER

	ADD	DI,5
	MOV	AL,0F0H			; PID
	STOSB

	REP MOVSB

	MOV	CX,DI
	POP	DI
	SUB	CX,DI			; LENGTH

	MOV	DS,CS:DATASEG
	MOV	MSGLENGTH[DI],CX
;
	PUSH	DI			; SAVE FOR RELEASE
	MOV	SI,DI

	CALL	SENDUIMESSAGE
	
	POP	DI
	CALL	RELBUFF

	POP	ES			; USER'S ES

UNPROTORET:

	MOV	CX,0
	MOV	DX,0

	IFDEF	OS2

	RET

	ELSE

	POP	DS
	IRET

	ENDIF

GETBPQFRAME:

	MOV	CX,0			; FRAME LENGTH

