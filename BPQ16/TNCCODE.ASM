;	BX = TNC DATA
;
	CMP	AL,0
	JE	GETVERSION

	CMP	AL,1
	JE	GETCONNECTEDCALL

	CMP	AL,2
	JE	GETUNACKED

	CMP	AL,10H
	JE	RELUSER

	JMP SHORT NODERET

