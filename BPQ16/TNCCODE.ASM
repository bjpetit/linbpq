;
;	NODE-SPECIFIC COMBIOS EXTENTIONS 
;
;	AL = 0 - RETURN VERSION AND NODE DS
;	AL = 1 - RETURN ES:SI = CONNECTED CALL (IN AX25 FORM), OR SI=0 ZERO
;		 IF NOT CONNECTED. ALSO PORT IN AL IF L2 
;
;	AL = 2 = RETURN UNACKED PACKETS ON CONNECTION (IN AH)
