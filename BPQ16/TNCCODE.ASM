CONSOLESEND:

	PUSH	DI
	MOV	CX,MSGLENGTH[DI]

	LEA	SI,L3PID+1[DI]
	SUB	CX,6			; HEADER LENGTH
	JZ	SENDCONSEND		; ZERO LENGTH TEXT
	
	CMP	CX,257
	JB	HOSTTEST

	MOV	CX,256			; PROTECTION

HOSTTEST:

SENDCONS00:

	LODSB
	CALL	PUTCHARINBUFFER
	LOOP	SENDCONS00

SENDCONSEND:

	POP	DI

	CALL	RELBUFF

	RET

KEYSUPPORT:

	CMP	KBBUFFER[BX],0
	JNE	KEYB05			; BUFFER ALREADY ALLOCATED
;
;	GET A BUFFER
;
WAITFORBUFFER:

	CMP	QCOUNT,20		; PLENTY FREE?
	JAE	ENOUGH			; YES

	PUSH	AX
	CALL	RELEASE_TIMESLICE	; WAIT FOR MORE TO BE RELEASED
	POP	AX

	JMP	WAITFORBUFFER

ENOUGH:
	PUSH	AX
	CALL	GETBUFF
	POP	AX

	JNZ	KEYSUPP00		

	RET 				; WOT NO BUFFERS!!

KEYSUPP00:

	MOV	KBBUFFER[BX],DI		; SAVE ADDR
	MOV	L3PID[DI],0F0H

	LEA	DI,L3PID+1[DI]
	MOV	CURSOR[BX],DI		; SET MESSAGE START
	MOV	MSGLEN[BX],0
KEYB05:
	TEST	MODEFLAG[BX],COMMAND
	JNZ	KEYB06C			; COMMAND MODE - SKIP TRANS TEST

	TEST	MODEFLAG[BX],TRANS
	JNZ	KEYB06T			; TRANS MODE	
;
;	CONV MODE - SEE IF CPACTIME ON
;
	CMP	CPACTIME[BX],0
	JE	KEYB06			; NOT CPACTIME

	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[BX],AH	; REPRIME SEND TIMER
	JMP SHORT KEYB06		; PROCESS CHAR

KEYB06T:
	MOV	AH,TRANSDELAY
	MOV	TRANSTIMER[BX],AH
	JMP SHORT KBNORM		; TRANSPARENT MODE

KEYB06:
;
;	STILL JUST CONV MODE
;
	CMP	AL,SENDPAC[BX]
	JNE	NOTSENDPAC
;
;	SEND PACKET CHAR - SHOUD WE SEND IT?
;
	MOV	TRANSTIMER[BX],0

	CMP	CRFLAG[BX],1
	JE	KBCR_JMP		; SEND IT

	JMP	SENDPACKET

KBCR_JMP:
	JMP	KBCR

NOTSENDPAC:

KEYB06C:
;
;	COMMAND OR CONV MODE
;
	CMP	AL,20H
	JB	KBCTRL
KBNORM:
	CMP	MSGLEN[BX],256
	JA	TOOLONG			; PROTECT BUFFER
;
	INC	MSGLEN[BX]
	MOV	DI,CURSOR[BX]
	STOSB
	MOV	CURSOR[BX],DI

TOOLONG:

	TEST	ECHOFLAG[BX],0FFH
	JZ	NOECHO

	CALL	KBECHO

NOECHO:
	MOV	AL,TPACLEN[BX]
	CMP	BYTE PTR MSGLEN[BX],AL
	JNE	NOSEND
;
;	DONT APPLY PACLEN IN COMMAND MODE
;
	TEST	MODEFLAG[BX],COMMAND
	JNZ	NOSEND

	JMP	SENDPACKET
NOSEND:
	RET

KBCTRL:
	CMP	LFIGNORE[BX],0
	JE	KBCTRL00
;
;	IF LF, IGNORE IT
;
	CMP	AL,0AH
	JE	NOSEND			; IGNORE

	RET


KBCTRL00:

	CMP	AL,8
	JE	KBBSPACE

	CMP	AL,1AH			; CTRL/Z
	JE	KBNORM			; FOR MBX TERMINATOR

	TEST	MODEFLAG[BX],CONV
	JNZ	CTRLREST
;
;	COMMAND MODE
;
	CMP	AL,2
	JNE	NOTID

	JMP	SENDIDMSG
NOTID:
	CMP	AL,5
	JNE	NOTNODES

	JMP	SENDNODES

NOTNODES:

	CMP	AL,0DH
	JE	KBCR

	JMP SHORT CTRLREST
;
;	CONV MODE
;
CTRLREST:

	CMP	AL,3
	JE	KBESCAPE		; RETURN TO COMMAND MODE
;
	CMP	AL,14H			; CTRL/T
	JNE	IGNORECTRL

	XOR	TRACEFLAG[BX],1
	RET

IGNORECTRL:

	JMP	KBNORM

KBESCAPE:
	CALL	SETCOMMANDMODE
	JMP	KBRET			; EXIT

SETCOMMANDMODE:

	CMP	MSGLEN[BX],0
	JE	SETCOMM00

	CALL	SENDPACKET		; PROCESS WAITING DATA

SETCOMM00:

	OR	MODEFLAG[BX],COMMAND	; BACK TO COMMAND MODE
	AND	MODEFLAG[BX],NOT (CONV+TRANS)
	MOV	TRANSTIMER[BX],0	; CANCEL TRANS MODE SEND TIMER
	MOV	AUTOSENDFLAG[BX],0	; IN CASE ALREADY SET

	MOV	SI,OFFSET CMDMSG
	MOV	CX,4
	CALL	SENDREPLY

	CALL	KBRET

	RET

KBBSPACE:
	CMP	MSGLEN[BX],0
	JE	KBBS00

	DEC	MSGLEN[BX]
	DEC	CURSOR[BX]

	TEST	ECHOFLAG[BX],0FFH
	JZ	KBBS00

	CALL	KBECHO
KBBS00:
	RET

KBCR:
	CALL	KBNORM			; PUT CR IN BUFFER

SENDPACKET:

	MOV	DI,KBBUFFER[BX]
	MOV	CX,CURSOR[BX]
	SUB	CX,DI			; MESSAGE LENGTH
	MOV	MSGLENGTH[DI],CX
;
;	SEE IF COMMAND STATE
;
	TEST	MODEFLAG[BX],COMMAND
	JZ	NOTCOMMAND

	JMP	TNCCOMMAND		; COMMAND TO TNC

NOTCOMMAND:
;
;	IF CONNECTED, SEND TO L4 (COMMAND HANDLER OR DATA),
;	   OTHERWISE SEND AS AN UNPROTO FRAME (TO ALL PORTS??)
;

	TEST	VMSR[BX],80H		; CONNECTED?
	JNZ	SENDTOL4
;
;	SEND BUFFER AS AN UNPROTO FRAME
;

	MOV	SI,DI			; MESSAGE TO SI
	CALL	SENDUIMESSAGE

	JMP	KBRET			; RESET MESSAGE BUFFER POINTERS


SENDTOL4:

	MOV	DI,KBBUFFER[BX]
	MOV	KBBUFFER[BX],0		; CLEAR BUFFER ALLOCATED
	MOV	MSGLEN[BX],0
;
;	SEE IF SESSION
;
	CALL	CHECKSESSION		; GET SESSION IN SI
	CMP	SI,0
	JE	KBCR10			; NO SESSION??
;
;	SEE IF FAR SESSION BUSY
;
	PUSH	SI			; SAVE SESSION

TESTFORBUSY:
;
	TEST	VMSR[BX],00010000B	; CHECK CTS
	JNZ	NOTBUSY

	IFDEF	PCSWITCH		; MUSTNT STOP IN DE VERSION

	CALL	RELEASE_TIMESLICE

	CALL	CHECKCTS

	JMP	TESTFORBUSY

	ENDIF
;
NOTBUSY:

	POP	SI

	LEA	SI,L4RX_Q[SI]
	CALL	Q_ADD			; PUT ON PORT RX QUEUE

	CALL	CHECKCTS		; SEE IF NOW BUSY

	RET

KBCR10:
;
;	NO FREE SESSIONS - ?? ERROR MSG
;
	CALL	RELBUFF
	MOV	KBBUFFER[BX],0
	RET

CHECKSESSION:

	MOV	SI,KBSESSION[BX]
	CMP	SI,0
	JNE	KBCR00			; OK
;
;	SET UP A SESSION FOR THE CONSOLE
;
	PUSH	BX
	PUSH	DI
	CALL	SETUPCONSOLESESSION
	POP	DI
;
	MOV	SI,BX
	POP	BX
;
;	SI=SESSION, BX=TNC
;
	CMP	SI,0
	JE	KBCR00			; NO SESSION??

	MOV	KBSESSION[BX],SI

	MOV	AL,TPACLEN[BX]
	MOV	SESSPACLEN[SI],AL	; COPY PACLEN TO SESSION LEVEL
KBCR00:
	RET

KBECHO:
	CALL	PUTCHARINBUFFER
;
	RET

TNCCOMMAND:
;
;	PROCESS COMMAND TO TNC CODE
;
	LEA	SI,L3PID+1[DI]
	MOV	DI,SI
	PUSH	SI
;
;	CONVERT TO UPPER CASE
;
	MOV	CX,200			; IN CASE GARBAGE!!
TNCUCLOOP:
	LODSB
	CMP	AL,0DH
	JE	TNCUCEND

	CMP	AL,61H
	JB	TNCUCOK
	CMP	AL,7AH
	JA	TNCUCOK

	SUB	AL,20H
TNCUCOK:
	STOSB
	LOOP	TNCUCLOOP
;
TNCUCEND:

	MOV	CMDENDADDR,SI		; SAVE REAL END OF COMMAND

	MOV	AL,20H
	MOV	CX,40
	REP STOSB			; PUT A FEW SPACES ON END FOR DECODE ROUTINES

	POP	SI
	CALL	SCAN

	CMP	BYTE PTR [SI],20H
	JE	NULLCMD			; BLANK LINE

	MOV	DI,OFFSET COMMANDLIST
	MOV	CX,NUMBEROFCOMMANDS

COMMANDLOOP:
	PUSH	SI
	PUSH	DI
	PUSH	CX

	MOV	CL,8[DI]		; MIN LENGTH
	REP CMPSB
	JE	COMMANDFOUND

	POP	CX
	POP	DI
	POP	SI
	ADD	DI,TYPE CMDX

	LOOP	COMMANDLOOP

TNCDUFF:
	MOV	SI,OFFSET WHATMSG
	MOV	CX,4
	CALL	SENDREPLY

NULLCMD:

	JMP 	SENDOK


COMMANDFOUND:

	CMP	BYTE PTR [SI],0DH
	JE	ENDOFCOMMAND

	CMP	BYTE PTR [SI],' '
	JE	ENDOFCOMMAND

	CMPSB
	JE	COMMANDFOUND		; LOOP TILL ALL COMPARED OR END
;
;	END OF STRING WRONG
;
	POP	CX
	POP	DI
	POP	SI

	JMP	TNCDUFF

ENDOFCOMMAND:

	POP	CX
	POP	DI
	POP	AX			; TO CLEAR STACK

	CALL	SCAN
	JMP	WORD PTR 9[DI]		; ENTER COMMAND PROCESSOR -
					; SI POINTS TO CHAR FOLLOWING COMMAND
	ENDIF

SCAN:
	MOV	CX,10			; DONT LOOK TOO FAR!
SCAN1:
	CMP	BYTE PTR [SI],20H
	JNE	SCANRET

	INC	SI
	LOOP 	SCAN1
	XOR	AL,AL			; RETURN Z

SCANRET:
	RET
