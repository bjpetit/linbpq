TNCTIMERPROC:

;
;	CALLED AT 3HZ
;
	IF	TNC2

;	CHECK FOR TRANSPARENT MODE SEND TIMOUT
;
	MOV	BX,TNCTABLE
	MOV	CX,NUMBEROFSTREAMS
	JCXZ	TIMRET

TIM000:
;	TEST	MODEFLAG[BX],TRANS
;	JZ	TIM100			; NOT TRANS MODE

	CMP	TRANSTIMER[BX],0
	JE	TIM100			; NOTHING DOING

	DEC	TRANSTIMER[BX]
	JNZ	TIM100			; STILL OK
;
	CMP	KBBUFFER[BX],0
	JE	TIM100			; NO MESSAGE TO SEND
;
	MOV	AUTOSENDFLAG[BX],1	; CANT DO IT HERE - WAIT TILL NEXT INT 14

TIM100:
	ADD	BX,TYPE TNCDATA		
	LOOP	TIM000
TIMRET:

