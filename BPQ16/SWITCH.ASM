	CMP	AL,' '
	JE	CONVAX40		; END OF NAME
;
	CMP	AL,'#'
	JNE	NOTHASH

	CMP	CL,7
	JE	CONVAX10		; OK AS FIRST CHAR
NOTHASH:
	CMP	AL,'/'			; ALLOW / AND DIGITS (FOR W0RLI!)
	JB	CONVAX49		; INVALID
	CMP	AL,'9'
	JBE	CONVAX10		; OK

	CMP	AL,'A'
	JB	CONVAX49		; INVALID
	CMP	AL,'Z'
	JBE	CONVAX10		; OK


	CMP	AL,'a'
	JB	CONVAX10		; OK
	CMP	AL,'z'
	JA	CONVAX49		; INVALID

	AND	AL,NOT 20H		; CONVERT TO UPPER CASE
CONVAX10:
	SAL	AL,1			; SHIFT UP
	STOSB
	LOOP	CONVAX00
;
;	HAVE PROCESSED 7 CHARS - NAME IS INVALID
;
	JMP SHORT CONVAX49

CONVAX20:
	LODSB				; FIRST CHAR IF SSID
	CMP	AL,'0'
	JB	CONVAX49		; INVALID
	CMP	AL,'9'
	JA	CONVAX49		; OK
;
	MOV	AH,AL			; SAVE
	LODSB
	CMP	AL,' '
	JE	CONVAX30		; END OF SSID

	INC	SI			; OTHER CODE EXPECTS SI TO POINT TO NEXT FIELD
	CMP	AL,'0'
	JB	CONVAX49		; INVALID
	CMP	AL,'5'			; LIMIT 10 - 15
	JA	CONVAX49		; OK
;
	SUB	AL,30H
	CMP	AH,'1'
	JNE	CONVAX49		; IF 2 DIGITS, FIRST MUST BE 1

	ADD	AL,10
	MOV	AH,AL
CONVAX30:
	SAL	AH,1			; UP 1 BIT
	OR	AH,01100000B		; DEFAULT RR BITS
	MOV	AX25CALL+6,AH

CONVAX40:
;
;	NO SSID SUPPLIED
;
	XOR	AL,AL			; SET ZERO
	RET

CONVAX49:
	OR	AL,1			; SET NZ
	RET



CONVFROMAX25:
;
;	CONVERT AX25 FORMAT CALL IN [SI] TO NORMAL FORMAT IN NORMCALL
;	   RETURNS LENGTH IN CX AND NZ IF LAST ADDRESS BIT IS SET
;
	PUSH	SI			; SAVE
	MOV	DI,OFFSET NORMCALL
	MOV	CX,10			; MAX ALPHANUMERICS
	MOV	AL,20H
	REP STOSB			; CLEAR IN CASE SHORT CALL
	MOV	DI,OFFSET NORMCALL
	MOV	CL,6
CONVAX50:
	LODSB
	CMP	AL,40H
	JE	CONVAX60		; END IF CALL - DO SSID

	SHR	AL,1
	STOSB
	LOOP	CONVAX50
CONVAX60:
	POP	SI
	ADD	SI,6			; TO SSID
	LODSB
	MOV	AH,AL			; SAVE FOR LAST BIT TEST
	SHR	AL,1
	AND	AL,0FH
	JZ	CONVAX90		; NO SSID - FINISHED
;
	MOV	BYTE PTR [DI],'-'
	INC	DI
	CMP	AL,10
	JB	CONVAX70
	SUB	AL,10
	MOV	BYTE PTR [DI],'1'
	INC	DI
CONVAX70:
	ADD	AL,30H			; CONVERT TO DIGIT
	STOSB
CONVAX90:
	MOV	CX,DI
	SUB	CX,OFFSET NORMCALL
	MOV	NORMLEN,CX		; SIGNIFICANT LENGTH

	TEST	AH,1			; LAST BIT SET?
	RET

Q_REM:
	MOV	DI,[SI]			; GET ADDR OF FIRST BUFFER
	CMP	DI,0
	JE	Q_RET			; EMPTY

	MOV	AX,[DI]			; CHAIN FROM BUFFER
	MOV	[SI],AX			; STORE IN HEADER

	POP	[DI]			; CALLERS ADDR 
	PUSH	[DI]
Q_RET:
	RET

;               
Q_ADD:
	CMP	DI,0
	JNE	BUFOK

	POP	DS:8			; RETURN ADDR
	PUSH	DS:8

	RET

BUFOK:

	IF	DEBUG
	CALL	CHECKBUFFER
	ENDIF

	PUSHF				; SAVE INT STATE
	CLI
Q_ADD05:
	CMP	WORD PTR [SI],0		; END OF CHAIN
	JE	Q_ADD10

	MOV	SI,[SI]			; NEXT IN CHAIN
	JMP	Q_ADD05
Q_ADD10:
	MOV	WORD PTR [DI],0		; CLEAR CHAIN ON NEW BUFFER
	MOV	[SI],DI			; CHAIN ON NEW BUFFER

	POPFF				; RECOVER INT STATE
	RET

;
;	ADD TO FRONT OF QUEUE - MUST ONLY BE USED FOR FREE QUEUE
;
Q_ADDF:
	CMP	DI,0
	JNE	BUFOK1

	POP	DS:10			; RETURN ADDR FOR DEBUGGING
	PUSH	DS:10

	RET

BUFOK1:
	IF	DEBUG
	CALL	CHECKBUFFER
	ENDIF

	MOV	AX,[SI]			; OLD FIRST IN CHAIN
	MOV	[DI],AX
	MOV	[SI],DI			; PUT NEW ON FRONT

	INC	QCOUNT
	RET

GETBUFF_FAR:

	CALL	GETBUFF
	RETF

GETBUFF:

	MOV	SI,OFFSET FREE_Q
	CLI
	CALL	Q_REM
	STI
;
	JZ	NOBUFFS
	DEC	QCOUNT
	MOV	AL,QCOUNT
	CMP	AL,MINBUFFCOUNT
	JA	GETBUFFRET
	MOV	MINBUFFCOUNT,AL
GETBUFFRET:
	OR	AL,1			; SET NZ
;
;	FLAG BUFFER WITH ADDR OF CALLING ROUTINE (TRYING TO TRACK DOWN LOST BUFFERS!)
;
	POP	[DI]
	PUSH	[DI]
 	RET

NOBUFFS:
NOBUFFERCHECK:

	INC	NOBUFFCOUNT
	XOR	AL,AL
	RET
