;
	PUSH	CX
	PUSH	DI

	MOV	AX,DI
	MOV	DI,OFFSET BUFFERLIST
	MOV	CX,BUFFERCOUNT

	REPNE SCASW
	JCXZ	DUFFBUFFER

	POP	DI
	POP	CX

	RET

