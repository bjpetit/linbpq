HOOKINT:
;
;	ATTACH PROCESS
;
;	PORT CONTROL TABLE  IN BX, LEVEL NUMBER IN INTLEVEL[BX]
;
	PUSH	ES

	MOV	AX,CODE
	MOV	ES,AX

	ASSUME	ES:CODE

	MOV	AL,INTLEVEL[BX]
	CBW
	SUB	AX,2
	ADD	AX,AX
	MOV	DI,OFFSET INT2PORTBASE
	ADD	DI,AX
;
;	SEE IF LEVEL ALREADY IN USE
;
	CMP	WORD PTR ES:[DI],0
	JE	NOTINUSE
;
;	LEVEL IN USE - CHAIN THIS PORT ENTRY TO FIRST
;
	MOV	DI,ES:[DI]		; GET FIRST PORT ENTRY

FINDENDOFCHAIN:

	CMP	INTCHAIN[DI],0
	JE	ENDOFCHAIN

	MOV	DI,INTCHAIN[DI]
	JMP	FINDENDOFCHAIN

ENDOFCHAIN:

	MOV	INTCHAIN[DI],BX
	POP	ES
	RET


NOTINUSE:

	MOV	WORD PTR ES:[DI],BX		; SAVE PORT CONTROL ADDRESS

	MOV	AL,INTLEVEL[BX]
	CBW
	SUB	AX,2
	ADD	AX,AX
	MOV	DI,OFFSET INTROUTINES
	ADD	DI,AX
;
	MOV	DX,CS:[DI]			; GET ADDR OF INTERRUPT ROUTINE
;
;	SET INTERRUPT VECTOR
;
	MOV	AL,INTLEVEL[BX]
	ADD	AL,8				; BASE 8259 ADDR
	CMP	AL,15
	JBE	INTOK

	ADD	AL,60H				; TO 70-77

INTOK:
	PUSH	DS
;
	MOV	CX,SEG CODE
	MOV	DS,CX
;
	MOV	AH,25H
	INT	21H			; SET VECTOR

	POP	DS

	CMP	INTLEVEL[BX],8
	JB	PIC1
;
;	USES PIC 2
;
	IN	AL,0A1H			; GET MASK
	DELAY

	MOV	AH,0FEH
	MOV	CL,INTLEVEL[BX]
	SUB	CL,8
	ROL	AH,CL
	AND	AL,AH			; UNMASK LEVEL
	OUT	0A1H,AL

	DELAY

	MOV	AL,58H
	ADD	AL,INTLEVEL[BX]	
	OUT	0A0H,AL			; SEND SPECIFIC EOI
;
	POP	ES

	RET
;
PIC1:

	IN	AL,21H			; GET MASK
	DELAY

	MOV	AH,0FEH
	MOV	CL,INTLEVEL[BX]
	ROL	AH,CL
	AND	AL,AH			; UNMASK LEVEL
	OUT	21H,AL

	DELAY

	MOV	AL,60H
	OR	AL,INTLEVEL[BX]	
	OUT	20H,AL			; SEND SPECIFIC EOI
;
	POP	ES

	RET
