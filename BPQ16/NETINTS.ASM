HOOKINT:
;
;	ATTACH PROCESS
;
;	PORT CONTROL TABLE  IN BX, LEVEL NUMBER IN INTLEVEL[BX]
;
 	CMP	IOBASE[BX],0
	JE	ISCC0
;
;	MUST BE SCC 2
;
	MOV	DI,OFFSET SCC2PORT

	mov	dx,INT1_CTRL
	mov	ax,INT1_INIT
	out	dx,ax			; SET UP LEVEL

	JMP	ISCC2

 
ISCC0:

	MOV	DI,OFFSET SCC0PORT

	mov	dx,INT0_CTRL
	mov	ax,INT0_INIT
	out	dx,ax

ISCC2:

	CMP	WORD PTR [DI],0
	JE	NOTINUSE
;
;	LEVEL IN USE - CHAIN THIS PORT ENTRY TO FIRST
;
	MOV	DI,[DI]			; GET FIRST PORT ENTRY

FINDENDOFCHAIN:

	CMP	INTCHAIN[DI],0
	JE	ENDOFCHAIN

	MOV	DI,INTCHAIN[DI]
	JMP	FINDENDOFCHAIN

ENDOFCHAIN:

	MOV	INTCHAIN[DI],BX
	RET


NOTINUSE:

	MOV	WORD PTR [DI],BX		; SAVE PORT CONTROL ADDRESS
	RET
