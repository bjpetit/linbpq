TCPINIT:
;
;	GET START TIME
;
	MOV	AX,40H
	MOV	ES,AX

	MOV	AX,ES:6CH
	MOV	TIMERTICKS,AX
;
;	SET UP DCT
;
	MOV	BX,OFFSET DCT
	MOV	CX,NUMBEROFSESSIONS+1
	MOV	AL,1
	MOV	AH,55

DCTINIT:

	MOV	DCTSESSNO[BX],AL
	MOV	HOSTPORT[BX],AH
;
;	UNTIL WE HAVE A CONFIG FILE, SET 4 INCOMING (LISTEN), AND 4
;	OUTGOING (APPLMASK=80H)
;
	PUSH	AX
	PUSH	CX

	MOV4	LOCALADDR[BX],MYIPADDR
	MOV	LOCALSOCKET[BX],23

	MOV4	REMOTEADDR[BX], HOSTADDR	; TESTING
	MOV	REMOTESOCKET[BX],3600

	CMP	DCTSESSNO[BX],4
	JBE	INIT_IN
;
;	OUTGOING (FROM SWITCH TO IP HOST) SESSIONS - RAISE APPLFLAGS
;
	MOV	SESSIONTYPE[BX],'O'

	MOV	AH,1
	MOV	AL,HOSTPORT[BX]

	MOV	CL,1			; COMMAND TO APPL 
	MOV	DL,80H			; APPL 8

	PUSH	BX
	CALL	IPNODE
	POP	BX

	JMP	INIT_REST

INIT_IN:

	MOV	SESSIONTYPE[BX],'I'
	MOV	TCPSTATE[BX],LISTEN
	MOV	REMOTESOCKET[BX],0

	MOV	AH,1
	MOV	AL,HOSTPORT[BX]

	MOV	CL,0
	MOV	DL,0

	PUSH	BX
	CALL	IPNODE
	POP	BX

INIT_REST:

	MOV	TCP_TIMER_START[BX],90	; RETRY TIMER

	POP	CX
	POP	AX

	INC	AL
	INC	AH

	ADD	BX,TYPE DCTENTRY
	LOOP	DCTINIT
;
;	AND TX BUFFER POOL
;
	MOV	SI,OFFSET TXFREE_Q
	MOV	DI,OFFSET TXPOOL
	MOV	CX,TXBUFFS

TXSETUPLOOP:

	CALL	Q_ADDF

	INC	TXBUFFCOUNT

	ADD	DI,TXBUFLEN
	LOOP	TXSETUPLOOP
;
	RET

