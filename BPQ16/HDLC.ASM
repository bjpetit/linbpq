TOSHINIT:				; INTERRUPT INITIALISATION CODE

	MOV	DX,IOBASE[BX]		; SCC ORIGIN
	CALL	INITPART1
;
;	SET UP ADDRESS LIST
;
	MOV	AX,IOBASE[BX]
	MOV	BSIOC[BX],AX		; B CHAN ADDR
	
	INC	AX

	MOV	ASIOC[BX],AX		; A ADDR
;
;	SEE WHICH CHANNEL TO USE
;
	CMP	CHANNELNUM[BX],'A'
	JE	INITACHANT
;
;	B CHANNEL
;
	DEC	AX			; BACK TO B ADDRESSES

	MOV	B_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	INITCOMT		; NO OTHER CHANNEL TO LINK

	MOV	A_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	B_PTR[DI],BX

	JMP SHORT INITCOMT

INITACHANT:

	MOV	A_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	INITCOMT		; NO OTHER CHANNEL TO LINK

	MOV	B_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	A_PTR[DI],BX


INITCOMT:

	MOV	SIOC[BX],AX
	INC	AX
	INC	AX
	MOV	SIO[BX],AX
;
;	SET UP VECTORS
;
	MOV	IOTXCA[BX],OFFSET IGNORE	; TX CHANNEL A
	MOV	IOTXEA[BX],OFFSET EXTINT
	MOV	IORXCA[BX],OFFSET SDADRX	; RX CHANNEL A
	MOV	IORXEA[BX],OFFSET SPCLINT	
;
;
;	INITIALISE COMMS CHIP
;
	MOV	DX,SIOC[BX]
	MOV	SI,OFFSET SDLCCMD	; PARAMS
	MOV	CX,SDLCLEN

	CMP	DI,0			; OTHER CHAN ALREADY SET UP?
	JE	SIOLOOPT		; NO

	ADD	SI,4
	SUB	CX,4			; SKIP RESET

SIOLOOPT:
	LODS	SDLCCMD
	OUT	DX,AL

	DELAY

	LOOP	SIOLOOPT
;
;	SET UP BRG FOR REQUIRED SPEED
;
	MOV	DX,WORD PTR TOSHCLOCKFREQ+2
	MOV	AX,WORD PTR TOSHCLOCKFREQ

	DIV	BAUDRATE[BX]

	SUB	AX,2			; GIVES BRG PARAM
	MOV	CX,AX			; SAVE

	MOV	DX,SIOC[BX]
	MOV	AL,12
	OUT	DX,AL

	DELAY
	MOV	AL,CL
	OUT	DX,AL			; SET LSB

	DELAY
	MOV	AL,13
	OUT	DX,AL

	DELAY
	MOV	AL,CH
	OUT	DX,AL			; SET MSB

	MOV	AL,11
	OUT	DX,AL

	MOV	AL,TOSHR11		; REDEFINE TX CLOCK FROM BRG
	OUT	DX,AL

	CALL	INITREST
	RET

	ELSE
;
;	DATA ENGINE OR MSC800
;
MSCINIT:
KANTINIT:				; INTERRUPT INITIALISATION CODE
;
;	CLEAR HWDATA IN CASE WARM START
;

	MOV	CX,TYPE HWDATA - HARDWAREDATA
	LEA	DI,ASIOC[BX]
	XOR	AL,AL
	REP STOSB

	MOV	DX,IOBASE[BX]		; SCC ORIGIN
	CALL	INITPART1
	CALL	INITPART2
	CALL	INITREST

 	RET

	ENDIF

INITPART1:
;
;	SET UP HOLD TIMER OF NEEDED
;
	CMP	FULLDUPLEX[BX],1
	JNE	NO_HOLD			; ONLY UED IN FULL DUP MODE

	MOV	AL,PORTTAILTIME[BX]
	MUL	TICKSPERSEC
	MOV	HOLDINIT[BX],AX

NO_HOLD:
;
;	SEE IF ANOTHER PORT IS ALREADY USING THE OTHER CHANNEL ON THIS CARD
;
	MOV	DI,PORTTABLE
	MOV	CX,NUMBEROFPORTS
INIT00:
	CMP	DI,BX
	JE	INIT10			; NONE BEFORE OURS

	MOV	AX,IOBASE[BX]
	CMP	AX,IOBASE[DI]
	JE	INIT20			; ANOTHER FOR SAME ADDRESS

	MOV	DI,PORTPOINTER[DI]

	LOOP	INIT00
INIT10:
	MOV	DI,0			; FLAG NOT FOUND
	JMP SHORT INIT30
INIT20:
;
;	ENSURE ENTRIES ARE FOR DIFFERENT CHANNELS
;
	MOV	AL,CHANNELNUM[BX]
	CMP	AL,CHANNELNUM[DI]
	JNE	INIT30

INIT25:
;
;	CHANNEL DEFINITION ERROR
;
	IFDEF	PCSWITCH
	MOV	DX,OFFSET ERRORMSG
	MOV	AH,9
	INT	21H
	ENDIF


	RET

INIT30:
;
;	MAKE SURE ONLY A OR B
;
	CMP	CHANNELNUM[BX],'A'
	JE	INIT31

	CMP	CHANNELNUM[BX],'B'
	JNE	INIT25

INIT31:
	RET

INITPART2:
;
;	SET UP ADDRESS LIST - THIS PATH FOR CARDS WITH 'NORMAL'
;	ADDRESSING - C/D=A0, A/B=A1, SO ORDER IS BCTRL BDATA ACTRL ADATA
;	OR DE, WHICH USES WORD ADDRESSES C/D=A1, A/B=A2 
;
	MOV	AX,DX

	IFDEF	KANT

	MOV	BSIOC[BX],AX		; B CHAN ADDR
	ADD	AX,4
	MOV	ASIOC[BX],AX		; A CHAN ADDR

	ELSE

	MOV	BSIOC[BX],AX		; B CHAN ADDR
	INC	AX
	INC	AX
	MOV	ASIOC[BX],AX		; A CHAN ADDR

	ENDIF

;
;	SEE WHICH CHANNEL TO USE
;
	CMP	CHANNELNUM[BX],'A'
	JE	INITACHAN
;
;	MUST BE B - CHECKED EARLIER
;
	IFDEF	KANT
	SUB	AX,4
	ELSE
	DEC	AX
	DEC	AX			; BACK TO B ADDRESSES
	ENDIF

	MOV	B_PTR[BX],BX		; POINT TO OUR ENTRY

	IFDEF	KANT

	MOV	DCDON[BX],LED4+1
	MOV	DCDOFF[BX],LED4

	MOV	RXON[BX],LED5+1
	MOV	RXOFF[BX],LED5

	ENDIF

	CMP	DI,0
	JE	INITCOM			; NO OTHER CHANNEL TO LINK

	MOV	A_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	B_PTR[DI],BX

	JMP SHORT INITCOM

INITACHAN:

	MOV	A_PTR[BX],BX		; POINT TO OUR ENTRY

	IFDEF	KANT

	MOV	DCDON[BX],LED1+1
	MOV	DCDOFF[BX],LED1

	MOV	RXON[BX],LED2+1
	MOV	RXOFF[BX],LED2

	ENDIF

	CMP	DI,0
	JE	INITCOM			; NO OTHER CHANNEL TO LINK

	MOV	B_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	A_PTR[DI],BX


INITCOM:

PINITREST:

	MOV	SIOC[BX],AX

	IFDEF	KANT
	INC	AX
	INC	AX			; DATA 2 ABOVE CONTROL
	ELSE
	INC	AX			; DATA 1 ABOVE CONTROL
	ENDIF

	MOV	SIO[BX],AX
;
;	SET UP VECTORS
;
	MOV	IOTXCA[BX],OFFSET IGNORE	; TX CHANNEL A
	MOV	IOTXEA[BX],OFFSET EXTINT
	MOV	IORXCA[BX],OFFSET SDADRX	; RX CHANNEL A
	MOV	IORXEA[BX],OFFSET SPCLINT	
;
;
;	INITIALISE COMMS CHIP
;
	MOV	DX,SIOC[BX]
	MOV	SI,OFFSET SDLCCMD	; PARAMS
	MOV	CX,SDLCLEN

	CMP	DI,0			; OTHER CHAN ALREADY SET UP?
	JE	SIOLOOP			; NO

	ADD	SI,4
	SUB	CX,4			; SKIP RESET

SIOLOOP:
	LODS	SDLCCMD
	OUT	DX,AL

	DELAY

	LOOP	SIOLOOP
;
	MOV	WR10[BX],00100000B	; NRZI
;
;	SET UP BRG FOR REQUIRED SPEED
;
	CMP	BAUDRATE[BX],0
	JNE	NORMCLOCK
;
;	SET EXTERNAL CLOCK
;
	MOV	DX,SIOC[BX]
	MOV	AL,11
	OUT	DX,AL

	DELAY
	IFDEF	KANT
	MOV	AL,00001000B		; RX = RTXC TX = TRXC
	ELSE
	MOV	AL,00100000B		; RX = TRXC TX = RTXC
	ENDIF
	OUT	DX,AL			; SET WR11

	RET


NORMCLOCK:

	MOV	DX,WORD PTR CLOCKFREQ+2
	MOV	AX,WORD PTR CLOCKFREQ

	IFDEF	PCSWITCH

	CMP	PORTTYPE[BX],12		; RLC400
	JNE	NOTRLC400		; NORMAL CLOCK SYSTEM
;
;	RLC 400 USES SAME CLOCK AS TOSH
;
	MOV	DX,WORD PTR TOSHCLOCKFREQ+2
	MOV	AX,WORD PTR TOSHCLOCKFREQ

NOTRLC400:

	ENDIF

	DIV	BAUDRATE[BX]

	SUB	AX,2			; GIVES BRG PARAM
	MOV	CX,AX			; SAVE

	MOV	DX,SIOC[BX]
	MOV	AL,12
	OUT	DX,AL

	DELAY
	MOV	AL,CL
	OUT	DX,AL			; SET LSB

	DELAY
	MOV	AL,13
	OUT	DX,AL

	DELAY
	MOV	AL,CH
	OUT	DX,AL			; SET MSB

	IFDEF	MSC

;	THERE IS NO /32 ON THE MSC800 BOARD, SO WILL USE BRG
;	FOR TRANSMIT. THIS REQUIRES IT TO BE REPROGRAMMED BETWEEN TX AND RX,
;	AND SO PREVENTS LOOPBACK OR FULLDUP OPERATION
;

	MOV	DX,WORD PTR CLOCKFREQ+2
	MOV	AX,WORD PTR CLOCKFREQ

	DIV	BAUDRATE[BX]

	SUB	AX,2			; GIVES BRG PARAM
	MOV	CX,AX			; SAVE

	MOV	RXBRG[BX],AX
;
;	CALC TX RATE
;
	ADD	AX,2
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1			; *32
	SUB	AX,2

	MOV	TXBRG[BX],AX

	MOV	DX,SIOC[BX]
	MOV	AL,12
	OUT	DX,AL

	DELAY
	MOV	AL,CL
	OUT	DX,AL			; SET LSB

	DELAY
	MOV	AL,13
	OUT	DX,AL

	DELAY
	MOV	AL,CH
	OUT	DX,AL			; SET MSB
;
;	SET TXC=BRG, RXC=DPLL
;
	MOV	DX,SIOC[BX]
	MOV	AL,11
	OUT	DX,AL

	DELAY
;
	MOV	AL,01110000B		; RXC=DPLL, TXC=BRG
	OUT	DX,AL

	ENDIF

	RET

	IFDEF	PCSWITCH

INITCIO:
;
;	INITIALISE CIO - DRSI ONLY
;
	CMP	DI,0
	JNE	CIODONE			; ALREADY SET UP

	MOV	DX,IOBASE[BX]
	ADD	DX,7			; TO CIO PORT

	IN	AL,DX
	MOV	AL,0
	DELAY
	OUT	DX,AL
	DELAY

	IN	AL,DX
	MOV	AL,0
	DELAY
	OUT	DX,AL
	DELAY

	MOV	AL,1
	OUT	DX,AL			; FORCE RESET
	DELAY

	MOV	AL,0
	OUT	DX,AL			; CLEAR RESET
	DELAY
;

	MOV	SI,OFFSET CIOPARAMS	; PARAMS
	MOV	CX,CIOLEN
CIOLOOP:
	LODS	CIOPARAMS
	OUT	DX,AL

	DELAY

	LOOP	CIOLOOP
;
CIODONE:
	RET

INITMODEM:
;
;	SETUP MODEM - PC120 ONLY
;
	CMP	DI,0
	JNE	MODDONE			; ALREADY SET UP

	MOV	DX,IOBASE[BX]		; BASE ADDR OF CARD
	MOV	AL,0AH
	OUT	DX,AL			; SET MODEM CONTROL LATCH

MODDONE:
	RET
;
CHECKCHAN:
;
;	ENTERED WITH AX=DIFFERENCE BETWEEN FIRST AND 2ND SCC BASE ADDR
;
;	IF CHANNEL =  C OR D SET TO SECOND SCC ADDRESS, AND CHANGE TO A OR B
;
	CMP	CHANNELNUM[BX],'B'
	JBE	CHANOK
;
;	SECOND SCC
;
	SUB	CHANNELNUM[BX],2
	ADD	IOBASE[BX],AX

CHANOK:
	RET


BAYCOMINIT:

	MOV	AX,2			; BASE ADJUST
	CALL	CHECKCHAN		; SEE IF C OR D

	MOV	DX,IOBASE[BX]		; SCC ORIGIN
	CALL	INITPART1
	CALL	BINITPART2
	CALL	INITREST
	RET

BINITPART2:
;
;	SET UP ADDRESS LIST
;
	MOV	AX,DX

	ADD	AX,4			; OFFSET CONTROL FROM DATA
	MOV	ASIOC[BX],AX		; A CHAN ADDR
	INC	AX
	MOV	BSIOC[BX],AX		; B CHAN ADDR
;
;	SEE WHICH CHANNEL TO USE
;
	MOV	AX,DX			; A CHAN DATA

	CMP	CHANNELNUM[BX],'A'
	JE	BINITACHAN
;
;	MUST BE B - CHECKED EARLIER
;
	INC	AX			; TO B DATA

	MOV	B_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	BINITCOM		; NO OTHER CHANNEL TO LINK

	MOV	A_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	B_PTR[DI],BX

	JMP SHORT BINITCOM

BINITACHAN:

	MOV	A_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	BINITCOM		; NO OTHER CHANNEL TO LINK

	MOV	B_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	A_PTR[DI],BX


BINITCOM:

	MOV	SIO[BX],AX
	ADD	AX,4			; TO CONTROL
	MOV	SIOC[BX],AX
;
;	SET UP VECTORS
;
	MOV	IOTXCA[BX],OFFSET IGNORE	; TX CHANNEL A
	MOV	IOTXEA[BX],OFFSET EXTINT
	MOV	IORXCA[BX],OFFSET SDADRX	; RX CHANNEL A
	MOV	IORXEA[BX],OFFSET SPCLINT	
;
;	INITIALISE COMMS CHIP
;
	MOV	DX,SIOC[BX]
	MOV	SI,OFFSET SDLCCMD	; PARAMS
	MOV	CX,SDLCLEN

	CMP	DI,0			; OTHER CHAN ALREADY SET UP?
	JE	BSIOLOOP		; NO

	ADD	SI,4
	SUB	CX,4			; SKIP RESET

BSIOLOOP:

	LODS	SDLCCMD
	OUT	DX,AL

	DELAY

	LOOP	BSIOLOOP
;
;	SET UP BRG FOR REQUIRED SPEED
;
	CMP	BAUDRATE[BX],0
	JNE	BNORMCLOCK
;
;	SET EXTERNAL CLOCK
;
	MOV	DX,SIOC[BX]
	MOV	AL,11
	OUT	DX,AL

	DELAY
	MOV	AL,00100000B		; RX = TRXC TX = RTXC
	OUT	DX,AL			; SET WR11
;
;	BAYCOM RUH PORT USES NRZ
;
	MOV	WR10[BX],00000000B	; NRZ

	RET


BNORMCLOCK:
;
	MOV	WR10[BX],00100000B	; NRZI
;
;	THERE IS NO /32 ON THE BAYCOM BOARD, SO FOR THE MOMENT WILL USE BRG
;	FOR TRANSMIT. THIS REQUIRES IT TO BE REPROGRAMMED BETWEEN TX AND RX,
;	AND SO PREVENTS LOOPBACK OR FULLDUP OPERATION
;

	MOV	DX,WORD PTR CLOCKFREQ+2
	MOV	AX,WORD PTR CLOCKFREQ

	DIV	BAUDRATE[BX]

	SUB	AX,2			; GIVES BRG PARAM
	MOV	CX,AX			; SAVE

	MOV	RXBRG[BX],AX
;
;	CALC TX RATE
;
	ADD	AX,2
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1
	SHL	AX,1			; *32
	SUB	AX,2

	MOV	TXBRG[BX],AX

	MOV	DX,SIOC[BX]
	MOV	AL,12
	OUT	DX,AL

	DELAY
	MOV	AL,CL
	OUT	DX,AL			; SET LSB

	DELAY
	MOV	AL,13
	OUT	DX,AL

	DELAY
	MOV	AL,CH
	OUT	DX,AL			; SET MSB
;
;	SET TXC=BRG, RXC=DPLL
;
	MOV	DX,SIOC[BX]
	MOV	AL,11
	OUT	DX,AL

	DELAY
;
	MOV	AL,01110000B		; RXC=DPLL, TXC=BRG
	OUT	DX,AL
	RET


PA0INIT:

	MOV	AX,4			; BASE ADJUST
	CALL	CHECKCHAN		; SEE IF C OR D

	MOV	DX,IOBASE[BX]		; SCC ORIGIN
	CALL	INITPART1
	CALL	INITPART2
	CALL	INITREST
	RET

	IF	0			; THIS DOESNT SEEM TO BE USED

PINITPART2:
;
;	SET UP ADDRESS LIST
;
	MOV	AX,DX
	MOV	ASIOC[BX],AX		; A CHAN ADDR
	ADD	AX,2
	MOV	BSIOC[BX],AX		; B CHAN ADDR
