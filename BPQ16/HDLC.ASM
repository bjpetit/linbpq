DRSIINIT:				; INTERRUPT INITIALISATION CODE

	MOV	DX,IOBASE[BX]		; SCC ORIGIN
	CALL	INITPART1
	PUSH	DX
	CALL	INITCIO			; SET UP CIO FOR /32
	POP	DX
	CALL	INITPART2
;
;	SET UP COUNTER UNLESS EXTERNAL CLOCK
;
	CMP	BAUDRATE[BX],0
	JNE	STARTCOUNTER

	JMP	EXTCLOCK

STARTCOUNTER:
;
	MOV	DX,IOBASE[BX]
	ADD	DX,7			; TO CIO PORT
;
;	B CHANNEL
;
;	SET COUNTER OUTPUT BIT ACTIVE
;	
	MOV	AL,2BH			; PORT B DIRECTION
	OUT	DX,AL

	DELAY

	IN	AL,DX			; GET IT

	CMP	CHANNELNUM[BX],'A'
	JE	INITACOUNTER
;
	AND	AL,0EFH			; SET BIT 4 AS OUTPUT

	PUSH	AX

	DELAY

	MOV	AL,2BH
	OUT	DX,AL

	DELAY

	POP	AX

	DELAY

	OUT	DX,AL			; UPDATE PORT B DIRECTION

	DELAY
;
;	ENABLE COUNTER
;
	MOV	AL,1			; MASTER CONFIG
	OUT	DX,AL

	DELAY

	IN	AL,DX			; GET IT
	OR	AL,40H			; ENABLE CT1

	PUSH	AX

	DELAY

	MOV	AL,1
	OUT	DX,AL

	DELAY

	POP	AX

	DELAY

	OUT	DX,AL			; UPDATE MASTER CONFIG

	DELAY
;
;	START COUNTER
;
	MOV	AL,0AH			; CT1 CONTROL
	OUT	DX,AL

	DELAY

	MOV	AL,6
	OUT	DX,AL			; START CT1
	DELAY

;
	JMP SHORT EXTCLOCK

INITACOUNTER:

	AND	AL,0FEH			; SET BIT 0 AS OUTPUT

	PUSH	AX

	DELAY

	MOV	AL,2BH
	OUT	DX,AL

	DELAY

	POP	AX

	DELAY

	OUT	DX,AL			; UPDATE PORT B DIRECTION

	DELAY
;
;	ENABLE COUNTER
;
	MOV	AL,1			; MASTER CONFIG
	OUT	DX,AL

	DELAY

	IN	AL,DX			; GET IT
	OR	AL,20H			; ENABLE CT2

	PUSH	AX

	DELAY

	MOV	AL,1
	OUT	DX,AL

	DELAY

	POP	AX

	DELAY

	OUT	DX,AL			; UPDATE MASTER CONFIG

	DELAY
;
;	START COUNTER
;
	MOV	AL,0BH			; CT2 CONTROL
	OUT	DX,AL

	DELAY

	MOV	AL,6
	OUT	DX,AL			; START CT2
	DELAY

EXTCLOCK:

	CALL	INITREST

 	RET

