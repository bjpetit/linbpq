;	*** F1 - RX ADDRESS
;	***
;	READ CHAR FROM SIO AND DISCARD 
;	SET VECTOR TO 'RX CONTROL BYTE' - F2
;	RETURN
;
;
SDADRX:

	IFDEF	KANT

	SIOCR				; GET RR0
	TEST	AL,08H			; HARD DCD
	JZ	NODCD

	ENDIF

;	MOV	SOFTDCD[BX],4		; SET RX ACTIVE

	MOV	SI,OFFSET FREE_Q
	CALL	Q_REM
	JNZ	GETB00

	CALL	NOBUFFERCHECK		; CHECK IF POOL IS CORRUPT
	JMP	NOBUFFERS

GETB00:

	DEC	QCOUNT

	MOV	[DI],OFFSET GETB00	; FLAG FOR DUMP ANALYSER

	SIOR				; GET FIRST BYTE OF ADDRESS

	MOV	CURALP[BX],DI		; SAVE ADDR
	ADD	DI,5
	MOV	[DI],AL			; ADDR TO BUFFER
	INC	DI
	MOV	SDRNEXT[BX],DI		; AND NEXT BYTE POINTER
;
	MOV	FRAMELEN[BX],5

	OR	SDFLAGS[BX],SDRINP	; SET RX IN PROGRESS

	SETRVEC	SDIDRX			; SET VECTOR TO 'GET DATA'

	IFDEF	KANT
	MOV	DX,RXON[BX]
	OUT	DX,AL
	ENDIF

	RET
;
	IFDEF	KANT

NODCD:

	SIOR				; CLEAR INT PENDING 

	MOV	AL,33H
	SIOCW				; ERROR RESET WR3

	DELAY
	MOV	AL,11011001B		; 8BIT, CRC EN, RX EN
	SIOCW   
;
	RET

	ENDIF

NOBUFFERS:
;
;	NO BUFFER FOR RECEIVE - SET TO DISCARD
;
	INC	OLOADS[BX]

	SETRVEC	SDOVRX
	SIOR				; CLEAR INT PENDING
	RET

SDIDRX:
;
;	NOW READ CHARACTER FROM SIO AND STORE
;
;	MOV	SOFTDCD[BX],4		; SET RX ACTIVE
	INC	FRAMELEN[BX]
;
	SIOR	SIO1			; GET CHAR
;
	CMP	FRAMELEN[BX],BUFFLEN-10
	JNE	SDID00			; SIZE OK
	SETRVEC	SDOVRX			; CANT TAKE ANY MORE
SDID00:
	MOV	DI,SDRNEXT[BX]		; GET NEXT BYTE POINTER
	STOSB
	MOV	SDRNEXT[BX],DI		; STORE UPDATED BYTE POINTER

	RET
;
SDOVRX:
;
;	DISCARD REST OF MESSAGE
;
;	MOV	SOFTDCD[BX],4		; SET RX ACTIVE
	SIOR	SIO1			; READ CHAR AND DISCARD
	RET

;***	RX SPECIAL CHARACTER INTERRUPT
;
SPCLINT:
	
	SIOR				; READ CHAR AND DISCARD
	MOV	AL,1
	DELAY

	SIOCW				; SELECT RR1
	DELAY

	SIOCR				; INPUT RR1

	MOV	AH,AL

	SIOCAD
	MOV	AL,30H
	OUT	DX,AL			; RESET SIO ERROR LATCHES

	TEST	AH,10000000B		; END OF FRAME?
	JNZ	SDEOF			; YES
;
;	NOT END OF FRAME - SHOULD BE OVERRUN - DISCARD FRAME
;
	ADD	L2ORUNC[BX],1		; FOR STATS
	ADC	L2ORUNC+2[BX],0
;
	TEST	SDFLAGS[BX],SDRINP
	JZ	SDSP07			; IF NOT SET, NO BUFFER IS ALLOCATED

	AND	SDFLAGS[BX],NOT SDRINP

	MOV	SI,OFFSET FREE_Q
	MOV	DI,CURALP[BX]
	CALL	Q_ADDF

SDSP07:
	SETRVEC SDOVRX			; DISCARD REST OF MESSAGE

	JMP	SPCLINTEXIT

SDEOF:
;
;	IF RESIDUE IS NONZERO, IGNORE FRAME
;
	MOV	AL,AH
	AND	AL,1110B		; GET RESIDUE BITS
	CMP	AL,0110B
	JNE	DONTCOUNT		; NOT MULTIPLE OF 8 BITS
;
;	END OF FRAME - SEE IF FCS OK
;
	TEST	AH,01000000B
	JZ	SDSP10			; J IF GOOD END-OF-FRAME
;
;	FCS ERROR
;
	CMP	FRAMELEN[BX],14H
	JB	DONTCOUNT		; TOO SHORT
	ADD	RXERRORS[BX],1
	ADC	RXERRORS+2[BX],0	; FOR STATS

DONTCOUNT:

	TEST	SDFLAGS[BX],SDRINP
	JZ	SDSP09			; IF NOT SET, NO BUFFER IS ALLOCATED

	AND	SDFLAGS[BX],NOT SDRINP

DISCARDFRAME:

	MOV	SI,OFFSET FREE_Q
	MOV	DI,CURALP[BX]
	CALL	Q_ADDF

SDSP09:
	SETRVEC SDADRX

	IFDEF	KANT
	MOV	DX,RXOFF[BX]
	OUT	DX,AL
	ENDIF

	JMP	SPCLINTEXIT
;
;	GOOD FRAME RECEIVED
;
SDSP10:
	TEST	SDFLAGS[BX],SDRINP
	JZ	SDSP11			; IF NOT SET, NO BUFFER IS ALLOCATED

	AND	SDFLAGS[BX],NOT SDRINP

	MOV	DI,SDRNEXT[BX]		; GET NEXT BYTE POINTER
	DEC	DI
	MOV	BYTE PTR [DI],0FEH	; OVERWRITE FIRSTS FCS BYTE WITH MARKER

	MOV	AX,FRAMELEN[BX]		; GET LENGTH
	CMP	AX,14H
	JB	DISCARDFRAME		; TOO SHORT


	MOV	DI,CURALP[BX]		; BUFFER ADDR
	MOV	3[DI],AX		; PUT IN LENGTH

	LEA	SI,RXMSG_Q[BX]
	CALL	Q_ADD			; QUEUE MSG FOR B/G
SDSP11:
	SETRVEC SDADRX			; READY FOR NEXT FRAME

	IFDEF	KANT
	MOV	DX,RXOFF[BX]
	OUT	DX,AL
	ENDIF

SPCLINTEXIT:
	RET

;	EXTERNAL/STATUS INTERRUPT
;
;		 - TRANSMIT UNDERRUN/EOM - IF AT END OF FRAME IGNORE
;					      ELSE SEND ABORT
;		 - ABORT START/END - IF RECEIVING, CANCEL MESSAGE
;
;
EXTINT:

	MOV	AH,RR0[BX]		; GET OLD RR0
	SIOCR
	MOV	RR0[BX],AL		; SAVE

	XOR	AH,AL			; GET CHANGES
	AND	AH,0C0H
	JZ	SDST40			; NO INTERESTING CHANGES

SDST00:
	TEST	SDFLAGS[BX],SDTINP
	JZ	SDST10			; J IF 'TX IN PROGRESS' NOT SET
;
;	WE ARE TRANSMITTING - CHECK FOR UNDERRUN
;
	TEST	AL,SDUNDER		; TX UNDERRUN?
	JZ	SDST10			; ONLY INTERESTING ONE

	TEST	SDTXCNT[BX],8000H	; IS IDP CHAR COUNT -VE?
	JNZ	SDST10			; J IF YES (NORMAL END-OF-FRAME)
;
;	UNDERRUN IN MID-FRAME
;	ABORT THE TRANSMISSION
;
	MOV	AL,SDABTX
	SIOCW				; SEND ABORT SEQUENCE
	SETTVEC	SDCMTX			; SET VECTOR TO 'TX COMPLETE'
	OR	SDFLAGS[BX],ABSENT	; SET ABORT SENT

	ADD	L2URUNC[BX],1		; UNDERRUNS
	ADC	L2URUNC+2[BX],0

	JMP SHORT SDST10		; SEE IF ANY RX ERROR BITS ALSO SET
;
;	IS RX IN PROGRESS?
;
SDST10:
	TEST	SDFLAGS[BX],SDRINP
	JZ	SDST40			; NO, SO PROBABLY ABORT FOLLOWING MSG 

	TEST	RR0[BX],SDABORT		; IS ABORT STATUS BIT SET?
	JZ	SDST40			; J IF NOT - ? ABORT TERMINATON

	MOV	DI,CURALP[BX]		; BUFFER ADDR

	MOV	SI,OFFSET FREE_Q
	CALL	Q_ADDF			; RELEASE BUFFER

	AND	SDFLAGS[BX],NOT SDRINP	; CLEAR RX IN PROGRESS

	SETRVEC SDADRX			; READY FOR NEXT FRAME

	IFDEF	KANT
	MOV	DX,RXOFF[BX]
	OUT	DX,AL
	ENDIF

SDST40:
	MOV	AL,SDEXTR
	SIOCW				; RESET EXTERNAL/STATUS INTERRUPT

	RET
;
;***	TX DATA
;
SDDTTX:
	
	DEC	SDTXCNT[BX]		; DECREMENT CURRENT IDP COUNT
;
	JNS	SDDT40			; J IF IDP NOT YET EMPTY
;
;	NO MORE DATA TO TRANSMIT
;
	MOV	AL,10
	SIOCW   
	DELAY
	MOV	AL,10000000B		; SET TO SEND CRC ON UNDERRUN
	OR	AL,WR10[BX]
	SIOCW   

	MOV	AL,SDRPEND		; RESET TX UNDERRUN/EOM LATCH +
	DELAY
	SIOCW				;       TX INTERRUPT PENDING
	SETTVEC	SDCMTX			; SET TX VECTOR TO 'TX COMPLETION'

	RET
;
;	TRANSMIT NEXT BYTE OF DATA
;
SDDT40:
	MOV	SI,SDTNEXT[BX]		; GET NEXT BYTE POINTER
	LODSB
	SIOW				; TRANSMIT NEXT BYTE
	MOV	SDTNEXT[BX],SI		; STORE UPDATED BYTE POINTER

	RET
;
;	*** F11 - TX COMPLETION
;
SDCMTX:
	
	MOV	AL,28H
	SIOCW				; RESET TX INT PENDING

	TEST	SDFLAGS[BX],ABSENT
	JZ	NOABORT
;
;	FRAME WAS ABORTED - SEND AGAIN
;
;	AND	SDFLAGS[BX],NOT ABSENT
;	MOV	DI,TXFRAME[BX]
;	JMP SHORT SENDAGAIN

NOABORT:

	MOV	DI,TXFRAME[BX]
	MOV	TXFRAME[BX],0

	MOV	SI,OFFSET TRACE_Q	; TRACE IT
	CALL	Q_ADD

	AND	SDFLAGS[BX],NOT SDTINP
;
;	SEE IF MORE TO SEND
;
	LEA	SI,PCTX_Q[BX]
	CALL	Q_REM

	JZ	NOMORETOSEND		; J IF QUEUE EMPTY

	MOV	TXFRAME[BX],DI		; SAVE ADDRESS OF FRAME

SENDAGAIN:

	MOV	AX,3[DI]
	SUB	AX,6
	MOV	SDTXCNT[BX],AX		; GET MESSAGE LENGTH FROM BUFFER

	LEA	DI,6[DI]
	MOV	SDTNEXT[BX],DI		; SET NEXT BYTE POINTER
;
	OR	SDFLAGS[BX],SDTINP

	SETTVEC	SDDTTX			; SET VECTOR TO 'TX DATA'

	MOV	AL,SDTXCRC
	SIOCW				; RESET TX CRC GENERATOR
;
	DELAY
	MOV	AL,-1[DI]		; FIRST BYTE
	SIOW				; TRANSMIT IT

	DELAY
	MOV	AL,SDTXUND
	SIOCW				; RESET TX UNDERRUN LATCH

	AND	RR0[BX],NOT SDUNDER	; KEEP STORE COPY

	DELAY
	MOV	AL,10
	SIOCW   

	DELAY
	MOV	AL,10000100B		; SET TO ABORT ON UNDERRUN
	OR	AL,WR10[BX]
	SIOCW   

	RET

NOMORETOSEND:
;
;	IF RUNNING IN CARRIER HOLD MODE, (RE)START TIMER
;
	CMP	HOLDINIT[BX],0
	JE	SENDPAD

	MOV	AX,HOLDINIT[BX]
	MOV	HOLDTIMER[BX],AX

	RET

SENDPAD:
;
;	SEND A FEW PADDING CHARS TO ENSURE FLAG IS CLEAR OF SCC
;
	SETTVEC	SENDDUMMY1

	XOR	AL,AL			; FIRST DUMMY BYTE
	SIOW	SIO1			; TRANSMIT IT

	RET

SENDDUMMY1:

	XOR	AL,AL
	SIOW				; SECOND DUMMY

	SETTVEC	SENDDUMMY2

	RET

SENDDUMMY2:

	XOR	AL,AL
	SIOW				; SECOND DUMMY

	SETTVEC	SENDDUMMY3

	RET

SENDDUMMY3:

	XOR	AL,AL
	SIOW				; SECOND DUMMY

	SETTVEC	DROPRTS

	RET

DROPRTS:

	AND	LINKSTS[BX],NOT 1	; SET NOT TRANSMITTING

	MOV	AL,5
	SIOCW

	DELAY

	MOV	AL,11100001B		; DROP RTS AND TXEN
	SIOCW

	SETTVEC	IGNORE

	IFNDEF	KANT

	CMP	RXBRG[BX],0
	JE	DONTCHANGERX
;
;	NEED TO RESET BRG FOR RECEIVE
;
	MOV	CX,RXBRG[BX]

	MOV	DX,SIOC[BX]
	MOV	AL,12

	OUT	DX,AL
	DELAY
	MOV	AL,CL
	OUT	DX,AL			; SET LSB

	DELAY
	MOV	AL,13
	OUT	DX,AL

	DELAY
	MOV	AL,CH
	OUT	DX,AL			; SET MSB
;
DONTCHANGERX:

	ENDIF

	RET
;

;***	TX INTERRUPT IGNORE
;
IGNORE:

	MOV	AL,SDRPEND
	SIOCW				; RESET TX INTERRUPT PENDING

	RET
;
SDTXUND	EQU	0C0H		; RESET TX UNDERRUN/EOM LATCH

SDABTX	EQU	18H
SDUNDER	EQU	40H
SDTXCRC	EQU	80H		; RESET TX CRC GEN
SDABORT	EQU	80H		; ABORT DETECTED
SDEXTR	EQU	10H		; RESET EXT/STATUS INTS
SDRPEND	EQU	28H		; RESET TX INT PENDING

	EVEN
;
	IFDEF	KANT

	PUBLIC	SCCINT

SCCINT:
;
;	SERIAL COMMS INTERRUPT HANDLER
;
	PUSHA
	CLD

	MOV	AL,63H			; SPECIFIC EOI TO LEV 1
	OUT	(IPFW),AL

	MOV	BX,HDLCPORT1

	INC	RAN4

	ENDIF

SIOINT: 					; ENTERED FROM HARDWARE INTERRUPT
;
;	BX HAS PORT DATA FOR ONE OF THE TWO CHANNELS, AND CONTAINS POINTERS
;	TO THE PORT DATA FOR EACH CHANNEL. 
;

SIOI10:

	MOV	DX,ASIOC[BX]
	MOV	AL,3
	OUT	DX,AL			; SELECT RR3

	DELAY

	IN	AL,DX			; GET PENDING INTS
	OR	AL,AL
	JZ	NOINTS			; NONE


	MOV	DX,BSIOC[BX]
	MOV	AL,2
	OUT	DX,AL			; SELECT RR2

	DELAY

	IN	AL,DX			; GET VECTOR
	CBW

	CMP	AL,8
	JAE	ACHAN

	MOV	BX,B_PTR[BX]		; GET DATA FOR B CHANNEL
	JMP SHORT INT_COMMON

ACHAN:
	SUB	AL,8
	MOV	BX,A_PTR[BX]

INT_COMMON:

	LEA	SI,VECTORS[BX]
	ADD	SI,AX
	CALL	[SI]

	MOV	DX,ASIOC[BX]
	MOV	AL,00111000B		; RESET IUS
	OUT	DX,AL

	JMP	SIOI10			; SEE IF ANY MORE 

NOINTS:
	IFDEF	KANT

	POPA
	IRET

	ELSE

	RET
;
	ENDIF

