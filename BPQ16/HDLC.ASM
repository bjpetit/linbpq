;
	ADD	BX,2			; TO B CTRL

	MOV	B_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	PINITCOM		; NO OTHER CHANNEL TO LINK

	MOV	A_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	B_PTR[DI],BX

	JMP SHORT PINITCOM

PINITACHAN:

	MOV	A_PTR[BX],BX		; POINT TO OUR ENTRY

	CMP	DI,0
	JE	PINITCOM		; NO OTHER CHANNEL TO LINK

	MOV	B_PTR[BX],DI		; CROSSLINK CHANNELS
	MOV	A_PTR[DI],BX


PINITCOM:

	JMP	PINITREST


	ENDIF				; OF UNUSED BIT

	ENDIF				; OF PC BITS



