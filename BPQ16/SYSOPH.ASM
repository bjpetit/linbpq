OUTERLOOP:

	MOV	AX,1000H
	INT	15H			; FOR DESQVIEW

	STI				; FOR IBM XT BIOS BUG

	CALL	BG

	JMP	OUTERLOOP

; Main background routine - searching for input from keyboard or Comms
; command lines are processed, and replies sent to screen or comms
;
BG:
	INC	WORD PTR RAN1
	CALL	MAINLOOP
	RET

MAINLOOP:

	CMP	RESFLAG,1
	JE	BG1			; LEAVE KBD ALONE

	MOV	AH,1
	INT	16H
	JZ	BG1			; NO KEYBOARD CHAR
;
	INC	RAN3

	MOV	AH,0
	INT	16H			; GET THE CHAR

	CMP	AL,19H			; CTRL/Y
	JNE	KB000
	JMP	CLOSEDOWN

;
KB000:
;	INC	ACTIVE			; SOMETHING HAPPENED
	CALL	PROCESSKEY
	RET

BG1:
;
;	LOOK FOR STATUS CHANGE OR DATA AVAILABLE
;
	MOV	AL,PORT
	MOV	AH,4
	CALL	NODE

	CMP	DX,0
	JNE	STATUSCHANGE
;
;	NO STATUS - LOOK FOR DATA
;
	MOV	DI,OFFSET REMLINE

	MOV	AL,PORT
	MOV	AH,3
	CALL	NODE

	CMP	CX,0
	JNE	GOTCOMMS

	RET

GOTCOMMS:
;
	ADD	RAN3,CL

	MOV	SI,OFFSET REMLINE
	PUSH	CX
	CALL	DISPLAYMSG
	POP	CX

	CALL	DOREMLINE		; PROCESS COMMAND LINE
	RET

IGNOREIT:
DISCONNECTED:

	MOV	REMOTEACTIVE,0
	RET
;
STATUSCHANGE:
;
;	SEE IF NOW CONNECTED - THEN SEE IF NEW CONNECT
;
	PUSH	 CX
;
	MOV	AL,PORT
	MOV	AH,5			; ACK THE STATUS CHANGE
	CALL	NODE

	POP	CX

	OR	CX,CX
	JZ	DISCONNECTED
;
;	CONNECTED - GET CALLSIGN
;
	MOV	AL,PORT
	MOV	AH,8
	MOV	DI,OFFSET CALLSIGN
	CALL	NODE

;	SEE IF A NEW CONNECT
;
	CMP	REMOTEACTIVE,0
	JNE	ALREADYACTIVE
;
;	A NEW CONNECT
;
;	SEND PASSWORD REQUEST
;
	CALL	SETUPPASSWORD

	MOV	SI,OFFSET SIGNINSTRING
	CALL	SENDSTRING		; PROMPT

	MOV	REMOTEACTIVE,1
	MOV	PWFLAG,1		; WAITING FOR PASSWORD

ALREADYACTIVE:

	RET


	



PROCESSKEY:

	OR	AL,AL			; TWO KEY PAIR?
	JNZ	NORMKEYBD		; NO

	MOV	AL,AH
;
	JMP	SPECIALKEY

NORMKEYBD:

	CMP	AL,20H
	JL	KBCTRL
KBNORM:

	CMP	KBCURSOR,OFFSET KBLINE+79
	JE	NORMRET

	MOV	DI,KBCURSOR
	MOV	[DI],AL
	INC	KBCURSOR

	CALL	DISPLAY

NORMRET:

	RET
KBCTRL:
	CMP	AL,7
	JE	KBNORM			; SO WE CAN SEND BELL

	CMP	AL,8
	JE	KBBSPACE

	CMP	AL,0DH
	JE	KBCR

	RET

KBBSPACE:
	CALL	CUL
	RET

KBCR:

	MOV	DI,KBCURSOR
	MOV	[DI],AL			; PUT IN BUFFER

	CALL	DISPLAY			; ECHO IT

	MOV	ORIGIN,'K'		; FROM KEYBOARD

	MOV	SI,OFFSET KBLINE
	CALL	TNCCOMMAND

	PUSH	DS
	POP	ES

	MOV	ORIGIN,'R'

	MOV	KBCURSOR,OFFSET KBLINE
	MOV	DI,OFFSET KBLINE
	MOV	CX,40
	MOV	AX,2020H
	REP STOSW

	RET


SPECIALKEY:				; PROCESS EXTENDED CODE KEYS

	CMP	AL,75
	JE	SPLBS			; CUR LEFT

	RET

SPLBS:
	MOV	AL,8
	CALL	KBBSPACE
	RET

CUL:					; CURSOR LEFT
	CMP	KBCURSOR,OFFSET KBLINE
	JE	CURRET

	DEC	KBCURSOR
	CALL	DISPLAY			; ECHO IT
CURRET:
	RET
;
DOREMLINE:

	CMP	REMLINE,0DH		; NULL?
	JE	SKIPREM

	MOV	ORIGIN,'R'		; REMOTE
	MOV	SI,OFFSET REMLINE

	CMP	PWFLAG,1
	JE	CHECKPASSWORD

	CALL	TNCCOMMAND

	PUSH	DS
	POP	ES

SKIPREM:

	RET

CHECKPASSWORD:

	MOV	CX,5
	MOV	BX,OFFSET PWCHARS	; STRING SEND
PW000:
	MOV	AL,[BX]
	INC	BX
	CBW
	MOV	DI,OFFSET PWTEXT-1
	ADD	DI,AX

	LODSB
	CMP	AL,[DI]
	JNE	DUFFPW

	LOOP	PW000

	JMP SHORT PWOK
DUFFPW:
	CALL	DOEXIT
PWOK:
	MOV	PWFLAG,0
	JMP	SKIPREM			; FINISHED

DOEXIT:
;
;	RETURN TO COMMAND MODE, AND RELEASE
;
	MOV	AH,6			; CONNECT/DISCONNECT
	MOV	AL,PORT

	MOV	CX,3			; RETURN TO NODE

	CALL	NODE

	RET

SENDSTRING:
;
;	SEND CR TERMINATED STRING TO NODE
;
	PUSH	SI
	MOV	CX,1

GETLENGTH:

	CMP	BYTE PTR [SI],0DH
	JE	GOTLENGTH

	INC	SI
	INC	CX

	JMP	GETLENGTH
 
GOTLENGTH:

	POP	SI


	PUSH	SI
	PUSH	CX

	CALL	DISPLAYMSG		; DISPLAY IT

	POP	CX
	POP	SI

	CMP	ORIGIN,'R'		; REMOTE>?
	JNE	SENDYY			; NO

	PUSH	ES
	PUSH	CS
	POP	ES

	MOV	AH,2
	MOV	AL,PORT

	CALL	NODE			; SEND TO NODE

	POP	ES

SENDYY:

	RET

PUTSTRING:
;
;	SEND STRING LENGTH CX TO NODE
;
	LODSB

	CALL	SENDCHAR

	LOOP	PUTSTRING

	RET

SENDCHAR:
;
;	SEND TO CONSOLE, AND TNC IF REMOTE
;
	CMP	ORIGIN,'R'
	JNE	SENDXX

	PUSH	DI

	MOV	DI,SENDCURSOR

	MOV	[DI],AL
	INC	DI			; ANT BE SURE ABOUT SEG REGS

	CMP	AL,0DH
	JNZ	SEND10
;
;	SEND BUFFER TO NODE
;
	PUSH	AX
	PUSH	CX
	PUSH	SI

	MOV	CX,DI
	MOV	SI,OFFSET SENDBUFFER
	SUB	CX,SI

	PUSH	ES

	PUSH	CS
	POP	ES

	MOV	AL,PORT
	MOV	AH,2

	CALL	NODE

	POP	ES

	POP	SI
	POP	CX
	POP	AX

	MOV	DI,OFFSET SENDBUFFER

SEND10:

	MOV	SENDCURSOR,DI

	POP	DI

SENDXX:
	CALL	DISPLAY
	RET

DISPLAYMSG:

	LODSB
	CALL	DISPLAY

	LOOP	DISPLAYMSG

	RET

DISPLAY:

	CMP	RESFLAG,1
	JE	NODISPLAY

	PUSH	AX
	MOV	AH,14
	INT	10H			; DISPLAY

	CMP	AL,0DH
	JNE	DISRET
;
;	ADD LF TO CR
;		
	MOV	AH,14
	MOV	AL,0AH
	INT	10H
DISRET:
	POP	AX
NODISPLAY:
	RET
;
;

CLOSEDOWN:

	CALL	DOEXIT			; KICK OFF ANY USER

	MOV	AH,1
	MOV	AL,PORT
	MOV	DL,0
	MOV	CL,0

	CALL	NODE			; DISABLE STREAM

	CALL	WAITLOOP

	MOV	AX,4C00H
	INT	21H
;
WAITLOOP:
	MOV	CX,1000
STOPLOOP:
	PUSH	CX
	CALL	MAINLOOP		; CLEAR ECHO OF COMMAND
	POP	CX

	LOOP	STOPLOOP

	RET
	
TNCCOMMAND:
;
;	PROCESS COMMAND TO TNC CODE
;
	MOV	DI,OFFSET COMMANDLINE
;
;	CONVERT TO UPPER CASE
;
	MOV	CX,50			; IN CASE GARBAGE!!
TNCUCLOOP:
	LODSB
	CMP	AL,0DH
	JE	TNCUCEND

	CMP	AL,61H
	JB	TNCUCOK
	CMP	AL,7AH
	JA	TNCUCOK

	SUB	AL,20H
TNCUCOK:
	STOSB
	LOOP	TNCUCLOOP
;
TNCUCEND:


	MOV	AL,20H
	MOV	CX,30
	REP STOSB			; PUT A FEW SPACES ON END FOR DECODE ROUTINES

	MOV	VALNODESFLAG,0
	MOV	PORTPARAMFLAG,0

	MOV	SI,OFFSET COMMANDLINE
	CALL	SCAN

	MOV	DI,OFFSET COMMANDLIST
	MOV	CX,NUMBEROFCOMMANDS

COMMANDLOOP:

	PUSH	SI
	PUSH	DI
	PUSH	CX

	MOV	CL,CMDLEN[DI]		; MIN LENGTH
	REP CMPSB
	JE	COMMANDFOUND

	POP	CX
	POP	DI
	POP	SI
	ADD	DI,TYPE CMDX

	LOOP	COMMANDLOOP

TNCDUFF:
	MOV	SI,OFFSET WHATMSG
	CALL	SENDSTRING

	RET


COMMANDFOUND:

	CMP	BYTE PTR [SI],0DH
	JE	ENDOFCOMMAND

	CMP	BYTE PTR [SI],' '
	JE	ENDOFCOMMAND

	CMPSB
	JE	COMMANDFOUND		; LOOP TILL ALL COMPARED OR END
;
;	END OF STRING WRONG
;
	POP	CX
	POP	DI
	POP	SI

	JMP	TNCDUFF

ENDOFCOMMAND:

	POP	CX
	POP	DI
	POP	AX			; TO CLEAR STACK

	CALL	SCAN
	JMP	CMDPROC[DI]		; ENTER COMMAND PROCESSOR -
					; SI POINTS TO CHAR FOLLOWING COMMAND
SCAN:
	MOV	CX,10			; DONT LOOK TOO FAR!
SCAN1:
	CMP	BYTE PTR [SI],20H
	JNE	SCANRET

	INC	SI
	LOOP 	SCAN1
	XOR	AL,AL			; RETURN Z

SCANRET:
	RET
;
ROUTECMD:
;
;	PROCESS ROUTES TABLE UPDATE COMMANDS
;
	CMP	BYTE PTR [SI],20H
	JNE	NOTDISPALL


	JMP	DISPROUTES

NOTDISPALL:

	CALL	CONVTOAX25

	CALL	SCAN

	CALL	GETVALUE		; GET NUMBER, UP TO SPACE , CR OR OFFH
	JC	BADROUTECMD		; INVALID DIGITS

	MOV	AL,NEWVALUE
	MOV	ROUTEPORT,AL

	CALL	SCAN

	MOV	ROUTEDISP,1

	CMP	BYTE PTR [SI],20H
	JE	JUSTDISPLAY

	MOV	ZAPFLAG,0

	CMP	BYTE PTR [SI],'Z'
	JNE	NOTZAP

	MOV	ZAPFLAG,1
	JMP SHORT JUSTDISPLAY

NOTZAP:

	MOV	ROUTEDISP,2		; LOCK UPDATE

	CMP	BYTE PTR [SI],'!'
	JE	JUSTDISPLAY

	CALL	GETVALUE		; GET NUMBER, UP TO SPACE , CR OR OFFH
	JC	BADROUTECMD		; INVALID DIGITS

	MOV	AL,NEWVALUE
	MOV	NEWROUTEVAL,AL

	MOV	ROUTEDISP,0

	CALL	SCAN			; SEE IF !
	MOV	AH,[SI]

JUSTDISPLAY:

	MOV	ES,NODEDS

	MOV	DI,ES:NEIGHBOURS
	MOV	CX,ES:MAXNEIGHBOURS

ROUTLOOP:

	MOV	SI,OFFSET AX25CALL

	PUSH	CX
	PUSH	DI

	PUSH	AX
	CALL	COMPARECALLS
	POP	AX

	MOV	ES,NODEDS
	POP	DI
	POP	CX

	JE	FOUNDROUT

NEXTROUTE:

	ADD	DI,ES:ROUTES_LEN
	LOOP	ROUTLOOP

BADROUTECMD:

	JMP	BADVALUE

FOUNDROUT:

	MOV	AL,ROUTEPORT
	CMP	ES:NEIGHBOUR_PORT[DI],AL
	JNE	NEXTROUTE		; WRONG PORT

	CMP	ZAPFLAG,1
	JNE	NOTCLEARCOUNTS

	XOR	AX,AX
	MOV	ES:WORD PTR NBOUR_IFRAMES[DI],AX
	MOV	ES:WORD PTR NBOUR_IFRAMES+2[DI],AX
	MOV	ES:WORD PTR NBOUR_RETRIES[DI],AX
	MOV	ES:WORD PTR NBOUR_RETRIES+2[DI],AX

	JMP SHORT NOUPDATE

NOTCLEARCOUNTS:

	CMP	ROUTEDISP,1
	JE	NOUPDATE

	CMP	ROUTEDISP,2
	JE	LOCKUPDATE

	MOV	AL,NEWROUTEVAL
	MOV	ES:NEIGHBOUR_QUAL[DI],AL

	CMP	AH,'!'
	JNE	NOUPDATE

LOCKUPDATE:

	XOR	ES:NEIGHBOUR_FLAG[DI],1	; FLIP LOCKED BIT

NOUPDATE:

	MOV	SI,DI
	CALL	DISPLAYROUTE
	RET


PORTVALX:
;
;	THESE SET TNC PARAMS, SO SET PARAM TIMER AFTERWARDS
;
	MOV	PORTPARAMFLAG,1
	JMP SHORT PORTVAL

VALNODES:

	MOV	VALNODESFLAG,1

PORTVAL:
;
;	PROCESS PORT VALUE COMMANDS
;
	CALL	GETVALUE		; GET NUMBER, UP TO SPACE , CR OR OFFH
	JC	BADVALUE		; INVALID DIGITS

VALOK:

	MOV	AL,NEWVALUE
	MOV	PORTNO,AL		; SAVE PORT NUMBER

	MOV	ES,NODEDS

	MOV	BX,ES:PORTTABLE
	MOV	CX,ES:NUMBEROFPORTS

FINDPORT:
	CMP	AL,ES:PORTNUMBER[BX]
	JE	PORTOK

	MOV	BX,ES:PORTPOINTER[BX]

	LOOP	FINDPORT

	PUSH	DS
	POP	ES

	MOV	SI,OFFSET BADPORT
	JMP	ERRCOM

PORTOK:

	CMP	VALNODESFLAG,1
	JE	DISPVALNODES

	CALL	SCAN
	CMP	BYTE PTR [SI],20H
	JBE	DISPVAL			; DISPLAY ONLY

	CALL	GETVALUE		; GET NUMBER, UP TO SPACE , CR OR OFFH
	JC	BADVALUE		; INVALID DIGITS

	MOV	CL,0
	CALL	KEYDISP			; DISPLAY KEYWORD WAS

	MOV	SI,CMDFLAG[DI]		; ADDR OF FLAG
	MOV	AH,ES:[SI][BX]		; GET OLD VALUE
	MOV	AL,NEWVALUE
	MOV	ES:[SI][BX],AL		; SET FLAG

	CMP	PORTPARAMFLAG,1
	JNE	NOTPP

	MOV	PARAMTIMER[BX],18	; 1 SEC

NOTPP:

	PUSH	DS
	POP	ES

	MOV	AL,AH			; DISPLAY OLD
	JMP	VALDISP

BADVALUE:
	MOV	SI,OFFSET BADMSG
ERRCOM:
	CALL	SENDSTRING

	PUSH	DS
	POP	ES

	RET

DISPVALNODES:

	CMP	ES:PERMITTEDCALLS[BX],0
	JNE	VALN00

	MOV	SI,OFFSET NOVALCALLS
	JMP	ERRCOM			; NONE TO DISPLAY

VALN00:

	MOV	SI,ES:PERMITTEDCALLS[BX]


VALN10:
	CMP	BYTE PTR ES:[SI],0
	JE	VALNRET			; FINISHED

	MOV	AX,0
	CALL	CMDR95

	ADD	SI,7
	JMP	VALN10

VALNRET:
	MOV	AL,CR
	CALL	SENDCHAR

	PUSH	DS
	POP	ES
	RET
	
DISPVAL:
