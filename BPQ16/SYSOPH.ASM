	INT	21H

EXIT:
	MOV	AX,4C00H
	INT	21H

VERSIONOK:

	MOV	AX,DS:4			; NODE DS

	POP	DS

	MOV	NODEDS,AX

	MOV	AH,1
	MOV	AL,PORT
	MOV	DL,4			; SYSOP APPL
	MOV	CL,0			; APPL FLAGS
	CALL	NODE
;
	MOV	DX,OFFSET PWFILE
	MOV	AH,3DH
	MOV	AL,0			; READ ONLY
	INT	21H			; OPEN IT

	JC	NOPWFILE

	MOV	BX,AX
	MOV	DX,OFFSET PWTEXT
	MOV	CX,80
	MOV	AH,3FH
	INT	21H

	MOV	AH,3EH
	INT	21H			; CLOSE

	MOV	SI,OFFSET PWTEXT
PWSETUP:
	CMP	BYTE PTR [SI],20H
	JB	FILEOK

	INC	SI
	INC	PWLEN

	JMP SHORT PWSETUP

NOPWFILE:

	MOV	DX,OFFSET PASSERR
	MOV	AH,9
	INT	21H

FILEOK:
;
;	SEE IF RESIDENT OPTION SPECIFIED
;
;
	MOV	SI,6DH
	MOV	DI,OFFSET RESIDENT
	MOV	CX,8
	REP CMPSB

	JNE	NOTRESIDENT

	CALL	MAKERESIDENT

	INT	27H

NOTRESIDENT:

	JMP	OUTERLOOP

MAKERESIDENT:

;
;	hook the system timer - we need to be able to poll the rx buffer
;
	MOV	RESFLAG,1
	MOV	WORD PTR SAVECMD,0	; DISABLE SAVENODES

	push	ds
	PUSH	ES

	MOV	AX,0
	MOV	DS,AX
;
	MOV	SI,8*4			; ADDR OF H/W TIMER INTERRUPT (INT 8)
;
	MOV	AX,[SI]			; SYSTEM TIMER ROUTINE OFFSET
	MOV	WORD PTR cs:HWTIMERLINK,AX
	MOV	AX,2[SI]
	MOV	WORD PTR cs:HWTIMERLINK+2,AX
;
	MOV	[SI+2],CS
	MOV	AX,OFFSET HWTIMERINT
	MOV	[SI],AX
;
	POP	ES
	POP	DS

	MOV	DX,OFFSET ENDOFPROG
	RET




MARKER:
;
;	FOLLOWING SEQUENCE OCCURS AT START OF THENODE CODE SEGMENT
;
	RET
	DB	'BPQ'			; FOR DUMP SYSTEM

FILENAME	DB	'BPQNODES',0

HANDLE		DW	0

DECODEDNAME	DB	20 DUP (0)

LINE		DB	80 DUP (0)

ROUTEADD	DB	'ROUTE ADD '
NODEADD		DB	'NODE ADD '

G8BPQ		LABEL BYTE
MSG1		DB	NAMESTRING,' Switch SYSOP Support Program for Version '
		db	VERSIONSTRING,' - ',DATESTRING,CR,LF,'$'
MSG2		DB	'CANT FIND BPQCODE',CR,LF,'$'
MSG3		DB	'TABLE DUMP COMPLETE',CR,LF,'$'


SAVENODES:
;
;	CHECK ALL INTS
;
	ASSUME	DS:NOTHING

	MOV	BX,0

CHECKLOOP:

	CALL	CHECKIT
	JE	CODEFOUND

	ADD	BX,4

	CMP	BX,400H
	JB	CHECKLOOP

	PUSH	CS
	POP	DS

	MOV	DX,OFFSET MSG2
	MOV	AH,9
	INT	21H

	RET

CODEFOUND:

	PUSH	DS

	MOV	AX,[SI]			; DATA SEG
	MOV	DS,AX

	PUSH	DS			; SAVE 

	PUSH	CS			; GET OUR SEG BACK FOR OPEN
	POP	DS
;
;	OPEN FILE
;
	MOV	DX,OFFSET FILENAME
	MOV	CX,0
	MOV	AH,3CH
	INT	21H

	MOV	HANDLE,AX

	POP	DS

	ASSUME DS:NOTHING

	CALL	DOROUTES
	CALL	DONODES

	POP	DS

CLOSE:

	PUSH	CS
	POP	DS

	MOV	BX,HANDLE
	MOV	AH,3EH
	INT	21H			; CLOSE FILE
;

	MOV	DX,OFFSET MSG3
	MOV	AH,9
	INT	21H


	RET

	
CHECKIT:

	MOV	AX,0
	MOV	DS,AX

	MOV	AX,2[BX]		; CODE SEGMENT
	MOV	DS,AX

	MOV	SI,0
	MOV	DI,OFFSET MARKER
	MOV	CX,4
	REP CMPSB

	RET

DONODES:
	MOV	SI,OFFSET NODEADD
	MOV	DI,OFFSET LINE
	MOV	CX,9
	CLI
	REP MOVS LINE, NORMCALL
	STI

	MOV	BX,DS:DESTS
	MOV	CX,DS:MAXNODES
CMDN10:
	PUSH	CX

	MOV	DI,OFFSET LINE+9

	CMP	DEST_CALL[BX],0
	JE	CMDN20			; SPARE
;
	TEST	DEST_STATE[BX],80H
	JNZ	CMDN20			; SPECIAL (BBS) NODE

	LEA	SI,DEST_CALL[BX]
	CALL	DECODENODENAME		; CONVERT TO ALIAS:CALL
	MOV	CX,18
CMDN15:
	LODS	NORMCALL
	STOSB
	CMP	AL,' '
	JE	CMDN16

	LOOP	CMDN15

CMDN16:

	PUSH	BX

	MOV	CX,3
CMDN110:
	MOV	SI,ROUT1_NEIGHBOUR[BX]
	CMP	SI,0
	JE	CMDN199

	PUSH	CX

	PUSH	SI			; SAVE NEIGHBOUR

	PUSH	DI
	CALL	CONVFROMAX25		; CONVERT TO CALL
	POP	DI
	MOV	SI,OFFSET NORMCALL
NCOPY:
	LODS	NORMCALL
	STOSB

	CMP	AL,' '
	JNE	NCOPY
;
;	GET PORT FROM ROUTES ENTRY
;
	POP	SI
	MOV	AL,NEIGHBOUR_PORT[SI]
	CALL	XCONV_DIGITS		; CONVERT AL TO DECIMAL DIGITS

	mov	AL,' '
	stosb

	MOV	AL,ROUT1_QUALITY[BX]
	CALL	XCONV_DIGITS		; CONVERT AL TO DECIMAL DIGITS

	mov	AL,' '
	stosb

	TEST	ROUT1_OBSCOUNT[BX],80H
	JZ	NOTLOCKEDDEST

	MOV	AX,' !'
	STOSW

NOTLOCKEDDEST:

	ADD	BX,4

	POP	CX
	LOOP	CMDN110

CMDN199:

	MOV	AX,0A0DH
	STOSW

	PUSH	DS

	PUSH	CS
	POP	DS

	MOV	DX,OFFSET LINE
	MOV	CX,DI
	SUB	CX,DX			; GET LENGTH
	MOV	BX,HANDLE

	MOV	AH,40H
	INT	21H

	POP	DS

	POP	BX

CMDN20:

	POP	CX
	ADD	BX,DS:DESTS_LEN
	LOOP	CMDN10_JMP		; TOO FAR!

CMDN99A:
	RET

CMDN10_JMP:
	JMP	CMDN10


DECODENODENAME:
;
;	SI = TABLE ENTRY WITH AX25 CALL AND ALIAS
;
;	RETURNS SI = 20 BYTE DECODED NAME IN FORM ALIAS:CALL
;
	PUSH	CX
	PUSH	DI

	MOV	DI,OFFSET DECODEDNAME
	MOV	CX,10
	MOV	AX,'  '
	REP STOSW			; CLEAR IT

	CALL	CONVFROMAX25

	PUSH	CX			; SAVE CALL LENGTH
	MOV	DI,OFFSET DECODEDNAME


	MOV	CX,6			; MAX LENGTH

DECODEN10:
	LODSB
	CMP	AL,' '
	JBE	DECODEN20
	STOSB

	LOOP	DECODEN10

DECODEN20:
	MOV	AL,':'
	STOSB

	POP	CX
	MOV	SI,OFFSET NORMCALL
	CLI
	REP MOVS LINE, NORMCALL
	STI

	POP	DI
	POP	CX

	MOV	SI,OFFSET DECODEDNAME
	RET



DOROUTES:
;
	MOV	SI,OFFSET ROUTEADD
	MOV	DI,OFFSET LINE
	MOV	CX,10
	CLI
	REP MOVS LINE, NORMCALL
	STI

	MOV	SI,DS:NEIGHBOURS
	MOV	CX,DS:MAXNEIGHBOURS
XCMDR10:
	PUSH	CX

	MOV	DI,OFFSET LINE+10

	CMP	NEIGHBOUR_CALL[SI],0
	JE	XCMDR92			; SPARE
;
	MOV	AX,0
	CALL	XCMDR95			; DISPLAY CALL

	MOV	AL,NEIGHBOUR_PORT[SI]
	CALL	XCONV_DIGITS

	MOV	AL,20H
	STOSB

	MOV	AL,NEIGHBOUR_QUAL[SI]
	CALL	XCONV_DIGITS
	MOV	AL,20H
	STOSB


	TEST	NEIGHBOUR_FLAG[SI],1
	JZ	XNOTLOCKED

	MOV	AL,'!'
	STOSB

XNOTLOCKED:
;
;	CHECK FOR DIGIS
;
	CMP	NEIGHBOUR_DIGI[SI],0
	JE	XCMDR50

	MOV	AL,20H
	STOSB

	MOV	AX,'IV'
	STOSW
	MOV	AX,' A'
	STOSW				; PUT IN VIA

	MOV	AX,7
	CALL	XCMDR95			; DISPLAY DIGI CALL  

	CMP	NEIGHBOUR_DIGI+7[SI],0
	JE	XCMDR50

	MOV	AX,14
	CALL	XCMDR95			; DISPLAY 2ND DIGI CALL  

XCMDR50:

	MOV	AL,20H
	STOSB

	MOV	AL,NBOUR_MAXFRAME[SI]
	CALL	XCONV_DIGITS

	MOV	AL,20H
	STOSB

	MOV	AL,NBOUR_FRACK[SI]
	CALL	XCONV_DIGITS

	MOV	AL,20H
	STOSB

	MOV	AL,NBOUR_PACLEN[SI]
	CALL	XCONV_DIGITS

XCMDR90:
	PUSH	DS

	PUSH	CS
	POP	DS

	MOV	AX,0A0DH
	STOSW

	MOV	DX,OFFSET LINE
	MOV	CX,DI
	SUB	CX,DX			; GET LENGTH
	MOV	BX,HANDLE

	MOV	AH,40H
	INT	21H

	POP	DS

XCMDR92:

	POP	CX
	ADD	SI,DS:ROUTES_LEN
	LOOP	XCMDR10_JMP

	RET

XCMDR10_JMP:

	JMP	XCMDR10


XCMDR95:

	PUSH	SI
	PUSH	DI

	ADD	SI,AX
	CALL	CONVFROMAX25
	INC	CX

	MOV	SI,OFFSET NORMCALL
	POP	DI
	CLI
	REP MOVS LINE, NORMCALL
	STI

	POP	SI
	RET

XCONV_DIGITS:

	CMP	AL,10
	JB	XUNITS

	cmp	AL,100
	jb	XTENS

	MOV	AH,0
	DIV	D100
	OR	AL,30H
	STOSB

	mov	AL,AH

XTENS:
	CBW
	DIV	D10
	OR	AL,30H
	STOSB

	MOV	AL,AH			; REMAINDER = UNITS
XUNITS:
	add	AL,30H
	STOSB

	ret

CODE	ENDS
