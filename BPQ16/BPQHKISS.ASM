CODE	SEGMENT

	ASSUME DS:CODE, CS:CODE

	ORG	0100H

	INCLUDE	ENVIRON.ASM

GO:
	JMP	INIT
;
;	400H BYTE RECEIVE BUFFER FOR EACH PORT
;
	DB	2FDH DUP (0)
;
;	CONTROL FIELDS FOR EACH PORT 
;
PORT		DB	?		; PORT NUMBER
RTSFLG		DB	0		; BIT 0 SET IF RTS/DTR UP
VLSR		DB	0		; LAST RECEIVED LSR VALUE
VMSR		DB	0
RXCOUNT		DW	0		; BYTES IN RX BUFFER
PUTPTR		DW	0		; POINTER FOR LOADING BUFFER
GETPTR		DW	0

OLDINT14	DD	0		; SAVED INT 14 VECTOR

INT14:

	STI				
					
	CMP	DL,CS:PORT		
	JZ	ITSFORUS
			
	JMP	DWORD PTR CS:OLDINT14	; NOT FOR OUR PORT - PASS DOWN CHAIN
					
ITSFORUS:

	PUSH	DS

	PUSH	CS
	POP	DS

	PUSH	DX			
	PUSH	CX			
	PUSH	BX			

	OR	AH,AH			
	JZ	INITPORT		; INIT

	DEC	AH			
	JZ	SENDCHAR		; SEND 

	DEC	AH			
	JZ	GETCHAR			; RECEIVE

	DEC	AH			
	JZ	L14A3			; STATUS

	DEC	AH			
	JZ	L14A6			; INSTALLED?

	DEC	AH			
	JZ	L14A9			; DROP RTS

	DEC	AH			
	JZ	L14AC			; RAISE RTS

	DEC	AH			
	JZ	L14AF			; SEND BREAK

	DEC	AH			
	JZ	NDREAD

	DEC	AH
	DEC	AH
	JNZ	INT14EXIT

	JMP	WRITEBUFFER

NDREAD:
;
;	NON - DESTRUCTIVE READ
;
	MOV	BX,[GETPTR]
	MOV	AL,[BX]
	JMP	INT14EXIT


					
GETCHAR:
	JMP	SHORT	GETCHARSUPPORT		
	NOP				
					
L14A3:
;
;	GET STUFF FROM NODE IF BUFFER EMPTY
;
	CMP	RXCOUNT,0
	JNE	GOTSOME

	PUSH	ES

	PUSH	DS
	POP	ES

	PUSH	SI
	PUSH	DI
	CALL	GETDATA
	POP	DI
	POP	SI

	POP	ES

GOTSOME:
	JMP	RETURNSTATUS			

L14A6:
	JMP	RETURNLOADEDFLAG			
L14A9:
	JMP	DROPRTS			
L14AC:
	JMP	RAISERTS			
L14AF:
	JMP	SENDBREAK			

INT14EXIT:

	POP	BX			
	POP	CX			
	POP	DX			

	POP	DS
	IRET				

					
INITPORT:

	JMP	RETURNSTATUS	

SENDCHAR:
;
;	SEND CHAR
;
	JMP	SHORT	RETURNSTATUS

GETCHARSUPPORT:

	MOV	BX,[GETPTR]
	JMP	GETC00

GETCWAIT:
	PUSH	AX
	MOV	AX,1000H
	INT	15H			; RELEASE TIMESLICE
	POP	AX
GETC00:
	CMP	[RXCOUNT],0
	JE	GETCWAIT		; WAIT FOR CHAR

	MOV	AL,[BX]		; GET CHAR
	PUSH	AX
	INC	BX
	DEC	WORD PTR [RXCOUNT]
	MOV	DX,0400H
	CMP	BX,DX			; BUFFER WRAP?
	JL	L153C			; NO

	MOV	BX,0			; RESET TO START
L153C:
	CMP	WORD PTR [RXCOUNT],128	; GETTING LOW?
	JNB	L1560				; NO

	MOV	AH,[RTSFLG]
	TEST	AH,1			; RTS UP?
	JZ	L1560			; YES
;
;	RTS HAD BEEN DROPPED TO STOP OTHER END SENDING - RAISE IT AGAIN
;
	AND	AH,0FEH			; CLEAR FLAG
	MOV	[RTSFLG],AH

L1560:
	POP	AX
	MOV	[GETPTR],BX

	MOV	AH,[VLSR]
	AND	AH,0FEH			; MASK DAV BIT

	CMP	WORD PTR [RXCOUNT],0
	JZ	L1579
	OR	AH,1			; SET DAV IF BUFFER NOT EMPTY
L1579:
	JMP	INT14EXIT

RETURNSTATUS:
;
;	GET STATUS - AH = LSR, AL = MODEM STATUS
;
	MOV	AL,VLSR
	AND	AL,0FEH				
	CMP	WORD PTR [RXCOUNT],0	
	JZ	L1599				
	OR	AL,1			; SET DATA AVAIL
L1599:
	MOV	AH,AL				
	MOV	AL,VMSR
	JMP	INT14EXIT			

RETURNLOADEDFLAG:
;
;	RETURN 'LOADED' FLAG
;
	MOV	CL,80H			; REQUEST MONITORING
	MOV	DL,0
	MOV	AL,STREAMBASE		; FIRST PORT
	MOV	AH,1			; SET APPL MASK

	CALL	NODE

	MOV	AX,0AA55H			
	JMP	INT14EXIT			

DROPRTS:
;
;	DROP DTR/RTS
;
	MOV	CL,0H			; DISABLE MONITORING
	MOV	DL,0
	MOV	AL,STREAMBASE		; FIRST PORT
	MOV	AH,1			; SET APPL MASK

	CALL	NODE

	JMP	INT14EXIT

RAISERTS:
;
;	RAISE DTR/RTS
;
	MOV	CL,80H			; ENABLE MONITORING
	MOV	DL,0
	MOV	AL,STREAMBASE		; FIRST PORT
	MOV	AH,1			; SET APPL MASK

	CALL	NODE

	JMP	INT14EXIT

					
SENDBREAK:

	JMP	INT14EXIT		

					
PUTCHAR:

	MOV	DI,[PUTPTR]
	MOV	[DI],AL	
	INC	DI		
	INC	WORD PTR [RXCOUNT]

	MOV	AX,400H			; BUFFER LENGTH
	CMP	DI,AX			; END OF BUFFER ADDR
	JL	L1648			; OK
;
;	BUFFER WRAPAROUND
;
	MOV	DI,0

L1648:

	CMP	DI,[GETPTR]		; BUFFER OVERRUN?
	JNE	L167B			; NO

	OR	BYTE PTR [VLSR],2	; SET OVERRUN ERROR
	DEC	WORD PTR [RXCOUNT]	; LOSE THE CHAR
	JMP	SHORT	L1680

L167B:
	MOV	[PUTPTR],DI

L1680:

	RET


WRITEBUFFER:
;
	PUSH	DI

WBLOOP:

	MOV	AL,ES:[DI]
	INC	DI

	PUSH	CX
	CALL	KISSCHAR
	POP	CX

	LOOP	WBLOOP

	POP	DI

	POP	BX			
	POP	CX			
	POP	DX			

	MOV	AX,0			; STATUS
	MOV	CX,0			; NOT SEND
	POP	DS
	IRET				



CURSOR		DW	OFFSET	LINEBUFFER+1
LINEBUFFER	DB	350 DUP (0)	; MSG FROM PC APPL
MSGLEN		DW	-1
	
STREAMBASE	DB	0

MONITORDATA	DB	350 DUP (0)	; RAW FRAME FROM NODE

D10		DB	10

MMASK		DB	02H
HOSTFLAG	DB	0
KISSESC		EQU	1B		; ESC RECEIVED
;
;	KISS CONTROL VALUES
;
FEND		EQU	0C0H		; KISS FRAME END
FESC		EQU	0DBH		; KISS ESCAPE

TFEND		EQU	0DCH		; ESCAPED FEND
TFESC		EQU	0DDH		; ESCAPED ESCAPE

;
;	BASIC LINK LEVEL MESSAGE BUFFER LAYOUT
;
MESSAGE	STRUC

MSGCHAIN	DW	?		; CHAIN WORD
MSGPORT		DB	?		; PORT 
MSGLENGTH	DW	?		; LENGTH
MSGDEST		DB	?

MESSAGE	ENDS
;
;	KISS MODE SUPPORT CODE
;
KISSCHAR:

	CMP	AL,FEND
	JE	KBFEND			; END OF FRAME RECEIVED

	CMP	AL,FESC
	JE	KBFESC			; ESCAPE

	TEST	HOSTFLAG,KISSESC
	JZ	NOESC
;
;	IN ESCAPE SEQUENCE - CHAR SHOULD BE TFEND OR TFESC
;
	AND	HOSTFLAG,NOT KISSESC	; CLEAR IT

	CMP	AL,TFEND
	JE	SND0C0

	CMP	AL,TFESC
	JE	SND0C1

KISSRET:

	RET				; IGNORE IT

SND0C1:
	MOV	AL,FESC
	JMP SHORT NOESC

SND0C0:
	MOV	AL,FEND
NOESC:
;
	MOV	BX,CURSOR
	MOV	[BX],AL
	INC	BX

	INC	MSGLEN
	CMP	MSGLEN,330
	JA	KISSRET			; OVERFLOW

	MOV	CURSOR,BX
	RET

KBFESC:

	OR	HOSTFLAG,KISSESC	; SET ESCAPE MODE
	RET


KBFEND:
;
;	FRAME DELIMITER
;
;	MAY BE START OR END FRAME 
;
	CMP	MSGLEN[BX],-1
	JE	KISSRET			; NULL FRAME - MUST BE START OF FRAME
;
;	WE HAVE AT LEAST ONE BYTE OF A FRAME
;
	MOV	AL,LINEBUFFER		; CONTROL BYTE
	AND	AL,0FH			; MASK OFF CHANNEL
	JZ	KBK000			; DATA
;
;	FOR NOW WE IGNORE ALL OTHERS - DO ENQUIRE LATER
;
;	CP	0EH			; ENQUIRE
;	JR	NZ,KBKEND
;
;	ENQUIRE COMMAND - RETURN FEND END FEND
;
;	LD	A,FEND
;	CALL	OUTCH0			; SEND DIRECT

;	LD	A,0EH
;	CALL	OUTCH0

;	LD	A,FEND
;	CALL	OUTCH0

	JMP SHORT RESETBUFFER

KBK000:
;
;	SEND DATA TO RADIO PORT
;
	MOV	AL,LINEBUFFER		; CTRL BYTE AGAIN
	OR	AL,AL
	JNE	KBK020			; WE HAVE A PORT NUMBER
;
;	GET DEFAULT PORT
;
	MOV	CL,MMASK		; SHOULD HAVE ONE BIT SET 
	OR	CL,CL
	JZ	RESETBUFFER		; NO BITS DEFINED

	MOV	AL,10H			; PORT NO IN UPPER NIBBLE

KBK010:

	TEST	CL,1
	JNZ	KBK020			; FOUND IT

	ADD	AL,10H
	SHR	CL,1

	JMP	KBK010			; LOOP BACK

KBK020:
;
;	PORT IN UPPER 4 BITS OF AL
;
	MOV	CL,4
	SHR	AL,CL
;
;	GET PORT ENTRY
;
	MOV	CX,MSGLEN
	MOV	AH,10			; RAW SEND FRAME TO PORT IN AL

	PUSH	ES

	PUSH	CS
	POP	ES

	PUSH	SI
	MOV	SI,OFFSET LINEBUFFER+1	; AFTER CONTROL BYTE
	
	PUSH	DI
	CALL	NODE
	POP	DI

	POP	SI
	POP	ES

RESETBUFFER:

	MOV	CURSOR,OFFSET LINEBUFFER+1; SET MESSAGE START
	MOV	MSGLEN,-1		; FOR CONTROL

	RET






GETDATA:
;
;	SEE IF ANYTHING QUEUED
;
	MOV	AL,STREAMBASE			; FIRST PORT
	MOV	AH,11
	MOV	DI,OFFSET MONITORDATA
	CALL	NODE

	CMP	CX,0
	JE	DATAPOLLRET

	MOV	DI,OFFSET MONITORDATA
;
;	ENSURE PLENTY OF SPACE
;
;	CHECK PORT
;
	MOV	CL,MSGPORT[DI]
	AND	CL,7FH			; MASK TX BIT

	DEC	CL
	MOV	AL,1
	SHL	AL,CL			; SHIFT BIT UP

	TEST	MMASK,AL
	JZ	DATAPOLLRET		; DONT WANT THIS PORT
;
	XOR	DH,DH			; CONTROL BYTE

	CMP	MMASK,AL
	JE	KISS_SINGLE		; SINGLE PORT MODE
;
;	MULITPORT KISS MODE - PUT PORT NUMBER IN UPPER NIBBLE OF DH
;
	MOV	DH,MSGPORT[DI]
	MOV 	CL,4
	SHL	DH,CL			; UP 4 BITS

KISS_SINGLE:

	MOV	CX,MSGLENGTH[DI]
	SUB	CX,5
	JC	DATAPOLLRET
	JCXZ	DATAPOLLRET

	LEA	SI,MSGDEST[DI]

	MOV	AL,0C0H			; FEND
	CALL	PUTCHAR
;
	MOV	AL,DH			; CONTROL
	CALL	PUTCHAR
;
KISSLOOP:

	LODSB
	CMP	AL,FEND
	JE	KTXFEND

	CMP	AL,FESC
	JNE	NOTFESC

	CALL	PUTCHAR			; SEND ESC

	MOV	AL,TFESC
	JMP SHORT NOTFESC		; SEND TFESC

KTXFEND:

	MOV	AL,FESC
	CALL	PUTCHAR
	MOV	AL,TFEND

NOTFESC:

	CALL	PUTCHAR
	LOOP	KISSLOOP

	MOV	AL,0C0H			; FEND
	CALL	PUTCHAR
;
DATAPOLLRET:

	RET



HEXOUT:
