CONFIGHANDLE	DW	0

INIT:

	MOV	DX,OFFSET MSG
	MOV	AH,9
	INT	21H
;
;	GET NODE INTERRUPT NUMBER FROM CONFIG FILE
;
	MOV	DX,OFFSET CONFIGFILENAME
	MOV	AH,3DH
	MOV	AL,0			; READ ONLY
	INT	21H			; OPEN IT

	JC	NOCONFIGFILE

	MOV	CONFIGHANDLE,AX

	MOV	BX,AX
	MOV	DX,OFFSET CONFIGTABLE
	MOV	CX,128
	MOV	AH,3FH

	INT	21H			; READ
	CMP	AX,CX
	JNE	SHORTCONFIG

	JMP SHORT PROCESSCONFIG

NOCONFIGFILE:

	MOV	DX,OFFSET NOCONFIGMSG
	JMP SHORT CONFIGERR

SHORTCONFIG:

	MOV	DX,OFFSET BADCONFIGMSG

CONFIGERR:

	MOV	AH,9
	INT	21H

	MOV	AX,4C01H
	INT	21H			; EXIT

PROCESSCONFIG:

	MOV	BX,CONFIGHANDLE
	MOV	AH,3EH
	INT	21H			; CLOSE IT

	MOV	AL,C_HOSTINTERRUPT	; INTERRUPT
	MOV	BYTE PTR INTVAL+1,AL	; PATCH NODE CALL INSTRUCTION

;
;	GET NODE VERSION
;
	MOV	AH,0
	ADD	AX,AX
	ADD	AX,AX
	MOV	SI,AX

	PUSH	DS

	XOR	AX,AX
	MOV	DS,AX

	LDS	SI,DS:[SI]		; GET POINTER TO HOST SUPPORT CODE
	SUB	SI,7
	MOV	DI,OFFSET G8BPQ
	MOV	CX,5
	REP CMPSB			; MAKE SURE SWITCH IS LOADED

	JE	SWITCHOK

	POP	DS

	MOV	DX,OFFSET NOTBPQERROR

	MOV	AH,9
	INT	21H

	JMP SHORT EXIT

SWITCHOK:

	CMP	BYTE PTR DS:[SI],MAJORVERSION
	JNE	BADVERSION

	CMP	BYTE PTR DS:1[SI],MINORVERSION
	JE	VERSIONOK


BADVERSION:

	POP	DS

	MOV	DX,OFFSET VERSERROR
	MOV	AH,9
	INT	21H

EXIT:
	MOV	AX,4C00H
	INT	21H

VERSIONOK:

	POP	DS
	MOV	SI,82H			; PARAM STRING

	CALL	GETVALUE		; NUMBER OF PORTS
	JC	BADPORTVAL

	MOV	AL,NEWVALUE
	OR	AL,AL
	JZ	BADPORTVAL

	CMP	AL,64
	JA	BADPORTVAL

	MOV	AL,NEWVALUE
	MOV	STREAMBASE,AL

	CALL	GETVALUE		; BIOS PORT
	JC	BADPORTVAL

	MOV	AL,NEWVALUE
	DEC	AL
	MOV	PORT,AL
;
	CALL	GETVALUE		; BIOS PORT
	JC	BADPORTVAL

	MOV	AL,NEWVALUE
	MOV	MMASK,AL
;
;	INSTALL INTERRUPTS
;
	MOV	AL,14H				
	MOV	AH,35H				
	INT	21H			; GET VECTOR	
	MOV	WORD PTR OLDINT14,BX		
	MOV	WORD PTR OLDINT14+2,ES	; SAVE	

	MOV	DX,OFFSET INT14			
	MOV	AL,14H				
	MOV	AH,25H			; SET NEW VECTOR
	INT	21H				

	MOV	AX,0			; START OF BUFFER				
	MOV	DS:[PUTPTR],AX		
	MOV	DS:[GETPTR],AX

	MOV	DX,OFFSET ENDOFCODE
	INT	27H


BADPORTVAL:

	MOV	AH,9
	MOV	DX,OFFSET ERRORMSG
	INT	21H

	RET

GETVALUE:
;
;	EXTRACT NUMBER (HEX OR DECIMAL) FROM INPUT STRING
;
	MOV	NEWVALUE,0

	LODSB
	CMP	AL,'$'			; HEX?
	JE	DECODEHEX
	DEC	SI
VALLOOP:
	LODSB
	CMP	AL,' '
	JE	ENDVALUE
	CMP	AL,0DH
	JE	ENDVALUE
	CMP	AL,','
	JE	ENDVALUE
;
;	ANOTHER DIGIT - MULTIPLY BY 10
;
	MOV	AL,NEWVALUE
	CMP	AL,26
	JNC	DUFFVALUE		; WILL OVERFLOW IF MULTIPLIED

	MUL	D10
	MOV	NEWVALUE,AL

	MOV	AL,-1[SI]
	SUB	al,'0'
	JC	DUFFVALUE
	CMP	AL,10
	JNC	DUFFVALUE

	ADD	NEWVALUE,AL

	JC	DUFFVALUE
	JMP	VALLOOP

DECODEHEX:
HEXLOOP:
	LODSB
	CMP	AL,' '
	JE	ENDVALUE
	CMP	AL,0DH
	JE	ENDVALUE
	CMP	AL,','
	JE	ENDVALUE
;
;	ANOTHER DIGIT - MULTIPLY BY 16
;
	MOV	AL,NEWVALUE
	MUL	D16
	MOV	NEWVALUE,AL

	MOV	AL,-1[SI]
	SUB	al,'0'
	JC	DUFFVALUE
	CMP	AL,10
	JC	HEXOK
	SUB	AL,7
	CMP	AL,10
	JC	DUFFVALUE

	CMP	AL,16
	JNC	DUFFVALUE
HEXOK:
	ADD	NEWVALUE,AL
	JMP	HEXLOOP

ENDVALUE:
	CLC
	RET

DUFFVALUE:
	STC
	RET




