BACKGROUND:
;
;	CHECK FOR TIMER ACTIVITY
;
	INC	RAN1


	INC	RANDOM			; FOR CSMA TIMER

	CMP	L2TIMERFLAG,10		; 10 PER SEC
	JB	NOTL2TIMER

	OUT	WATCHDOG,AL		; KICK WATCHDOG
	INC	RAN2

	INC	TENHZCOUNT

	SUB	L2TIMERFLAG,10
	CALL	L2TIMERPROC

NOTL2TIMER:

	CMP	L3TIMERFLAG,100*60	; 1 PER MIN
	JB	NOTL3TIMER

	SUB	L3TIMERFLAG,100*60
;
;	CHECK BUFFER POOL - IF BELOW 9 FOR 2 MINS, FORCE A WARM START
;
	CMP	QCOUNT,9
	JA	COUNTOK

	INC	BUFFERSLOW
	CMP	BUFFERSLOW,2
	JB	DOL3			; WAIT A BIT LONGER

	JMP	WARMSTART		; BUFFER PROBLEMS - RESTART

COUNTOK:

	MOV	BUFFERSLOW,0

DOL3:

	CALL	L3TIMERPROC

	CALL	STATSTIMER

NOTL3TIMER:

	CMP	L4TIMERFLAG,100		; 1 PER SEC
	JB	NOTL4TIMER

	SUB	L4TIMERFLAG,100

	CALL	L3FASTTIMER
	CALL	L4TIMERPROC

	CALL	TNCTIMERPROC

	CALL	CHECKSWITCHSTATE	; SEE IF SWITCH HAS CHANGED


NOTL4TIMER:
;
;	CHECK FOR MESSAGES RECEIVED FROM COMMS LINKS
;
	MOV	BX,PORTTABLE
	MOV	CX,NUMBEROFPORTS
MAINL00:
	MOV	AL,PORTNUMBER[BX]
	MOV	CURRENTPORT,AL		; PORT NUMBER
	MOV	CURRENTPORTPTR,BX

	LEA	SI,PORTRX_Q[BX]
	CLI
	CALL	Q_REM			; SEE IF ANYTHING THERE
	STI
	JZ	MAINL10
;
	INC	RAN3

	PUSH	BX
	PUSH	CX
	MOV	AL,CURRENTPORT
	MOV	MSGPORT[DI],AL		; PUT PORT INTO MSG BUFFER
;
	CALL	L2ROUTINE		; LEVEL 2 PROCESS
	POP	CX
	POP	BX
MAINL10:
	LEA	SI,PORTTX_Q[BX]
	CLI
	CALL	Q_REM			; SEE IF ANYTHING THERE
	STI
	JZ	MAINL15
;
;	MESSAGE TO SEND - SEND IT
;
	CMP	PORTDISABLED[BX],0
	JE	PORTACTIVE
;
;	PORT HAS BEEN DISABLED
;
	CALL	RELBUFF
	JMP SHORT MAINL15


PORTACTIVE:

	PUSH	BX
	PUSH	CX
	INC	L2FRAMESSENT[BX]
	JNZ	INC000
	INC	L2FRAMESSENT+2[BX]
INC000:
	CALL	PORTTXROUTINE[BX]	; LEVEL 2 PROCESS
	POP	CX
	POP	BX
MAINL15:
	PUSH	BX
	PUSH	CX
	CALL	PORTRXROUTINE[BX]	; SEE IF MESSAGE RECEIVED
	POP	CX
	POP	BX

	MOV	BX,PORTPOINTER[BX]
	LOOP	MAINL00
;
;	CHECK FOR INCOMING MESSAGES ON LINK CONTROL TABLE -
;	   BY NOW ONLY 'I' FRAMES WILL BE PRESENT -
;	   LEVEL 2 PROTOCOL HANDLING IS DONE IN MESSAGE RECEIVE CODE
;	   AND LINK HANDLING INTERRUPT ROUTINES
;
	MOV	BX,LINKS
	MOV	CX,MAXLINKS
MAINL20:
	LEA	SI,RX_Q[BX]
	CLI
	CALL	Q_REM			; SEE IF ANYTHING THERE
	STI
	JZ	MAINL30
;
	PUSH	CX
	PUSH	BX
	CALL	PROCESSIFRAME
	POP	BX
	POP	CX
MAINL30:
;
;	CHECK FOR OUTGOING MSGS
;
	CMP	L2STATE[BX],5
	JB	MAINL40			; CANT SEND TEXT TILL CONNECTED

;	CMP	VER1FLAG[BX],1
;	JE	MAINL35			; NEED TO RETRY WITH I FRAMES IF VER 1

; 	CMP	L2RETRIES[BX],0
;	JNE	MAINL40			; CANT SEND TEXT IF RETRYING
MAINL35:
	TEST	L2FLAGS[BX],RNRSET
	JNZ	MAINL40			; CANT SEND TEXT IF RNR

	PUSH	BX
	PUSH	CX

	CALL	SDETX

	POP	CX
	POP	BX
MAINL40:
	ADD	BX,TYPE LINKTABLE
	LOOP	MAINL20
;
	CALL	L4BG			; DO LEVEL 4 PROCESSING
	CALL	L3BG

;
;	SEE IF ANY FRAMES TO TRACE
;
	MOV	SI,OFFSET TRACE_Q
	CLI
	CALL	Q_REM
	STI
	JZ	NOTRACERET
;
;	IF BUFFER HAS A LINK TABLE ENTRY ON END, RESET TIMEOUT
;
	MOV	BX,BUFFLEN-2[DI]
	OR	BX,BX
	JZ	NOTIMER

	CMP	L2TIMER[BX],0
	JE	NOTIMER			; ALREADY EXPIRED

	MOV	AL,L2TIME[BX]
	XOR	AH,AH
	MOV	L2TIMER[BX],AX

	MOV	WORD PTR BUFFLEN-2[DI],0	; CLEAR FLAG FROM BUFFER

NOTIMER:

	MOV	CX,NUMBEROFSTREAMS
	JCXZ	TXTRACEEND

	MOV	BX,TNCTABLE
;
TXTRACE00:

	CMP	MTX[BX],0
	JE	TXTRACE10		; TX TRACE DISABLED

;	PUSH	BX
;	PUSH	CX
	PUSH	DI
	CALL	DISPLAYFRAMETX
	POP	DI
;	POP	CX
;	POP	BX

TXTRACE10:

;	ADD	BX,TYPE TNCDATA
;	LOOP	TXTRACE00

TXTRACEEND:

	CALL	RELBUFF

NOTRACERET:
;
;	CHECK FOR TNC ACTIVITY
;
	CMP	NUMBEROFSTREAMS,0
	JE	NOTNCS

	MOV	BX,TNCTABLE

	CALL	KANTTNCBG

NOTNCS:

	JMP	BACKGROUND


CHECKSWITCHSTATE:
;
;	LOOK FOR CHANGE IN SWITCH (USED TO CONTROL ASYNC PORT)
;
	CMP	ASYNCPORT,0
	JE	NOCHANGE		; LEAVE ALONE IF NO ASYNC PORTS

	IN	AL,IPFW

	TEST	AL,100B			; LEVEL 2 IS SWITCH
	JNZ	SWCLOSED
;
;	SWITCH IS OUT, SO SELECT TERMINAL MODE (UNLESS ALREADY SET)
;
	CMP	ASYNCMODE,'T'
	JE	NOCHANGE
 
	LED	0,ON
	MOV	ASYNCMODE,'T'
;
;	DISABLE ASYNC PORT(S)
;
	MOV	BX,PORTTABLE
	MOV	CX,NUMBEROFPORTS

DISPORT10:

	CMP	PORTTYPE[BX],0		; ASYNC?
	JNE	DISPORT20

	MOV	PORTDISABLED[BX],1	; DISABLE PORT

DISPORT20:

	MOV	BX,PORTPOINTER[BX]
	LOOP	DISPORT10

	PUSH	DS

	XOR	AX,AX
	MOV	DS,AX

	MOV	SI,32*4

	MOV	WORD PTR 4[SI],OFFSET SCUINTTNC

	POP	DS
;
	JMP SHORT NOCHANGE

SWCLOSED:
;
;	SELECT KISS MODE - UNLESS ALREADY SET
;
	CMP	ASYNCMODE,'K'
	JE	NOCHANGE
 
	LED	0,OFF
	MOV	ASYNCMODE,'K'
;
;	REENABLE ASYNC PORT(S)
;
	MOV	BX,PORTTABLE
	MOV	CX,NUMBEROFPORTS

ENPORT10:

	CMP	PORTTYPE[BX],0		; ASYNC?
	JNE	ENPORT20

	MOV	PORTDISABLED[BX],0	; REENABLE PORT

ENPORT20:

	MOV	BX,PORTPOINTER[BX]
	LOOP	ENPORT10


	PUSH	DS

	XOR	AX,AX
	MOV	DS,AX

	MOV	SI,32*4

	MOV	WORD PTR 4[SI],OFFSET SCUINT

	POP	DS
;

NOCHANGE:

	LED	7,ON			; FLASH TOP LED TO SHOW WE ARE STILL OK
	XOR	LEDFLAG,1
	JZ	LED7RET

	LED	7,OFF

LED7RET:

	RET




TRACEFRAME:

	PUSH	BX
	MOV	BX,TNCTABLE
	MOV	CX,NUMBEROFSTREAMS
	JCXZ	TRACEEND
;
TRACELOOP:

	PUSH	BX
	PUSH	CX
	PUSH	DI
	CALL	DISPLAYFRAME
	POP	DI
	POP	CX
	POP	BX

	ADD	BX,TYPE TNCDATA
	LOOP	TRACELOOP
TRACEEND:
	POP	BX
	RET

RELBUFF:

	MOV	SI,OFFSET FREE_Q
	CLI
	CALL	Q_ADDF			; RETURN BUFFER TO FREE QUEUE
	STI

	RET


FINDLINK:
;
;	FIND LINK FOR AN INCOMING MESSAGE
;
;	MESSAGE ADDR IN DI

	MOV	BX,0
	MOV	SI,LINKS
	MOV	CX,MAXLINKS
FINDLK00:
	CMP	BYTE PTR [SI],0
	JNE	FINDLK05		; NOT A SPARE ENTRY

	CMP	BX,0
	JNE	FINDLK10		; ALREADY FOUND A SPARE

	MOV	BX,SI			; POINTER TO FIRST FREE
	JMP SHORT FINDLK10		; TRY NEXT ENTRY

FINDLK05:
	PUSH	SI
	PUSH	DI
	PUSH	CX

	LEA	DI,MSGORIGIN[DI]	; ORIGIN FROM MESSAGE

	CALL	COMPARECALLS		; COMPARE WITH LINKCALL

	POP	CX
	POP	DI
	POP	SI

	JNE	FINDLK10

	PUSH	SI
	PUSH	DI
	PUSH	CX

	LEA	DI,MSGDEST[DI]
	ADD	SI,7			; OURCALL IN TABLE
	CALL	COMPARECALLS		; COMPARE WITH DESTINATON IN MSG

	POP	CX
	POP	DI
	POP	SI

	JE	FINDLKRET		; YES
FINDLK10:
	ADD	SI,TYPE LINKTABLE
	LOOP	FINDLK00
;
;	ENTRY NOT FOUND - BX HAS FIRST FREE ENTRY, OR ZERO IF TABLE FULL
;
	OR	AL,1
	RET
FINDLKRET:
	MOV	BX,SI
	XOR	AL,AL
	RET

COMPARECALLS:
;
;	COMPARE AX25 CALLSIGNS IN SI, DI IGNORING EXTRA BITS IN SSID
;
	MOV	CX,6
	REP CMPSB			; COMPARE WITH LINKCALL

	JNE	COMPCALLRET		; DONT MATCH

	LODSB
	MOV	AH,[DI]

	AND	AX,0001111000011110B	; MASK NON-SSID BITS
	CMP	AL,AH
COMPCALLRET:
	RET


GETPORTQUALITY:

	PUSH	BX

	CALL	GETPORTTABLEENTRY
	MOV	AL,PORTQUALITY[BX]

	POP	BX
	RET
			
GETPORTTABLEENTRY:

	PUSH	CX
	MOV	BX,PORTTABLE
	MOV	CX,NUMBEROFPORTS
GETPORT10:
	CMP	AL,PORTNUMBER[BX]
	JE	GETPORT20

	MOV	BX,PORTPOINTER[BX]
	LOOP	GETPORT10

	MOV	BX,PORTTABLE		; TO FIRST IF NOT DEFINED

GETPORT20:
	POP	CX
	RET

CONVTOAX25:
;
;	CONVERT CALLSIGN IN [SI] TO AX25 FORMAT IN AX25CALL
;	RETURN NZ IF CALLSIGN INVALID
;
	MOV	DI,OFFSET AX25CALL
	MOV	CX,6			; MAX ALPHANUMERICS
	MOV	AL,40H
	REP STOSB			; CLEAR IN CASE SHORT CALL
	MOV	AL,01100000B		; DEFAULT SSID
	STOSB
	MOV	DI,OFFSET AX25CALL
	MOV	CL,7
CONVAX00:
	LODSB
	CMP	AL,'-'
	JE	CONVAX20		; SSID FOLLOWS
	CMP	AL,' '
	JE	CONVAX40		; END OF NAME
;
	CMP	AL,'#'
	JNE	NOTHASH

	CMP	CL,7
	JE	CONVAX10		; OK AS FIRST CHAR
NOTHASH:
	CMP	AL,'/'			; ALLOW / AND DIGITS (FOR W0RLI!)
	JB	CONVAX49		; INVALID
	CMP	AL,'9'
	JBE	CONVAX10		; OK

	CMP	AL,'A'
	JB	CONVAX49		; INVALID
	CMP	AL,'Z'
	JBE	CONVAX10		; OK


	CMP	AL,'a'
	JB	CONVAX10		; OK
	CMP	AL,'z'
	JA	CONVAX49		; INVALID

	AND	AL,NOT 20H		; CONVERT TO UPPER CASE
CONVAX10:
	SAL	AL,1			; SHIFT UP
	STOSB
	LOOP	CONVAX00
;
;	HAVE PROCESSED 7 CHARS - NAME IS INVALID
;
	JMP SHORT CONVAX49

CONVAX20:
	LODSB				; FIRST CHAR IF SSID
	CMP	AL,'0'
	JB	CONVAX49		; INVALID
	CMP	AL,'9'
	JA	CONVAX49		; OK
;
	MOV	AH,AL			; SAVE
	LODSB
	CMP	AL,' '
	JE	CONVAX30		; END OF SSID

	INC	SI			; OTHER CODE EXPECTS SI TO POINT TO NEXT FIELD
	CMP	AL,'0'
	JB	CONVAX49		; INVALID
	CMP	AL,'5'			; LIMIT 10 - 15
	JA	CONVAX49		; OK
;
	SUB	AL,30H
	CMP	AH,'1'
	JNE	CONVAX49		; IF 2 DIGITS, FIRST MUST BE 1

	ADD	AL,10
	MOV	AH,AL
CONVAX30:
	SAL	AH,1			; UP 1 BIT
	OR	AH,01100000B		; DEFAULT RR BITS
	MOV	AX25CALL+6,AH

CONVAX40:
;
;	NO SSID SUPPLIED
;
	XOR	AL,AL			; SET ZERO
	RET

CONVAX49:
	OR	AL,1			; SET NZ
	RET



CONVFROMAX25:
;
;	CONVERT AX25 FORMAT CALL IN [SI] TO NORMAL FORMAT IN NORMCALL
;	   RETURNS LENGTH IN CX AND NZ IF LAST ADDRESS BIT IS SET
;
	PUSH	SI			; SAVE
	MOV	DI,OFFSET NORMCALL
	MOV	CX,10			; MAX ALPHANUMERICS
	MOV	AL,20H
	REP STOSB			; CLEAR IN CASE SHORT CALL
	MOV	DI,OFFSET NORMCALL
	MOV	CL,6
CONVAX50:
	LODSB
	CMP	AL,40H
	JE	CONVAX60		; END IF CALL - DO SSID

	SHR	AL,1
	STOSB
	LOOP	CONVAX50
CONVAX60:
	POP	SI
	ADD	SI,6			; TO SSID
	LODSB
	MOV	AH,AL			; SAVE FOR LAST BIT TEST
	SHR	AL,1
	AND	AL,0FH
	JZ	CONVAX90		; NO SSID - FINISHED
;
	MOV	BYTE PTR [DI],'-'
	INC	DI
	CMP	AL,10
	JB	CONVAX70
	SUB	AL,10
	MOV	BYTE PTR [DI],'1'
	INC	DI
CONVAX70:
	ADD	AL,30H			; CONVERT TO DIGIT
	STOSB
CONVAX90:
	MOV	CX,DI
	SUB	CX,OFFSET NORMCALL
	MOV	NORMLEN,CX		; SIGNIFICANT LENGTH

	TEST	AH,1			; LAST BIT SET?
	RET

Q_REM:
	MOV	DI,[SI]			; GET ADDR OF FIRST BUFFER
	CMP	DI,0
	JE	Q_RET			; EMPTY

	MOV	AX,[DI]			; CHAIN FROM BUFFER
	MOV	[SI],AX			; STORE IN HEADER

	POP	[DI]			; CALLERS ADDR 
	PUSH	[DI]
Q_RET:
	RET

;               
Q_ADD:
	PUSHF				; SAVE INT STATE
	CLI
Q_ADD05:
	CMP	WORD PTR [SI],0		; END OF CHAIN
	JE	Q_ADD10

	MOV	SI,[SI]			; NEXT IN CHAIN
	JMP	Q_ADD05
Q_ADD10:
	MOV	WORD PTR [DI],0		; CLEAR CHAIN ON NEW BUFFER
	MOV	[SI],DI			; CHAIN ON NEW BUFFER

	POPF				; RECOVER INT STATE
	RET

;
;	ADD TO FRONT OF QUEUE - MUST ONLY BE USED FOR FREE QUEUE
;
Q_ADDF:

	MOV	AX,[SI]			; OLD FIRST IN CHAIN
	MOV	[DI],AX
	MOV	[SI],DI			; PUT NEW ON FRONT

	INC	QCOUNT
	RET

GETBUFF:

	MOV	SI,OFFSET FREE_Q
	CLI
	CALL	Q_REM
	STI
;
	JZ	NOBUFFS
	DEC	QCOUNT
	MOV	AL,QCOUNT
	CMP	AL,MINBUFFCOUNT
	JA	GETBUFFRET
	MOV	MINBUFFCOUNT,AL

GETBUFFRET:

	OR	AL,1			; SET NZ
;
;	FLAG BUFFER WITH ADDR OF CALLING ROUTINE (TRYING TO TRACK DOWN LOST BUFFERS!)
;
	POP	[DI]
	PUSH	[DI]
 	RET


NOBUFFS:

NOBUFFERCHECK:

	CMP	QCOUNT,0
	JA	MESSED_UP		; WOT?

	INC	NOBUFFCOUNT
	XOR	AL,AL
	RET
;
MESSED_UP:
;
;	BUFFER POOL MUST BE CORRUPT - RESTART
;
	JMP	WARMSTART	

TCUINT:
;
;	TIMER INT - 10 MS
;
	PUSHA
	CLD

	MOV	AL,60H			; SPECIFIC EIO TO LEV 0
	OUT	(IPFW),AL
;
	INC	L2TIMERFLAG		; INCREMENT FLAG FOR BG
	INC	L3TIMERFLAG		; INCREMENT FLAG FOR BG
	INC	L4TIMERFLAG		; INCREMENT FLAG FOR BG
;
	INC	REALTIMETICKS

	CMP	TIMACTIVE,0
	JE	TIMOK
;
;	PREVENT REENTRY
;
	MOV	SI,0CH
	ADD	WORD PTR [SI],1		; LOCAL STATS 	
	ADC	WORD PTR 2[SI],0

	POPA
	IRET

TIMOK:

	MOV	TIMACTIVE,1
	STI

;	CALL PORT TIMER ROUTINES
;
 	MOV	BX,PORTTABLE
	MOV	CX,NUMBEROFPORTS
TIMERINT00:
	PUSH	BX
	PUSH	CX
	CALL	PORTTIMERCODE[BX]
	POP	CX
	POP	BX

	MOV	BX,PORTPOINTER[BX]
	LOOP	TIMERINT00
;
	CLI
	MOV	TIMACTIVE,0
TIMEXIT:

	POPA
	IRET
;
	EXTRN	GOTTNCCHAR:NEAR,TNCSENDCHAR:NEAR
;
;	SERIAL I/O INTERRUPT HANDLER FOR TNC MODE LINK
;
SCUINTTNC:
;
;	SERIAL COMMS INTERRUPT HANDLER
;
	PUSHA
	CLD

	MOV	AL,61H			; SPECIFIC EOI TO LEV 1
	OUT	(IPFW),AL

	INC	RAN5

	MOV	BX,TNCTABLE
	IN	AL,SST			; ASYNC STATUS

	TEST	AL,RXRDY
	JZ	NORX

	CALL	GOTTNCCHAR

	POPA
	IRET


NORX:
	CALL	TNCSENDCHAR
	POPA
	IRET

;
	PUBLIC	HEXOUT

HEXOUT:

	RET

CODE	ENDS

	END	START

