	OUT	(IIW2),AL		; VECTORS START AT 32

	MOV	AL,0F4H
	OUT	(IMKW),AL		; MASK ALL BUT LEVEL 0 (TIMER) 1(SCU) 3(SCC)

	MOV	AL,0
	MOV	DX,TCKS
	OUT	(DX),AL			; ALL COUNTERS FROM INTERNAL /2 CLOCK
;
;	FIRE UP COUNTER 0 AS A TIMEBASE, COUNTER 1 AS BRG FOR SERIAL I/O
;
	MOV	AL,00110110B		; TIMER 0, LOW/HI, MODE3, BINARY
	OUT	(TMD),AL

	MOV	AX,HZ100		; 100 HZ
	OUT	(TCT0),AL

	MOV	AL,AH
	OUT	(TCT0),AL

	MOV	AL,01110110B		; TIMER 1, LOW/HI, MODE3, BINARY
	OUT	(TMD),AL

