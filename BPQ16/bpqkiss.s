	PUSH	HL
	PUSH	DE

	LD	HL,FREE_Q
	LD	DE,(IN_BUF)
	CALL	Q_ADDF

	XOR	A
	LD	(IN_BUF+1),A

	POP	DE
	POP	HL
	JP	IB_RC9		;;; AND GET OUT OF HERE!

