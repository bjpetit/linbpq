	JR	Z,IB_R0		;;; IN STATE 0, WAITING FOR FEND

	CP	1
	JR	Z,IB_R1		;;; IN STATE 1, SAW FEND

	CP	2
	JP	Z,IB_R2		;;; IN STATE 2, DATA TO FOLLOW

	CP	3
	JP	Z,IB_R3		;;; SAW FESC, EXPECTING TFESC OR TFEND

	CP	100
	JP	Z,IDCHR1	; ID BYTE 1

	CP	101
	JP	Z,IDCHR2	; ID BYTE 2

