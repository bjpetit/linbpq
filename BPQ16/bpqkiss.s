				; SET UP RX CCC CORRECTLY

	PUSH	BC
	LD	B,A		; SAVE
	AND	0F0H		; GET UPPER NIBBLE 
	LD	C,A		; AND SAVE

	LD	A,(CHANNEL)
	CP	C		; CHANNEL FROM MSG

	LD	A,B		; RECOVER
	POP	BC

