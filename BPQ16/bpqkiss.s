IB_R1:
	CALL	STA_OFF
	IN	A,(B_DAT)
	CP	FEND
	JP	Z,IB_RC9	;;; JUST ANOTHER FEND, KEEP LOOKING FOR CMD

	CALL	STA_ON		;;;GETTING VALID SLIP; SHOW IN STA LED
;
;;; HERE IF WE DO NOT HAVE AN FEND (EXPECTING COMMAND BYTE)
;
;	CHECK CHANNEL NUMBER - IF NOT OURS, FLUSH FRAME
;
