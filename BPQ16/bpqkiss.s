	INC	DE
	INC	DE
	INC	DE
	INC	DE		; SKIP POINTER AND LENGTH

	POP	AF		; CONTROL BYTE
;
;	IF CONTROL BYTE IS NONZERO, THIS MESSAGE REQUIRES AN ACK
