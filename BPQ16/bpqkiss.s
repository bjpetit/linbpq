;
;
;	QUEUE HANDLING ROUTINES
;
;	    EACH QUEUE CONSISTS OF A TWO BYTE HEADER AND A NUMBER OF ENTRIES,
;	    THE FIRST TWO BYTES OF EACH IS A FORWARD CHAIN POINTER
;		THE POINTER IS THE ADDRESS OF THE NEXT ENTRY, STORED WITH THE
;		HIGH ORDER BYTE FIRST (IE NOT THE NORMAL Z80 WAY ROUND)
;		A ZERO FIRST BYTE INDICATES END OF CHAIN
;
;	    THE FOLLOWING ROUTINES ARE PROVIDED:-
;
;		ADD ENTRY TO QUEUE - ON RETURN A CONTAINS NUMBER OF ENTRIES
;				     WHICH WERE ON THE QUEUE PRIOR TO THE CALL
;
;		GET FIRST ENTRY    - Z FLAG SET IF QUEUE EMPTY
;
;		REMOVE FIRST ENTRY - Z FLAG SET IF QUEUE EMPTY
;
;
;	    ALL ROUTINES REQUIRE HL SET TO QUEUE HEADER ON ENTRY
;	    DE CONTAINS THE ADDRESS OF THE BLOCK FOUND OR TO BE ADDED
;
;
Q_ADDF:					; ADD TO FRONT OF Q
	LD	A,(HL)			; OLD Q HEADER
	LD	(DE),A			; CHAIN TO NEW ONE
	INC	HL
	INC	DE
	LD	A,(HL)			; OLD Q HEADER
	LD	(DE),A			; CHAIN TO NEW ONE
	DEC	DE			; RESET BUFFER ADDR
	LD	(HL),E			; CHAIN NEW BUFFER TO Q HEADER
	DEC	HL
	LD	(HL),D

	LD	HL,QCOUNT
	INC	(HL)
	RET
;
Q_ADD:
	XOR	A			; NUMBER IN QUEUE
	LD	(DE),A			; CLEAR CHAIN POINTER IN NEW BUFFER
Q_ADD1:
	LD	B,(HL)			; FIRST BYTE OF POINTER
	INC	B
	DEC	B			; 0BEST WAY TO SET FLAGS
	JR	Z,Q_ADD5		; END
;
	INC	HL
	LD	L,(HL)
	LD	H,B			; HL = ADDR OF NEXT BUFFER
	INC	A			; NO OF ENTRIES
	JP	Q_ADD1			; LOOP TILL END FOUND
;
Q_ADD5:
	LD	(HL),D			; CHAIN NEW BLOCK
	INC	HL
	LD	(HL),E
	RET
;
;
;
Q_FIRST EQU	$
	LD	D,(HL)
	INC	D
	DEC	D			; TO SET FLAGS
	RET	Z
	INC	HL
	LD	E,(HL)
	RET
;
;
;
Q_REM:
	CALL	Q_FIRST 		; SET FIRST ENTRY
	RET	Z			; QUEUE EMPTY
;
	DEC	HL
	EX	DE,HL			; DE = Q HEADER HL = FIRST ENTRY
	LD	B,(HL)
	INC	HL
	LD	C,(HL)			; BC = NEW FIRST ENTRY
;
	DEC	HL
	EX	DE,HL			; HL = HEADER
	LD	(HL),B
	INC	HL
	LD	(HL),C			; HEADER NOW UPDATED
;
	RET
;

