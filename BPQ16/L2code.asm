	JE	SDET10			; JUMP IF IT IS
;
;	NOT ALL I-FRAMES HAVE BEEN ACK'ED - RESTART TIMER
;
	MOV	AL,L2TIME[BX]
	MOV	AH,0
	MOV	L2TIMER[BX],AX
	RET

SDET10:
;
;	ALL FRAMES HAVE BEEN ACKED - CANCEL TIMER UNLESS RETRYING
;	   IF RETRYING, MUST ONLY CANCEL WHEN RR(F) RECEIVED
;
	CMP	VER1FLAG[BX],1
	JE	SDET11			; STOP TIMER IF LEVEL 1

	CMP	L2RETRIES[BX],0
	JNE	SDET12			; RETRYING
SDET11:
	MOV	L2TIMER[BX],0
	AND	L2FLAGS[BX],NOT POLLSENT; CLEAR I(P) SET (IN CASE TALKING TO OLD BPQ!)

SDET12:
;
;	IF DISCONNECT REQUEST OUTSTANDING, AND NO FRAMES ON TX QUEUE,
;	   SEND DISC
;
	TEST	L2FLAGS[BX],DISCPENDING
	JZ	SDET13			; NO DISC PENDING

	CMP	TX_Q[BX],0
	JNE	SDET13			; STILL MORE TO SEND

	AND	L2FLAGS[BX],NOT DISCPENDING

	MOV	L2TIMER[BX],1		; USE TIMER TO SEND DISC
	MOV	L2STATE[BX],4		; DISCONNECTING

SDET13:
	RET
;	

SDETX:
;
;	SEE IF EXTERNAL L2 LINK
;
	PUSH	BX
	CALL	GETPORTTABLEENTRY
	MOV	BP,BX			; PORT TO BP
	POP	BX

	CMP	DS:PORTTYPE[BP],EXTERNAL
	JNE	NOTEXT

	CMP	DS:PROTOCOL[BP],L2
	JNE	NOTEXT
;
;	EXTERNAL DRIVER IS PROVIDING L2 FUNCTIONS:
;
;	CALL IT TO SEE IF IT HAS BUFFER SPACE AVAILABLE, AND IF SO PASS MSG
;	TO IT
;

	PUSHF
	MOV	AH,4			; STATUS
	CALL	DS:PORT_EXT_ADDR[BP]

	LEA	SI,TX_Q[BX]
	CALL	Q_REM

	PUSHF
	MOV	AH,8			; L2 SEND ?
	CALL	DS:PORT_EXT_ADDR[BP]

	RET


NOTEXT:
;
;	DONT SEND IF RESEQUENCING RECEIVED FRAMES - CAN CAUSE FRMR PROBLEMS
;
	CMP	L2RESEQ_Q[BX],0
	JNE	SDET32A			; DONT SEND
;
;	SEE IF AT WINDOW LIMIT
;
;	CMP	RECENT_REJ[BX],0
;	JNE	SDET32A			; DONT SEND TILL WAIT TIMER EXPIRES


	MOV	AL,LINKNS[BX]
	SUB	AL,LINKOWS[BX]		; WINDOW START
	JNC	SDET31			; NO WRAP

	ADD	AL,8			; ALLOW FOR WRAP
SDET31:
	CMP	AL,LINKWINDOW[BX]	; LIMIT
	JC	SDET33			; OK

SDET32A:

	RET				; CANT SEND ANY MORE YET

SDET33:	

	SDGETS	SDTSLOT[BX]		; CALCULATE TX SLOT ADDR (IN SI)
	CMP	WORD PTR [SI],0		; IS NEXT SLOT EMPTY?
	JNE	SDET20			; J IF NOT (NO ROOM IN CCB)
;
	PUSH	SI
	LEA	SI,TX_Q[BX]
	CALL	Q_REM
	POP	SI

	JZ	SDET20			; J IF QUEUE EMPTY

	CALL	Q_ADD			; PUT MESSAGE ADDR IN SLOT
	SDUPDS	SDTSLOT[BX]		; UPDATE IT
	JMP 	SDETX
;
SDET20:

	TEST	L2FLAGS[BX],POLLSENT
	JNZ	SDET50			; P BIT OUTSTANDING - WAIT

	SDGETS	LINKNS[BX]		; CALCULATE SLOT OFFSET
	CMP	WORD PTR [SI],0
	JZ	SDET50			; NOTHING TO SEND
;
;	GET BUFFER FOR COPY OF MESSAGE - HAVE TO KEEP ORIGINAL FOR RETRIES
;
	MOV	SI,[SI]			; GET BUFFER ADDR

	PUSH	SI
	CALL	GETBUFF
	POP	SI
	JNZ	SDETOK
;
;	CANNOT SEND AN I-FRAME
;
SDET50:
	RET

SDETOK:

	PUSH	DI			; SAVE BUFFER START ADDR
	PUSH	SI			; SAVE MESSAGE ADDRESS

	CALL	SETUPADDRESSES		; GET ADDRESSES FROM LINK TABLE
;
;	DI NOW POINTS TO COMMAND BYTE
;
;	GOING TO SEND I FRAME - WILL ACK ANY RECEIVED FRAMES
;
	MOV	L2ACKREQ[BX],0		; CLEAR ACK NEEDED
	MOV	AX,T3
	MOV	L2SLOTIM[BX],AX		; SET FRAME SENT RECENTLY
	MOV	KILLTIMER[BX],0		; RESET IDLE CIRCUIT TIMER

;
;	FLAG BUFFER TO CAUSE TIMER TO BE RESET AFTER SEND
;


	MOV	AL,LINKNR[BX]		; GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	ROR	AL,1			; INSERT N(S) COUNT IN
	OR	AL,LINKNS[BX]		; BITS 1-3 OF CONTROL BYTE
	ROL	AL,1

	INC	LINKNS[BX]		; INCREMENT NS
	AND	LINKNS[BX],7
;
;	SET P BIT IF END OF WINDOW OR NO MORE TO SEND
; 
	CMP	VER1FLAG[BX],1
	JE	SDET25			; NO POLL BIT IF V1

	MOV	AH,LINKNS[BX]
	SUB	AH,LINKOWS[BX]		; WINDOW START
	JNC	SDET22			; NO WRAP

	ADD	AH,8			; ALLOW FOR WRAP
SDET22:
	CMP	AH,LINKWINDOW[BX]	; LIMIT
	JNC	SDET24			; CANT SEND ANY MORE YET
;
;	SEE IF MORE TO GO
;
	PUSH	AX
	SDGETS	LINKNS[BX]		; CALCULATE SLOT OFFSET
	POP	AX

	CMP	WORD PTR [SI],0
	JNZ	SDET25			; MORE TO SEND
;
SDET24:

	OR	AL,PFBIT
	OR	L2FLAGS[BX],POLLSENT

SDET25:
	STOSB				; TO DATA (STARTING WITH PID)

	POP	SI			; GET MESSAGE
	MOV	CX,MSGLENGTH[SI]	; GET LENGTH

	ADD	SI,5			; SKIP CHAIN, PORT, LENGTH
	SUB	CX,5
	JC	SDET32
	JCXZ	SDET32			; SHOULD ALWAYS BE A PID, BUT BETTER SAFE THAN SORRY
;  	
	REP MOVSB			; COPY MESSAGE
SDET32:
	MOV	CX,DI
	POP	DI
	OR	BYTE PTR MSGDEST+6[DI],80H	; SET COMMAND
	SUB	CX,DI
	MOV	MSGLENGTH[DI],CX	; SET NEW LENGTH

	MOV	L2TIMER[BX],ONEMINUTE	; (RE)SET TIMER
;
;	FLAG BUFFER TO CAUSE TIMER TO BE RESET AFTER SEND
;
	MOV	BUFFLEN-2[DI],BX

	MOV	AL,LINKPORT[BX]
	MOV	MSGPORT[DI],AL
;

	PUSH	BX

	CALL	GETPORTTABLEENTRY	; POINT TO PORT

	CALL	PUT_ON_PORT_Q

	POP	BX

	JMP	SDETX			; SEE IF MORE
;
ADJUSTFORDIGIS:
;
;	ADJUST DI TO ALLOW FOR DIGIS IN MSG HEADER
;
	MOV	CX,8			; MAX DIGIS
ADJDIGI00:
	TEST	MSGORIGIN+6[DI],1
	JNZ	ADJDIGI10		; END OF LIST

	ADD	DI,7
	SUB	AX,7			; LENGTH
;
	LOOP	ADJDIGI00		; MUST BE OK TO HAVE GOT THIS FAR
 
ADJDIGI10:
	RET

L2SETUPCROSSLINK:
;
;	BX POINTS TO A NEIGHBOUR - FIND AN L2 SESSION FROM US TO IT, OR
;	   INITIATE A NEW ONE
;
	MOV	AL,NEIGHBOUR_PORT[BX]
	MOV	COMPAREPORT,AL		; SET PORT

	PUSH	BX

	MOV	DI,OFFSET COMPAREFIELD
	MOV	SI,OFFSET MYCALL
	MOV	CX,7
	REP MOVSB

	LEA	SI,NEIGHBOUR_CALL[BX]
	MOV	CX,7
	REP 	MOVSB
;
	MOV	DI,OFFSET COMPAREFIELD
	CALL	FINDLINKXX		; GET LINK FOR THIS ADDRESS PAIR (IN BX)
;
	JNZ	L2SETUP10		; NOT FOUND
;
;	SESSION ALREADY EXISTS
;
	MOV	LINKTYPE[BX],3		; MAKE SURE IT KNOWS ITS A CROSSLINK

	MOV	SI,BX
	POP	BX
	MOV	NEIGHBOUR_LINK[BX],SI
	MOV	NEIGHBOUR[SI],BX
	RET

L2SETUP10:
;
;	SET UP NEW SESSION (OR RESET EXISTING ONE)
;
	CMP	BX,0
	JNE	L2SETUP20
;
;	NO FREE ENTRIES
;
	POP	BX
	RET

L2SETUP20:
;
;	COPY ADDRESS INFO TO LINK TABLE
;
	POP	SI			; NEIGHBOUR TABLE

	MOV	NEIGHBOUR_LINK[SI],BX
	MOV	NEIGHBOUR[BX],SI

	MOV	AL,NEIGHBOUR_PORT[SI]
	MOV	LINKPORT[BX],AL		; SET PORT

	PUSH	BX
	CALL	GETPORTTABLEENTRY
	MOV	BP,BX			; PORT TO BP
	POP	BX

;
;	IF ROUTE HAS A FRACK, SET IT
;
	MOV	AL,NBOUR_FRACK[SI]
	OR	AL,AL
	JNZ	SETFRACK
;
;	NO ROUTE VALUE, SO SET LINK VALUE
;	
	MOV	AL,DS:PORTT1[BP]

SETFRACK:

	MOV	L2TIME[BX],AL		; SET TIMER VALUE
	MOV	TEMP,AL			; SAVE
;
;	IF ROUTE HAS A WINDOW, SET IT
;
	MOV	AL,NBOUR_MAXFRAME[SI]
	OR	AL,AL
	JNZ	SETMAXF
;
;	NO ROUTE VALUE, SO SET LINK VALUE
;	
	MOV	AL,DS:PORTWINDOW[BP]
SETMAXF:
	MOV	LINKWINDOW[BX],AL

	MOV	L2STATE[BX],2

	PUSH	SI

	LEA	SI,NEIGHBOUR_CALL[SI]
	LEA	DI,LINKCALL[BX]
	MOV	CX,7
	REP MOVSB

	PUSH	SI

	MOV	SI,OFFSET MYCALL
	LEA	DI,OURCALL[BX]		; OUR CALL/ALIAS BEING USED 
	MOV	CX,7
	REP MOVSB
;
	POP	SI
;
;	ADD ANY DIGI CALLS, AND ADJUST FRACK
;

	CMP	BYTE PTR [SI],0
	JE	NODIGI

	MOV	AL,TEMP
	ADD	AL,AL
	ADD	L2TIME[BX],AL

	MOV	CX,7
	REP MOVSB			; ADD DIGI

	CMP	BYTE PTR [SI],0
	JE	NODIGI

	ADD	L2TIME[BX],AL

	MOV	CX,7
	REP MOVSB			; ADD DIGIS

NODIGI:

	MOV	LINKTYPE[BX],3		; CROSSLINK

	POP	SI			; NEIGHBOUR


	CALL	SENDSABM

	RET

FINDLINK:

	LEA	DI,MSGDEST[DI]

FINDLINKXX:
;
;	FIND LINK FOR AN ADDRESS PAIR (OUR CALL - FAR CALL) IN DI
;
	MOV	BX,0
	MOV	SI,LINKS
	MOV	CX,MAXLINKS
FINDLK00:
	CMP	BYTE PTR [SI],0
	JNE	FINDLK05		; NOT A SPARE ENTRY

	CMP	BX,0
	JNE	FINDLK10		; ALREADY FOUND A SPARE

	MOV	BX,SI			; POINTER TO FIRST FREE
	JMP SHORT FINDLK10		; TRY NEXT ENTRY

FINDLK05:

	MOV	AL,LINKPORT[SI]
	CMP	AL,COMPAREPORT
	JNE	FINDLK10		; WRONG PORT

	PUSH	SI
	PUSH	DI
	PUSH	CX

	LEA	DI,7[DI]		; ORIGIN FROM MESSAGE
	CALL	COMPARECALLS		; COMPARE WITH LINKCALL

	POP	CX
	POP	DI
	POP	SI

	JNE	FINDLK10

	PUSH	SI
	PUSH	DI
	PUSH	CX

	ADD	SI,7			; OURCALL IN TABLE
	CALL	COMPARECALLS		; COMPARE WITH DESTINATON IN MSG

	POP	CX
	POP	DI
	POP	SI

	JE	FINDLKRET		; YES
FINDLK10:
	ADD	SI,TYPE LINKTABLE
	LOOP	FINDLK00
;
;	ENTRY NOT FOUND - BX HAS FIRST FREE ENTRY, OR ZERO IF TABLE FULL
;
	OR	AL,1			; SET NZ
	RET
FINDLKRET:
	MOV	BX,SI
	XOR	AL,AL
	RET

COMPARECALLS:
;
;	COMPARE AX25 CALLSIGNS IN SI, DI IGNORING EXTRA BITS IN SSID
;
	MOV	CX,6
	REP CMPSB			; COMPARE

	JNE	COMPCALLRET		; DONT MATCH

	LODSB
	MOV	AH,[DI]

	AND	AX,0001111000011110B	; MASK NON-SSID BITS
	CMP	AL,AH
COMPCALLRET:
	RET

GETPORTWINDOW:

	PUSH	BX

	MOV	AL,LINKPORT[BX]
	CALL	GETPORTTABLEENTRY
	MOV	AL,PORTWINDOW[BX]

	POP	BX
	RET
			
GETPORTPACLEN:

	PUSH	BX

	MOV	AL,LINKPORT[BX]
	CALL	GETPORTTABLEENTRY
	MOV	AL,PORTPACLEN[BX]

	POP	BX
	RET
			
RR_OR_RNR:

	AND	L2FLAGS[BX],NOT RNRSENT
;
;	SET UP APPROPRIATE SUPER COMMAND
;
	CMP	LINKTYPE[BX],3
	JE	CHKBUFFS		; NODE TO NODE - ONLY BUSY IF SHORT OF BUFFERS
;
;	UP OR DOWN LINK - SEE IF SESSION IS BUSY
;
	CMP	CIRCUITPOINTER[BX],0
	JE	CHKBUFFS		; NOT CONNECTED
;
;	UPLINK WITH CIRCUIT
;
	PUSH	BX			; SAVE LINK
	MOV	BX,CIRCUITPOINTER[BX]	; TO CIRCUIT ENTRY

	CALL	CHECKIFBUSY		; TARGET SESSION BUSY?
	POP	BX

	TEST	AL,L4BUSY
	JNZ	SENDRNR			; BUSY

CHKBUFFS:

	MOV	AH,RR
	CMP	QCOUNT,9
	JBE	SENDRNR			; NOT ENOUGH
;
;	SEND REJ IF IN REJ STATE
;
	CMP	L2STATE[BX],6
	JNE	HTIM142			; OK

	MOV	AH,REJ
	RET

SENDRNR:
	MOV	AH,RNR			; RUNNING SHORT
	OR	L2FLAGS[BX],RNRSENT	; REMEMBER
HTIM142:
	RET


COUNTLINKS:
;
;	COUNT LINKS ON PORT IN AL
;
	MOV	SI,LINKS
	MOV	CX,MAXLINKS
	MOV	AH,0
COUNTL00:
	CMP	BYTE PTR [SI],0
	JE	COUNTL10		; SPARE ENTRY

	CMP	AL,LINKPORT[SI]
	JNE	COUNTL10		; WRONG PORT

	INC	AH

COUNTL10:

	ADD	SI,TYPE LINKTABLE
	LOOP	COUNTL00
;
	RET

PUT_ON_PORT_Q:
;
;	TIME STAMP IT
;
	IFDEF	PCSWITCH

	MOV	AX,WORD PTR MM
	MOV	BUFFLEN-4[DI],AX

	ENDIF

	PUSH	BX

	CMP	TXPORT[BX],0
	JE	NORMALPORT
;
;	NEED TO SEND TO DIFERENT PORT
;
	MOV	AL,TXPORT[BX]
	MOV	MSGPORT[DI],AL		; CHANGE HEADER (FOR KISS TX)

	CALL	GETPORTTABLEENTRY

NORMALPORT:

	LEA	SI,PORTTX_Q[BX]
	CALL	Q_ADD			; PUT ON QUEUE

	POP	BX
	RET

MHPROC:
;
;	MAINTAIN MHEARD LIST
;
	CMP	PORTMHEARD[BX],0
	JE	MHRET			; NOT ENABLED
;
;	GET CONTROL BYTE
;
	PUSH	DI
	MOV	CX,8			; MAX DIGIS
CTRLLOOP:
	TEST	BYTE PTR MSGCONTROL-1[DI],1
	JNZ	CTRLFOUND

	ADD	DI,7
	LOOP	CTRLLOOP
;
;	INVALID FRAME
;
	POP	DI
MHRET:
	RET

CTRLFOUND:

	MOV	AL,MSGCONTROL[DI]
	POP	DI
;
	TEST	AL,1			; I FRAME
	JZ	MHIFRAME

	AND	AL,NOT PFBIT		; CLEAR P/F
	CMP	AL,3			; UI
	JNE	MHRET			; ONLY USE I AND UI FRAMES
;
MHIFRAME:
;
;	LOOK FOR CALL IN LIST
;
	MOV	SI,PORTMHEARD[BX]
	LEA	DI,MSGORIGIN[DI]
	MOV	CX,20			; MAX ENTRIES

MHF000:
	CMP	BYTE PTR [SI],0
	JE	MHF120			; EMPTY ENTRY FOUND - USE IT

MHF005:

	PUSH	CX
	PUSH	DI
	PUSH	SI

	CALL	COMPARECALLS		; COMPARE WITH LIST

	POP	SI
	POP	DI
	POP	CX

	JE	MHF120			; FOUND

	ADD	SI,TYPE MHSTRUC

	LOOP	MHF000
;
;	TABLE FULL AND ENTRY NOT FOUND - MOVE DOWN ONE, AND ADD TO TOP
;
	MOV	CX,(19*TYPE MHSTRUC)
	JMP SHORT MHF130


MHF120:
;
;	ENTRY FOUND - MOVE EARLIER ONES DOWN, AND PUT ON FRONT
;
	MOV	AX,20
	SUB	AX,CX
	JZ	MHF140			; FIRST ENTRY

	MUL	MHLEN
	MOV	CX,AX

MHF130:

	MOV	SI,PORTMHEARD[BX]
	ADD	SI,(TYPE MHSTRUC)-1
	ADD	SI,CX			; TO END + LEN-1

	MOV	DI,SI
	SUB	SI,TYPE MHSTRUC		; TO END
	STD
	REP MOVSB
	CLD


MHF140:

	MOV	DI,PORTMHEARD[BX]
	MOV	SI,BUFFER
	LEA	SI,MSGORIGIN[SI]
	MOV	DL,6[SI]		; SAVE LAST BYTE OF CALL (FOR DIGI CHK) 

	MOV	CX,7
	REP MOVSB			; COPY CALL

	SUB	DI,7
;
;	GET TIME FROM BIOS DATA AREA OR RTC
;
	IFDEF	KANT

	MOV	AL,1
	OUT	CLOCK_D,AL

GETTIMELOOP:

	IN	AL,CLOCK_D		; WAIT FOR NOT BUSY
	TEST	AL,2
	JZ	OKTOREAD

	MOV	AL,0
	OUT	CLOCK_D,AL

	MOV	AL,1
	OUT	CLOCK_D,AL

	JMP	GETTIMELOOP

OKTOREAD:

	IN	AL,CLOCK_HOUR+1
	MOV	CL,4
	SHL	AL,CL
	MOV	CH,AL

	IN	AL,CLOCK_HOUR
	AND	AL,0FH
	ADD	CH,AL			; HOURS TO CH

	MOV	BYTE PTR MHTIME[DI],CH

	IN	AL,CLOCK_MINUTE+1
	MOV	CL,4
	SHL	AL,CL
	MOV	CH,AL
	MOV	CH,AL

	IN	AL,CLOCK_MINUTE
	AND	AL,0FH
	ADD	CH,AL			; MINS TO CH

	MOV	BYTE PTR MHTIME+1[DI],CH

	IN	AL,CLOCK_SECOND+1
	MOV	CL,4
	SHL	AL,CL
	MOV	CH,AL

	IN	AL,CLOCK_SECOND
	AND	AL,0FH
	ADD	CH,AL			; HOURS TO CH

	MOV	BYTE PTR MHTIME+2[DI],CH

	MOV	AL,0
	OUT	CLOCK_D,AL

	ENDIF

	IFDEF	MSC

	MOV	AL,HOURS
	MOV	BYTE PTR MHTIME[DI],AL
	MOV	AL,MINS
	MOV	BYTE PTR MHTIME+1[DI],AL
	MOV	AL,SECS
	MOV	BYTE PTR MHTIME+2[DI],AL

	ENDIF

	IFDEF	PCSWITCH

	MOV	AL,HH
	MOV	BYTE PTR MHTIME[DI],AL
	MOV	AL,MM
	MOV	BYTE PTR MHTIME+1[DI],AL
	MOV	AL,SSECS
	MOV	BYTE PTR MHTIME+2[DI],AL

	ENDIF

	MOV	MHDIGI[DI],20H		; ASSUME NOT DIGI-ED

	TEST	DL,1
	JNZ	MHRET1			; LAST BIT SET, SO NO DIGIS
;
;	THERE ARE DIGIS - IF DIGI-ED BIT IS SET ON FIRST, WE ARE NOT HEARING
;	  THIS PACKET DIRECT
;
	MOV	SI,BUFFER
	TEST	BYTE PTR MSGORIGIN+7+6[SI],80H
	
	JZ	MHRET1			; NOT DIGIED - MUST HAVE HEARD IT DIRECT

	MOV	MHDIGI[DI],'*'

MHRET1:

	RET


CODE	ENDS
	END

