;	*** B903 - RECEIVED N(R) CHECK
;	***
;	CHECK RECEIVED N(R)
;	IF	WITHIN CURRENT WINDOW LIMITS
;	THEN
;		SET LOWER WINDOW LIMIT = RECEIVED N(R)
;		SET 'ACKNOWLEDGE' FLAG
;	ELSE
;		SET 'INVALID N(R)' FLAG
;		SAVE FRAME CONTROL BYTE FOR 'FRMR' RESPONSE
;	ENDC
;	RETURN
;
;
	
;***	CHECK RECEIVED N(R) COUNT
;
SDNRCHK:
	MOV	AL,SDCBYTE		; GET FRAME CONTROL BYTE
	ROL	AL,1			; ISOLATE N(R) IN L.S. BITS
	ROL	AL,1
	ROL	AL,1
	AND	AL,7
	CMP	AL,LINKWS[BX]		; N(R) >= WINDOW START?
	JC	SDNR30			; J IF NO
;
;	N(R) ABOVE OR EQUAL TO WINDOW START - OK IF NOT ABOVE N(S), OR N(S) BELOW WS
;
	CMP	AL,LINKNS[BX]		; N(R) <= WINDOW END?
	JC	SDNR20			; J IF YES
	JE	SDNR20			; J IF YES
SDNR10:
;
;	N(R) ABOVE N(S) - DOES COUNT WRAP?
;
	MOV	AH,AL			; KEEP TRACK OF RECEIVED N(R)
	MOV	AL,LINKNS[BX]		; GET WINDOW END
	CMP	AL,LINKWS[BX]		; WINDOW START > WINDOW END?
	JNC	SDNR40			; J IF NOT (ERROR)
	MOV	AL,AH			; RESTORE RECEIVED N(R)
;
;	RECEIVED N(R) IS VALID
;
SDNR20:
	MOV	LINKWS[BX],AL		; NEW WINDOW START = RECEIVED N(R)
	CALL	ACKMSG
	RET
;
SDNR30:
;
;	N(R) LESS THAN WINDOW START - ONLY OK IF WINDOW	WRAPS
;
	CMP	AL,LINKNS[BX]		; N(R) <= WINDOW END?
	JC	SDNR20			; J IF YES
	JE	SDNR20			; J IF YES
;
;	RECEIVED N(R) IS INVALID
;
SDNR40:
	OR	SDREJF[BX],SDNRER	; FLAG A REJECT CONDITION
	MOV	AL,SDCBYTE
	MOV	SDRBYTE,AL		; SAVE FOR FRMR RESPONSE
	RET

;	FRAME REJECT CONDITION - SEND FRMR RESPONSE
;
SDFRMR:

	ADD	DS:L2FRMRTX[BP],1
	ADC	DS:L2FRMRTX+2[BP],0

	MOV	L2STATE[BX],3		; ENTER FRMR STATE

	MOV	AL,L2TIME[BX]
	MOV	AH,0
	MOV	L2TIMER[BX],AX		; SET TIMER

	MOV	DI,ADJBUFFER
	MOV	MSGCONTROL[DI],FRMR	; CONTROL BYTE = FRMR

	MOV	AL,SDRBYTE		; MOVE REJECT C-BYTE
	MOV	MSGCONTROL+1[DI],AL
	MOV	AL,LINKNR[BX]		; GET OUR N(R)
	ROR	AL,1			; SHIFT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	MOV	AH,AL			; SAVE IT
	MOV	AL,LINKNS[BX]		; GET OUR N(S)
	SHL	AL,1			; SHIFT TO BITS 1-3
	OR	AL,AH			; COMBINE WITH N(S)
	MOV	MSGCONTROL+2[DI],AL	; STORE
	MOV	AL,SDREJF[BX]		; MOVE REJECT FLAGS TO IDP
	MOV	MSGCONTROL+3[DI],AL

	LEA	CX,MSGCONTROL+4[DI]	; END OF MESSAGE
	MOV	DI,BUFFER
	SUB	CX,DI			; CALCULATE LENGTH
 	MOV	MSGLENGTH[DI],CX	; SET UP BYTE COUNT

	CALL	L2SWAPADDRESSES		; SWAP ADDRESSES AND SET RESP BITS

	CALL	L2QUEUEONPORT		; SEND DIRECT TO PORT
;
	RET
