L2TIMERPROC:
;
;	CHECK FOR TIMER EXPIRY OR BUSY CLEARED  
;
	MOV	CX,MAXLINKS
	MOV	BX,LINKS
HTIM00:
	CMP	BYTE PTR [BX],0
	JNE	HTIM01

	JMP	HTIM400			; SPARE ENTRY

HTIM01:

	PUSH	BX

	MOV	AL,LINKPORT[BX]
	CALL	GETPORTTABLEENTRY

	MOV	BP,BX
	POP	BX

	MOV	AX,L2TIMER[BX]
	OR	AX,AX
	JZ	HTIM100			; NOT RUNNING

	DEC	AX
	MOV	L2TIMER[BX],AX		; DECREMENT

	JNZ	HTIM101			; NOT EXPIRED

	CALL	L2TIMEOUT		; TIMEOUT HAS OCCURED

	JMP SHORT HTIM101

HTIM100:
;
;	TIMER NOT RUNNING - MAKE SURE STATE NOT BELOW 5 - IF
;	IT IS, SOMETHING HAS GONE WRONG, AND LINK WILL HANG FOREVER
;
	CMP	L2STATE[BX],2
	JE	HTIM101			; CONNECT - PROBABLY TO CQ

	CMP	L2STATE[BX],5
	JAE	HTIM101			; OK

	MOV	L2TIMER[BX],2		; ARBITRARY VALUE

HTIM101:
;
;	TEST FOR RNR SENT, AND NOT STILL BUSY
;
	TEST	L2FLAGS[BX],RNRSENT
	JZ	NOTBSY			; RNR NOT SENT

	CALL	RR_OR_RNR		; SEE IF STILL BUSY

	CMP	AH,RNR
	JE	NOTBSY			; STILL BUSY

	MOV	L2ACKREQ[BX],1		; SEND RR

NOTBSY:

	CMP	L2ACKREQ[BX],0		; DELAYED ACK TIMER
	JE	HTIM200			; NOT RUNNING
	
	CMP	L2RETRIES[BX],0
	JNE	HTIM200			; DONT SEND RR RESPONSE WHILEST RR(P) OUTSTANDING

 	DEC	L2ACKREQ[BX]
	JNZ	HTIM200			; STILL OK
;
	MOV	AL,0			; NO F BIT
	CALL	SEND_RR_RESP

HTIM200:
;
;	CHECK FOR REJ TIMEOUT
;
	CMP	REJTIMER[BX],0
	JE	NOTREJTIMER

	DEC	REJTIMER[BX]
	JNZ	NOTREJTIMER
;
;	REJ HAS TIMED OUT (THIS MUST BE A VERSION 1 SESSION)
;
;
;	CANCEL REJ STATE
;
	CMP	L2STATE[BX],6		; REJ?
	JNE	NOTREJTIMER

	MOV	L2STATE[BX],5		; CLEAR REJ 


NOTREJTIMER:

	CMP	L2SLOTIM[BX],0
	JZ	HTIM300
	DEC	L2SLOTIM[BX]
	JNZ	HTIM300			; STILL OK

	JMP	HTIM40			; SEND RR/RNR(P) AS LINK VALIDATION

HTIM300:
	INC	KILLTIMER[BX]
	MOV	AX,L2KILLTIME
	OR	AX,AX
	JZ	HTIM400			; ZERO = RUN FOR EVER

	CMP	KILLTIMER[BX],AX
	JB	HTIM400
;
;	CIRCUIT HAS BEEN IDLE TOO LONG - SHUT IT DOWN
;
	MOV	KILLTIMER[BX],0
	MOV	L2TIMER[BX],1		; TO FORCE DISC
	MOV	L2STATE[BX],4		; DISCONNECTING

	PUSH	BX
	PUSH	CX
	CALL	INFORMPARTNER		; TELL OTHER LEVELS
	POP	CX
	POP	BX
	MOV	CIRCUITPOINTER[BX],0
HTIM400:
	ADD	BX,TYPE LINKTABLE
	LOOP	HTIM00_J
	RET

HTIM00_J:
	JMP	HTIM00

SEND_RR_RESP:

	PUSH	BX
	PUSH	CX
;
;	SEND RR RESPONSE TO ACK OUTSTANDING FRAMES
;
	PUSH	AX			; F BIT
	CALL	RR_OR_RNR		; GET COMMAND
	MOV	AL,LINKNR[BX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE

	POP	CX			; F BIT
	OR	AL,CL
;
	CALL	SETUPL2MESSAGE
	JZ	HTIM143			; NO BUFFERS
;
	OR	BYTE PTR MSGORIGIN+6[DI],80H	; SET RESPONSE
	CALL	L2QUEUEFRAME

HTIM143:
	MOV	AX,T3
	MOV	L2SLOTIM[BX],AX		; SET FRAME SENT RECENTLY

	CALL	ACKMSG			; SEE IF STILL WAITING FOR ACK
	POP	CX
	POP	BX
	RET

L2TIMEOUT:
;
;	TIMER EXPIRED
;
;	    IF LINK UP (STATE 5 OR ABOVE) SEND RR/RNR AS REQUIRED
;	    IF S2, REPEAT SABM
;	    IF S3, REPEAT FRMR
;	    IF S4, REPEAT DISC
;
;
	ADD	DS:L2TIMEOUTS[BP],1	; FOR STATS	
	ADC	DS:L2TIMEOUTS+2[BP],0

	CMP	L2STATE[BX],1		; NO IDLE STATE
	JLE	HTIMRET

	CMP	L2STATE[BX],4
	JA	HTIM30			; LINK UP
	JE	HTIM05			; SEND DISC AGAIN

	CMP	L2STATE[BX],2
	JE	HTIM10			; SEND SABM AGAIN
;
;	FRMR
;
	INC	L2RETRIES[BX]
	MOV	AL,DS:PORTN2[BP]
	CMP	L2RETRIES[BX],AL
	JL	HTIM60
;
;	RETRIED N2 TIMES - RESET LINK
;
	MOV	L2RETRIES[BX],0
	MOV	L2STATE[BX],2

	JMP	SENDSABM

HTIM60:
;
	CALL	SENDFRMR
HTIMRET:
	RET

HTIM05:
;
;	DISCONNECTING
;
	INC	L2RETRIES[BX]
	MOV	AL,DS:PORTN2[BP]
	CMP	L2RETRIES[BX],AL
	JL	HTIM08
;
;	RETRIED N2 TIMES - JUST CLEAR OUT LINK
;
	PUSH	BX
	PUSH	CX
	CALL	CLEAROUTLINK	
	POP	CX
	POP	BX
	RET

HTIM08:
	PUSH	BX
	PUSH	CX
	MOV	AL,DISC OR PFBIT
	CALL	L2SENDCOMMAND
	POP	CX
	POP	BX

	RET

HTIM10:
;
;	CONNECTING
;
	INC	L2RETRIES[BX]
	MOV	AL,DS:PORTN2[BP]
	CMP	L2RETRIES[BX],AL
	JL	HTIM12
;
;	RETRIED N2 TIMES - FAIL LINK
;
	PUSH	BX
	PUSH	CX

	CALL	CONNECTFAILED		; TELL LEVEL 4 IT FAILED
	CALL	CLEAROUTLINK	
	POP	CX
	POP	BX
	RET

HTIM12:
SENDSABM:
	PUSH	BX
	PUSH	CX
	MOV	AL,SABM OR PFBIT
	CALL	L2SENDCOMMAND
	POP	CX
	POP	BX

	RET

HTIM30:
;
;	STATE 5 OR ABOVE
;
;	SEND RR(P) UP TO N2 TIMES
;
	INC	L2RETRIES[BX]
	MOV	AL,DS:PORTN2[BP]
	CMP	L2RETRIES[BX],AL
	JL	HTIM40
;
;	RETRIED N2 TIMES - SEND A COUPLE OF DISCS AND THEN CLOSE
;
	PUSH	BX
	PUSH	CX
	CALL	INFORMPARTNER		; TELL OTHER END ITS GONE

	SUB	L2RETRIES[BX],2
	MOV	L2STATE[BX],4		; CLOSING
	POP	CX
	POP	BX

	CALL	HTIM08
	RET
;
HTIM40:
	CMP	VER1FLAG[BX],1
	JE	HTIMVER1		; VERSION 1 TIMEOUT
;
;	SEND RR/RNR/REJ(P)
;
	PUSH	BX
	PUSH	CX
;
;	SEND RR COMMAND - EITHER AS LINK VALIDATION POLL OR FOLLOWING TIMEOUT
;
	MOV	L2ACKREQ[BX],0		; CLEAR ACK NEEDED

	CALL	RR_OR_RNR

;	MOV	L2STATE[BX],5		; CANCEL REJ - ACTUALLY GOING TO 'PENDING ACK'

	MOV	AL,LINKNR[BX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE
	OR	AL,PFBIT
	OR	L2FLAGS[BX],POLLSENT

	CALL	L2SENDCOMMAND
	MOV	AX,T3
	MOV	L2SLOTIM[BX],AX		; SET FRAME SENT RECENTLY

	POP	CX
	POP	BX
	RET

HTIMVER1:
;
;	RESET TO RESEND I FRAMES
;
	MOV	AL,LINKOWS[BX]
	MOV	LINKNS[BX],AL

	PUSH	CX
	CALL	SDETX			; PREVENT FRMR (I HOPE)
	POP	CX

	RET

SENDFRMR:

;	RESEND FRMR
;
	PUSH	BX
	PUSH	CX

	MOV	AL,L2TIME[BX]
	MOV	AX,0
	MOV	L2TIMER[BX],AX		; SET TIMER


	CALL	GETBUFF
	JZ	HTIM66			; NO BUFFER - JUST PRETEND, AND SET TIMER

	PUSH	DI
	CALL	SETUPADDRESSES		; SET UP ADDRESS STRING

	MOV	AL,FRMR
	STOSB

	MOV	AL,SDRBYTE		; MOVE REJECT C-BYTE
	STOSB

	MOV	AL,LINKNR[BX]		; GET OUR N(R)
	ROR	AL,1			; SHIFT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	MOV	AH,AL			; SAVE IT
	MOV	AL,LINKNS[BX]		; GET OUR N(S)
	SHL	AL,1			; SHIFT TO BITS 1-3
	OR	AL,AH			; COMBINE WITH N(S)

	STOSB
	MOV	AL,SDREJF[BX]		; MOVE REJECT FLAGS TO IDP
	STOSB

	MOV	CX,DI
	POP	DI

	SUB	CX,DI
	MOV	MSGLENGTH[DI],CX	; SET LENGTH
;
	OR	BYTE PTR MSGORIGIN+6[DI],80H	; SET RESPONSE
	CALL	L2QUEUEFRAME

HTIM66:
	POP	CX
	POP	BX

	RET

CLEAROUTLINK:

	CALL	CLEARL2QUEUES		; TO RELEASE ANY BUFFERS

	MOV	DI,BX
	MOV	CX,TYPE LINKTABLE
	XOR	AL,AL
	REP STOSB			; CLEAR OUT ENTRY

	RET


RESET3:
	RET



L2ROUTINE:
;
;	LEVEL 2 PROCESSING
;
;		BX = PORT CONTROL TABLE
;		DI = BUFFER ADDR
;		CURRENTPORT = PORT NUMBER
;

	MOV	AL,CURRENTPORT
	MOV	COMPAREPORT,AL		; PORT FOR FIND LINK ROUTINES
	MOV	SAVEPORT,AL		; FOR L3 ROUTINES
;
	MOV	BP,BX			; PORT CONTROL

 	ADD	DS:L2FRAMES[BP],1
	ADC	DS:L2FRAMES+2[BP],0

	MOV	BBSMSG,0
	MOV	ALIASMSG,0
	MOV	MSGFLAG,0		; CMD/RESP UNDEFINED

;
;	Check for Corrupted Callsign in Origin
;	(to keep MH list clean)
;

	MOV	BUFFER,DI		; SAVE BUFFER  11/3/2001

	MOV	CX,6

CHKL2LOOP:

	CMP	MSGORIGIN[DI],40H	; SPACE SHIFTED BY ONE
	JB	L2DISCARD_J
	
	INC	DI

	LOOP	CHKL2LOOP

	SUB	DI,6

	CALL	TRACEFRAME		; TRACE

	MOV	DI,BUFFER
	CALL	MHPROC

	MOV	DI,BUFFER
;
;	CHECK THAT ALL DIGIS HAVE BEEN ACTIONED,
;	   AND ADJUST FOR DIGIPEATERS IF PRESENT
;
	MOV	CX,8			; MAX DIGIS (NOT OUR LIMIT - AX25'S)
L2DIGI00:
	TEST	MSGORIGIN+6[DI],1
	JZ	L2DIGI02		; MORE TO COME
;
	JMP	L2DIGI10		; END OF LIST

L2DIGI02:

	ADD	DI,7
;
	TEST	MSGORIGIN+6[DI],80H	; REPEATED BIT
	JZ	L2DIGI05		; NOT SET

	LOOP	L2DIGI00
;
;	MESSAGE CORRUPT - DISCARD IT
;
L2DISCARD_J:

	JMP	L2DISCARD

L2DIGI05:
;
;	FRAME HAS NOT BEEN REPEATED THROUGH CURRENT DIGI -
;	   SEE IF WE ARE MEANT TO DIGI IT
;
	PUSH	DI
	MOV	SI,OFFSET MYCALL
	LEA	DI,MSGORIGIN[DI]	; TO DIGI CALL
 	CALL	COMPARECALLS
	POP	DI
	JE	L2DIGIPEAT		; WE MUST REPEAT IT
;
	PUSH	DI
	MOV	SI,OFFSET MYALIAS
	LEA	DI,MSGORIGIN[DI]	; TO DIGI CALL
 	CALL	COMPARECALLS
	POP	DI
	JE	L2DIGIPEAT		; WE MUST REPEAT IT

	PUSH	DI
	LEA	SI,PORTALIAS[BP]
	LEA	DI,MSGORIGIN[DI]	; TO DIGI CALL
 	CALL	COMPARECALLS
	POP	DI
	JE	L2DIGIPEAT		; WE MUST REPEAT IT

	PUSH	DI
	LEA	SI,PORTALIAS2[BP]
	LEA	DI,MSGORIGIN[DI]	; TO DIGI CALL
 	CALL	COMPARECALLS
	POP	DI
	JE	L2DIGIPEAT		; WE MUST REPEAT IT

DISCARDIT:
	
	JMP	L2DISCARD

L2DIGIPEAT:
;
;	WE MAY HAVE DISABLED DIGIPEAT ALTOGETHER, (DIGIFLAG=0),
;	OR ALLOW ALLOW ONLY UI FRAMES TO BE DIGIED (DIGIFLAG=-1)
;
	CMP	DS:DIGIFLAG[BP],0
	JE	DISCARDIT

	OR	MSGORIGIN+6[DI],80H	; SET HAS BEEN REPEATED
	CMP	DS:DIGIFLAG[BP],-1
	JNE	OKTODIGI
;
;	SEE IF UI FRAME
;
FINDCTRL:

	TEST	MSGORIGIN+6[DI],1	; LAST CALL BIT?
	JNZ	CHKCTRL			; YES

	ADD	DI,7
	LOOP	FINDCTRL

	JMP	DISCARDIT		; DUFF

CHKCTRL:

	MOV	AL,MSGORIGIN+7[DI]	; CONTROL BYTE
	AND	AL,0FFH-10H		; MASK OFF P/F BIT
	CMP	AL,3
	JNE	DISCARDIT		; NOT UI FRAME

OKTODIGI:

 	ADD	DS:L2DIGIED[BP],1
	ADC	DS:L2DIGIED+2[BP],0

	MOV	AL,DS:DIGIPORT[BP]	; CROSSBAND DIGI ENABLED?
	OR	AL,AL
	JZ	NOTMAPPED		; NO PORT MAPPING DEFINED

	MOV	DI,BUFFER
	MOV	2[DI],AL		; CHANGE PORT

	CALL	GETPORTTABLEENTRY
	MOV	CURRENTPORTPTR,BX

NOTMAPPED:
	JMP	L2QUEUEONPORT		; SEND IT

L2DIGI10:

	MOV	ADJBUFFER,DI
;
;	GET CMD/RESP BITS
;
	MOV	DI,BUFFER
	MOV	AH,MSGDEST+6[DI]
	MOV	AL,MSGORIGIN+6[DI]	; GET COMMAND/RESPONSE BITS
	TEST	AH,80H
	JNZ	L2_CMD

	TEST	AL,80H
	JNZ	L2_RESP			; VALID COMBINATION

L2_VER1:				; MUST BE VERSION 1
	OR	MSGFLAG,VER1
	JMP SHORT CHECKFORUS

L2_RESP:
	OR	MSGFLAG,RESP
	JMP SHORT CHECKFORUS

L2_CMD:
	TEST	AL,80H
	JNZ	L2_VER1			; BOTH SET - ASSUME VER 1

	OR	MSGFLAG,CMD		; SET COMMAND

CHECKFORUS:
;
;	SEE IF FOR AN ACTIVE LINK SESSION
;
	MOV	DI,ADJBUFFER		; BASE EXCLUDING DIGIS
	MOV	AL,MSGCONTROL[DI]
	MOV	SDCBYTE,AL		; SAVE
;
;	IF A UI, THERE IS NO SESSION
;
	MOV	DI,BUFFER
	CALL	FINDLINK		; GET LINK FOR THIS ADDRESS PAIR (IN BX)
	JNZ	NOSESSION

	JMP	L2LINKACTIVE		; THERE IS AN ACTIVE SESSION

NOSESSION:
;
;	NOT FOR ACTIVE LINK - SEE IF ADDRESSED TO OUR NODE ADDR
;
;	FIRST TRY PORT ADDR/ALIAS
;
	TEST	DS:PORTBBSFLAG[BP],1
	JNZ	PORTCALLISBBS		; PORT CALL/ALIAS ARE FOR BBS

	CMP	NODE,0
	JNE	USINGNODE
;
PORTCALLISBBS:
;
;	NODE IS NOT ACTIVE, SO PASS CALLS TO PORTCALL/ALIAS TO BBS
;
	MOV	BBSMSG,1

USINGNODE:

 	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	LEA	SI,PORTCALL[BP]
	CALL	COMPARECALLS		; COMPARE WITH LINKCALL
	JE	L2FORUS_JMP1		; WE ARE TARGET
;
	MOV	ALIASMSG,1

	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	LEA	SI,PORTALIAS[BP]
	MOV	CX,6
	REP CMPSB			; ALLOW ANY SSID
	JE	L2FORUS_JMP		; WE ARE TARGET
;
	CMP	NODE,0
	JE	TRYBBS			; NOT USING NODE SYSTEM

	MOV	BBSMSG,0
	MOV	ALIASMSG,0

 	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	MOV	SI,OFFSET MYCALL
	CALL	COMPARECALLS		; COMPARE WITH LINKCALL
L2FORUS_JMP1:
	JE	L2FORUS_JMP		; WE ARE TARGET
;
	MOV	ALIASMSG,1

	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	MOV	SI,OFFSET MYALIAS
	MOV	CX,6
	REP CMPSB			; ALLOW ANY SSID
	JE	L2FORUS_JMP		; WE ARE TARGET
;
;	WE MAY NOT BE ALLOWED TO USE THE BBS CALL ON SOME BANDS DUE TO
;	THE RATHER ODD UK LICENCING RULES!
;
	CMP	DS:BBSBANNED[BP],1
	JE	SEEIFNODES
TRYBBS:

 	MOV	BBSMSG,1		; MSG TO BBS ADDR

	CMP	BBS,0
	JE	SEEIFNODES		; NOT USING BBS CALLS

	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	MOV	SI,OFFSET BBSCALL
	CALL	COMPARECALLS		; COMPARE WITH LINKCALL
	JE	L2FORUS_JMP		; WE ARE TARGET
;
	MOV	DI,BUFFER
	LEA	DI,MSGDEST[DI]		; DEST FROM MESSAGE
	MOV	SI,OFFSET BBSALIAS
	MOV	CX,6
	REP CMPSB			; ALLOW ANY SSID
	JNE	SEEIFNODES

L2FORUS_JMP:
	JMP	L2FORUS			; WE ARE TARGET
;
;	NOT FOR US - SEE IF 'NODES' OR IP/ARP BROADCAST MESSAGE
;
SEEIFNODES:

	MOV	DI,BUFFER

	PUSH	DI			; SAVE

	MOV	SI,OFFSET QSTCALL
	LEA	DI,MSGDEST[DI]		; DEST CALL

	CALL	COMPARECALLS

	POP	DI
	JE	L2IPMSG			; BROADCAST
;
	CMP	MSGPID[DI],0CFH		; NETROM MSG?
	JNE	L2NOTFORUS		; NO

	PUSH	DI			; SAVE

	MOV	SI,OFFSET NODECALL
	LEA	DI,MSGDEST[DI]		; DEST CALL

	CALL	COMPARECALLS

	POP	DI
	JNE	L2DISCARD		; NOT TO NODES
;
	CMP	MSGDATA[DI],0FFH
	JNE	L2DISCARD		; NOT CORRECT FORMAT?

	CALL	PROCESSNODEMESSAGE

	JMP SHORT L2DISCARD

L2IPMSG:

	MOV	DI,BUFFER

	CALL	Q_IP_MSG		; PASS WHOLE FRAME

	RET

L2NOTFORUS:
;
;	MAY JUST BE A REPLY TO A 'PRIMED' CQ CALL
;
	MOV	AL,SDCBYTE
	AND	AL,NOT 10H		; MASK P/F
	CMP	AL,SABM
	JNE	L2DISCARD		; NOT LINK SETUP
;
;	LOOK FOR A CQ ENTRY IN OUR TABLES MATCHING TARGET CALL
;
	MOV	DI,BUFFER
	LEA	SI,MSGDEST[DI]

	MOV	DI,OFFSET COMPAREFIELD
	MOV	CX,7
	REP MOVSB

	MOV	SI,OFFSET CQ		; AX25 CQ
	MOV	CX,7
	REP 	MOVSB
;
	MOV	DI,OFFSET COMPAREFIELD
	CALL	FINDLINKXX		; GET LINK FOR THIS ADDRESS PAIR (IN BX)
;
	JNZ	L2DISCARD
;
;	MATCHING CIRCUIT FOUND - UPDATE CALLS, SET LINK ACTIVE, AND
;	   SEND CONNECTED MSG TO REMOTE STATION
;
	CALL	SETUPNEWSESSION
	
	CMP	L2STATE[BX],5		; SESSION SETUP SUCCESSFUL?
	JE	CQSETUPOK

	JMP	L2SENDDM		; FAILED - ? TOO MANY DIGIS, ETC

CQSETUPOK:

	MOV	LINKTYPE[BX],2		; DOWNLINK
	PUSH	BX
	CALL	L2SENDUA
	POP	BX
;
;	TELL PARTNER CONNECTION IS ESTABLISHED
;
	CALL	GETBUFF			; WILL USE CQ MSG BUFFER EVENTUALLY
	JZ	CQEXIT			; NO BUFFERS!

	MOV	SI,OFFSET CONNECTEDMSG
	MOV	CX,LCONNECTEDMSG

	CALL	SENDCONNECTREPLY
CQEXIT:
	RET



L2DISCARD:
	
	MOV	DI,BUFFER
	CALL	RELBUFF
	RET

L2FORUS:
;
;	MESSAGE ADDRESSED TO OUR CALL OR ALIAS, BUT NOT FOR AN ACTIVE SESSION
;
	ADD	DS:L2FRAMESFORUS[BP],1
	ADC	DS:L2FRAMESFORUS+2[BP],0
;
;	ONLY SABM IS ALLOWED IF NO SESSION 
;
	MOV	AL,SDCBYTE
	AND	AL,NOT 10H		; MASK P/F

	CMP	AL,3
	JNE	NOT_ADDR_UI

ADDRUI:
;
;	A UI ADDRESSED TO US - SHOULD ONLY BE FOR IP
;
	MOV	DI,ADJBUFFER
	CMP	MSGPID[DI],0CDH
	JE	TCP			; ARP

	CMP	MSGPID[DI],0CCH
	JE	TCP			; TCP/IP

	CMP	MSGPID[DI],08H		; NOS FRAGMENTED AX25 TCP/IP
	JNE	L2DISCARD

TCP:

	MOV	DI,BUFFER
	JMP	L2IPMSG


NOT_ADDR_UI:
;
	CMP	AL,SABM
	JNE	NOTSABM

	IF	BLACKBITS
;
;	CHECK BLACKLIST
;
	EXTRN	CHECKBLACKLIST:NEAR

	MOV	DI,BUFFER
	LEA	SI,MSGORIGIN[DI]	; ORIGIN FROM MESSAGE

	CALL	CHECKBLACKLIST		; RETURN Z IF NODE IS IN LIST

	JZ	IGNORESABM		; DONT ACCEPT

	ENDIF
;
;	IF WE HAVE A PERMITTED CALLS LIST, SEE IF HE IS IN IT
;
	CMP	DS:PERMITTEDCALLS[BP],0
	JE	CALLISOK		; NO LIMITS
;
	PUSH	BP
	MOV	BP,DS:PERMITTEDCALLS[BP]

CHECKLOOP:

	MOV	DI,BUFFER
	LEA	DI,MSGORIGIN[DI]	; ORIGIN FROM MESSAGE
	MOV	SI,BP

	CMP 	BYTE PTR [SI],0
	JE	ENDLIST

	MOV	CX,6			; IGNORE SSID
	REP CMPSB

	JE	CALLISINLIST

	ADD	BP,7
	JMP	CHECKLOOP

ENDLIST:
;
; 	NOT IN LIST
;
	POP	BP

IGNORESABM:

	JMP	L2DISCARD		; IGNORE IT
;


CALLISINLIST:

	POP	BP

CALLISOK:
;
;	IF CALL REQUEST IS FROM A LOCKED NODE WITH QUALITY ZERO, IGNORE IT
;
	MOV	DI,BUFFER
	LEA	SI,MSGORIGIN[DI]

	PUSH	BX
	CALL	FINDNEIGHBOUR		; IS IT A NODE?
	JNE	NOTNODE

	CMP	NEIGHBOUR_FLAG[BX],1
	JNE	NOTNODE			; NOT LOCKED

	CMP	NEIGHBOUR_QUAL[BX],0
	JNE	NOTNODE			; NOT ZERO QUALITY

	POP	BX
DISCARDJMP:
	JMP	L2DISCARD		; IGNORE IT


NOTNODE:

	POP	BX
;
;	CHECK PORT CONNECT LIMITS
;
	CMP	DS:USERS[BP],0
	JE	SABMOK			; NO LIMIT

	MOV	AL,CURRENTPORT
	CALL	COUNTLINKS

	CMP	AH,DS:USERS[BP]
	JL	L2SABM

	JMP	L2SENDDM

SABMOK:
	JMP	L2SABM

NOTSABM:

	MOV	BBSMSG,0		; ONLY DO BBS PROCESSING ON CONNECT
	MOV	ALIASMSG,0
;
;	IF COMMAND WITH P SET, SEND DM
;
	TEST	MSGFLAG,CMD
	JZ	DISCARDJMP		; RESPONSE

	TEST	SDCBYTE,PFBIT
	JZ	DISCARDJMP		; P NOT SET

	JMP	L2SENDDM		; SEND DM IF NOT FOUND

L2LINKACTIVE:
;
;	MESSAGE ON AN ACTIVE LINK (IN BX)
;
	ADD	DS:L2FRAMESFORUS[BP],1
	ADC	DS:L2FRAMESFORUS+2[BP],0

	MOV	AL,SDCBYTE
	AND	AL,NOT 10H		; MASK P/F

	CMP	AL,3
	JNE	NOTACTUI
;
;	UI ON ACTIVE LINK - MUST BE IP DATAGRAM
;
	JMP	ADDRUI

NOTACTUI:

	CMP	AL,DISC
	JE	L2_DISC			; DISCONNECT REQUEST
;
	CMP	AL,SABM
	JE	L2SABM_OLD		; LINK SETUP

	CALL	L2_PROCESS
	RET

L2_DISC:
;
;	SEE IF CROSSLINK ACTIVE
;
	CALL	INFORMPARTNER		; SEND DISC TO OTHER END
;
;	CLEAR OUT TABLE ENTRY, AND RETURN RESPONSE (UA) VIA PORT TX QUEUE 
;
	CALL	CLEAROUTLINK

	CALL	L2SENDUA
	RET

L2SABM_OLD:
;
;	SABM ON EXISTING SESSION - IF DISCONNECTING, REJECT
;
	CMP	L2STATE[BX],4		; DISCONNECTING?
	JNE	OLDSABMOK
;
	JMP	L2SENDDM		; FAILED

OLDSABMOK:
;
;	THIS IS A SABM ON AN EXISTING SESSION
;
;	THERE ARE SEVERAL POSSIBILITIES:
;
;	1. RECONNECT COMMAND TO TNC
;	2. OTHER END THINKS LINK HAS DIED
;	3. RECOVERY FROM FRMR CONDITION
;	4. REPEAT OF ORIGINAL SABM COS OTHER END MISSED UA
;
;	FOR 1-3 IT IS REASONABLE TO FULLY RESET THE CIRCUIT, BUT IN 4
;	SUCH ACTION WILL LOSE THE INITIAL SIGNON MSG IF CONNECTING TO A
;	BBS. THE PROBLEM IS TELLING THE DIFFERENCE. I'M GOING TO SET A FLAG 
;	WHEN FIRST INFO RECEIVED - IF SABM REPEATED BEFORE THIS, I'LL ASSUME
;	CONDITION 4, AND JUST RESEND THE UA
;

	CMP	SESSACTIVE[BX],0
	JNE	L2SABM			; RESET OF ACTIVE CIRCUIT

SENDUA_JMP1:
;
	JMP	L2SENDUA

L2SABM:
;
;	SET UP NEW SESSION (OR RESET EXISTING ONE)
;
	CMP	BX,0
	JNE	L2SABM000		; OK

	JMP	L2SENDDM		; NO LINK ENTRIES - SEND DM RESPONSE
;

L2SABM000:

	CALL	SETUPNEWSESSION
	CMP	L2STATE[BX],5		; SETUP OK?
	JE	SABM021

	JMP	L2SENDDM		; FAILED

SABM021:
;
;	IF CONNECT TO BBS ADDRESS, SET UP BBS SESSION
;
	CMP	BBSMSG,0
	JNE	SABM22			; TRY TO ATTACH TO BBS
;
;	IF CONNECT TO ALIAS, SEND CTEXT
;
	CMP	FULL_CTEXT,1
	JE	SENDCT			; FOR ANY CONNECT

	CMP	ALIASMSG,0
	JE	SENDUA_JMP1

SENDCT:

	CMP	CTEXTLEN,0
	JE	SENDUA_JMP1		; NOT DEFINED

	PUSH	BX
	CALL	L2SENDUA		; SEND ACK
	POP	BX

	CALL	GETBUFF
	JZ	CTEXTRET		; NO BUFFERS

	MOV	BUFFER,DI

	ADD	DI,5
	MOV	AL,0F0H
	STOSB				; PID

	MOV	SI,CTEXTMSG
	MOV	CX,CTEXTLEN

	REP MOVSB

	MOV	CX,DI
	MOV	DI,BUFFER
	SUB	CX,DI

	MOV	MSGLENGTH[DI],CX

	LEA	SI,TX_Q[BX]
	CALL	Q_ADD			; SEND MESSAGE TO CALLER

CTEXTRET:

	RET

SABM22:
;
	CMP	LINKTYPE[BX],1
	JNE	L2SENDUA_JMP		; RESET OF DOWN/CROSSLINK

	CMP	CIRCUITPOINTER[BX],0
	JNE	L2SENDUA_JMP		; ALREADY SET UP - MUST BE REPEAT OF SABM OR LINK RESET
;
;	IF RUNNING ONLY BBS (NODE=0), THIS MAY BE EITHER A USER OR NODE
;	TRYING TO SET UP A L4 CIRCUIT - WE DONT WANT TO ATTACH A NODE TO
;	THE BBS!
;
	CMP	NODE,1
	JE	CALLTOBBS		; MUST BE L2 CALL TO BBS
;
;	NOW THINGS GET DIFICULT - WE MUST EITHER WAIT TO SEE IF A PID CF MSG
;	ARRIVES, OR ASSUME ALL NODES ARE IN NEIGHBOURS - I'LL TRY THE LATTER
;	AND SEE HOW IT GOES. tHIS MEANS THAT YOU MUST DEFINE ALL ROUTES
;	IN CONFIG FILE
;
	MOV	DI,BUFFER
	LEA	SI,LINKCALL[BX]

	PUSH	BX
	CALL	FINDNEIGHBOUR		; IS IT A NODE?
	POP	BX
	JE	L2SENDUA_JMP		; YES

CALLTOBBS:


	PUSH	BX
	CALL	SETUPUSERSESSION	; CREATE INCOMING L4 SESSION
	POP	BX
	JNZ	BBSFAILED		; COULDNT
;
;	NOW TRY A BBS CONNECT
;
	PUSH	BX			; SAVE LINK
	MOV	BX,CIRCUITPOINTER[BX]	; SESSION

	MOV	AL,DS:PORTPACLEN[BP]	; PACLEN FOR THIS PORT
	MOV	SESSPACLEN[BX],AL	; TO SESSION

	MOV	APPLMASK,1		; APPLICATION 1 IS BBS
	CALL	ATTACHTOBBS		; AL = PACLEN

	POP	BX

	JZ	L2SENDUA_JMP		; SUCCESS
;
;	NO BBS AVAILABLE
;
	PUSH	BX
	MOV	BX,CIRCUITPOINTER[BX]
	CALL	CLEARSESSIONENTRY		; RELEASE SESSION
	POP	BX

BBSFAILED:

	CALL	CLEAROUTLINK		; DELETE NEW SESSION

L2SENDDM_JMP:
	JMP	L2SENDDM		; SEND BUSY

L2SENDUA_JMP:
	JMP	L2SENDUA

SETUPNEWSESSION:
;
;	COPY ADDRESS INFO TO LINK TABLE
;
	MOV	DI,BUFFER
	PUSH	DI

	LEA	SI,MSGORIGIN[DI]
	LEA	DI,LINKCALL[BX]
	MOV	CX,7
	REP MOVSB

	AND	BYTE PTR -1[DI],00011110B	; MASK SSID

	POP	DI
	PUSH	DI

	LEA	SI,MSGDEST[DI]		; DEST FROM MESSAGE
	LEA	DI,OURCALL[BX]		; OUR CALL/ALIAS BEING USED 
	MOV	CX,7
	REP MOVSB
;
	AND	BYTE PTR -1[DI],00011110B	; MASK SSID

	POP	DI

	LEA	SI,MSGCONTROL[DI]	; START OF DIGIS IF PRESENT
	LEA	DI,DIGIS[BX]		; OUR CALL/ALIAS BEING USED 
	PUSH	DI

	MOV	CX,MAXDIGIS*7
	JCXZ	NODIGIS

	XOR	AL,AL
	REP STOSB			; CLEAR DIGI FIELD IN CASE RECONNECT

NODIGIS:
	POP	DI
;
;	COPY DIGIS IF PRESENT
;
	MOV	AL,DS:PORTT1[BP]
	MOV	L2TIME[BX],AL		; SET L2 TIMOUT FOR NO DIGIS
SABM010:
	TEST	BYTE PTR -1[SI],1
	JNZ	SABM020			; NONE
;
;	THERE ARE DIGIS TO PROCESS - COPY TO WORK AREA, THEN COPY BACK
;
	MOV	DI,OFFSET TEMPDIGI
	MOV	CX,7*8
	XOR	AX,AX
	REP STOSB				; CLEAR (MAX 8 DIGIS, 7 BYTES EACH)

	SUB	DI,7				; TO LAST TEMP FIELD
SABM012:
	MOV	CX,7
	REP MOVSB

	AND	BYTE PTR -1[DI],01111110B	; MASK REPEATED AND LAST BITS
	TEST	BYTE PTR -1[SI],1
	JNZ	SABM014				; END OF LIST

	SUB	DI,14				; TO PREVIOUS
	JMP	SABM012
SABM014:
;
;	LIST OF DIGI CALLS COMPLETE - COPY TO LINK CONTROL ENTRY
;
	MOV	SI,DI
	SUB	SI,7			; TO START OF LAST DIGI (BECOMES FIRST)
	LEA	DI,DIGIS[BX]
	MOV	AH,DS:PORTMAXDIGIS[BP]
SABM016:
	CMP	BYTE PTR [SI],0
	JE	SABM020			; NO MORE

	CMP	AH,0
	JE	SABM018			; TOO MANY

	MOV	CX,7
	REP MOVSB
	DEC	AH

	MOV	AL,DS:PORTT1[BP]
	ADD	L2TIME[BX],AL		; ADJUST TIMEOUT FOR DIGI'ED LINK
	ADD	L2TIME[BX],AL		; ADJUST TIMEOUT FOR DIGI'ED LINK
	JMP	SABM016			; LOOP

SABM018:
	CALL	CLEAROUTLINK		; GET RID OF PART BUILT SESSION ENTRY
	RET

SABM020:
;
;	THIS MAY BE RESETTING A LINK - BEWARE OF CONVERTING A CROSSLINK TO 
;	AN UPLINK AND CONFUSING EVERYTHING
;
	MOV	AL,CURRENTPORT		; FROM PORT TABLE
	MOV	LINKPORT[BX],AL		; PUT IN

	CMP	LINKTYPE[BX],0
	JNE	SABM020X

	MOV	LINKTYPE[BX],1		; UPLINK

SABM020X:

	MOV	L2TIMER[BX],0		; CANCEL TIMER

	MOV	AX,T3
	MOV	L2SLOTIM[BX],AX		; SET FRAME SENT RECENTLY

	MOV	AL,DS:PORTWINDOW[BP]
	MOV	LINKWINDOW[BX],AL

	CALL	RESET2			; RESET ALL FLAGS

	MOV	L2STATE[BX],5
;
;	IF VERSION 1 MSG, SET FLAG
;
	TEST	MSGFLAG,VER1
	JZ	SABM020A		; NOMAL (VERSION 2)

	MOV	VER1FLAG[BX],1
SABM020A:
	RET


L2SENDUA:
	MOV	AL,UA
	JMP	L2SENDRESP		; SEND UA

L2SENDDM:
	MOV	AL,DM
L2SENDRESP:
;
;	QUEUE RESPONSE TO PORT CONTROL - MAY NOT HAVE A LINK ENTRY 
;
	MOV	DI,ADJBUFFER		; ADDRESS ADJUSTED FOR DIGIS
;
;	SET APPROPRIATE P/F BIT 
;
	MOV	AH,AL
	MOV	AL,SDCBYTE		; LAST RECEIVED COMMAND
	AND	AL,10H			; GET P/F BIT
	OR	AH,AL			; MODIFIED RESPONSE IN AH

	MOV	MSGCONTROL[DI],AH
	LEA	CX,MSGCONTROL+1[DI]	; END OF MESSAGE

	MOV	DI,BUFFER
	SUB	CX,DI			; CALCULATE LENGTH
 	MOV	MSGLENGTH[DI],CX	; SET UP BYTE COUNT

	CALL	L2SWAPADDRESSES		; SWAP ADDRESSES AND SET RESP BITS

L2QUEUEONPORT:

	MOV	DI,BUFFER
	MOV	BX,CURRENTPORTPTR

	CALL	PUT_ON_PORT_Q
	RET

L2SWAPADDRESSES:
;
;	EXCHANGE ORIGIN AND DEST, AND REVERSE DIGIS (IF PRESENT)
;
	MOV	DI,BUFFER

	LEA	SI,MSGORIGIN[DI]
	LEA	DI,TEMPFIELD
	MOV	CX,7
	REP MOVSB			; SAVE ORIGIN

	MOV	DI,BUFFER

	LEA	SI,MSGDEST[DI]	
	LEA	DI,MSGORIGIN[DI]
	MOV	CX,7
	REP MOVSB
;
	AND	BYTE PTR -1[DI],00011110B	; MASK SSID
	OR	BYTE PTR -1[DI],11100000B	; SET RESPONSE AND RESERVED

	MOV	DI,BUFFER

	LEA	SI,TEMPFIELD	
	LEA	DI,MSGDEST[DI]
	MOV	CX,7
	REP MOVSB
;
	AND	BYTE PTR -1[DI],00011110B	; MASK SSID - C BIT ZERO FOR RESPONSE
	OR	BYTE PTR -1[DI],01100000B	; SET RESERVED
;
	ADD	DI,7				; TO FIRST DIGI IF PRESENT
	TEST	BYTE PTR -1[SI],1
	JNZ	L2SWAPRET
;
;	THERE ARE DIGIS TO PROCESS - COPY TO WORK AREA, THEN COPY BACK
;
	MOV	SI,DI				; TO FIRST DIGI

	PUSH	SI				; SAVE

	MOV	DI,OFFSET TEMPDIGI
	MOV	CX,7*8
	XOR	AX,AX
	REP STOSB				; CLEAR (MAX 8 DIGIS, 7 BYTES EACH)

	SUB	DI,7				; TO LAST TEMP FIELD
L2SWAP000:
	MOV	CX,7
	REP MOVSB

	AND	BYTE PTR -1[DI],01111110B	; MASK REPEATED AND LAST BITS
	TEST	BYTE PTR -1[SI],1
	JNZ	L2SWAP010			; END OF LIST

	SUB	DI,14				; TO PREVIOUS
	JMP	L2SWAP000

L2SWAP010:
	MOV	SI,DI
	SUB	SI,7				; TO START OF LAST DIGI (NOW FIRST)
	POP	DI
L2SWAP020:
	CMP	BYTE PTR [SI],0
	JE	L2SWAPRET			; NO MORE

	MOV	CX,7
	REP MOVSB
	JMP	L2SWAP020			; LOOP
L2SWAPRET:
	OR	BYTE PTR -1[DI],1		; SET END OF ADDRESS BIT
	RET

L2_PROCESS:
;
;	PROCESS LEVEL 2 PROTOCOL STUFF
;
;	SEE IF COMMAND OR RESPONSE
;
	TEST	MSGFLAG,CMD
	JNZ	L2_COMMAND
;
;	RESPONSE OR VERSION 1
;
L2_RESPONSE:
;
;	IF RETRYING, MUST ONLY ACCEPT RESPONSES WITH F SET (UNLESS RUNNING V1) 
;
	CMP	VER1FLAG[BX],1
	JE	HDRX09			; VERSION 1

	TEST	SDCBYTE,PFBIT
	JZ	HDRX10			; F NOT SET - DONT RESET TIMER
;
;	F  SET - CAN CANCEL TIMER
;
HDRX09:
	MOV	L2TIMER[BX],0		; CANCEL LINK TIMER
	JMP SHORT HDRX10

L2_COMMAND:
HDRX10:
	CMP	L2STATE[BX],3
	JNE	NOTFRMRSTATE
;
;	FRMR STATE - IF C(P) SEND FRMR, ELSE IGNORE
;
	MOV	AL,SDCBYTE
	AND	AL,NOT PFBIT

	CMP	AL,FRMR
	JE	BOTHFRMR		; DONT EXCHANGE FOR EVER!

	TEST	MSGFLAG,CMD
	JZ	FRMRDISCARD		; RESPONSE

	TEST	SDCBYTE,PFBIT
	JZ	FRMRDISCARD
;
;	SEND FRMR AGAIN
;
	JMP	SDFRMR

FRMRDISCARD:

	JMP	L2DISCARD

BOTHFRMR:

	JMP	SDUF80			; SEND SABM

NOTFRMRSTATE:

	CMP	L2STATE[BX],5		; LINK OK?
	JB	SDNDM			; NO - PROCESS LINK SETUP 
;
;	LINK IN STATE 5 OR ABOVE - LINK RUNNING
;
	MOV	AL,SDCBYTE		; GET CONTROL BYTE
	AND	AL,0FFH-10H		; MASK OFF P/F BIT

	TEST	AL,1
	JNZ	NOTIFRM

	JMP	SDIFRM			; J IF AN I-FRAME

NOTIFRM:

	TEST	AL,2
	JZ	NOTUFRM

	JMP	SDUFRM			; J IF UNNUMBERED FORMAT
NOTUFRM:
	AND	AL,0FH			; ELSE SUPERVISORY, MASK OFF N(R) AND P-BIT
	CMP	AL,RR
	JNE	NOTRR

	JMP	SDSFRM			; J IF 'RECEIVE READY'
NOTRR:
	CMP	AL,RNR
	JNE	NOTRNR

	JMP	SDSFRM			; J IF 'RECEIVE NOT READY'
NOTRNR:
	CMP	AL,REJ
	JNE	SDRX50
;
	ADD	DS:L2REJCOUNT[BP],1
	ADC	DS:L2REJCOUNT+2[BP],0
;
	JMP	SDSFRM
;
;	UNRECOGNISABLE COMMAND
;
SDRX50:
	MOV	AL,SDCBYTE
	MOV	SDRBYTE,AL		; SAVE FOR FRMR RESPONSE
 	OR	SDREJF[BX],SDINVC	; SET INVALID COMMAND REJECT
	CALL	SDFRMR			; PROCESS FRAME REJECT CONDITION
	RET

;	NORMAL DISCONNECT MODE
;
;	COULD BE UA, DM - SABM AND DISC HANDLED ABOVE
;
SDNDM:
	MOV	AL,SDCBYTE		; GET FRAME CONTROL BYTE
	AND	AL,0FFH-10H		; MASK P/F

	CMP	AL,UA
	JE	SDND40

	CMP	AL,DM
	JE	SDND30

	CMP	AL,FRMR
	JNE	NOTFRMR

	JMP	SDUF80
NOTFRMR:

	JMP	L2DISCARD		; ANY OTHER - IGNORE

;
SDND30:
;
;	DM RESPONSE - IF TO SABM, SEND BUSY MSG
;
	CMP	L2STATE[BX],2		; CONNECTING?
	JNE	SDND35			; NO - CLEAR OUT LINK

	PUSH	BX
	CALL	CONNECTREFUSED		; SEND MESSAGE IF DOWNLINK
	POP	BX
SDND35:
;
;	DM OR UA RESP TO DISC RECEIVED - OTHER END HAS LOST SESSION
;
;
;	CLEAR OUT TABLE ENTRY - IF INTERNAL TNC, SHOULD SEND *** DISCONNECTED
;
	CALL	L2DISCARD
	CALL	CLEAROUTLINK

	RET

SDND40:
;
;	UA RECEIVED
;
	CMP	L2STATE[BX],2		; RESPONSE TO SABM?
	JNE	SDND42			; NO
;
;	RESPONSE TO SABM - SET LINK  UP
;
	CALL	RESET2X			; LEAVE QUEUED STUFF

	MOV	L2STATE[BX],5
	MOV	L2TIMER[BX],0		; CANCEL TIMER
	MOV	L2RETRIES[BX],0
	MOV	AX,T3
	MOV	L2SLOTIM[BX],AX		; SET FRAME SENT RECENTLY
;
;	IF VERSION 1 MSG, SET FLAG
;
	TEST	MSGFLAG,VER1
	JZ	SDND41			; NORMAL (VERSION 2)

	MOV	VER1FLAG[BX],1

SDND41:

;	TELL PARTNER CONNECTION IS ESTABLISHED

	MOV	SI,OFFSET CONNECTEDMSG
	MOV	CX,LCONNECTEDMSG

	PUSH	BX
	MOV	DI,BUFFER
	CALL	SENDCONNECTREPLY
	POP	BX

	RET

SDND42:
	CMP	L2STATE[BX],4		; DISCONNECTING?
	JE	SDND35			; CLEAR OUT LINK
;
;	UA, BUT NOT IN STATE 2 OR 4 - IGNORE
; 
	JMP	L2DISCARD



SDSF20:
	JMP	SDFRMR			; PROCESS FRAME REJECT CONDITION
;
;	SUPERVISORY COMMAND
;
;	ONLY VALID ONES ARE RR/RNR
;
;	CHECK COUNTS, AND IF RNR INDICATE BUFFER SHORTAGE AT OTHER END
;
SDSFRM:
	CMP	SDREJF[BX],0		; ARE ANY REJECT FLAGS SET?
	JNE	SDSF20			; J IF YES
SDSF10:
	CALL	SDNRCHK			; CHECK RECEIVED N(R)
	CMP	SDREJF[BX],0		; ARE ANY REJECT FLAGS SET?
	JNE	SDSF20			; J IF YES
;
;	VALID RR/RNR RECEIVED
;
	AND	L2FLAGS[BX],NOT RNRSET	; CLEAR RNR

	MOV	AL,SDCBYTE		; GET CONTROL BYTE
	AND	AL,0FH
	CMP	AL,RNR			; IS COMMAND RNR?
	JNE	SDSF15			; NO

	OR	L2FLAGS[BX],RNRSET	; SET OTHER END CANT RECEIVE
SDSF15:
	TEST	MSGFLAG,CMD
	JNZ	SDSF17			; ALWAYS REPLY TO RR COMMAND

	TEST	SDCBYTE,PFBIT
	JNZ	SDSF16			; RESPONSE WITH F SET - RESET N(S)
;
;	RESPONSE WITHOUT P/F DONT RESET N(S) (UNLESS V1)
;
	CMP	VER1FLAG[BX],1
	JE	SDSF16

	JMP	L2DISCARD

SDSF16:
;
;	RESPONSE WITH P/F - MUST BE REPLY TO POLL FOLLOWING TIMEOUT OR I(P)
;
	AND	L2FLAGS[BX],NOT POLLSENT; CLEAR I(P) SET
;
;	THERE IS A PROBLEM WITH REPEATED RR(F), SAY CAUSED BY DELAY AT L1
;
;	AS FAR AS I CAN SEE, WE SHOULD ONLY RESET N(S) IF AN RR(F) FOLLOWS
;	AN RR(P) AFTER A TIMEOUT - AN RR(F) FOLLOWING AN I(P) CANT POSSIBLY
;	INDICATE A LOST FRAME. ON THE OTHER HAND, A REJ(F) MUST INDICATE
;	A LOST FRAME.
;
;
;	AL STILL CONTAINS THE COMMAND
;
	CMP	AL,REJ
	JE	SDSF16A			; RESET NS
;
	CMP	L2RETRIES[BX],0		; RETRYING?
	JE	SDSF16B			; NO, SO PROBABLY REPLY TO REPEATED
					; RR(P), DELAYED IN LINK LEVEL CODE

SDSF16A:

	MOV	AL,SDCBYTE
	ROL	AL,1			; SHIFT IT TO BOTTOM 3 BITS
	ROL	AL,1
	ROL	AL,1
	AND	AL,7
	CALL	RESETNS			; RESET N(S) AND COUNT RETRIED FRAMES 

	MOV	L2RETRIES[BX],0
	MOV	L2TIMER[BX],0		; WILL RESTART TIMER WHEN RETRY SENT

SDSF16B:

	JMP	L2DISCARD
;
;	RR/RNR/REJ COMMAND - SEND RESPONSE
;
SDSF17:
;
;	FIRST PROCESS RESEQ QUEUE
;
;	PUSH	BUFFER
;	CALL	PROCESS_RESEQ
;	POP	BUFFER
;
;	IGNORE IF AN 'F' HAS BEEN SENT RECENTLY
;
	IFDEF	PCSWITCH

	MOV	AX,REALTIMETICKS
	SUB	AX,LAST_F_TIME[BX]
	CMP	AX,27			; 1.5 SECS
	JB	SDSF16B			; DISCARD

	ELSE

	MOV	AX,REALTIMETICKS	; 10 MS 
	SUB	AX,LAST_F_TIME[BX]
	CMP	AX,150			; 1.5 SECS
	JB	SDSF16B			; DISCARD

	ENDIF


	CALL	RR_OR_RNR

	MOV	AL,LINKNR[BX]		; ELSE GET CURRENT N(R)
	ROR	AL,1			; SHIFT IT TO TOP 3 BITS
	ROR	AL,1
	ROR	AL,1
	OR	AL,AH			; COMBINE WITH CONTROL BYTE
SDCB10:
	MOV	L2ACKREQ[BX],0		; CANCEL DELAYED ACK

	MOV	CX,T3
	MOV	L2SLOTIM[BX],CX		; SET FRAME SENT RECENTLY

	PUSH	BX
	CALL	L2SENDRESP
	POP	BX
;
;	SAVE TIME IF 'F' SENT'
;
	TEST	SDCBYTE,10H
	JZ	SDSF18

	MOV	AX,REALTIMETICKS	; 10 MS 
	MOV	LAST_F_TIME[BX],AX

SDSF18:
	RET

RESETNS:
	MOV	AH,LINKNS[BX]
	MOV	LINKNS[BX],AL		; RESET N(S)

	SUB	AH,AL			; FRAMES TO RESEND
	AND	AH,7			; MODULO SEVEN

	CMP	LINKTYPE[BX],3
	JNE	RESETNSRET

	PUSH	BX
	MOV	BX,NEIGHBOUR[BX]
	OR	BX,BX
	JZ	NONEIGHBOUR		; WOT!!

	MOV	AL,AH
	CBW
	ADD	WORD PTR NBOUR_RETRIES[BX],AX
	ADC	WORD PTR NBOUR_RETRIES+2[BX],0

NONEIGHBOUR:

	POP	BX

RESETNSRET:

	RET





