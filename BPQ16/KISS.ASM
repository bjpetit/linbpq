	PUBLIC	KISSINIT


KISSINIT:				; INTERRUPT INITIALISATION CODE

	IFNDEF	PCSWITCH
;
;	CLEAR HWDATA IN CASE WARM START
;
	MOV	CX,TYPE KISSDATA - HARDWAREDATA
	LEA	DI,LINKSTS[BX]
	XOR	AL,AL
	REP STOSB

	ENDIF

	MOV	PORTINTERLOCK[BX],0	; CANT USE INTERLOCK ON KISS

	IFDEF	PCSWITCH

	MOV	NEEDKISS,1		; NEED TO KEEP KISS CODE

	ENDIF

	CALL	CHECKIOADDR		; SEE IF ANOTHER ENTRY FOR THIS ADDR
	MOV	FIRSTPORT[BX],BX	; MAY BE CHANGED IN ANOTHER CHANNEL USED
;
	MOV	AL,CHANNELNUM[BX]
	SUB	AL,'A'
	MOV	CL,4
	SAL	AL,CL			; TO UPPER NIBBLE

	MOV	OURCTRL[BX],AL		; KISS CONTROL

	CMP	DI,0			; FIRST ENTRY FOR THIS ADDRESS
	JE	INITCOM			; NO OTHER CHANNEL TO LINK
;
;	THIS IS NOT THE FIRST PORT ON THIS I/O ADDR - WE MUST BE USING
;	AN ADDRESSABLE PROTOCOL - IE KISS AS DEFINED FOR KPC4 ETC
;
 	MOV	FIRSTPORT[BX],DI	; QUEUE TX FRAMES ON FIRST
;
;	SET UP SUBCHANNEL CHAIN - ALL PORTS FOR THIS IO ADDR ARE CHAINED
;
SUBLOOP:
	CMP	SUBCHAIN[DI],0
	JE	ENDOFCHAIN

	MOV	DI,SUBCHAIN[DI]		; FOLLOW CHAIN
	JMP	SUBLOOP

ENDOFCHAIN:

	MOV	SUBCHAIN[DI],BX		; PUT OURS ON END

	CALL	INITCOMMON		
	RET				; INT ALREADY HOOKED

INITCOM:
;
;	FIRST PORT USING THIS IO ADDRESS
;
	MOV	POLLPOINTER[BX],BX	; SET FIRST PORT TO POLL

	CALL	INITCOMMON		; SET UP THE PORT

	IFDEF	PCSWITCH

	CMP	QUADFLAG,1
	JE	INITQUAD

	MOV	PORTINTERRUPT[BX],OFFSET SIOINT
	CALL	HOOKINT			; ATTACH INTERRUPT

	ENDIF

	RET

	IFDEF	PCSWITCH

INITQUAD:
;
;	SET UP INTERRUPT SYSTEM
;
	CMP	INTNUMBER,0
	JNE	QUADOK			; ALREADY HAVE FIRST

	MOV	INTNUMBER,1		; FOR LEVEL 3

	CMP	INTLEVEL[BX],3
	JE	INTOK

	MOV	INTNUMBER,2		; ONLY SUPPORT 3 OR 4

INTOK:

	MOV	PORTINTERRUPT[BX],OFFSET QUADINT
	MOV	QUADFIRST,BX		; SAVE PORT NO

QUADOK:

	MOV	QUADFLAG,0
;
;	BUILD CHAIN OF PORTS
;
	MOV	DI,FIRSTQUAD
	CMP	DI,0
	JNE	NOTFIRST

	MOV	FIRSTQUAD,BX
	RET

	PUBLIC	CHECKQUAD

CHECKQUAD:
;
;	CALLED WHEN ALL PORTS SET UP TO INITIALISE QUADPORT INT SYSTEM
;
	CMP	QUADFIRST,0
	JE	NOTQUAD			; NO QUADPORT CARD IN SYSTEM

	MOV	BX,QUADFIRST
	CALL	HOOKINT

	MOV	DX,INTPORT			
	MOV	AL,INTNUMBER			
	OUT	DX,AL			; SET BOARD INT CONTROL

NOTQUAD:

	RET

NOTFIRST:
;
;	ADD TO END OF CHAIN
;
	CMP	QUADCHAIN[DI],0
	JE	ENDOFQCHAIN

	MOV	DI,QUADCHAIN[DI]
	JMP	NOTFIRST
;
ENDOFQCHAIN:

	MOV	QUADCHAIN[DI],BX
	RET


	ENDIF

CHECKIOADDR:
;
;	SEE IF ANOTHER PORT IS ALREADY DEFINED ON THIS CARD
;
	MOV	DI,PORTTABLE
	MOV	CX,NUMBEROFPORTS
INIT100:
	CMP	DI,BX
	JE	INIT110			; NONE BEFORE OURS

	MOV	AX,IOBASE[BX]
	CMP	AX,IOBASE[DI]
	JE	INIT120			; ANOTHER FOR SAME ADDRESS

	MOV	DI,PORTPOINTER[DI]

	LOOP	INIT100
INIT110:
	MOV	DI,0			; FLAG NOT FOUND
INIT120:
	RET

INITCOMMON:

	IFDEF	KANT

	MOV	AL,01110110B		; TIMER 1, LOW/HI, MODE3, BINARY
	OUT	(TMD),AL

	MOV	DX,WORD PTR CLOCKFREQ+2
	MOV	AX,WORD PTR CLOCKFREQ

	DIV	BAUDRATE[BX]

	OUT	(TCT1),AL

	MOV	AL,AH
	OUT	(TCT1),AL
;
;	GET SERIAL I/O UNIT GOING
;
	MOV	AL,11001110B		; 2 STOP NO PARITY 8 BIT 16X
	OUT	(SMD),AL
 
	MOV	AL,25H			; SRDY + RXEN +TXEN
	OUT	(SCM),AL
;
	MOV	AL,0
	OUT	(SIMK),AL		; UNMASK TX AND RX INTS
;
	ENDIF

	IFDEF	PCSWITCH

	PUSH	BX
;
;	SET UP ADDRESS LIST
;
	MOV	CX,7
	MOV	AX,IOBASE[BX]
INIT00:
	MOV	IOADDR[BX],AX
	INC	AX
	ADD	BX,2
	LOOP	INIT00

	POP	BX

	MOV	DX,IER[BX]		; INTERRUPT CONTROL REG
	MOV	AL,5
	OUT	DX,AL			; ALLOW RX AND RX ERROR ONLY
	INC	DX
	DELAY
	IN	AL,DX			; CLEAR ANY INTERRUPT
;
;	SET SPEED
;
	INC	DX
	MOV	AL,80H			; DLAB
	DELAY
	OUT	DX,AL

	MOV	DX,1
	MOV	AX,49664		; 115200

	DIV	BAUDRATE[BX]

	MOV	DX,IOADDR[BX]
	OUT	DX,AL			; SET LSB
	DELAY

	INC	DX
	MOV	AL,AH
	OUT	DX,AL			; SET MSB

	MOV	DX,LCR[BX]
	MOV	AL,03H			; 8 BIT NO PARITY
	OUT	DX,AL

	INC	DX			; TO MCR
	MOV	AL,0BH			; DTR RTS OUT2

	CMP	PROTOCOL[BX],2		; NETROM?
	JNE	NEEDRTS
;
;	NETROM - CLEAR KISS OPTIONS, JUST IN CASE!
;
	MOV	KISSFLAGS[BX],0

;	CMP	FULLDUPLEX[BX],1	; NETROM DROPS RTS TO INHIBIT!	
;	JE	NEEDRTS
;
;	MOV	AL,9			; OUT2 DTR
;
NEEDRTS:

	DELAY
	OUT	DX,AL
;
;	CHECK FOR 16550 - IF PRESENT ENABLE FIFOS
;
	MOV	DX,IIR[BX]
	MOV	AL,10000001B		; 8 BYTE TRIGGER
	OUT	DX,AL

	DELAY

	IN	AL,DX
	AND	AL,11000000B
	CMP	AL,11000000B
	JE	FIFOOK

	XOR	AL,AL
	OUT	DX,AL			; DISABLE FIFO AGAIN

	JMP	FIFO_DONE

FIFOMSG	DB	'16550 Detected and FIFO enabled $'

FIFOOK:

	MOV	FIFO[BX],1		; INDICATE FIFO ACTIVE

	MOV	DX,OFFSET FIFOMSG	; TELL USER	

	IFDEF	OS2

	EXTRN	OS2MSG:NEAR

	MOV	AX,CS

	CALL	OS2MSG

	ELSE

	PUSH	DS

	MOV	AH,9

	PUSH	CS
	POP	DS

	INT	21H

	POP	DS

	ENDIF

FIFO_DONE:

	MOV	DX,LSR[BX]		; CLEAR LINE STATUS INTS
	IN	AL,DX

	DELAY

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; CLEAR ANY PENDING RX INTERRUPT
;
	MOV	IORXEA[BX],OFFSET RXAPTY

	ENDIF

	SETARVEC RXASTX			; INITIAL INTERRUPT ENTRY
	SETATVEC IGNORE

;	IF KISS, SET TIMER TO SEND KISS PARAMS
;
	CMP	PROTOCOL[BX],2		; NETROM?
	JE	NOPARAMS

	MOV	PARAMTIMER[BX],TICKS*30	; 30 SECS FOR TESTING

NOPARAMS:

	RET
