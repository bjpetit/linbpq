	ASSUME	CS:CODE,DS:DATA,ES:DATA

	PUBLIC	KISSTX,KISSRX,KISSTIMER
;
	EXTRN	Q_ADD:NEAR,Q_REM:NEAR,Q_ADDF:NEAR,RELBUFF:NEAR,GETBUFF:NEAR
	EXTRN	HEXOUT:NEAR,NOBUFFERCHECK:NEAR


KISSRX:

	CMP	POLLED[BX],1		; ZERO = NOT POLLED, 1 = POLL RECOGNISED
	JBE	NOPOLL
;
;	POLL OR POLL RESPONSE HAS BEEN RECEIVED
;
	TEST	KISSFLAGS[BX],POLLINGKISS
	JZ	POLLRXED
;
;	POLL RESPONSE RECEIVED - CLEAR POLL OUTSTANDING
;
	MOV	POLLED[BX],0
	MOV	POLLFLAG[BX],0		; CLEAR POLL OUTSTANDING
	JMP SHORT NOPOLL		; SEE IF ANYTHING ELSE

POLLRXED:
;
;	WE ARE A SLAVE, AND THIS IS A POLL. SEE IF FOR US, AND IF SO, REPLY
;

	MOV	AL,POLLED[BX]
	MOV	POLLED[BX],0

	AND	AL,0F0H			; PORT

	CMP	AL,OURCTRL[BX]
	JE	OURPOLL

	JMP SHORT NOPOLL		; NOT FOR US

OURPOLL:
;
;	SEE IF ANYTHING QUEUED
;
	CMP	KISSTX_Q[BX],0
	JE	SENDACK
;
	MOV	POLLED[BX],1		; LET TIMER DO THE SEND
	JMP	NOPOLL

SENDACK:

	MOV	SI,BX
	CALL	SENDPOLL		; OTHER END DOESNT CARE ABOUT ADDR

NOPOLL:
CHKRX:

	LEA	SI,ASYNCMSG_Q[BX]
	CLI	
	CALL	Q_REM
	STI
	JNZ	GOTAFRAME

	RET				; NOTHING RECEIVED

GOTAFRAME:
;
;	IF NETROM, CAN PASS ON NOW
;
	CMP	PROTOCOL[BX],2
	JE	FOUNDIT			; NETROM PROTOCOL
;
;	MESSAGE MAY BE DATA OR DATA ACK. IT HAS NOT YET BEEN CHECKSUMMED
;
	MOV	AL,MSGPORT[DI]
	TEST	AL,0FH
	JZ	CHECK_CHECKSUM		; NORMAL DATA
;
;	SPECIAL FRAME - IS IT AN ACK?
;
	AND	AL,0FH
	CMP	AL,0CH			; ACK
	JE	ACKFRAME

	JMP	NOTFOUND		; DONT RECOGNISE TYPE

ACKFRAME:
;
;	ACK FRAME. WE DONT SUPPORT ACK REQUIRED FRAMES AS A SLAVE - THEY ARE
;	ONLY ACCEPTED BY TNCS
;
	TEST	KISSFLAGS[BX],POLLEDKISS
	JNZ	ACKREQFRAME		; FRAME REQUIRING AN ACK
;
;	THIS IS AN ACK OF A DATA FRAME
;
	MOV	BL,5[DI]
	MOV	BH,6[DI]

	CMP	L2TIMER[BX],0
	JE	NOTIMER			; ALREADY EXPIRED

	MOV	AL,L2TIME[BX]
	XOR	AH,AH
	MOV	L2TIMER[BX],AX

NOTIMER:

	JMP SHORT NOTFOUND		; RETURN IT


ACKREQFRAME:
;
;	NOT ALLOWED - DISCARD
;
	JMP	DISCARDFRAME

CHECK_CHECKSUM:

	TEST	KISSFLAGS[BX],CHECKSUM
	JZ	KISSRX00
;
;	SUM MESSAGE, AND IF DUFF DISCARD. IF OK DECREMENT COUNT TO REMOVE SUM
;
	MOV	CX,3[DI]		; LENGTH, INCLUDING SUM

	CMP	CX,14H
	JB	DISCARDFRAME		; TOO SHORT FOR VALID AX25

	DEC	WORD PTR 3[DI]
;
	MOV	AH,MSGPORT[DI]		; IS INCLUDED IN SUM

	LEA	SI,5[DI]		; TO DATA
	SUB	CX,5

SUMLOOP:

	LODSB
	XOR	AH,AL

	LOOP	SUMLOOP

	OR	AH,AH
	JZ	KISSRX00			; SUM OK

	ADD	RXERRORS[BX],1
	ADC	RXERRORS+2[BX],0

	JMP SHORT DISCARDFRAME

FOUNDIT:

	LEA	SI,PORTRX_Q[BX]
	CALL	Q_ADD			; PUT ON PORT RX QUEUE

	JMP SHORT CHKRX			; SEE IF MORE

KISSRX00:
;
;	AT THIS STAGE, KISS CONTROL BYTE IS IN PORT FIELD - FIND CORRECT
;	SUBPORT RECORD TO ADD IT TO
;
	MOV	AL,MSGPORT[DI]
;
;	SHOULD ONLY HAVE NORMAL DATA NOW
;
FINDLOOP:

	CMP	OURCTRL[BX],AL
	JE	FOUNDIT

	MOV	BX,SUBCHAIN[BX]
	OR	BX,BX
	JZ	NOTFOUND

	JMP	FINDLOOP

NOTFOUND:
DISCARDFRAME:
;
;	INVALID CONTROL BYTE - DISCARD IT (MAYBE SHOULD COUNT ??)
;
	CALL	RELBUFF
	RET

KISSTX:
;
;	START TRANSMISSION - DI = ADDR OF MESSAGE
;
	PUSH	BX
	MOV	BX,FIRSTPORT[BX]	; ALL FRAMES GO ON SAME Q

	CLI

	TEST	KISSFLAGS[BX],POLLEDKISS
	JNZ	QUEUEIT			; MUST QUEUE - CANT SEND TILL POLLED

	CMP	KISSTX_Q[BX],0
	JNE	QUEUEIT			; ALREADY SOMETHING QUEUED

	CMP	POLLFLAG[BX],0
	JNE	QUEUEIT			; POLL OUTSTANDING, SO QUEUE FRAME

	CMP	TXTADDR[BX],0
	JE	OKTOSEND
;
;	ALREADY SENDING - QUEUE FRAME
;
QUEUEIT:

	LEA	SI,KISSTX_Q[BX]
	CALL	Q_ADD
	STI
	POP	BX
	RET

OKTOSEND:
;
;	IF NETROM PROTOCOL AND NOT FULL DUPLEX AND BUSY, QUEUE IT
;
	IFDEF	PCSWITCH

	CMP	PROTOCOL[BX],2		; NETROM?
	JNE	DONTCHECKDCD		; NO

	CMP	FULLDUPLEX[BX],1
	JE	DONTCHECKDCD		; FULLDUP - NO CHECK
;
;	NETROM USES RTS, CROSS-CONNECTED TO CTS AT OTHER END, TO MEAN
;	NOT BUSY
;
	MOV	DX,MSR[BX]
	IN	AL,DX

	TEST	AL,CTSBIT		; CTS HIGH?
	JZ	QUEUEIT			; NO, SO QUEUE FRAME
;
;	GOING TO SEND - DROP RTS TO INTERLOCK OTHERS 
;
	MOV	DX,MCR[BX]
	MOV	AL,09H			; DTR OUT2

	OUT	DX,AL
;
;	MAKE SURE CTS IS STILL ACTIVE - IF NOT, WE HAVE A COLLISION,
;	SO RELEASE RTS AND WAIT
;
	DELAY

	MOV	DX,MSR[BX]
	IN	AL,DX
	TEST	AL,CTSBIT
	JNZ	DONTCHECKDCD		; STILL HIGH, SO CAN SEND
;
;	RAISE RTS AGAIN, AND QUEUE FRAME
;
	DELAY

	MOV	DX,MCR[BX]
	MOV	AL,0BH			; RTS DTR OUT2

	OUT	DX,AL
;
	JMP	QUEUEIT

DONTCHECKDCD:

	ENDIF

	MOV	TXTADDR[BX],1		; ENSURE POLL ISNT SENT
	STI

	CALL	SENDFRAME
	POP	BX
	RET

SENDFRAME:
;
;	GET REAL PORT TABLE ENTRY - IF MULTIPORT, FRAME IS QUEUED ON
;	FIRST
;
	CMP	PROTOCOL[BX],2		; NETROM
	JE	NRTX00

	PUSH	BX
	MOV	AL,2[DI]		; PORT NUMBER
TXPORTLOOP:
	CMP	PORTNUMBER[BX],AL
	JE	OURPORT

	MOV	BX,SUBCHAIN[BX]
	CMP	BX,0
	JNE	TXPORTLOOP

	POP	BX
	MOV	TXTADDR[BX],0		; ALLOW OTHER ACTIONS

	CALL	RELBUFF
	RET

OURPORT:

	MOV	AL,OURCTRL[BX]		; CORRECT CONTROL BYTE
	MOV	AH,KISSFLAGS[BX]	; CORRECT KISSFLAGS

	POP	BX
	MOV	TXCTRL[BX],AL		; FOR TX ROUTINE
	MOV	REALKISSFLAGS[BX],AH

	SETATVEC TXKISS			; SEND FRAME DELIMITER FIRST
;
	MOV	ESCTXCHAR[BX],0
	JMP SHORT TXCOMMON

NRTX00:
	SETATVEC TXTEXT			; SEND FRAME DELIMITER FIRST
	MOV	ESCTXCHAR[BX],STX	; SEND STX FIRST
	MOV	TXCCC[BX],0
;
TXCOMMON:

	MOV	TXTADDR[BX],DI		; SAVE ADDR OF LAST MESSAGE
;
	MOV	CX,3[DI]		; MSG LENGTH
	SUB	CX,5			; HEADER LENGTH
	ADD	DI,5
	
	MOV	AX,WORD PTR [DI]	; KISS CONTROL FRAME?
	AND	AH,0FH			; MASK OUT PORT BITS
	CMP	AX,1C0H			; KISS CONTROL FRAME?
	JNE	TXNORM			; DATA FRAME
;
;	KISS CONTROL FRAME
;
	SETATVEC TXKISSCTRL
TXNORM:
	MOV	TXCHAR[BX],DI		; SAVE
	MOV	TXCOUNT[BX],CX
;
;	MOST HARDWARE SEEMS TO INTERRUPT WHEN TX INT IS ENABLED, CAUSING
;	THE FIRST CHAR TO BE SENT. HOWEVER SOME SEEM NOT TO, SO THE CODE
;	CHECKS THAT THE INTERRUPT OCCURS IN A REASONABLE TIME, AND IF IT
;	DOESNT, IT SENDS THE FIRST CHAR DIRECTLY.
;
	MOV	INTFLAG[BX],1		; INT EXPECTED 
;
	IFDEF	KANT

	MOV	AL,0
	OUT	SIMK,AL

	ENDIF


	IFDEF	PCSWITCH

 	MOV	DX,IER[BX]
	MOV	AL,7			; ENABLE TX INT
	OUT	DX,AL


	ENDIF

	MOV	CX,15
WAITLOOP:
	CMP	INTFLAG[BX],0
	JE	SENDRET			; INT HAS OCCURED

	LOOP	WAITLOOP
;
;	INT FAILED
;
	CALL	IOTXCA[BX]		; DO FIRST SEND
SENDRET:
	MOV	TXACTIVE[BX],TICKS*30	; ALLOW 30 SECS TO SEND
	RET
;
KISSTIMER:
;
;	IF FRAMES QUEUED, AND NOT SENDING, START
;
	CMP	BX,FIRSTPORT[BX]		; ALL FRAMES GO ON SAME Q
	JNE	NOTMAINPORT_J1
;
;	SEE IF POLL HAS TIMED OUT
;
	TEST	KISSFLAGS[BX],POLLINGKISS
	JZ	NOTPOLLED

	CLI
	CMP	POLLFLAG[BX],0
	JE	NOTPOLLED

	DEC	POLLFLAG[BX]
	STI
	JNZ	NOTMAINPORT_J1		; TIMING OUT OR RECEIVING
;
;	POLL HAS TIMED OUT - MAY NEED TO DO SOMETHING
;
	IFNDEF	PCSWITCH
	CMP	ASYNCMODE,'K'		; TERMINAL OR KISS MODE?
	JNE	NOTPOLLED		; TERMINAL MODE
	ENDIF

	PUSH	BX
	MOV	BX,POLLPOINTER[BX]
	ADD	L2URUNC[BX],1		; PUT IN UNDERRUNS FIELD
	ADC	L2URUNC+2[BX],0
	POP	BX

NOTPOLLED:

	CLI
	CMP	TXTADDR[BX],0
	JE	NOTSENDING
;
	JMP	NOTCTSWAIT

;	IFNDEF	KANT
;
;	CMP	TXTADDR[BX],0FFFFH
;	JNE	NOTCTSWAIT
;
;	WAITING FOR CTS
;
;	MOV	DX,MSR[BX]
;	IN	AL,DX
;	TEST	AL,CTSBIT
;	JNZ	TIMERSEND		; OK TO SEND NOW
;
;	ENDIF

BUSY_J:
	JMP	BUSY			; WAIT FOR CTS


NOTMAINPORT_J1:

	JMP	NOTMAINPORT

NOTCTSWAIT:
;
;	SEE IF BEEN SENDING TOO LONG
;
	DEC	TXACTIVE[BX]
	JNZ	BUSY_J
;
;	SEEM TO HAVE LOST A TX INTERRUPT - TRY CALLING TX ROUTINE
;
	ADD	L1DISCARD[BX],1		; FOR STATS
	ADC	L1DISCARD+2[BX],0

	MOV	TXACTIVE[BX],2

	CALL	IOTXCA[BX]

	JMP	BUSY

NOTSENDING:
;
;	SEE IF ANYTHING TO SEND
;
	STI

	CMP	POLLED[BX],1
	JE	BEENPOLLED		; OK EVEN IF SLAVE MODE

	TEST	KISSFLAGS[BX],POLLEDKISS
	JNZ	NOTMAINPORT_J		; CANT SEND TILL WE GET POLL

BEENPOLLED:
;
;	IF IN FIFO MODE, ENSURE FIFO IS EMPTY
;
	IFDEF	PCSWITCH

	TEST	FIFO[BX],1
	JZ	KTIM50			; NOT USING FIFO

 	MOV	DX,LSR[BX]
	IN	AL,DX
	TEST	AL,40H
	JZ	NOTMAINPORT		; DONT SEND ANYTHING YET

KTIM50:

	ENDIF

	CMP	KISSTX_Q[BX],0
	JE	NOTHINGTOSEND
;
;	IF NETROM MODE AND NOT FULL DUP, CHECK DCD
;
	MOV	POLLED[BX],0

	IFDEF	PCSWITCH

	CMP	PROTOCOL[BX],2		; NETROM?
	JNE	DONTCHECKDCD_1

	CMP	FULLDUPLEX[BX],1
	JE	DONTCHECKDCD_1

	MOV	DX,MSR[BX]
	IN	AL,DX

	TEST	AL,CTSBIT		; DCD HIGH?
	JZ	NOTHINGTOSEND		; NO, SO WAIT
;
;	DROP RTS TO LOCK OUT OTHERS
;
	MOV	DX,MCR[BX]
	MOV	AL,09H			; DTR OUT2

	OUT	DX,AL
;
;
;	MAKE SURE CTS IS STILL ACTIVE - IF NOT, WE HAVE A COLLISION,
;	SO RELEASE RTS AND WAIT
;
	DELAY

	MOV	DX,MSR[BX]
	IN	AL,DX
	TEST	AL,CTSBIT
	JNZ	TIMERSEND		; STILL HIGH, SO CAN SEND
;
;	RAISE RTS AGAIN, AND WAIT A BIT MORE
;
;	DELAY
;
	MOV	DX,MCR[BX]
	MOV	AL,0BH			; RTS DTR OUT2

	OUT	DX,AL

	JMP SHORT NOTHINGTOSEND

DONTCHECKDCD_1:
TIMERSEND:

	ENDIF

	PUSH	SI
	PUSH	DI
	PUSH	DX

	LEA	SI,KISSTX_Q[BX]
	CALL	Q_REM

	CALL	SENDFRAME

	POP	DX
	POP	DI
	POP	SI

NOTMAINPORT_J:

	JMP SHORT NOTMAINPORT

NOTHINGTOSEND:
;
;	IF POLLED MODE, SEND A POLL TO NEXT PORT
;
	TEST	KISSFLAGS[BX],POLLINGKISS
	JZ	NOTMAINPORT
;
;	FIND WHICH CHANNEL TO POLL NEXT
;
	PUSH	SI
	MOV	SI,POLLPOINTER[BX]
	MOV	SI,SUBCHAIN[SI]
	CMP	SI,0
	JNE	POLLTHISONE

	MOV	SI,BX			; BACK TO FIRST

POLLTHISONE:

	MOV	POLLPOINTER[BX],SI	; FOR NEXT TIME

	CALL	SENDPOLL
	POP	SI

BUSY:
	STI

NOTMAINPORT:


KISSSLOWTIMER:
;
;	SEE IF TIME TO REFRESH KISS PARAMS
;
	TEST	KISSFLAGS[BX],POLLEDKISS
	JNZ	KTIMERRET		; WE ARE THE SLAVE

	CMP	PROTOCOL[BX],2		; NETROM?
	JNE	KISSXX

KTIMERRET:

	RET				; NOT ON NETROM

KISSXX:

	DEC	PARAMTIMER[BX]
	JNZ	KTIMERRET
;
;	QUEUE A 'SET PARAMS' FRAME
;
	PUSH	SI
	PUSH	DI

	CMP	PORTDISABLED[BX],0
	JNE	NOBUFFERS		; PORT DISABLED

	CALL	GETBUFF
	JZ	NOBUFFERS
;
	PUSH	DI

	ADD	DI,5
	MOV	AL,FEND
	STOSB
	MOV	AH,OURCTRL[BX]
	MOV	AL,1
	OR	AL,AH			; PUT IN CHANNEL BITS
	STOSB

	MOV	AL,PORTTXDELAY[BX]
	STOSB
	MOV	AL,FEND
	STOSB
	STOSB

	MOV	AL,2
	OR	AL,AH			; PUT IN CHANNEL BITS
	STOSB
	MOV	AL,PORTPERSISTANCE[BX]
	STOSB
	MOV	AL,FEND
	STOSB
	STOSB

	MOV	AL,3
	OR	AL,AH			; PUT IN CHANNEL BITS
	STOSB
	MOV	AL,PORTSLOTTIME[BX]
	STOSB
	MOV	AL,FEND
	STOSB
	STOSB

	MOV	AL,4
	OR	AL,AH			; PUT IN CHANNEL BITS
	STOSB
	MOV	AL,PORTTAILTIME[BX]
	STOSB
	MOV	AL,FEND
	STOSB
	STOSB

	MOV	AL,5
	OR	AL,AH			; PUT IN CHANNEL BITS
	STOSB
	MOV	AL,FULLDUPLEX[BX]
	STOSB

	MOV	CX,DI
	POP	DI
	SUB	CX,DI

	MOV	3[DI],CX		; SET LENGTH

	PUSH	BX
	MOV	BX,FIRSTPORT[BX]	; ALL FRAMES GO ON SAME Q
;
;	QUEUE FRAME
;
	LEA	SI,KISSTX_Q[BX]
	CLI
	CALL	Q_ADD
	STI
	POP	BX

NOBUFFERS:

	MOV	PARAMTIMER[BX],TICKS*60*5	; 5 MINS

	POP	DI
	POP	SI

	RET

SENDPOLL:
;
;	SEND POLL TO PORT IN SI
;
	MOV	POLLFLAG[BX],TICKS/3	; ALLOW 1/3 SEC 

	MOV	AL,OURCTRL[SI]		; CORRECT CONTROL BYTE
	OR	AL,0EH			; POLL BITS
	MOV	TXCTRL[BX],AL		; FOR TX ROUTINE

	SETATVEC TXKISSPOLL		; SEND FRAME DELIMITER FIRST
;
;	MOST HARDWARE SEEMS TO INTERRUPT WHEN TX INT IS ENABLED, CAUSING
;	THE FIRST CHAR TO BE SENT. HOWEVER SOME SEEM NOT TO, SO THE CODE
;	CHECKS THAT THE INTERRUPT OCCURS IN A REASONABLE TIME, AND IF IT
;	DOESNT, IT SENDS THE FIRST CHAR DIRECTLY.
;
	MOV	INTFLAG[BX],1		; INT EXPECTED 
;
 	IFDEF	PCSWITCH

 	MOV	DX,IER[BX]
	MOV	AL,7			; ENABLE TX INT
	OUT	DX,AL


	ELSE

	CMP	ASYNCMODE,'K'		; TERMINAL OR KISS MODE?
	JNE	PSENDRET		; TERMINAL MODE

	ENDIF

	IFDEF	KANT

	MOV	AL,0
	OUT	SIMK,AL

	ENDIF

	IFDEF	MSC

;	!!!!!!!!

	ENDIF

	MOV	CX,15
PWAITLOOP:
	CMP	INTFLAG[BX],0
	JE	PSENDRET		; INT HAS OCCURED

	LOOP	PWAITLOOP
;
;	INT FAILED
;
	CALL	IOTXCA[BX]		; DO FIRST SEND

PSENDRET:

	MOV	TXACTIVE[BX],TICKS*30	; ALLOW 30 SECS TO SEND
	RET
;
TXKISSPOLL:

	MOV	INTFLAG[BX],0		; INTERRUPT HAS OCCURED
	SETATVEC SENDPOLLCTRL

	MOV	AL,FEND
	JMP	TXA100			; SEND FEND


RXASTX:
	AND	LINKSTS[BX],0FFH-10H	; CLEAR PARITY ERROR
;
	IFDEF	KANT

	LED	0,OFF

	IN	AL,SRB

	ELSE

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; READ A CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

	CMP	PROTOCOL[BX],2
	JE	RXNR00			; NETROM PROTOCOL

	CMP	AL,FEND			; FRAME DELIMITER?
	JNE	RXA010			; NO - IGNORE
;
;	GET BUFFER
;
	MOV	SI,OFFSET FREE_Q
	CALL	Q_REM

	JNZ	GETB00

	CALL	NOBUFFERCHECK		; CHECK IF POOL IS CORRUPT
	JMP	RXA010

GETB00:

	DEC	QCOUNT

	MOV	CURALP[BX],DI		; SAVE IT
	MOV	WORD PTR [DI],OFFSET RXA005

RXA005:

	ADD	DI,5			; SKIP CHAIN, PORT, LENGTH
	MOV	NEXTCHR[BX],DI		; SAVE BUFFER ADDRESS
;
	MOV	FRAMELEN[BX],5		; INITIAL LENGTH	
	SETARVEC RXACTL

	IFDEF	KANT

	LED	0,ON

	ENDIF

	RET

RXA010:
	RET

RXNR00:
	CMP	ESCFLAG[BX],1
	JE	RXA011			; DONT ACCEPT DLE/STX

	CMP	AL,STX
	JE	RXA002

	CMP	AL,DLE
	JNE	RXA010			; IGNORE

	MOV	ESCFLAG[BX],1
	RET


RXA002:
;
;	GET BUFFER
;
	MOV	SI,OFFSET FREE_Q
	CALL	Q_REM

	JNZ	GETBNR

	CALL	NOBUFFERCHECK		; CHECK IF POOL IS CORRUPT
	JMP	RXA011			; IGNORE (? WAIT FOR ETX OR TIMEOUT)

GETBNR:

	MOV	WORD PTR [DI],OFFSET GETBNR
	DEC	QCOUNT
	MOV	CURALP[BX],DI		; SAVE IT

	ADD	DI,5			; SKIP CHAIN, PORT, LENGTH
	MOV	NEXTCHR[BX],DI		; SAVE BUFFER ADDRESS
;
	MOV	FRAMELEN[BX],5		; INITIAL LENGTH	
	MOV	RXCCC[BX],0
	SETARVEC RXAMSGNR

	IFDEF	KANT

	LED	0,ON

	ENDIF

RXA011:
	MOV	ESCFLAG[BX],0
	RET


RXACTL:

	IFDEF	KANT

	IN	AL,SRB

	ELSE

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; READ A CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF
;
;	SHOULD BE PORT NUMBER - IF FEND WE HAVE GOT OUT OF STEP
;
	MOV	DI,CURALP[BX]		; GET BUFFER ADDR

	CMP	AL,FEND
	JE	RXA005			; TRY AGAIN
;
	MOV	MSGPORT[DI],AL		; PUT CONTROL BYTE IN PORT FIELD
	XOR	RXCCC[BX],AL		; MAINTAIN CCC
	
	AND	AL,0FH
	CMP	AL,0EH			; POLL OR POLL REPLY?
	JNZ	NOTPOLLREPLY
;
;	POLL OR REPLY TO POLL
;
	MOV	AL,MSGPORT[DI]
	MOV	POLLED[BX],AL		; FOR BG TO PROCESS

	IFDEF	KANT
	LED	0,OFF
	ENDIF

	RET				; ACCEPT FEND OR CONTROL NEXT

NOTPOLLREPLY:

	SETARVEC RXAMSG
	TEST	KISSFLAGS[BX],POLLINGKISS
	JZ	NOTPOLL00
;
;	POLLED MODE - EXTEND TIMEOUT FOR DATA FRAME
;
	MOV	POLLFLAG[BX],5*TICKS	; 5 SECS - SHOULD BE PLENTY

NOTPOLL00:

	RET

RXAMSG:
	IFDEF	KANT

	IN	AL,SRB

	ELSE

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; READ A CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

	CMP	AL,FEND		
	JE	MSGEND			; END OF FRAME

	CMP	AL,FESC
	JNE	NOTESC
;
;	ESC - SET FLAG 
;
	MOV	ESCFLAG[BX],1		; SET FESC RECEIVED
	RET

NOTESC:
	CMP	ESCFLAG[BX],1
	JNE	RXNORM

	CMP	AL,TFESC		; ESC'ED FESC?
	JNE	NOTTFESC

	MOV	AL,FESC			; REAL FESC 
	JMP SHORT RXNORM

NOTTFESC:
	CMP	AL,TFEND		; ESC'ED FEND?
	JNE	RXNORM
 
	MOV	AL,FEND
RXNORM:
	MOV	DI,NEXTCHR[BX]		; GET POINTER
	STOSB				; PUT IN BUFFER
	MOV	NEXTCHR[BX],DI		; SAVE POINTER

RXA500:
	MOV	ESCFLAG[BX],0
	INC	FRAMELEN[BX]
	CMP	FRAMELEN[BX],BUFFLEN-10
	JB	RXARET			; OK
	DEC	NEXTCHR[BX]		; PREVENT OVERRUN

	OR	LINKSTS[BX],10H		; SET ERROR
RXARET:
	RET
;

MSGEND:

	MOV	DI,NEXTCHR[BX]		; GET POINTER
	STOSB				; PUT FEND ON END

	MOV	DI,CURALP[BX]
	MOV	POLLFLAG[BX],0		; LINK NOW IDLE

QMSG:

	TEST	LINKSTS[BX],10H
	JNZ	RXAERROR		; OVERRUN, ETC

	MOV	CX,FRAMELEN[BX]
	MOV	3[DI],CX		; LENGTH

	LEA	SI,ASYNCMSG_Q[BX]	; PUT ON RX QUEUE - SUBPORT DETAILS ARE
					; SORTED OUT LATER
	CALL	Q_ADD
	JMP	RXAEXIT

RXAERROR:

	MOV	DI,CURALP[BX]
	MOV	SI,OFFSET FREE_Q
	CALL	Q_ADDF

RXAEXIT:
	MOV	IORXCA[BX],OFFSET RXASTX	; FOR NEXT MESSAGE

	IFDEF	KANT

	LED	0,OFF

	ENDIF

	RET
;
RXAMSGNR:

	IFDEF	KANT

	IN	AL,SRB

	ELSE

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; READ A CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

	CMP	AL,DLE
	JNE	NOTDLE
;
;	DLE - SET FLAG UNLESS ALREADY SET
;
	CMP	ESCFLAG[BX],1
	JE	NOTDLE			; MUST BE DLE/DLE - MEANS REAL 10H!

	MOV	ESCFLAG[BX],1		; SET DLE RECEIVED
	RET
NOTDLE:
	MOV	DI,NEXTCHR[BX]		; GET POINTER
	STOSB				; PUT IN BUFFER
	MOV	NEXTCHR[BX],DI		; SAVE POINTER

	CMP	AL,ETX			; END?
	JNE	RXA500NR		; NO
;
	CMP	ESCFLAG[BX],1
	JE	RXA500NR		; DLE/ETX

	SETARVEC RXACCC
;
	RET
;
RXA500NR:
	ADD	RXCCC[BX],AL		; UPDATE LRC
	JMP	RXA500			; CHECK FOR BUFFER OVERFLOW, ETC

RXACCC:
	IFDEF	KANT

	IN	AL,SRB

	ELSE

	MOV	DX,IOADDR[BX]
	IN	AL,DX			; READ A CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

;
	CMP	AL,RXCCC[BX]		; ACCUMULATED VALUE
	JE	OK_CRC

	ADD	RXERRORS[BX],1
	ADC	RXERRORS+2[BX],0

;	JMP	RXAERROR		; DUFF

OK_CRC:

	MOV	DI,CURALP[BX]
	JMP	QMSG			; QUEUE FOR BG TO PROCESS

;
	IFDEF	PCSWITCH

RXAPTY:

;
;	ERROR INTERRUPT (FRAMING/OVERRUN)
;
	MOV	DX,LSR[BX]
	IN	AL,DX			; READ LINE STATUS TO CLEAR INTERRUPT
;
	ADD	L2ORUNC[BX],1		; FOR STATS
	ADC	L2ORUNC+2[BX],0

	OR	LINKSTS[BX],10H		; SET ERROR FLAG
	RET
;
	ENDIF

TXKISS:

	MOV	INTFLAG[BX],0		; INTERRUPT HAS OCCURED
	SETATVEC TXNULL

	MOV	AL,FEND
	JMP	TXA100			; SEND FEND

TXNULL:
;
;	SEND CONTROL BYTE
;
	SETATVEC TXTEXT

	MOV	AL,TXCTRL[BX]
;
;	SEE IF ACK MODE IN USE
;
	TEST	REALKISSFLAGS[BX],ACKMODE
	JZ	NOACKMODE
;
;	SEE IF FRAME NEEDS AN ACK
;

	MOV	SI,TXTADDR[BX]
	CMP	WORD PTR BUFFLEN-2[SI],0
	JE	NOACKMODE

	SETATVEC TXHDDR1

	OR	AL,0CH			; RESPONSE MODE

NOACKMODE:


	MOV	TXCCC[BX],FEND		; INITIALISE CRC - HAVE ALREADY SEND FEND
	XOR	TXCCC[BX],AL

	JMP	TXA100			; SEND KISS CONTROL


TXHDDR1:
;
;	SEND FIRST ACK BYTE
;

	MOV	SI,TXTADDR[BX]
	MOV	AL,BUFFLEN-2[SI]

	SETATVEC TXHDDR2

	JMP	TXA100			; SEND IT


TXHDDR2:
;
;	SEND SECOND ACK BYTE
;

	MOV	SI,TXTADDR[BX]
	MOV	AL,BUFFLEN-1[SI]
	MOV	WORD PTR BUFFLEN-2[SI],0; CLEAR INDICATOR	

	SETATVEC TXTEXT

	JMP	TXA100			; SEND IT


TXTEXT:

	MOV	INTFLAG[BX],0		; INTERRUPT HAS OCCURED
	MOV	AL,ESCTXCHAR[BX]
	OR	AL,AL	
	JZ	TXA000			; NORMAL

	MOV	ESCTXCHAR[BX],0
	JMP SHORT TXA100		; SEND CHAR FOLLOWING ESC

TXA000:
	CMP	TXCOUNT[BX],0
	JZ	TXETX

	DEC	TXCOUNT[BX]

	MOV	SI,TXCHAR[BX]
	LODSB				; NEXT CHAR
	MOV	TXCHAR[BX],SI

	CMP	PROTOCOL[BX],2
	JE	CHECKDLE		; NETROM MODE

	XOR	TXCCC[BX],AL

	CMP	AL,FEND
	JNE	NOTTXFEND

	MOV	ESCTXCHAR[BX],TFEND	; ESC'ED FEND
	MOV	AL,FESC
	JMP SHORT TXA100

NOTTXFEND:
	CMP	AL,FESC
	JNE	TXA100

	MOV	ESCTXCHAR[BX],TFESC	; ESC'ED ESC
	JMP SHORT TXA100

CHECKDLE:
;
;	CHECK FOR NETROM CHARS TO PREFIX
;
	ADD	TXCCC[BX],AL
	CMP	AL,DLE
	JE	TXDLE00
	CMP	AL,STX
	JE	TXDLE00
	CMP	AL,ETX
	JNE	TXA100

TXDLE00:
	MOV	ESCTXCHAR[BX],AL
	MOV	AL,DLE
	JMP SHORT TXA100


TXETX:

	CMP	PROTOCOL[BX],2
	JE	TXETXNR

	TEST	KISSFLAGS[BX],CHECKSUM
	JNZ	TXETXP

	SETATVEC NEXTFRAME
	MOV	AL,FEND			; ETX
TXA100:
	IFDEF	KANT

	OUT	STB,AL

	ELSE

	MOV	DX,IOADDR[BX]
	OUT	DX,AL			; SEND CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

	RET

TXETXNR:
	SETATVEC ASENDCC
	MOV	AL,03H
	JMP SHORT TXA100

TXETXP:
	SETATVEC ASENDFEND
;
;	SEND CHECKSUM - BEWARE! - IT MAY BE FEND OR FESC
; 
	MOV	AL,TXCCC[BX]
	XOR	AL,FEND			; HAVNT INCLUDED FEND YET 

	CMP	AL,FEND
	JNE	NOTCHKFEND

	MOV	ESCTXCHAR[BX],TFEND	; ESC'ED FEND
	MOV	AL,FESC
	JMP SHORT TXA100

NOTCHKFEND:

	CMP	AL,FESC
	JNE	TXA100

	MOV	ESCTXCHAR[BX],TFESC	; ESC'ED ESC
	JMP SHORT TXA100


ASENDCC:
;
;	NETROM END OF FRAME
;

	MOV	AL,TXCCC[BX]
	SETATVEC NEXTFRAMENR
	JMP SHORT TXA100


ASENDFEND:
;
;	USED TO SEND FINAL CHAR OF CHECKSUMMED FRAME
;
;	IF THE CHECKSUM WAS FEND OR FESC, WE HAVE TO SEND THE 'ESC'ED VALUE
;	FIRST
;
	MOV	AL,ESCTXCHAR[BX]
	OR	AL,AL	
	JZ	SENDFEND00		; NORMAL

	MOV	ESCTXCHAR[BX],0
	JMP SHORT TXA100		; SEND CHAR FOLLOWING ESC

SENDFEND00:

	MOV	AL,FEND
	SETATVEC NEXTFRAME
	JMP SHORT TXA100


NEXTFRAMENR:
;
;	IF NOT FULL DUP, RAISE RTS
;
	IFDEF	PCSWITCH

	CMP	FULLDUPLEX[BX],1
	JE	NEXTFRAME

	MOV	DX,MCR[BX]
	MOV	AL,0BH			; DTR OUT2

	OUT	DX,AL
	
	ENDIF

NEXTFRAME:

	MOV	DI,TXTADDR[BX]

	IFDEF	PCSWITCH

	MOV	SI,OFFSET TRACE_Q
	CALL	Q_ADD			; FEED TO TRACE CODE

	ELSE

	MOV	SI,OFFSET FREE_Q
	CALL	Q_ADDF

	ENDIF

	JMP	ENDFRAME

;	TEST	KISSFLAGS[BX],POLLINGKISS
;	JZ	ENDFRAME
;
;	SEND A POLL
;
SENDPOLLCTRL:

	MOV	AL,TXCTRL[BX]		; CHANNEL BITS

	TEST	KISSFLAGS[BX],POLLINGKISS
	JZ	POLLRESP		; MUST BE SENDING POLL RESPONSE

	SETATVEC SENDPOLLFEND

	JMP	TXA100			; SEND IT

SENDPOLLFEND:

	MOV	AL,FEND

POLLRESP:

	SETATVEC ENDFRAME

	JMP	TXA100			; SEND IT

ENDFRAME:

	MOV	TXTADDR[BX],0		; CLEAR SENDING INDICATOR
	MOV	TXACTIVE[BX],0
;
	IFDEF	KANT

	MOV	AL,10B
	OUT	SIMK,AL			; MASK TX INT

	ENDIF
	IFDEF	PCSWITCH

	MOV	DX,IER[BX]
	MOV	AL,5
	OUT	DX,AL			; MASK TX, ENABLE RX & RX ERROR

	ENDIF

	SETATVEC IGNORE
	RET

IGNORE:
	IFDEF	KANT

	MOV	AL,10B
	OUT	SIMK,AL			; MASK TX INT

	ENDIF

	RET
;
TXKISSCTRL:
;
;	SEND KISS CONTROL FRAME
;
	MOV	INTFLAG[BX],0		; INTERRUPT HAS OCCURED

	CMP	TXCOUNT[BX],0
	JZ	TXETXC

	DEC	TXCOUNT[BX]

	MOV	SI,TXCHAR[BX]
	LODSB				; NEXT CHAR
	MOV	TXCHAR[BX],SI

	JMP SHORT TXA100C

TXETXC:

	MOV	DI,TXTADDR[BX]
	MOV	SI,OFFSET FREE_Q
	CALL	Q_ADDF

	SETATVEC ENDFRAME

	MOV	AL,FEND			; ETX
TXA100C:
	IFDEF	KANT

	OUT	STB,AL

	ELSE

	MOV	DX,IOADDR[BX]
	OUT	DX,AL			; SEND CHAR
;
;	ADD	CHARS,1
;	ADC	CHARS+2,0

	ENDIF

	RET

	IFDEF	PCSWITCH

QUADINT:
;
;	SEE WHICH PORT THE INTERRUPT IS FOR
;
