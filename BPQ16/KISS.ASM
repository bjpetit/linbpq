	MOV	CX,15			; MAX WRITES - 1

FIFOTXLOOP:

	CMP	IOTXCA[BX],OFFSET IGNORE
	JE	SIOI10			; ALL MESSAGE IS NOW IN FIFO

	PUSH	CX	
	CALL	IOTXCA[BX]
	POP	CX

	LOOP	FIFOTXLOOP

	JMP	SIOI10			; SEE IF ANOTHER

