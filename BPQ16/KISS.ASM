	IFDEF	PCSWITCH

SIOI10:
	MOV	DX,IIR[BX]		; INTERRUPT IDENT
	IN	AL,DX
	AND	AL,1111B
	CMP	AL,6
	JE	INTRXEA

	CMP	AL,4
	JE	INTRXCA

	CMP	AL,2
	JE	INTTXCA

	CMP	AL,0CH			; FIFO TIMEOUT FOR 16550
	JE	INTRXCA

	CMP	AL,0
	JNE	NOINT			; NOTHING PENDING
